<div class="subheading"><strong><i>Heat Zones of the Earth</i></strong></div>

The mid-day sun is exactly overhead at least
once a year on all latitudes in between the Tropic
of Cancer and the Tropic of Capricorn. This area,
therefore, receives the maximum heat and is called
the Torrid Zone.
<br>
<br>
The mid-day sun never shines overhead on
any latitude beyond the Tropic of Cancer and the
Tropic of Capricorn. The angle of the sun's rays
goes on decreasing towards the poles. As such,
the areas bounded by the Tropic of Cancer and
the Arctic Circle in the Northern Hemisphere, and
the Tropic of Capricorn and the Antarctic Circle
in the Southern Hemisphere, have moderate
temperatures. These are, therefore, called
Temperate Zones.
<br>
<br>
Areas lying between the Arctic Circle and the
North Pole in the Northern Hemisphere and the
Antarctic Circle and the South Pole in the
Southern Hemisphere, are very cold. It is because
here the sun does not rise much above the
horizon. Therefore, its rays are always slanting.
These are, therefore, called Frigid Zones.
Great Circles: Any circle which divides a
globe into hemispheres is a great circle. The
equator is a great circle and Greenwich meridian
together with meridian 180° make another great
circle. The number of great circle is limitless. Great
circle can extend in any direction: east to west,
north to south, north east to south west, and so
on. Great circles are of equal length.
<br>
<br>
<b>Longitude:</b>
<br>
The longitude shows the distance of a point
east or west of the Prime Meridian which is at 0°
and passes through Greenwich, near London. For
each degree of longitude there is a difference of
four minutes in time.
<br>
<br>
<b>Longitude and Time:</b> The best means of
measuring time is by the movement of the earth
and the moon. The sun regularly rises and sets
every day, and naturally, it is the best time-keeper
throughout the world.
<br>
<br>
When the Prime Meridian has the sun at the
highest point in the sky, all the places along this
meridian will have mid-day or noon. As the earth
rotates from west to east, those places east of
Greenwich will be ahead of Greenwich time and
those to the west will be behind it. The rate of
difference can be calculated as follows. The earth
rotates 360° in about 24 hours, which means 15°
an hour or 1° in four minutes. Thus, when it is 12
noon at Greenwich, the time at 15° east of
Greenwich will be 15 × 4 = 60 minutes, i.e., 1 hour
ahead of Greenwich time, which means 1 p.m.
But at 15° west of Greenwich, the time will be
behind Greenwich time by one hour, i.e., it will
be 11.00 a.m. Similarly, at 180°, it will be midnight
when it is 12 noon at Greenwich.
<br>
<br>
<b>Greenwich Mean Time:</b> The local time at
Greenwich or any place on the Prime Meridian.
All meridians to the east of Greenwich meridian
have sunrise before that meridian. Local times
along these meridians are therefore ahead of
G.M.T. Meridians to the west of Greenwich
meridian have sunrise after this meridian and
therefore their local times are behind G.M.T.
Standard Time: A particular meridian of
longitude passing through a country is chosen as
the reference meridian. The local time along this
meridian, calculated with respect to Greenwich
Mean Time in terms of its longitude is taken as
the Standard Time for that country.


<div class="subheading"><strong><i>Why do we have standard time?</i></strong></div>

The local times of places which are on different
meridians are bound to differ. For example, it will
be difficult to prepare a time-table for trains which
cross several longitudes. In India, for instance,
there will be a difference of about 1 hour and 45
minutes in the local times of Dwarka in Gujarat
and Dibrugarh in Assam. It is, therefore, necessary
to adopt the local time of some central meridian
of a country as the standard time for the country.
Indian Standard Time: Time along 82 ½° E
meridians, calculated with respect to G.M.T.
India, for being a large country, is unusual in
having a single time zone all over the country. It
is 5½ hours ahead of G.M.T.
International Date Line: An imaginary
zigzag line on the globe, approximately along the
180° meridian of longitude. When a person
crosses this line from East to West, he gains one
day and when he crosses from West to East, he
loses one day.
<br>
<br>
<b>Solar Day:</b> It is the time interval between
successive crossings of the sun across the meridian
of the celestial sphere of any fixed place in the
same direction. This is equal to 24 hours.
Sidereal Day: The period of rotation of the
earth about its axis. This is calculated with respect
to any fixed star. It is 4 minutes less than 24 hours.
Solar Year (Tropical year): It is the average
interval between successive returns of the sun in
its apparent motion along the ecliptic to a fixed
position on the celestial sphere of any fixed place.
This is equal to 365.24 mean solar days.
Sidereal Year: The period of revolution of the
earth around the sun. It is calculated with
reference to any fixed star. It is approximately
equal to 365.26 days.
<br>
<br>
To account for 1/4 of a day in a year, the
leap year system is adopted in the Gregorian
calendar. To account for the excess of 11 minutes
in a year, the centurial year is considered a leap
year only when it is divisible by 400.

