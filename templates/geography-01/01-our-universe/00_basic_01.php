<div class="subheading"><strong><i>Major domains of the earth</i></strong></div>

Earth is the only planet which has life.
Human beings can live here because the life
sustaining elements of land, water and air are
present on the earth. The surface of the earth is a
complex zone in which three main components
of the environment meet, overlap and interact.
The solid portion of the earth on which we live is
called the lithosphere. The gaseous layers that
surround the earth, is the atmosphere, where
oxygen, nitrogen, carbon dioxide and other gases
are found. Water covers a very big area of the
earth's surface and this area is called the
hydrosphere. The hydrosphere comprises water
in all its forms, that is, ice, water and water
vapour. The biosphere is the narrow zone where
we find land, water and air together, which
contains all forms of life.

<div class="subheading"><strong><i>Lithosphere</i></strong></div>

The solid portion of the earth is called the
lithosphere. It comprises the rocks of the earth's
crust and the thin layers of soil that contain
nutrient elements which sustain organisms. There
are two main divisions of the earth's surface. The
large landmasses are known as the continents
and the huge water bodies are called the ocean
basins. All the oceans of the world are connected
with one another. The level of seawater remains
the same everywhere. Elevation of land is
measured from the level of the sea, which is taken
as zero.
<br>
<br>
The highest mountain peak Mt. Everest is
8,848 metres above the sea level. The greatest
depth of 11,022 metres is recorded at Mariana
Trench in the Pacific Ocean.

<div class="subheading"><strong><i>Continents</i></strong></div>

There are seven major continents. These are
separated by large water bodies. These continents
are - Asia, Europe, Africa, North America, South
America, Australia and Antarctica.
<br>
<br>
Asia is the largest continent. It covers about
one-third of the total land area of the earth. The
continent lies in the Eastern Hemisphere. The
Tropic of Cancer passes through this continent.
Asia is separated from Europe by the Ural
Mountains on the west. The combined landmass
of Europe and Asia is called the Eurasia (Europe + Asia).
<br>
<br>
Europe is much smaller than Asia. The
continent lies to the west of Asia. The Arctic Circle
passes through it. It is bound by water bodies on
three sides.
<br>
<br>
Africa is the second largest continent after
Asia. The Equator or 0° latitude runs almost
through the middle of the continent. A large part
of Africa lies in the Northern Hemisphere. It is
the only continent through which the Tropic of
Cancer, the Equator and the Tropic of Capricorn
pass. The Sahara Desert, the world's largest hot
desert, is located in Africa. The continent is bound
on all sides by oceans and seas. The world's
longest river, the Nile, flows through Africa.
North America is the third largest continent
of the world. It is linked to South America by a
very narrow strip of land called the Isthmus of
Panama. The continent lies completely in the
Northern and Western Hemisphere. Three oceans
surround this continent.
<br>
<br>
South America lies mostly in the Southern
Hemisphere. The Andes, world's longest
mountain range, runs through its length from
north to south. South America has the world's
largest river, the Amazon.
Australia is the smallest continent that lies
entirely in the Southern Hemisphere. It is
surrounded on all sides by the oceans and seas. It
is called an island continent.
<br>
<br>
Antarctica, completely in the Southern
Hemisphere, is a huge continent. It is larger than
the combined area of Europe and Australia. The
South Pole lies almost at the centre of this
continent. As it is located in the South Polar
Region, it is permanently covered with thick ice
sheets. There are no permanent human
settlements. Many countries have research
stations in Antarctica. India also has research
stations there. These are named as Maitri and
Dakshin Gangotri.

<div class="subheading"><strong><i>Hydrosphere</i></strong></div>

The earth is called the blue planet. More than
71 per cent of the earth is covered with water
and 29 per cent is with land. Hydrosphere consists
of water in all its forms. As running water in
oceans and rivers and in lakes, ice in glaciers,
underground water and the water vapour in
atmosphere, all comprise the hydrosphere. More
than 97% of the Earth's water is found in the
oceans and is too salty for human use. A large
proportion of the rest of the water is in the form
of ice-sheets and glaciers or under the ground and
a very small percentage is available as fresh water
for human use.

<div class="subheading"><strong><i>Oceans</i></strong></div>

Oceans are the major part of hydrosphere.
They are all interconnected. The ocean waters are
always moving. The three chief movements of
ocean waters are the waves, the tides and the
ocean currents. The four major oceans are the
Pacific Ocean, the Atlantic Ocean, the Indian
Ocean and the Arctic Ocean, in order of their size.
The Pacific Ocean is the largest ocean. It is
spread over one-third of the earth. Mariana
Trench, the deepest part of the earth, lies under
the Pacific Ocean. The Pacific Ocean is circular
in shape. Asia, Australia, North and South
Americas surround it.
<br>
<br>
The Atlantic Ocean is the second largest
Ocean in the world. It is 'S' shaped. It is flanked
by the North and South Americas on the western
side, and Europe and Africa on the eastern side.
The coastline of Atlantic Ocean is highly indented.
This irregular and indented coastline provides
ideal location for natural harbours and ports.
From the point of view of commerce, it is the
busiest Ocean.
<br>
<br>
The Indian Ocean is the only ocean named
after a country, that is, India. The shape of ocean
is almost triangular. In the north, it is bound by
Asia, in the west by Africa and in the east by
Australia.
<br>
<br>
The Arctic Ocean is located within the Arctic
Circle and surrounds the North Pole. It is
connected with the Pacific Ocean by a narrow
stretch of shallow water known as Bering Strait.
It is bound by northern coasts of North America
and Eurasia.

<div class="subheading"><strong><i>Atmosphere</i></strong></div>

The earth is surrounded by a layer of gas
called the atmosphere. This thin blanket of air is
an integral and important aspect of the planet. It
provides us with the air we breathe and protects
us from the harmful effects of sun's rays. The
atmosphere extends up to a height of about 1,600
km.
<br>
<br>
The atmosphere is divided into five layers
based on composition, temperature and other
properties. These layers starting from earth's
surface are the troposphere, the stratosphere, the
mesosphere, the thermosphere and the exosphere.
The atmosphere is composed mainly of
nitrogen and oxygen, which make up about 99
per cent of clean, dry air. Nitrogen 78 per cent,
oxygen 21 per cent and other gases like carbon
dioxide, argon and others comprise 1% by volume.
The density of the atmosphere varies with
height. It is maximum at the sea level and
decreases rapidly as we go up. The climbers
experience problems in breathing due to this
decrease in the density of air. The temperature
also decreases as we go upwards.
The atmosphere exerts pressure on the earth.
This varies from place to place. Some areas
experience high pressure and some areas low
pressure. Air moves from high pressure to low
pressure. Moving air is known as wind.

<div class="subheading"><strong><i>Biosphere</i></strong></div>
 
The biosphere is the narrow zone of contact
between the land, water and air. It is in this zone
that life exists. All the living organisms including
humans are linked to each other and to the
biosphere for survival. The organisms in the
biosphere may broadly be divided into the plant
kingdom and the animal kingdom.
<br>
<br>
The three domains of the earth interact with
each other and affect each other in some way or
the other. For example, cutting of forests for
fulfilling our needs of wood, or clearing land for
agriculture may lead to fast removal of soil from
slopes. Similarly earth's surface may be changed
due to natural calamities like earthquakes or
tsunamis.
Discharge of waste material into lakes and
rivers makes the water unsuitable for human use.
It also damages other forms of life. Emission from
industries, thermal power plants and vehicles,
pollute the air. Carbon dioxide (CO2) is an
important constituent of air. But increase in the
amount of CO2 leads to increase in global
temperatures. This is termed as global warming.
There is thus, a need to limit the use of resources
of the earth to maintain the balance of nature
between the domains of the lithosphere, the
atmosphere and the hydrosphere.

