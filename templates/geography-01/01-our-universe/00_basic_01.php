<h3>Our Universe</h3>

<div class="subheading"><strong><i>Origin</i></strong></div>


<b><i>The big bang theory</i></b> explains the origin of our
universe. According to this theory, 15 billion years
ago, cosmic matter was in a compressed state from
which expansion started by a primordial
explosion. The super-dense ball broke to form
galaxies, which again broke to form stars and
finally stars broke to form planets including earth.
<br>
<br>
Since the outer space is limitless, conventional
units for measuring distances are not suitable.
Hence new units as follows are used:
<ol>
    <ul>
        <li><b>Light Year:</b> Distance covered by light in
one year in vacuum at a speed of 3x108
m/s. One light year is equal to 9.46 x
1012 kilometers.</li>
        <li><b>Astronomical Unit:</b> The Mean distance
between the Sun and the Earth (1.49 x
108 km). One light year is equal to 60,000 AU.</li>
        <li><b>Cosmic Year:</b> Sun's period of revolution
around the galactic centre (250 million years). Also called as 'galactic year'</li>
        <li><b>Parsec:</b> Distance at which the mean radius of the Earth's orbit subtends an
angle of one second of an arc. It is equal
to 3.26 light years.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Galaxies</i></strong></div>

These are huge congregation of stars that hold
together by force of gravity e.g. the Milky Way,
Andromeda galaxy, large and small magellanic
cloud, Ursa Minor system, sculptor system, etc.
Milky Way or Akashganga is our home galaxy.
Our solar system is located in this galaxy.


<div class="subheading"><strong><i>Stars</i></strong></div>

Stars are self luminous bodies that account
for 98 percent of the matter in a galaxy. In the
universe, some stars appear small but emit more
energy than the other stars of the Milky Way.
Such stars are called 'Quasars'. When the dense
galactic nucleus is compressing to form a star, this
stage in star formation is called a 'protostar' stage.
Due to high temperature hydrogen converts to
helium and heat and light is emitted. Thus a star
is formed. When the hydrogen of a star is depleted,
its outer regions swell and redden. This stage of a
star is called a 'Red Giant'. Our sun will turn into
a 'Red Giant' in 5 billion years. 'Novae Stars' are
stars whose brightness increases suddenly by 10
to 20 magnitudes due to explosion and then the
stars again fade into normal brightness. 'Super
Novae' are stars whose brightness suddenly
increases by more than 20 magnitudes. After the
explosion, the dense core of comparatively smaller
stars is called the 'white dwarf'. The dense core
of the comparatively larger stars is called the
'Neutron star'. The neutron star rotates at a high
speed emitting radio waves. Such stars are called
'Pulsar'. 'Black hole' stage of the star occurs when
the ancient star collapses. Gravity becomes so
intense in the hole that nothing escapes, even
light.

<div class="subheading"><strong><i>Constellations</i></strong></div>

In the sky at night there are various patterns
formed by different groups of stars. These are
called constellations. Ursa Major or Big Bear is
one such constellation. One of the most easily
recognizable constellations is the small bear or
Saptarishi (Sapta-seven, Rishi-sages). It is a group
of seven stars that forms a part of the large Ursa
Major Constellation.


