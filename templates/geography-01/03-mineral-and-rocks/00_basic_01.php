        <li>
        <div class="subheading"><strong><i>Metamorphic rocks</i></strong></div>
            
The word metamorphic means ‘change of
form’. These rocks form under the action of
pressure, volume and temperature (PVT)
changes. Metamorphism occurs when rocks are
forced down to earth’s interior by tectonic
processes or when molten magma rising through
the crust comes in contact with the crustal rocks
or the underlying rocks are subjected to great
amounts of pressure by overlying rocks.
Metamorphism is a process by which already
consolidated rocks undergo recrystallization and
reorganization of materials within original rocks.
Mechanical disruption and reorganization
of the original minerals within rocks due to
breaking and crushing without any appreciable
chemical changes is called dynamic
metamorphism. The materials of rocks
chemically alter and recrystallize due to thermal
metamorphism. There are two types of thermal
metamorphism - contact metamorphism and
regional metamorphism.
<br>
<br>
In contact metamorphism the rocks come in
contact with hot intruding magma and lava and
the rock materials recrystallize under high
temperatures. Quite often new materials form
out of magma or lava are added to the rocks.
In regional metamorphism, rocks undergo
recrystallization due to deformation caused by
tectonic shearing together with high temperature
or pressure or both. In the process of
metamorphism in some rocks grains or minerals
get arranged in layers or lines. Such an
arrangement of minerals or grains in
metamorphic rocks is called foliation or lineation.
Sometimes minerals or materials of different
groups are arranged into alternating thin to thick
layers appearing in light and dark shades. Such
a structure in metamorphic rocks is called
banding and rocks displaying banding are called
banded rocks. Types of metamorphic rocks
depend upon original rocks that were subjected
to metamorphism.
<br>
<br>
Metamorphic rocks undergo complete
alteration in the appearance of pre-existing rocks
due to change in mineral composition and texture
through temperature and pressure changes.
Gneiss, granite, slate, schist, marble, quartzite etc.
are some examples of metamorphic rocks. They
are classified as mentioned below:
<ol>
    <ul>
        <li>Contact or thermal metamorphism: here
metamorphism occurs when the mineral
composition of the surrounding rocks is changed
due to intense heat e.g. Limestone is changed to
marble.</li>
        <li>Regional or dynamic metamorphism:
here pressure plays an important role so that
rocks are altered in their forms in an extensive
area.</li>
    </ul>
</ol>
</li>
    </ul>
</ol>
 
<div class="subheading"><strong><i>Rock Cycle</i></strong></div>

Rocks do not remain in their original form
for long but may undergo transformation. Rock
cycle is a continuous process through which old
rocks are transformed into new ones. Igneous
rocks are primary rocks and other rocks
(sedimentary and metamorphic) form from these
primary rocks. Igneous rocks can be changed
into metamorphic rocks. The fragments derived
out of igneous and metamorphic rocks transform
into sedimentary rocks. Sedimentary rocks
themselves can turn into fragments and the
fragments can be a source for formation of other
sedimentary rocks. The crustal rocks (igneous,
metamorphic and sedimentary) once formed
may be carried down into the mantle (interior
of the earth) through subduction process (parts
or whole of crustal plates going down under
another plate in zones of plate convergence). The
same can melt down due to increase in
temperature in the interior and turn into molten
magma, the original source for igneous rocks.



