<h3>Mineral & Rocks</h3>

<div class="subheading"><strong><i>Minerals</i></strong></div>

A mineral is a naturally occurring substance
that is solid and stable at room temperature,
representable by a chemical formula, usually
abiogenic, and has an ordered atomic structure
Mineral are divided as follows:
<ol>
    <ul>
        <li>
        <div class="subheading"><strong><i>Metallic Minerals</i></strong></div>    
        
These minerals contain metal content and
can be sub-divided into three types:
<ol>
    <ul>
        <li>Precious metals: gold, silver, platinum etc.</li>
        <li>Ferrous metals: iron and other metals often
mixed with iron to form various kinds of
steel.</li>
        <li>Non-ferrous metals: include metals like
copper, lead, zinc, tin, aluminium etc.</li>
    </ul>
</ol></li>
        <li>
        <div class="subheading"><strong><i>Non-Metallic Minerals</i></strong></div>    
        These minerals do not contain metal content.
Sulphur, phosphates and nitrates are examples
of non-metallic minerals. Cement is a mixture of
non-metallic minerals.</li>
        <li></li>
        <li></li>
        <li></li>
    </ul>
</ol>
<div class="subheading"><strong><i>Some Major Minerals and Their Characteristics</i></strong></div>


<div class="subheading"><strong><i>Feldspar</i></strong></div>

Silicon and oxygen are common elements in
all types of feldspar and sodium, potassium,
calcium, aluminium etc. are found in specific
feldspar variety. Half of the earth’s crust is
composed of feldspar. It has cream to salmon
pink colour. It is used in ceramics industries.
<div class="subheading"><strong><i>Quartz</i></strong></div>

It is one of the most important components
of sand and granite. It consists of silica. It is a
hard mineral virtually insoluble in water. It is
white or colourless and used in radio and radar.
It is one of the most important components of
granite.

<div class="subheading"><strong><i>Pyroxene</i></strong></div>

Pyroxene consists of calcium, aluminum,
magnesium, iron and silica. Pyroxene forms 10
per cent of the earth’s crust. It is commonly found
in meteorites. It is in green or black colour.

<div class="subheading"><strong><i>Amphibole</i></strong></div>

Aluminium, calcium, silica, iron, magnesium
are the major elements of amphiboles. They form
7 per cent of the earth’s crust. It is in green or
black colour and is used in asbestos industry.
Hornblende is another form of amphiboles.

<div class="subheading"><strong><i>Mica</i></strong></div>

It comprises of potassium, aluminium,
magnesium, iron, silica etc. It forms 4% of the
earth’s crust. Commonly found in igneous and
metamorphic rocks, it is used in electrical
instruments.

<div class="subheading"><strong><i>Olivine</i></strong></div>

Magnesium, iron and silica are major
elements of olivine. It is used in jewellery. It is
usually a greenish crystal, often found in basaltic
rocks. Other minerals like chlorite, calcite,
magnetite, haematite, bauxite and barite are also
present in some quantities in the rocks.


<div class="subheading"><strong><i>Rocks</i></strong></div>

The earth’s crust is composed of rocks. A
rock is an aggregate of one or more minerals.
Rock may be hard or soft and in varied colours.
For example, granite is hard, sandstone is soft.
Gabbro is black and quartzite can be milky white.
Rocks do not have definite composition of
mineral constituents. Feldspar and quartz are the
most common minerals found in rocks.
The crustal rocks are classified on the basis
of mode of formation, physical and chemical
properties, location etc. On the basis of mode of
formation the rocks are divided into three
categories (i) igneous rocks (ii) sedimentary
rocks (iii) metamorphic rocks.
<ol>
    <ul>
        <li>
        <div class="subheading"><strong><i>Igneous rocks</i></strong></div>    
        
As igneous rocks form out of magma and
lava from the interior of the earth, they are
known as primary rocks. The igneous rocks are
formed when magma cools and solidifies. When
magma in its upward movement cools and turns
into solid form it is called igneous rock. The
process of cooling and solidification can happen
in the earth’s crust or on the surface of the earth.
Igneous rocks are characterized on the basis
of texture. Texture depends upon size and
arrangement of grains or other physical
conditions of the materials. If molten material is
cooled slowly at great depths, mineral grains may
be very large. Sudden cooling (at the surface)
results in small and smooth grains. Intermediate
conditions of cooling would result in
intermediate sizes of grains making up igneous
rocks. Granite, gabbro, pegmatite, basalt,
volcanic breccia and tuff are some of the
examples of igneous rocks.
<br>
<br>
Igneous rocks are roughly hard rocks and
water percolates with great difficulty. They do
not have strata and are less affected by chemical
weathering. They don’t contain fossils. The
number of joints increases upwards. They are
mostly associated with volcanic activity.
They are classified on several grounds as
mentioned below:
<ol>
    <ul>
        <li>On the basis of silica content:
            <ol>
                <ul>
                    <li>Acidic igneous rocks have more silica e.g. Granites</li>
                    <li>Basic igneous rocks have less silica e.g. Gabbro.</li>
                </ul>
            </ol>
        </li>
        <li>On the basis of chemistry and
mineralogical composition:
            <ol>
                <ul>
                    <li>Felsic igneous rock (feldspar is dominant)</li>
                    <li>Mafic igneous rock (magnesium and ferrous are dominant)</li>
                    <li>Ultra mafic igneous rock (Peridotite and dunite are dominant).</li>
                </ul>
            </ol>
        </li>
        <li>On the mode of occurrence:
            <ol>
                <ul>
                    <li>Intrusive igneous rocks— they are
cooled and solidified below the surface of the
earth. They are further divided into plutonic and
hypabyssal igneous rocks. Plutonic rocks cool
deep beneath the earth e.g. Granite. Hypabyssal
rocks cool just beneath the earth surface e.g.
Batholith, laccolith, phacolith, lapolith, sills,
dykes, etc.</li>
                    <li>Extrusive igneous rocks are formed due
to cooling and solidification of hot and molten
lava at the earth’s surface e.g. Basalt, Gabbro,
obsidian.</li>
                </ul>
            </ol>
        </li>
    </ul>
</ol>
</li>
        <li>
        <div class="subheading"><strong><i>Sedimentary rocks</i></strong></div>    
        
The word ‘sedimentary’ is derived from the
Latin word sedimentum, which means settling.
Rocks (igneous, sedimentary and metamorphic)
of the earth’s surface are exposed to
denudational agents, and are broken up into
various sizes of fragments. Such fragments are
transported by different exogenous agencies and
deposited. These deposits through compaction
turn into rocks. This process is called lithification.
In many sedimentary rocks, the layers of
deposits retain their characteristics even after
lithification. Hence, we see a number of layers
of varying thickness in sedimentary rocks like
sandstone, shale etc.
<br>
<br>
Depending upon the mode of formation,
sedimentary rocks are classified into three major
groups: (i) mechanically formed - e.g. sandstone,
conglomerate, shale, loess etc. (ii) organically
formed - e.g. chalk, limestone, coal etc. (iii)
chemically formed – e.g. chert, halite, potash etc.
These rocks are formed due to aggregation
and compaction of sediments. These rocks
contain fossils of plants and animals. They cover
75 percent of surface area of the globe. However
they form only 5 percent of the volume of earth’s
crust. They contain several layers or strata but
these are seldom crystalline rocks. They are
seldom found in original and horizontal manner.
They may be well consolidated, poorly
consolidated and even unconsolidated. They are
characterized by different sizes of joints. Most
sedimentary rocks are porous and permeable.
The formation of sedimentary rocks takes place
in three stages:
<ol>
    <ul>
        <li>Transportation: after weathering and
erosion the fragments of parental rocks
are transported by the agents of erosion
like stream, wind, air, etc.</li>
        <li>Deposition: transported materials are
deposited in sea, lakes, etc. The particles
are deposited in parallel layers and their
process of layer formation is called
“stratification”.</li>
        <li>Consolidation: when the number of layer
is large, the weight of upper layer begins to affect
the lower layers and the further compression
solidifies the sediments into rocks.</li>
    </ul>
</ol>

They are classified under different schemes:
<ol>
    <ul>
        <li>On the basis of nature of sediments:
            <ol>
                <ul>
                    <li>Mechanically formed or clastic rocks e.g.
Sandstones. Conglomerates, clay rock,
shale, loess.</li>
                    <li>Chemically formed sedimentary rocks e.g.
gypsum, salt rock.</li>
                    <li>Organically formed sedimentary rocks e.g.
Limestone, dolomites, coal, peats, etc.</li>
                </ul>
            </ol>
</li>
        <li>On the basis of transporting agents:
            <ol>
                <ul>
                    <li>Argillaceous or aqueous rocks: (a) Marine
rocks, (b) Lacustrine rocks, (c) Riverine
rocks</li>
                    <li>Aeolian rocks e.g. Loess.</li>
                    <li>Glacial sedimentary rocks e.g. Till, moraine.</li>
                </ul>
            </ol>
 </li>
    </ul>
</ol>
