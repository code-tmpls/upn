<h3>Forces Effecting The Earth MOVEMENTS</h3>


The forces which affect the earth’s
movement are involved in the creation,
destruction, recreation and maintenance of
various types of relief features of varying
magnitudes. On the basis of origin these forces
are divided into (i) endogenetic forces and (ii)
exogenetic forces. While endogenetic forces
create relief features on the earth’s surface, the
exogenetic forces through their erosional and
depositional activities destroy them and help in
the planation process.

<div class="subheading"><strong><i>Endogenetic forces</i></strong></div>

Term endogenic refers to internal processes
and phenomena that occur beneath the Earth's
surface. These forces are divided into sudden
forces and diastrophic forces.
<ol>
    <ul>
        <li>Sudden forces: events like earthquake and
volcanic eruption occur suddenly and the
resultant forces work very quickly. They
are constructive forces as they create cones,
lakes, plateaus, lava plains etc.</li>
        <li>Diastrophic forces: they include both
vertical and horizontal movements.
<ol>
    <ul>
        <li>Vertical movement: they include
emergence and subsidence of land masses.
Emergence may occur due to upliftment of
the whole continent or part there of or
upliftment of coastal land of the continents.
Submergence may occur when the land
near the sea coast subsides below sea level.</li>
        <li>Horizontal movement: these forces work
into two ways
<ol>
    <ul>
        <li>In opposite direction - this includes
tensional or divergent forces which create
faults, rupture, fracture, cracks etc.</li>
        <li>Towards each other - This includes
compressional or convergent forces which
create folding, warping etc.</li>
    </ul>
</ol>
</li>
    </ul>
</ol>
</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Folding:</i></strong></div>

It is the process whereby the rock strata are
bent into a series of arches (anticlines) and toughs
(syncline) as a result of horizontal earth
movements which cause compression within the
crust. The anticlines of the folds generally form
the mountains and the adjacent synclines form
the valleys. Most of the mountain ranges of the
world consist of Fold Mountains e.g. the Alps,
the Andes, the Rockies and the Himalayas.
Geometry of Folds - Folds are described by
their form and orientation. The sides of a fold
are called limbs. The limbs intersect at the tightest
part of the fold, called the hinge. A line
connecting all points on the hinge is called the
fold axis. In the diagrams above, the fold axes
are horizontal, but if the fold axis is not
horizontal the fold is called a plunging
fold and the angle that the fold axis makes with
a horizontal line is called the plunge of the fold.
An imaginary plane, that includes the fold axis
and divides the fold as symmetrically as possible,
is called the axial plane of the fold.

<div class="subheading"><strong><i>Types of Folds</i></strong></div>

Not all folds are equal on both sides of the
axis of the fold. Those with limbs of relatively
equal length are termed symmetrical, and those
with highly unequal limbs are asymmetrical.
Asymmetrical folds generally have an axis at an
angle to the original unfolded surface they
formed on. Other kinds of folds are:
<ol>
    <ul>
        <li><b>Anticlines</b> - Up folds.
When the upper part of the fold is eroded
away, the oldest rocks are in the center of
the fold, and the youngest rocks are on
each side. Also, the rocks dip (or slope)
away from the central axis of the fold.</li>
        <li>Synclines - Down folds.
When the upper part of the fold is eroded
away, the youngest rocks are in the center
of the fold, and the oldest rocks are on
each side. Also, the rocks dip toward the
central axis of the fold.</li>
        <li><b>Monoclines</b> - a bend in otherwise
horizontal strata.</li>
        <li>Isoclinal folds have undergone greater
stress that has compressed the limbs of the
folds tightly together.</li>
        <li>The limbs of overturned folds dip in the
same direction, indicating that the upper
part of the fold has overridden the lower
part. Depending on where the exposure is in an overturned
fold, the oldest strata might actually be on
top of the sequence and be misinterpreted
as the youngest rock unit.</li>
        <li>Recumbent folds, found in areas of the
greatest tectonic stress, are folds that are
so overturned that the limbs are essentially
horizontal and parallel.</li>
        <li><b>Chevron:</b> angular fold with straight limbs
and small hinges.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Faulting:</i></strong></div>

It is the process by which the tensional earth
movements under the effect of considerable
pressure create a fracture in the earth's crust.
Faulting gives rise to relief features like block
mountains (horsts), rift valleys, etc. A rift valley
is a long, relatively narrow depression formed
by the sinking of a block of land between two
more or less parallel faults. Examples: East
African rift valley, Narmada and Tapti valleys.
Columns of faulting bring four distinguishable
landforms as:
<ol>
    <ul>
        <li>Rift valley: it is the result of the subsidence
of the central column. When the central
column of the two fault-lines subsides, the
rift valley is made. "Damodar valley" is such
an example.</li>
        <li>Ramp valley: when both side columns are
raised and the central column is standstill,
then the made landform is ramp valley.
"Brahmaputra" river passes through a ramp
valley.</li>
        <li>Block Mountain: this is the result of the
subsidence of side column. The central
column gets steep rim along the fault scarps and the
raised landform is Block Mountain.
"Satpura hills" of India is such an example.</li>
        <li>Horst: Horst is a similar landform but is
supposed to be due to upward force from
beneath the central column. Side-columns
are standstill. "Harz Mountain" of Germany
is an example.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Exogenetic forces</i></strong></div>

Exogenic forces refer to external processes
and phenomena that occur on or above the
Earth's surface. Comet and meteoroid impacts,
the tidal force of the moon and sun's radiations
are all exogenic. Weathering effects and erosion
are also exogenic processes. They also affect the
planation processes. These are also called
denudational or destructive forces. The erosional
process is affected by running water, ground
water, glaciers, sea waves etc. These processes
form erosional and depositional land forms.



