<h3>Geogmorphological landforms</h3>

<div class="subheading"><strong><i>Mountains</i></strong></div>

A mountain is defined as “a natural
elevation of the earth surface rising more or less
abruptly from the surrounding level and
attaining an altitude which, relative to the
adjacent elevation, is impressive or notable”.
Mountains can be classified on the basis of their
structure or their origin.
<ol>
    <ul>
        <li>

        <div class="subheading"><strong><i>Structural classification:</i></strong></div>
        <ol>
            <ul>
                <li><b>Fold Mountains:</b> These mountains have
originated due to compressional tectonic
forces and have been thrown up to form
fold mountains e.g. Himalayas, Andes, Alps
etc. The folds consist of two inclined parts
called limbs, the upfold is called anticline
and the downward portion is called
syncline.
<br>
<br>
All young folded mountains have originated
from geosynclines. Geosynclines are long narrow
and shallow water depressions characterized by
sedimentation and the subsequent subsidence.
The conversion of geosynclines into folded
mountains requires geologically long time with
definite phases of mountain building process-
<br>
Orogenesis: After horizontal compression
has completed its task, vertical uplift starts.
This is the real stage of mountain building.
<br>
(c) Glyptogenesis: In this phase the
characteristic land forms are sculptured by
erosion.
On the basis of age the Fold Mountains can be
grouped into:
<ol>
    <ul>
        <li>New or Young fold Mountains: Example:
The Alps, the Himalayas, the Circum-Pacific
oceanic Mountains, etc. The main features
of these mountains are the complex folding
of the rocks, faulting, volcanic activities, and
the erosion caused by running water, ice,
winds, etc.</li>
        <li>Old Fold Mountains: Example: The
Caledonian and Hercynian mountains of
central Europe, the Pennines, the Highland
of Scotland, etc. These mountains were
folded in very ancient times, and then
subjected to denudation and uplift. Many
faults were formed and the layers of the
rock were wrapped. Many mountains exist
as relicts due to erosion.</li>
        <li></li>
        <li></li>
    </ul>
</ol>
</li>
                <li><b>Block Mountains:</b> They are originated by
tensile forces leading to formation of rift
valleys. They are also called horst
mountains e.g. black forest, Vosges,
Vindhya, Satpura, Sierra Nevada etc. When
the crust cracks due to tension or
compression faulting takes place. A section
of the landform may subside or rise above
the surrounding level giving rise to Rift
valley or Graben and Block Mountains or
Horst. The Block Mountains have a steep
slope towards the rift valley but the slope
on the other side is long and gentle.</li>
                <li><b>Dome Mountains:</b> They are originated by
magmatic intrusion and upwarping of
crustal surface e.g. lava domes, Batholith
domes etc.</li>
                <li><b>Mountain of Accumulation:</b> They are
originated by accumulation of volcanic
material e.g. cinder cones, composite cones
etc. These are formed by the emission and
deposition of lava and so they are also
called volcanic mountains. The slope of the
mountains becomes steep and the height
increases due to the development of the
cones of various types like Cinder cones,
Composite Cones, Acid lava cones, Basic
lava cones, etc. Some of the examples of
this type are Popocatepetl of Mexico, Mount
Rainier of Washington, Lessen Peak of
California, the Vesuvius of Italy, the
Fujiyama in Japan, the Aconcagua in Chile
etc.</li>
                <li><b>Circum Erosional or Relict Mountain:</b> e.g.
Vindhyachal ranges, Aravallis, Satpura,
Eastern and Western Ghats, Nilgiris,
Parasnath, Girnar, Rajmahal. These
mountains have been subjected to
weathering and erosion for a long time and
lowered down. They represent the old stage
of mountain life cycle.</li>
            </ul>
        </ol>
        </li>
        <li>
        <div class="subheading"><strong><i>Classification on the basis of Mountain Building periods</i></strong></div>    
        <ol>
            <ul>
                <li>Pre-Cambrian Mountains: Rocks of
these mountains are older than the Cambrian era, and are found in older stable
blocks or old shields which are now
metamorphosed. Some of those old
shields are Laurentia, Fennoscandinevia
(Europe), Angaraland (Asia),
Gondwanaland (Asia), etc.</li>
                <li>Caledonian Mountains: (320 m.yrs.):
Mountains of Scandinavia, Scotland, N.
America, Aravallis, Mahadeo, Satpura
fall under this category. This mountain
building process started at the end of
the Silurian period or at the beginning
of the Devonian period.</li>
                <li>Hercynian Mountains: (240m.yrs.):
These Mountains were formed during
Permian and Permo-Carboniferous period. They include Appalachian in N.
America, Meseta in Spain, Vosges and
Black Forest in Germany, Harz, Donetz
area of Ural , Altai, Kinghan ,Tien
Shan, Alai, Nan-Shan, etc. Meseta
Mountains in Morocco; the High Atlas
Mountains also represent this category.</li>
                <li>Alpine Mountains (30m.yrs.): It started
by the end of the Mesozoic era and continued upto the Tertiary period. These
are the highest mountains of the world.
Being newer, the erosional forces could
not erode them into a Peneplain like the
Himalayas, the Alps, the Rockies, the
Andes, the Atlas, etc.</li>
            </ul>
        </ol>
</li>
        <li></li>
    </ul>
</ol>


<b>Stages of Mountains Building:</b> The life
history of mountains can be divided into youth,
maturity and old stage. Following are the
characteristics of mountains in different stages:-
<ol>
    <ul>
        <li>
        <div class="subheading"><strong><i>The Youth Mountains:</i></strong></div>
            <ol>
                <ul>
                    <li>The rivers are youthful and the valleys are deep and their flow is fast.</li>
                    <li>Landslide and volcanic activities are common.</li>
                    <li>The mountains are high.</li>
                    <li>The slopes are steep and the piedmont is bare.</li>
                    <li>The sky line is irregular.</li>
                </ul>
            </ol>
</li>
        <li>
        <div class="subheading"><strong><i>The Maturity of Mountains:</i></strong></div>    
        <ol>
            <ul>
                <li>The rivers are mature and many watergaps exist in the area.</li>
                <li>The height of the mountains is not much.</li>
                <li>The peaks are rounded, generally covered
by thick vegetation.</li>
                <li>Landslides are uncommon and no
earthquakes are experienced.</li>
                <li>Slopes are not steep. Pebbles and rock
fragments are accumulated in the piedmont
area.</li>
            </ul>
        </ol>
     </li>
        <li>
        <div class="subheading"><strong><i>The Old-Age of Mountains:</i></strong></div>    
        <ol>
            <ul>
                <li>The rivers have attained old age.</li>
                <li>Monadnocks are found denuded and are a
common sight.</li>
                <li>The mountains are low. Peneplain
condition seems imminent.</li>
                <li>The area is broad, low and leveled which
has wavy hills at some places.</li>
            </ul>
        </ol>
</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Plateau</i></strong></div>

Plateaus are extensive upland areas
characterized by flat and rough top surface and
steep walls which rise above the neighbouring
ground surface at least for 300 m.
On the basis of mode of formation the plateaus
can be classified into:
<ol>
    <ul>
        <li>Plateaus Formed by Running Water: Many
parts of the Deccan of India (Kaimur
Plateau, Rewa Plateau, Rohtas Plateau,
Bhander Plateau), Brazilian Plateau.</li>
        <li>Plateaus formed by Glacial Erosion:
Plateau of Greenland and Antarctica,
Garhwal Plateau.</li>
        <li>Plateaus formed by Glacial Deposition:
Russian Plateau, Finland Plateau, Merg of
Kashmir.</li>
        <li>Aeolian plateaus: Loess Plateau of China,
Potwar Plateau of Rawalpindi in Pakistan.</li>
        <li>Plateaus formed by endogenic processes:
            <ol>
                <ul>
                    <li>Intermontane Plateaus: Tibetan Plateau,
Bolivian Plateau, Peruvian Plateau,
Columbian Plateau, Mexican Plateau.</li>
                    <li>Piedmont Plateaus: Appalachian Plateau, Patagonian Plateau, Colorado Plateau.</li>
                    <li>Dome Plateaus: Ozark Massif (USA),
Chhotanagpur Plateau.</li>
                    <li>Lava Plateaus: Columbian Plateau,
Mahabaleshwar Plateau.</li>
                    <li>Continental Plateau: Deccan plateau,
Ranchi plateau, Shillong plateau, Columbia Plateau, Mexican Plateau etc. etc.</li>
                    <li>Coastal Plateau: Coromandal coastal
upland of India.</li>
                    <li>Rejuvenated Plateau: Missouri Plateau
(USA).</li>
                    <li>Mature Plateau: Ranchi Plateau,
Hazaribagh Plateau, Appalachian Plateau (USA).</li>
                    <li>Young Plateau: Idaho Plateau (USA),
Colorado Plateau (USA),
Mahabaleshwar Plateau, Khandala upland (Maharashtra).</li>
                </ul>
            </ol>
</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Plains</i></strong></div>

Plains can be defined as flat areas with low
height. They may be above or below sea level
e.g. coastal plains of Netherlands.
The plains may be classified as under:
    <ol>
        <ul>
            <li>Formation of plain due to deposition of
sediments over submerged coastlands e.g.
Coromandal coastal plains.</li>
            <li>River deposited plains e.g. north Indian
plains</li>
            <li>Piedmont alluvial plain e.g. Bhabar plain</li>
            <li>Flood plains e.g. Khadar and Bhangar
plains</li>
            <li>Lava plains e.g. plains of New Zealand, Iceland etc.</li>
            <li>Glaciated plains e.g. north west Eurasian
plain.</li>
        </ul>
    </ol>

    <div class="subheading"><strong><i>Erosional Plains</i></strong></div>
<ol>
    <ul>
        <li>Plains of Fluvial Erosion: The plains
formed by river erosion have a lot of
variation because of the stages of
erosional development, the initial slope and
the structure of basal rocks.
<ol>
    <ul>
        <li>The Dissected Plains of the Youth: The
Colorado, Kansas, Nebraska, east of the
Rockies belong to this category of plains.
The broad water-divides, large valleys are
the main characteristics of such plains. The
drainage is dendritic in nature.</li>
        <li>The Dissected Plains of Maturity: Such
plains are found in North Missouri,
Southern Iowa and Eastern Nebraska of
USA. Areas of gentle slope are very limited
and plain areas are available more in the
valleys and the water divides are reduced
to small ridges.</li>
        <li>The Plains of Old Age: Peneplain and
Panplains usually represent this stage of
plains.</li>
    </ul>
</ol>
Peneplain: Very few areas like Guinea plain
in the north-east S. America are fully developed
peneplains. The Appalachian had developed
into peneplains in the ancient times but was later
uplifted again. Here the high summits are of
equal heights.
<br>
<br>
Panplains: A plain formed of flood plains
joined by their own strength. It is a product of
lateral erosion by streams.</li>
        <li>Glaciated Plains : When the ice sheet
melted specially in N. America and W.
Eurasia , the area eroded by ice was
exposed . Here the rivers have adjusted
themselves before the extension of ice sheet.
Lakes, swamps, waterfalls and rapids are
common.</li>
        <li>Aeolian Plains: Winds blow the sand and
starts the activities of deflection, abrasion,
etc. The plains produced by the wind
actions are Reg, Serir and Hamada.</li>
        <li>Plains of Semi-arid Denudation : This
type of plain includes the peneplains of
USA and the pediplains of south-west
of Africa.</li>
        <li>Plains at Continental Edges: Theses have
evolved at the sea coast by the action of
waves and later uplifted. The flat plains
situated at the coast of Norway fall into
this category.</li>
        <li>Karst Plains: They are found in limestone
areas. The underground water removed the
limestone layer by the process of solution.
A large number of depressions are
produced in these plains e.g. the coastal
plain of Adriatic Sea and the Karst plain
of Florida (USA).</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Depositional Plains</i></strong></div>
<ol>
    <ul>
        <li>Plains of Alluvial Deposition : The
deposition of the sediments takes place
in three areas - the floor , the mouth and
the valley of the river where the slope
suddenly decreases. The shape of such
depositional plain changes according to the
method and place of deposition and forms
three types of plains.
<ol>
    <ul>
        <li>Flood Plains: Here the river deposits its
sediments by meandering through its
course. The flood plains of Mississippi,
Ganga, Indus and Nile are good examples.</li>
        <li>Deltaic Plains: When the river terminates in the sea or lake, the deltas are
formed due to deposition. The deltaic
plains resemble flood plains but the existence of large number of distributaries
provides them with a distinction.
Marshes and natural levees are common
here. The Deltaic plains of the Ganga,
the Indus, the Nile and the Mississippi
are famous.</li>
        <li>Piedmont Alluvial Plains: The piedmont
alluvial fans combine together and form
a plain. Rough particles are found at
the apex but the particles of debris get
finer as we move towards the periphery.</li>
    </ul>
</ol>
</li>
        <li>Plains of Glacial Deposition: These are
found in N. America and Europe, in areas
which were affected by glacial action. The
surface is slightly undulating and has low
and broad ridges and depressions.</li>
        <li>Desert Plains of Wind Erosion: The Loess
Plain of China was formed by the
windblown deposition of Gobi desert,
situated west of it. Some other examples of
such plains are the Sahara of Africa, the
Koum of Russian Turkistan, the northcentral Nebraska, etc.</li>
        <li>Plains of Marine Deposition: They develop
near the coast of shallow sea. Sand,
alluvium, vegetation, etc. are deposited at
the coastal areas of Netherlands, Germany,
Denmark, The Gulf of Mexico in U.S.A.,
etc.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Lakes</i></strong></div>

Lakes may be defined as non-permanent
features of static water on the land surface. The
lakes can be classified as under:
<ol>
    <ul>
        <li>Fresh water lakes e.g. the great lakes of
U.S.A.</li>
        <li>Saline lakes e.g. great salt lake of Utah,
Caspian sea, dead sea, lake van etc.</li>
        <li>Fluvial lakes e.g. Wular lake, Marigot lake,
Mayeh lake</li>
        <li>Lakes formed by volcanic Activity: Crater
and Caldera Lakes- Lake Oregon (USA),
Lakes Toba (Sumatra)</li>
        <li>Lakes formed by earth movements:
            <ol>
                <ul>
                    <li>Tectonic Lakes: - Lake Titicaca (Andes),
(highest Lake of world), Caspian Sea
(Largest Lake of the world).</li>
                    <li>Rift valley Lakes:- Tanganyika, Malawi,
Rudolf, Edward, Albert, Dead Sea (1256
ft below mean sea level the world’s
Lowest Lakes)</li>
                </ul>
            </ol>     
</li>
        <li>Lakes formed by deposition:
            <ol>
                <ul>
                    <li>Due to river deposits – Ox-bow Lakes</li>
                    <li>Due to marine deposits – Lagoons, Delta</li>
                </ul>
            </ol>
 </li>
        <li>Lakes formed by Erosion:
            <ol>
                <ul>
                    <li>Karst Lakes – Lake Scutari (Yugoslavia)</li>
                    <li>Wind – deflated Lakes (Salt Lakes & Playas)</li>
                </ul>
            </ol>
         </li>
        <li>Lakes formed by Glaciations:
            <ol>
                <ul>
                    <li>Cirque Lakes of Tarns – Lake Red Tarn
(U.K.)</li>
                    <li>Kettle Lakes – Orkney (Scotland)</li>
                    <li>Rock-hollow Lakes – Lakes of Finland
(the land of lakes)</li>
                    <li>Lake due to Moraines – Lake
Windermere (U.K.)</li>
                    <li>Lakes due to deposition of glacial driftsNorth Ireland</li>
                </ul>
            </ol>
</li>
    </ul>
</ol>



