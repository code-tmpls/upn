<div class="subheading"><strong><i>Earthquakes are classified on the basis of
causative factors:</i></strong></div>
<ol>
    <ul>
        <li>Natural earthquakes: They are caused by
endogenetic forces:
    <ol>
        <ul>
            <li>Volcanic earthquake: they are caused
due to volcanic eruptions. E.g. Earthquakes caused by explosion of Krakatau
volcano in 1883 and Etna volcano in
1968.</li>
            <li>Tectonic earthquake: they are caused
due to dislocation of rock blocks during
faulting e.g. 1906 earthquake of California and 1923 earthquake of Sangami
bay, Japan etc.</li>
            <li>Isostatic earthquake: they are triggered
due to sudden disturbance in the isostatic balance at regional scale due to
imbalance in geological processes e.g.
near active mountain building zones.</li>
            <li>Plutonic earthquakes: these are deep
focus earthquakes generally located between 240 and 670 km deep.</li>
        </ul>
    </ol>
</li>
        <li>Artificial earthquakes: They are caused
due to man-made activities like pumping
water and mineral oil underground,
blasting of rocks, nuclear explosion, storage
of huge volume of water in reservoirs etc.
Examples of earthquake due to construction
of huge dams include Koyna earthquake
of 1967 and Hoover dam earthquake of
1936.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Distribution of Earthquakes</i></strong></div>

About 68% of all the earthquakes are
observed in the vast region of the pacific ocean
known as the “ring of fire” and is closely linked
with the region of crustal dislocations and
volcanic eruptions. Chile, California, Alaska,
Japan, Philippines, New Zealand constitute the
ring of fire.
<br>
<br>
Around 21% of the earthquakes occur in the
mid-word mountain belt extending parallel to
the equator from Mexico across Atlantic Ocean,
the Mediterranean Sea from Alpine-Caucasus
ranges to the Caspian, Himalayan Mountains
and the adjoining belts: the earthquakes in India
are at present mainly confined to the Himalayan
regions and its foot hills. They are also felt in the
Ganga valley.

