<h3>Volcanoes And earthquakes</h3>


<div class="subheading"><strong><i>Volcano</i></strong></div>

A volcano is a vent or opening usually
circular in form through which heated materials
consisting of gases, water, liquid lava and
fragments of rocks are ejected from the highly
heated interior to the surface of the earth.
Magma is molten rock within the Earth’s crust.
When magma erupts through the earth’s
surface it is called lava. Lava can be thick and
slow-moving or thin and fast-moving. Rock also
comes from volcanoes in other forms, including
ash (finely powdered rock that looks like dark
smoke coming from the volcano), cinders (bits
of fragmented lava), and pumice (light-weight
rock that is full of air bubbles and is formed in
explosive volcanic eruptions - this type of rock
can float on water).
<br>
<br>
Volcanic eruptions are closely associated with
several interconnected processes such as
<ol>
    <ul>
        <li>The gradual increase in temperature with
increasing depth at a rate of 1°c per 32m
due to heat generated by degeneration of
radioactive elements inside the earth.</li>
        <li>origin of magma because of lowering of
melting point caused by reduction in
pressure of overlying rocks due to fractures
caused by splitting of plates</li>
        <li>origin of gases and water vapour due to
heating of water</li>
        <li>ascent of magma due to pressure from
gases and vapour</li>
        <li>Occurrence of volcanic eruption. These
eruptions are closely associated with plate
boundaries.</li>
    </ul>
</ol>

