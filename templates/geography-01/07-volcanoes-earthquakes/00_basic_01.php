<div class="subheading"><strong><i>Plate tectonics and Volcanoes</i></strong></div>
<ol>
    <ul>
        <li>
        <div class="subheading"><strong><i>Divergent plate boundaries</i></strong></div>    
        
At the mid-oceanic ridges, two tectonic
plates diverge from one another. New oceanic
crust is being formed by hot molten rock slowly
cooling and solidifying. The crust is very thin at
mid-oceanic ridges due to the pull of the tectonic
plates. The release of pressure leads to partial
melting of the mantle causing volcanism and
creating new oceanic crust. Most divergent plate
boundaries are at the bottom of the oceans,
therefore most volcanic activity is submarine,
forming new seafloor. Black smokers or deep
sea vents are an example of this kind of volcanic
activity. Where the mid-oceanic ridge is above
sea-level, volcanic islands are formed, for
example, Iceland.</li>
        <li>
        <div class="subheading"><strong><i>Convergent plate boundaries</i></strong></div>    
        
Subduction zones are places where two
plates, usually an oceanic plate and a continental
plate, collide. In this case, the oceanic plate
subducts or submerges under the continental
plate forming a deep ocean trench just offshore.
Water released from the subducting plate lowers
the melting temperature of the overlying mantle
wedge, creating magma. This magma tends to
be very viscous due to its high silica content so
that it often does not reach the surface and cools
at depth. When it does reach the surface, a
volcano is formed. Typical examples for this kind
of volcano are Mount Etna and the volcanoes
in the Pacific Ring of Fire.</li>
        <li>
        <div class="subheading"><strong><i>Hotspots</i></strong></div>    
        
Hotspots are not usually located on the
edges of tectonic plates, above mantle plumes,
where the convection of the Earth’s mantle
creates a column of hot material that rises until
it reaches the crust, which tends to be thinner
than in other areas of the Earth. The
temperature of the plume causes the crust to melt
and form pipes, which can vent magma. Because
the tectonic plates move whereas the mantle
plume remains in the same place, each volcano
becomes dormant after a while and a new
volcano is then formed as the plate shifts over
the hotspot. The Hawaiian Islands are thought
to be formed in such a manner, as well as the
Snake River Plain, with the Yellowstone Caldera
of the North American plate currently above the
hot spot. Another example is India’s Deccan
plateau which is the result of lava outflow from
the Reunion hotspot.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Topography produced by volcanoes:</i></strong></div>
<ol>
    <ul>
        <li>Cinder or ash cone: They are of low height
and are formed of volcanic dust, ashes and
pyroclastic matter. Its formation takes place
due to accumulation of finer particles
around the volcanoes vent.</li>
        <li>Shield volcanoes: So named for their
broad, shield-like profiles, are formed by
the eruption of low-viscosity lava that can
flow a great distance from a vent, but not
generally explode catastrophically. Since
low-viscosity magma is typically low in
silica, shield volcanoes are more common
in oceanic settings. The Hawaiian volcanic
chain is a series of shield cones, and they
are common in Iceland as well.</li>
        <li>Composite cones: They are formed due to
the accumulation of different layers of
various volcanic materials.</li>
        <li>Parasite cones: When lava comes out of
the minor pipes coming out of the main
central pipe, parasite cones are formed.</li>
        <li>Basic lava cone: It has less quantity of silica
in its lava.</li>
        <li>Acidic lava cone: It has more silica in its
lava.</li>
        <li>Lava domes: These are formed due to
accumulation of solidified lavas around the
volcanic vents.</li>
        <li>Lava plugs: They are formed due to
plugging of volcanic pipes and vents when
volcano becomes extinct.</li>
        <li>Craters: The depression formed at the
mouth of a volcanic vent is called a crater.
When it is filled with water, it becomes a
‘crater lake’ e.g. Lake Lonar in
Maharashtra.</li>
        <li>Calderas: Generally enlarged form of
craters is called caldera. It is formed due to
subsidence of a crater.</li>
        <li>Geysers: They are intermittent hot springs
that from time to time spout steam and hot
water from their craters.</li>
        <li>Fumaroles: It is a vent through which
there is emission of gases and water vapour.</li>
        <li>Cryptodomes: These are formed when
viscous lava forces its way up and causes
a bulge. The 1980 eruption of Mount St.
Helens was an example. Lava under great
pressure forced a bulge in the mountain,
which was unstable and slid down the
north side.</li>
        <li>A supervolcano is a large volcano that
usually has a large caldera and can
potentially produce devastation on an
enormous, sometimes continental, scale.
Such eruptions can cause severe cooling of
global temperatures for many years
afterwards because of the huge volumes
of sulphur and ash erupted. They are the
most dangerous type of volcano. Examples
include Yellowstone Caldera in
Yellowstone National Park and Valles
Caldera in New Mexico (both western
United States), Lake Taupo in New
Zealand, Lake Toba in Sumatra and
Ngorogoro Crater in Tanzania.
<br>
<br>
Supervolcanoes are hard to identify
centuries later, given the enormous areas
they cover. Large igneous provinces are
also considered supervolcanoes because of
the vast amount of basalt lava erupted,
but are non-explosive.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Distribution of volcanoes</i></strong></div>

Volcanoes are unevenly distributed over the
earth and vast areas have no active volcanoes at
all. There are no volcanoes in Australia. In Asia,
they are largely concentrated in circum-pacific
region and Africa has a few of them. Thus, the
pacific belt is truly known as the “ring of fire”
because of the largest number of active volcanoes
along the coasts of America and Asia around
this region. Iceland, Sicily and Japan are the
biggest volcanic islands in the world. Most of the
volcanoes in the world occur along linear belts
or lines of weakness marked by intense folding
and faulting.

<div class="subheading"><strong><i>Earthquake</i></strong></div>

An earthquake is a vibration or oscillation
of the surface of the earth caused by a transient
disturbance of the elastic or gravitational
equilibrium of the rocks at or beneath the surface.
The magnitude or intensity of energy released
by an earthquake is measured by the richter scale.
The place of the origin of an earthquake is called
focus which is hidden inside the earth. The place
on the ground surface which is perpendicular
to the buried focus is called ‘epicentre’. Seismic
waves are recorded by an instrument called
‘seismograph’. Isoseismal lines join places which
experience the earthquake at the same time.

<div class="subheading"><strong><i>Causes of Earthquakes:</i></strong></div>

If seen broadly we can say that earthquakes
are caused due to two major reasons. The first
reason is the eruption of volcanoes, which are
sudden. Volcanoes are seats of inner disturbance
and can affect the plates which are the second
cause of earthquakes. Earthquakes are caused
due to disturbance in the movement of plates,
which again can be caused due to various
reasons like under-crust waves or cracks in the
plates.
<ol>
    <ul>
        <li>
        <div class="subheading"><strong><i>Plate Tectonic Theory</i></strong></div>    
        
The outer layer of the earth is divided into
many sections known as plates, which are
floating on the molten magma beneath the
earth’s crust. Now the movement of these plates
is determined by the convection current in the
molten magma. Therefore after intervals there
are plates that get submerged in the molten
magma and there are plates that rise upwards
and at times even new crust is formed from the
molten magma which in turn forms a new plate
until it connects itself with the already existing
ones. At times these plates and can be pushed
up to form mountains and hills and the
movement is so slow that it is really hard to
comprehend that there is any movement at all.
The movement and the results come out to be
visible suddenly. Now these plates are the bases
on which the continents stand and when these
plates move the continents also move. Most of
the earthquakes occur on the edges of the plates
where a plate is under, on or across another
plate. This movement disrupts the balance and
position of all plates, which leads to tremors,
which are called earthquakes.</li>
        <li>
        <div class="subheading"><strong><i>Volcanic Eruptions</i></strong></div>    
        
When volcanoes erupt it is because the
molten magma under the crust of the earth is
under enormous pressure and to release that
pressure it looks for an opening and exerts
pressure on the earth’s crust and the plate in turn.
A place, which is the seat of an active volcano,
is often prone to earthquakes as well.
Earthquakes are also caused after a volcanic
eruption since the eruption also leads to a
disturbance in the position of plates, which either
move further or resettle and can result into severe
or light tremors.
