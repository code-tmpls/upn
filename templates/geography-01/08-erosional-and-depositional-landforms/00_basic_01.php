<h3>Erosional and Depositional Landforms</h3>

<div class="subheading"><strong><i>Fluvial Landforms</i></strong></div>

Rivers are a sizable stream of freshwater
flowing through a natural channel in the land.
Rivers are among the most powerful natural
forces in shaping the earth’s surface. In draining
the land of surplus water, rivers wear down
mountains, plateaus, and other high landforms.
In a never-ending process, eroded material is
carried by rivers. Some is deposited to form
floodplains in the valleys, some forms deltas at
the rivers’ mouths, and some is deposited in the
sea.
<ol>
    <ul>
        <li>
        <div class="subheading"><strong><i>Erosional Landforms</i></strong></div>    
        <ol>
            <ul>
                <li>
                <div class="subheading"><strong><i>Valleys</i></strong></div>    
                
Valleys start as small and narrow rills; the
rills will gradually develop into long and wide
gullies; the gullies will further deepen, widen
and lengthen to give rise to valleys. Depending
upon dimensions and shape, many types of
valleys like V-shaped valley, gorge, canyon, etc.
forms.
<br>
<br>
A gorge is a deep valley with very steep to
straight sides and a canyon is characterised by
steep step-like side slopes and may be as deep
as a gorge. A gorge is almost equal in width at
its top as well as its bottom. In contrast, a canyon
is wider at its top than at its bottom. In fact, a
canyon is a variant of gorge. Valley types
depend upon the type and structure of rocks in
which they form. For example, canyons
commonly form in horizontal bedded
sedimentary rocks and gorges form in hard
rocks.</li>
                <li>
                <div class="subheading"><strong><i>Potholes and Plunge Pools</i></strong></div>    
                
Over the rocky beds of hill-streams more or
less circular depressions called potholes form
because of stream erosion aided by the abrasion
of rock fragments. Once a small and shallow
depression forms, pebbles and boulders get
collected in those depressions and get rotated
by flowing water and consequently the
depressions grow in dimensions. Such large and
deep holes at the base of waterfalls are called
plunge pools.</li>
                <li>
                <div class="subheading"><strong><i>Meanders</i></strong></div>    
                
River meanders refers to the bends of
longitudinal cources of the rivers. The shape of
the meander is usually semi circular. It is
governed by lithological characteristics,
topographic characteristics, annual
precipitation, cycle of erosion etc.</li>
            </ul>
        </ol>
</li>
        <li>
        <div class="subheading"><strong><i>Depositional Landforms</i></strong></div>    
        <ol>
            <ul>
                <li>
                <div class="subheading"><strong><i>Alluvial Fans</i></strong></div>  
                
Alluvial fans are fan-shaped deposits of
water-transported material (alluvium). They
typically form at the base of topographic features
where there is a marked break in slope.
Consequently, alluvial fans tend to be coarsegrained, especially at their mouths. At their
edges, however, they can be relatively finegrained.</li>
                <li>
                <div class="subheading"><strong><i>Delta</i></strong></div> 
                    
Deltas are built from primarily river-borne
sediment. It forms when the amount of sediment
delivered at the mouth of a river exceeds the
amount removed by waves and tidal currents.</li>
                <li>
                <div class="subheading"><strong><i>Natural leeves</i></strong></div>  
                
The narrow belt of ridges of low height built
by the deposition of sediments by the spill water
of the stream on the either bank is called natural
leeve.</li>
            </ul>
        </ol>
 </li>
    </ul>
</ol>

<div class="subheading"><strong><i>Karst Topography</i></strong></div> 

Karst is a distinctive topography in which
the landscape is largely shaped by the dissolving
action of water on carbonate bedrock (usually
limestone, dolomite, or marble).
This geological process, occurring over many
thousands of years, results in unusual surface
and subsurface features ranging from sinkholes,
vertical shafts, disappearing streams, and
springs, to complex underground drainage
systems and caves.
<br>
<br>
The process of karst formation involves what
is referred to as “the carbon dioxide cascade.”
As rain falls through the atmosphere, it picks
up CO2 which dissolves in the droplets.
Once the rain hits the ground, it percolates
through the soil and picks up more CO2
 to form
a weak solution of carbonic acid:
<b>H2O+CO2=H2CO3.</b>
<br>
<br>
The infiltrating water naturally exploits any
cracks or crevices in the rock. Over long periods,
with a continuous supply of CO2 - enriched
water, carbonate bedrock begins to dissolve.
Openings in the bedrock increase in size and
an underground drainage system begins to
develop, allowing more water to pass, further
accelerating the formation of karst. Eventually
this leads to the development of subsurface caves.
<ol>
    <ul>
        <li>
        <div class="subheading"><strong><i>Erosional Landforms</i></strong></div>    
        <ol>
            <ul>
                <li>
                <div class="subheading"><strong><i>Pools, Sinkholes, Lapies and Limestone
Pavements</i></strong></div>    
                
Small to medium sized round to subrounded shallow depressions called swallow
holes form on the surface of limestones through
solution. Sinkholes are very common in
limestone/karst areas. A sinkhole is an opening
more or less circular at the top and funnelshapped towards the bottom with sizes varying
in area from a few sq. m to a hectare and with
depth from a less than half a metre to thirty
metres or more. Some of these form solely
through solution action (solution sinks) and
others might startas solution forms first and if
the bottom of a sinkhole forms the roof of a void
or cave underground, it might collapse leaving
a large hole opening into a cave or a void below
(collapse sinks). Quite often, sinkholes are
covered up with soil mantle and appear as
shallow water pools. The term doline is
sometimes used to refer the collapse sinks. When
sink holes and dolines join together because of
slumping of materials along their margins or due
to roof collapse of caves, long, narrow to wide
trenches called valley sinks or Uvalas form.
Gradually, most of the surface of the limestone
is eaten away by these pits and trenches, leaving
it extremely irregular with a maze of points,
grooves and ridges or lapies.</li>
                <li>
                <div class="subheading"><strong><i>Caves</i></strong></div>    
                
In areas where there are alternating beds of
rocks (shales, sandstones, quartzites) with
limestones or dolomites in between or in areas
where limestones are dense, massive and
occurring as thick beds, cave formation is
prominent. Water percolates down either
through the materials or through cracks and
joints and moves horizontally along bedding
planes. It is along these bedding planes that the
limestone dissolves and long and narrow to wide
gaps called caves result. There can be a maze of
caves at different elevations depending upon the
limestone beds and intervening rocks. Caves
normally have an opening through which cave
streams are discharged. Caves having openings
at both the ends are called tunnels.</li>
            </ul>
        </ol>
</li>
        <li>
        <div class="subheading"><strong><i>Depositional Landforms</i></strong></div>    
        
Many depositional forms develop within the
limestone caves. The chief chemical in limestone
is calcium carbonate which is easily soluble in
carbonated water (carbon dioxide absorbed
rainwater). This calcium carbonate is deposited
when the water carrying it in solution evaporates
or loses its carbon dioxide as it trickles over rough
rock surfaces.
<ol>
    <ul>
        <li>Stalactites, Stalagmites and Pillars
Stalactites hang as icicles of different
diameters. Normally they are broad at their bases
and taper towards the free ends showing up in
a variety of forms. Stalagmites rise up from the
floor of the caves. In fact, stalagmites form due
to dripping water from the surface or through
the thin pipe, of the stalactite, immediately below
it
Stalagmites may take the shape of a column,
a disc, with either a smooth, rounded bulging
end or a miniature crater like depression. The
stalagmite and stalactites eventually fuse to give
rise to columns and pillars of different diameters.
Coastal Geomorphology
Coastal Geomorphology encompasses the
study of coastal processes and the evolution of
landforms.
<br>
<br>
The formation and development of cliffs,
beaches, salt marshes, reefs and other coastal
landforms reflect the pressures and forces acting
upon a coastline, both natural and manmade.</li>
    </ul>
</ol>
</li>
        <li>
        <div class="subheading"><strong><i>Erosional Coastal Landforms</i></strong></div>    
        
Some of the most spectacular scenery is
found along coastlines and produced by the
effects of wave erosion. Wave erosion undercuts
steep shorelines creating coastal cliffs.
<ol>
    <ul>
        <li>Sea cliff is a vertical precipice created
by waves crashing directly on a steeply inclined
slope. Hydraulic action, abrasion, and chemical
solution all work to cut a notch at the high water
level near the base of the cliff. Constant
undercutting and erosion causes the cliffs to
retreat landward.</li>
        <li>Sea caves form along lines of weakness
in cohesive but well-jointed bedrock. Sea caves
are prominent headlands where wave refraction
attacks the shore.
<br>
<br>
A sea arch forms when sea caves merge
from opposite sides of a headland. If the arch
collapses, a pillar of rock remains behind as a
sea stack.
<br>
<br>
Seaward of the retreating cliffs, wave
erosion forms a broad erosional platform called
a wave-cut bench or wave-cut platform. After
the constant grinding and battering, eroded
material is transported to adjacent bays to
become beaches or seaward coming to rest as a
wave-built terrace. If tectonic forces raise the
bench above the water level a marine terrace
forms. Some shorelines have several marine
terraces creating during various episodes of
uplift.</li>
    </ul>
</ol>
 </li>
        <li>
        <div class="subheading"><strong><i>Depositional Landforms</i></strong></div>    
        <ol>
            <ul>
                <li>
                <div class="subheading"><strong><i>Beaches and Dunes</i></strong></div>  
                    
Beaches are characteristic of shorelines that
are dominated by deposition, but may occur as
patches along even the rugged shores. Most of
the sediment making up the beaches comes from
land carried by the streams and rivers or from
wave erosion. Beaches are temporary features.
The sandy beach which appears so permanent
may be reduced to a very narrow strip of coarse
pebbles in some other season.
Most of the beaches are made up of sand
sized materials. Beaches called shingle beaches
contain excessively small pebbles and even
cobbles.</li>
                <li>
                <div class="subheading"><strong><i>Bars, Barriers and Spits</i></strong></div>    
                
A ridge of sand and shingle formed in the
sea in the off-shore zone (from the position of
low tide waterline to seaward) lying
approximately parallel to the coast is called an
off-shore bar.
<br>
<br>
An off-shore bar which is exposed due to
further addition of sand is termed a barrier bar.
The off-shore bars and barriers commonly form
across the mouth of a river or at the entrance of
a bay. Sometimes such barrier bars get keyed up
to one end of the bay when they are called spits
(Figure 7.15). Spits may also develop attached
to headlands/hills. The barriers, bars and spits
at the mouth of the bay gradually extend leaving
only a small opening of the bay into the sea and
the bay will eventually develop into a lagoon.
The lagoons get filled up gradually by sediment
coming from the land or from the beach itself
(aided by wind) and a broad and wide coastal
plain may develop replacing a lagoon.</li>
            </ul>
        </ol>
</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Glaciated Topography</i></strong></div>

Glaciers have played an important role in
the shaping of landscapes in the middle and high
latitudes and in alpine environments. Their
ability to erode soil and rock, transport
sediment, and deposit sediment is
extraordinary. During the last glacial period
more than 50 million square kilometers of land
surface were geomorphically influenced by the
presence of glaciers.
<ol>
    <ul>
        <li>
        <div class="subheading"><strong><i>Erosional Landforms</i></strong></div>    
        
Glacial erosion consists of two processes: (i)
plucking or the tearing away of blocks of rock
which have become frozen into the base and
sides of a glacier, and (ii) abrasion or the wearing
away of rocks beneath a glacier by the scouring
action of the rocks embedded in the glacier.
<ol>
    <ul>
        <li>
        <div class="subheading"><strong><i>Cirque</i></strong></div>    
        
Cirques are the most common of landforms
in glaciated mountains. The cirques quite often
are found at the heads of glacial valleys. The
accumulated ice cuts these cirques while moving
down the mountain tops. They are deep, long
and wide troughs or basins with very steep
concave to vertically dropping high walls at its
head as well as sides. A lake of water can be
seen quite often within the cirques after the
glacier disappears. Such lakes are called cirque
or tarn lakes. There can be two or more cirques
one leading into another down below in a
stepped sequence.</li>
        <li>
        <div class="subheading"><strong><i>Horns and Serrated Ridges</i></strong></div>    
        
Horns form through head ward erosion of
the cirque walls. If three or more radiating
glaciers cut headward until their cirques meet,
high, sharp pointed and steep sided peaks called
horns form. The divides between cirque side
walls or head walls get narrow because of
progressive erosion and turn into serrated or sawtoothed ridges sometimes referred to as arêtes
with very sharp crest and a zig-zag outline.</li>
        <li>
        <div class="subheading"><strong><i>Glacial stairways</i></strong></div>   
            
The advancing ice of glaciers carves out giant
stairways through the process of abrasion and
plucking of step faults coming across the path
of moving glaciers</li>
    </ul>
</ol>
</li>
        <li>
        <div class="subheading"><strong><i>Deposional Landforms</i></strong></div>
        <ol>
            <ul>
                <li>
                <div class="subheading"><strong><i>Moraines</i></strong></div>
                    
A valley glacier carries a large amount of
rock waste called moraine. The moraine forming
along the sides of a glacier is called lateral
moraine; that along the front of a glacier is called
terminal moraine; that at the bottom of a glacier
is the ground moraine. When two glaciers join
together, their inner lateral moraines coalesce to
give a medial moraine. Terminal moraine
material is carried down-valley by the melt
waters issuing from the glacier’s snout (front)
and is deposited as a layer called an outwash
plain. One of the most conspicuous features of
lowlands which have been glaciated by ice sheets
is the widespread morainic deposits. Because of
the numerous boulders in the clay these are called
boulder clay deposits.</li>
                <li>
                <div class="subheading"><strong><i>Drumlins</i></strong></div>
                
The swarms of rounded hummocks resulting
from the deposition of glacial till are called
drumlins. They look like inverted boat or spoon.</li>
                <li>
                <div class="subheading"><strong><i>Eskers</i></strong></div>
                
When glaciers melt in summer, the water
flows on the surface of the ice or seeps down
along the margins or even moves through holes
in the ice. These waters accumulate beneath the
glacier and flow like streams in a channel
beneath the ice. Such streams flow over the
ground (not in a valley cut in the ground) with
ice forming its banks. Very coarse materials like
boulders and blocks along with some minor
fractions of rock debris carried into this stream
settle in the valley of ice beneath the glacier and
after the ice melts can be found as a sinuous ridge
called esker.</li>
                <li>
                <div class="subheading"><strong><i>Outwash fan</i></strong></div>    
                
An outwash fan is a fan-shaped body of
sediments deposited by braided streams from
a melting glacier. Sediment locked within the
ice of the glacier, gets transported by the streams
of meltwater, and deposits on the outwash plain,
at the terminus of the glacier. The outwash,
the sediment transported and deposited by the
melt water and that makes up the fan, is usually
poorly sorted due to the short distance traveled before being deposited.</li>
                <li></li>
                <li></li>
            </ul>
        </ol>
</li>
    </ul>
