<h3>Drainage System and Patterns</h3>


The origin and evolution of any drainage
system in a particular region are determined and
controlled by two factors- (a) Nature of original
surface and slope (b) Geological structure.
Streams or drainage systems are divided in
two broad categoriesI. Sequent Drainage System
<ol>
    <ul>
        <li><b>Consequent Stream:</b> The upland forms
the catchment area of rivers, where precipitation is heaviest and where there is
a slope down which the run off can
flow. The initial stream that exists as a
consequence of the slope is called the
consequent stream. Most of the streams,
draining the coastal plains of India are
of this type.</li>
        <li><b>Subsequent Stream:</b> When the master
consequent stream is joined by its tributary at right angles, it is called subsequent stream. For example, the river Son,
a tributary of the Ganga is a subsequent
stream.</li>
        <li><b>Obsequent Streams:</b> The stream which
flows following the direction of the slope
opposite to master consequent stream.
For example the Mahabharata Range of
Lesser Himalayas has originated several
streams from its northern slope which
join the subsequent stream from the direction opposite to the consequent stream
e.g. Sun Kosi runs west to east as
obsequent stream and the master consequent streams like Ganga and Yamuna
flow in the opposite direction.</li>
        <li><b>Resequent Streams:</b> Such streams follow the direction of master consequent
stream and meet the subsequent streams
at right angles.</li>
    </ul>
</ol>


<b>Insequent Drainage System:</b> The streams
which do not follow the regional slopes and
drain across the geological structures are
called insequent or inconsequent streams.
<ol>
    <ul>
        <li>Antecedent Drainage: The stream
which originated before the upliftment
of the surface on which they flow. For
example, Indus, Sutlej, Alaknanda and
Brahmaputra are antecedent rivers as
they originated before the upliftment of
Himalayan ranges.</li>
        <li>Superimposed Drainage: It is formed
when the nature and characteristics of
the valley and the flow direction of a
consequent stream, developed on the
upper geological formation and structure, are superimposed on the lower
geological formation of entirely different characteristics. For example the river
Subarnarekha is superimposed on
Dalma Hills of Chhotanagpur Plateau
of Jharkhand.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Drainage Patterns</i></strong></div>
<ol>
    <ul>
        <li>Dendritic Drainage: A drainage pattern
consisting of a single main stream with
tributaries, resembling the branches of a
tree. This pattern develops perfectly where
the underlying rocks are of a uniform type
and the structures are simple.</li>
        <li>Trellis Drainage: It is a rectangular
pattern of river channels. It may develop
where a slope is crossed at right angles by
the strike of alternating hard and soft rock
strata. Long streams develop along the soft
rock strata and the short streams follow
the slope.</li>
        <li>Radial Drainage: Here the streams radiate
from a central peak or upland mass in all
directions. Dome structures commonly
develop radial drainage as in the English
Lake District of England. The entire
drainage network of Sri Lanka, Hazaribagh
Plateau, Panchet Hills and Maikal Range
are of such type.</li>
        <li>Rectangular Drainage: A pattern of
drainage consisting of two main directions
of flow at right angles to one another. This
pattern is common where the streams
follow the fault lines.</li>
        <li>Annular Drainage: Here streams follow
roughly in circular pattern. Such patterns
are usually produced on domed structures
where the rivers follow the outcrops of
weaker beds of rock in an alternating band
of hard and soft beds.</li>
        <li>Parallel Drainage: A pattern in which the
main streams and tributaries follow
virtually parallel courses. This develops
where there is a strong structural control
in one direction or where strata are gently
dipping.</li>
        <li>Barbed Drainage: In this pattern the
tributaries flow in opposite direction to their
master streams. The tributaries join their
master streams in a hook-shaped bend.
Such pattern is generally developed due to
river capture.</li>
        <li>Centripetal Drainage: When the streams
converge at a point, which is generally a
depression or a basin they form centripetal
or inland drainage pattern.</li>
        <li>Herringbone Drainage: When the
consequent streams are developed in the
longitudinal parallel valley while the
tributaries, after originating from the hill
slopes of the bordering parallel ridges, join
the longitudinal consequents almost at right
angle, it is known as herringbone pattern
or rib pattern. Jhelum River in the Vale of
Kashmir receives many tributaries from
both the sides, following the rib pattern.</li>
    </ul>
</ol>




