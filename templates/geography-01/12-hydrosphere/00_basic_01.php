<div class="subheading"><strong><i>Profile of Ocean Floor</i></strong></div>

The ocean basins are in many ways similar
to the land surface. There are submarine ridges,
plateau, canyons, plains and trenches. The great
variety of relief is largely due to the interaction
of tectonic, volcanic, erosional and depositional
processes. In general the ocean floor can be
divided into four major divisions - the
continental shelf, the continental slope, the
continental rise and the Abyssal Plain.
<br>
<br>
<b>Continental Shelf:</b> It is the land portion,
submerged under sea water and is a transitional
zone between the land and the actual sea bottom.
<ol>
    <ul>
        <li>The isobath of 100 fathoms (around 200m)
demarcates the continental shelf.</li>
        <li>The average width is about 70 km. and the
mean slope is less than 1°.</li>
        <li>About 7.5 percent of the total ocean area is
covered by it.</li>
        <li>It is almost absent in the eastern Pacific
ocean, especially in South America.</li>
        <li>At the eastern coast of USA it is about 120
km wide and also very wide on the eastern
coast of India.</li>
        <li>Individually it covers about 13.3% part of
Atlantic Ocean, 5.7% of Pacific Ocean and
4.2% of Indian Ocean.</li>
        <li>It is the area of terrigenous deposits i.e.
sediments are derived from land.</li>
        <li>They provide the richest fishing ground in
the world.</li>
        <li>About 20% of world petroleum and natural
gas are found here.</li>
    </ul>
</ol>

<b>Continental Slope:</b> It lies at the edge of the
continental shelf, generally up to the depth of
2000 fathoms (3660 m) from the mean sea level.
<ol>
    <ul>
        <li>It has a steep slope with the angle of slope
varying from 2° to 5°.</li>
        <li>It covers about 8.5% of the total oceanic
area and individually about 12.4% of the
Atlantic Ocean, 7.1% of the Pacific Ocean
and 6.5% of the Indian Ocean.</li>
        <li>The continental blocks are supposed to end
at the site of continental slope.</li>
        <li>The continental slope along the many coasts
of the world is furrowed by deep canyons
like trenches terminating as fan-shaped
deposits at the base.</li>
    </ul>
</ol>


<b>Continental Rise:</b> The place where the
continental slopes end, the gentle sloping
continental rise begins. The average slope is
between 0.5° and 1° & its general relief is low. With
increasing depth the Continental Rise becomes
virtually flat and it merges with abyssal plain.
<br>
<br>
<b>Abyssal/Deep Sea Plains:</b> Beyond
Continental Rise, it is found at the depth of 3000
to 6000 m. They cover about 40% of the total
ocean floor and generally are bounded by hills
(ridges) on the seaward side. They are almost
flat with a gradient less than 1:100. The irregular
topography of the abyssal plain are buried
forming relatively flat areas due to large supply
of sediments.
<br>
<br>
<b>Submarine Ridges:</b> These are the mountain
ranges on the ocean floor and some of them are
the largest mountain system on the earth.
<ol>
    <ul>
        <li>A large number of submarine ridges are
placed centrally in the oceans.</li>
        <li>At some places their summits may rise
above the sea level forming islands.</li>
    </ul>
</ol>


<b>Abyssal Hills:</b> A deep sea floor also
contains numerous isolated abyssal hills, seamounts and guyots.
<ol>
    <ul>
        <li>A submarine mountain peak rising more
than 1000 m above ocean floor are known
as "seamount".</li>
        <li>Flat topped seamounts are known as
"Guyots".</li>
        <li>All the above features are volcanic in origin,
very common in Pacific Ocean.</li>
    </ul>
</ol>

<b>Submarine Trenches or Deeps:</b> A long
narrow and steep sided depression on the ocean
floor is called trench. These are the deepest part
of the ocean.
<ol>
    <ul>
        <li>They lie along the fringes of the deep sea
plains and usually run parallel to the
bordering fold mountains or the island
claims.</li>
        <li>They are believed to have resulted from
faulting or down folds of the earth crust
and so tectonic in origin.</li>
        <li>They are most common in the Pacific Ocean
like "Mariana Trench" off the Guam Island
Chain which is deepest (11 km) in the
world.</li>
    </ul>
</ol>

<b>Submarine Canyons:</b> These are the deep
gorges on the ocean floor and are restricted to
the continental shelves, slopes and rises.
<ol>
    <ul>
        <li>Some canyons begin at the edge of the
continental shelf and extend down the
continental slope. For example -
"Oceanographer Canyon" near New
England.</li>
        <li>Some canyons have dendritic appearance
like off the east coast of Southern California.</li>
        <li>Some begins at the river mouth and extends
over the continental shelf as "Zaire",
"Mississippi" and "Indus" canyons.</li>
    </ul>
</ol>

<b>Bank, Shoal & Reef:</b> Banks are almost flat
topped elevation located in the continental
margin. Shoals are detached elevation with
shallow depth. Reefs are the mound or rocky
elevation like ridges made of organic deposit.
<ol>
    <ul>
        <li>The depth of water is relatively small in
banks but adequate for navigation.</li>
        <li>These are sites of rich fishing e.g. "Dogger
Bank" (North Sea), "Grand Bank" (off
Newfoundland).</li>
        <li>Shoals are dangerous for navigation as they
are only 18-20 m below the sea level.</li>
        <li>'Great Barrier Reef' (off Queens land,
Australia) is the largest reef in the world.</li>
    </ul>
</ol>


