    <div class="subheading"><strong><i>Temperature</i></strong></div>

Horizontal Distribution of Temperature: The
mean annual temperature of the surface water of
the oceans is 25°C varying from - 5°C to over 33°C.
<ol>
    <ul>
        <li>The temperature decreases as we move
away from the equator. The average
temperature of ocean water is 26°C at the
equator, 23°C at latitude 20°.</li>
        <li>The coldest month in the ocean is February
and the warmest month is August in the
Northern Hemisphere and reverse in the
Southern Hemisphere.</li>
        <li>The highest temperature of ocean water is
found in enclosed or partially enclosed seas
in the equatorial areas e.g. a temperature
of 38°C has been recorded in Red Sea
though the average temperature in summer
is only 29°C.</li>
        <li>The temperature of the warm current is
higher than the surrounding areas. Thus
Gulf Stream does not allow the Norwegian
coast to freeze even in winter.</li>
        <li>The prevailing winds deflect the warm and
cold currents and cause change in
temperature of the ocean water. So, in the
tropical zone the western section of the
oceans are warmer than the eastern section
owing to the influence of trade winds. In
the Temperate zone the westerlies makes
the eastern section warmer than the
western section.</li>
        <li>The temperature decreases as we move
away from equator.</li>
    </ul>
</ol>

<b>Vertical distribution of temperature:</b>
Though the sea temperature decreases with 
increasing depth, the rate of decrease is not uniform. 
The change in sea temperature below 200 m is negligible.

<div class="subheading"><strong><i>Salinity</i></strong></div>

The salinity of the Ocean means the
degree of saltiness of the oceans. The average
salinity of the oceans is 35.3% i.e. about 35
parts of salt in 1,000 parts of water. In the
Baltic Sea, where there is much dilution by
fresh water and melting ice, the salinity is
much lower only about 4%. Very high salinity
is recorded in inland seas and lakes. Lake van
in Turkey records the highest salinity of 330%.
Red Sea (240%), Dead sea (238%), Great Salt
Lake with (220%) are other areas of high
salinity.
<br>
<br>
The variation of salinity in the various seas
and oceans is affected by:
<ol>
    <ul>
        <li>The rate of evaporation.</li>
        <li>The amount of fresh water added by
precipitation, streams and icebergs; and</li>
        <li>The degree of water mixing by currents.
            <ol>
                <ul>
                    <li>The origin of salinity is attributed to
erosion of earth's crust by dissolving
action of running water which causes
erosion in the oceanic crust and volcanic
ash which contains minerals like
Calcium, Boron, Iodine, etc.</li>
                    <li>Salinity is excessive in region of high
temperature, strong winds and less rain.
For example, it is lower in the equatorial
region due to high relative humidity.</li>
                    <li>Influx of fresh water by rivers reduces the
salinity and so there is less salinity near
the mouths of rivers like Amazon, Congo,
Niger, Ganga etc.</li>
                    <li>In spite of high temperature salinity is less
is equatorial region because of his rainfall.
Around the Poles there is a belt of low
salinity because of addition of fresh water
in the form of icebergs and excessive
snow falls.</li>
                </ul>
            </ol>
 </li>
    </ul>
</ol>

