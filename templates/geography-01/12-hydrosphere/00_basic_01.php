<h3>Hydrosphere</h3>


All the water of the earth including the oceans,
lakes, rivers, ice sheets and the water in the
atmosphere is called hydrosphere and it covers
about 71% of the earth's surface. The ocean
predominates over land areas in the S-Hemisphere
far more than that in the N-Hemisphere.

<div class="subheading"><strong><i>Major Oceans</i></strong></div>

<b>The Pacific Ocean:</b> It is the largest and deepest
ocean covering one third of the globe. Its average
depth is 4200 m. The deepest parts are the
Philippine Trench about 10,380 m. and the Marina
Trench about 10,800 m. The Pacific -Ocean has a
string of volcanoes along the coastal margins of
the conti-nents known as 'The Ring of Fire'.
<br>
<br>
<b>The Atlantic Ocean:</b> Though the Atlantic is
smaller than the Pacific, its total coastline is more
than that of the Pacific and the Indian Ocean
combined. There is a long submarine ridge running north to south in the middle of Atlantic. It
is the greatest mountain chain in the world
(16,000 km long). It is known as the Dolphin
Ridge in the North Atlantic and the Challenger
Ridge in the South Atlantic. There are also continental islands such as the British Isles, Newfoundland, the West Indies etc. The Atlantic is
the greatest commercial highway of the world.
<br>
<br>
<b>Indian Ocean:</b> It is small in size but has an
average depth of 4,000 m. The two great bays
on either side of the peninsula of India, namely
the Bay of Bengal and the Arabian Sea belong to
the India Ocean. The Indian Ocean is dotted with
thousands of small islands some of which are of
coral formation, e.g. the Maldives and
Lakshadweep islands, while other like the
Mauritius and the Reunions are volcanic. Sri
Lanka and Malagasy are continental islands.
<br>
<br>
<b>Arctic Ocean:</b> It is found around the North
Pole. It covers only one thirtieth of the sea area.
It is almost completely covered with ice to a
depth of about 3 m.
<br>
<br>
<b>Antarctic Ocean:</b> The remaining area of the
sea is included in the Antarctic Ocean surrounding the Antarctic Continent.

