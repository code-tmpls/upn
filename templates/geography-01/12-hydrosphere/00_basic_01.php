<div class="subheading"><strong><i>Coral reefs & Atolls</i></strong></div>

Corals are a kind of calcareous rock chiefly
made of the skeletons of minute sea organisms
called 'polyps'. Coral reefs and atolls are formed
due to accumulation and compaction of
skeletons of these lime secreting organisms.
Conditions for Growth of Coral Reefs
<ol>
    <ul>
        <li>The coral and the associated organisms and
algae which are the most common reef
builders are con-fined to the tropical belt.
The water temperature must not fall below
20°C and not exceed 35°C; the most
favourable is 23°C to 25°C.</li>
        <li>Corals can live only in saline water, and
for their proper growth the average salinity
should be between 27 to 40%.</li>
        <li>For growth of coral the depth of the water
should not exceed 200m. Most vigorous
growth is confined to shallow water less
than 50 m. deep.</li>
        <li>Corals also require sediment-free, clean
water which is disturbed by ocean waves
and currents is beneficial for the corals.</li>
        <li>In the open seas it is necessary to have
platforms which may act as foundations
for the corals. These platforms should not
be deeper than 90 m.</li>
    </ul>
</ol>



The coral reefs are classified on the basis of
nature, shape and mode of occurrence into (i)
Fringing reef (ii) Barrier reef (iii) Atoll.
Fringing Reef: Coral reefs that develop
along the continental margins or along the
islands are called fringing reefs. The seaward
slope is steep and vertical while the landward
slope is gentle. Such reefs are found near
Rameshwaram in the Gulf of Mannar.
<br>
<br>
Occasionally the fringing reef is separated from
the shore by shallow lagoon known as "Boat
Channel" as found in Madagascar and Red Sea.
Example of fringing reefs: South Florida reef,
Mehetia Island, Sakau Island in New Hebrides.
Barrier Reef: They are the largest, most
extensive, highest and widest reefs of all types
of coral reefs. They are formed off the coastal
platforms and parallel to them. There is an
extensive but shallow lagoon between the coastal
land and the barrier reef. Generally barrier reefs
encircle islands in an irregular and broken ring.
The Great Barrier Reef of Australia is the largest
barrier reef in the world.
<br>
<br>
<b>Atoll:</b> A ring of narrow growing corals of
horse shoe shape and crowned with palm trees
is called an atoll. It is generally found around an
island or in an elliptical form on a submarine
platform. There is a lagoon in the middle of a
coral ring. They are more common in Pacific
Ocean. The circular ring is broken at few places
to allow the free flow of water. The depth of the
lagoon is only a few metres with sand and
limestone debris at the bottom. Example of Atolls:
Fiji Atoll, Trunt Atoll of W-Carolinas, Suvadivo
in Maldives, Funafoothis Atoll of Ellice.

<div class="subheading"><strong><i>Geographical Distribution of Coral Reefs</i></strong></div>
<ol>
    <ul>
        <li>Coral reefs are limited to the tropical seas
and are found almost entirely between
latitudes 30°N and 25°S.</li>
        <li>Rich growth of reefs is found off the eastern
coasts of North America and Australia.</li>
        <li>On coasts where the rivers bring large
quantities of sediments from the land, corals
are not found e.g. coasts of South America.</li>
        <li>The coral reefs are most common in the
Pacific and the Indian Ocean, due to their
shallow, warm and clean water.</li>
        <li>The most important area of coral reef
growth lies in the seas off the east coast of
Australia and in the Philippines.</li>
        <li>Only a few coral reefs lie outside the IndoPacific tropical area, those of Gulf of Mexico
and the Caribbean Sea have some reefs.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>El Nino and La Nina</i></strong></div>

El Nino is a warm sub-surface current in the
Pacific Ocean off the Peruvian coast. El Nino
literally means 'child of the Christ'. It is a
destructive weather system pushed into action
by the warming of the cold ocean current in the
east pacific. El Nino's destructive capacity peaks
by late October or November, when it starts to
cool down and is called La-Nina or literally 'The
girl'. El Nino affects the monsoon in India. An El
Nino circulation in the winter suggests a strong
walker circulation in the following summer and
consequently a weak monsoon. However
scientists are still skeptical about its relation with
monsoon. Recently its link with the fire in
Indonesia has been subjected to much debate.

<div class="subheading"><strong><i>Ocean currents</i></strong></div>

The regular movements of water from one
part of the ocean to another are called "Ocean
Currents". They are mainly caused by the
difference in density of sea water due to
variations in temperature and salinity. The
prevailing winds push them onwards. The
position of the land masses and the shape and
depth of the ocean basins also have some
influence. Main causes of ocean currents include
- winds, differences in density owing to
variations in temperature and salinity. Ocean
currents may be cold or warm.
<ol>
    <ul>
        <li>
        <div class="subheading"><strong><i>Atlantic Ocean Current</i></strong></div>    
        <ol>
            <ul>
                <li>North Equatorial Current: Attributed to
NE-Trade winds, it starts from west coast
of Africa where upwelling of cold water
takes place and moves westward between
5° and 20° N as warm current. A branch
of it is diverted into Caribbean Sea as
"Antilles Current."</li>
                <li>South Equatorial Current: Flows south of
the equator between 0° and 12° latitude in
between the coast of Africa and S.
America. This warm current is virtually the
