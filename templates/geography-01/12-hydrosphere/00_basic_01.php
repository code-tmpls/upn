<div class="subheading"><strong><i>Tides</i></strong></div>

The alternative rise and fall of the level of
the Sea, approximately twice a day, caused by
the gravitational pull of the moon and the Sun
is called tide. The gravitational attraction of the
moon is twice as powerful as that of the Sun.
The moon despite being much smaller in size than
the Sun is relatively very close to the earth and
is thus able to attract more than the Sun.
High Tide: When the intensity of the wave
is great, the waves are of considerable amplitude
and so the sea water comes over the coast to some
extent. Such waves constitute a high tide.
Low Tide: When the height of the wave is
not much, sea water recedes from the shore. Such
waves constitute a low tide.
<br>
<br>
<b>Ebb Tide:</b> The tide between a high tide and
a low tide is known as ebb tide.
<br>
<br>
<b>Spring Tide:</b> When the amplitude difference
of the tide in a day is at maximum, the tide is
called spring tide. It occurs on new moon and
full moon days. High tides are very high and low
tides very low on those days.
<br>
<br>
<b>Neap Tide:</b> When the amplitude of the tide
is minimum. It occurs on the first quarter and last
quarter of the moon. High tides are comparatively
low and low tides comparatively high.
<br>
<br>
<b>Tidal Bores:</b> When a tidal wave meets a
tidal river or estuary, a tidal bore is formed.
Where the outgoing river currents are strong and
the tidal river rather shallow and tunnel shaped,
the rapidly rising high water advances up stream
like a high vertical wall, known as tidal bore.
<ol>
    <ul>
        <li>Bore occurs at river mouths that face the
direction of tidal surge and where tidal
range is large. Rivers like Amazon,
Hooghly, Colorado, Yangtze are
characterized by tidal bores.</li>
        <li>Although tides occur twice a day, their
interval is not exactly 12 hours; actually
it is of 12 hours and 25 minutes. This is
due to the revolution of the moon and
the rotation of the earth.</li>
        <li>Generally tides occur twice a day. But
Southampton, along the southern coast of
England experiences tides four times a day
because the tidal water comes through the
English Channel and through the North
Sea at different intervals.</li>
        <li>Tides in the area of the sea, known as
"Gulf Tides" also causes the horizontal
movement of water of the sea known as
"Tidal Current".</li>
        <li>Monthly tides occur due to revolution of
moon and its position at perigee and
apogee.</li>
        <li>Equinoctial Spring tides recur at an
interval of 6 months, due to revolution
of the earth around the sun and sun's
varying declination.</li>
        <li>Yearly tides recur due to revolution of the
earth and its position at perihelion and
aphelion.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Importance of tides</i></strong></div>

<ol>
    <ul>
        <li>Tides make some of the rivers navigable for
ocean going vessels e.g., Kolkata, London etc.</li>
        <li>Tides clear the sediments brought by rivers
and thus retard the process of delta
formation.</li>
        <li>Tides are agents of distribution; biologically
they distribute and redistribute the
plankton and nektons along with coastal
water which helps in fishing industries.</li>
        <li>The tidal force may be used as the source of
electricity e.g. In France, Japan, India etc.</li>
    </ul>
</ol>


