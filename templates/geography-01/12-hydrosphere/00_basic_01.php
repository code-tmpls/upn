</ol>


<div class="subheading"><strong><i>Ocean Deposits</i></strong></div>

The unconsolidated sediments derived
from various sources, deposited at the sea
floors are called ocean deposits. The ocean
deposits are classified on the basis of their
location as terrigenous and pelagic deposits.
<br>
<b>Terrigenous Deposits:</b> These are deposits
of the continental shelf and slope. They consist
of material derived from wear and tear of land,
the remains of animals and plants that live on
the bed of the sea and volcanic material. On
the basis of size of rock fragments, the
sediments are classified into gravel, sand and
mud. Mud is further classified as red, blue or
green mud on the basis of their colour.
Volcanic deposits consist of sub-aerial and sub
marine volcanic deposits. The organic deposits
consist of shells and skeletons of animals living
in the continental shelf.
<br>
<b>Pelagic Deposits:</b> These deposits are
found in deep sea plains. They cover 75 per
cent of the ocean area. The organic deposits
consist of liquid mud known as 'ooze' which
contain shells of various organisms. They are
subdivided into Calcareous ooze and Siliceous
ooze. The former contains Pteropod and
Globigerina ooze. The latter consists of
Radiolarian and Diatom ooze. The inorganic
deposits contain Red Clay, which is formed
from the decomposition of volcanic material
which is carried out to sea and occupies the
maximum space of the ocean floor.


