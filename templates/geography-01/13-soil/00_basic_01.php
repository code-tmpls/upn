<div class="subheading"><strong><i>Water retention of different soil types:</i></strong></div>

Pure sand holds least water while pure clay
holds the most. Loam holds the intermediate
amount. Sand transmits the water downward
most rapidly and the clay most slowly. Sand
reaches its full capacity very rapidly and added
water is wasted. Clay-rich loam takes up water
very slowly and if irrigation is too rapid, water
will be lost by surface runoff. Sandy soil requires
more frequent watering than clay-rich soil. The
intermediate loam texture is generally best as
agricultural soil because it drains well and also
has favourable water-retention properties.

<div class="subheading"><strong><i>Soil Profile</i></strong></div>

It is a vertical section of soil through all its
horizons and extends upto the parent materials.
A study of soil profile is important both from the
standpoint of soil formation and development
(pedology) and crop husbandry (edaphology). In
deep soils the soil profile may be studied upto one
metre and a quarter and in others upto the parent
material. The layers (horizons) in the soil profile
which vary in thickness may be distinguished
from the morphological characteristics which
include colour, texture, structure, etc.
<ol>
    <ul>
        <li>
        <div class="subheading"><strong><i>O Horizon</i></strong></div>    
        
At the top of the profile is the O horizon.
This is primarily composed of organic matter.
Fresh litter is found at the surface, while at depth
all signs of vegetation structure have been
destroyed by decomposition. The decomposed
organic matter or humus enriches the soil with
nutrients, aids soil structure (acts to bind
particles), and enhances soil moisture retention.</li>
        <li>
        <div class="subheading"><strong><i> A Horizon (The Top Soil)</i></strong></div>    
       
Beneath the O horizon is the A horizon. The
A horizon marks the beginning of the true
mineral soil. In this horizon organic material
mixes with inorganic products of weathering.
A horizon is typically dark colored due to the
presence organic matter. Eluviation, the removal
of inorganic and organic substances from a
horizon by leaching, occurs in the A horizon.
Eluviation is driven by the downward movement
of soil water.</li>
        <li>
        <div class="subheading"><strong><i>E Horizon</i></strong></div>    
        
The E horizon generally is a light-colored
horizon with eluviation being the dominant
process. Leaching or the removal of clay
particles, organic matter and/or oxides of iron
and aluminum is active in this horizon. Under
coniferous forests, the E horizon often has a high
concentration of quartz giving the horizon an
ashy-gray appearance.</li>
        <li>
        <div class="subheading"><strong><i>B Horizon (Subsoil)</i></strong></div>    
        
Beneath the E horizon, lies the B horizon.
This is a zone of illuviation where eluviated
materials from the upper horizons are
accumulated. The accumulation of fine material
leads to the creation of a dense layer in the soil.
In some soils the B horizon is enriched with
calcium carbonate in the form of nodules or as a
layer. This occurs when Capillary action brings
cations like calcium and sodium dissolved in soil
water upwards where they precipitate from the
water.
<br>
<br>
Eluviation is significant in humid climates
where ample precipitation exists and a surplus
in the water balance occurs. Illuvial layers are
found low in the soil profile. Illuvial zones are
found closer to the surface in semiarid and arid
climates where precipitation is scarce.</li>
        <li>
        <div class="subheading"><strong><i>C Horizon - Big rocks</i></strong></div>    
        
The C horizon represents the soil parent
material, either created in situ or transported
into its present location. Beneath the C horizon
lies Horizon R (Bedrock). The bedrock layer is
present in just about every different type of soil
profile. This layer is made of hard, solid rock,
which is eroded and weathered to produce most
of the soil above it.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Soil Classification</i></strong></div>

<b>Soil Orders:</b> Soils can be subdivided into three
orders known as zonal, intrazonal and azonal.
<ol>
    <ul>
        <li>Zonal soils: They are formed under the
conditions of good soil drainage through the
prolonged action of climate and vegetation
and are by far the most important and
widespread of the three orders.</li>
        <li>Azonal soils: They have no well-defined profile
either because they have had insufficient time
to develop or because they are on slopes too
steep to allow profile development.</li>
        <li>Intrazonal soils: They are simply those
formed under condition of very poor
drainage or upon limestone whose
influence is dominant.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Major soil groups and their characteristics:</i></strong></div>
<ol>
    <ul>
        <li>Podzol: one of the most widespread and most
developed soils. Rich in humus, low in fertility,
deficient in bases like calcium, magnesium,
potassium and phosphorous. Closely
associated with the sub-arctic climate and the
cooler parts of the marine west coast climate.</li>
        <li>Latosols: characteristics of humid tropics.
Chief characteristics include:
<ol>
    <ul>
        <li>Complete chemical and mechanical
decomposition of the parent rock.</li>
        <li>Silica entirely leached from the soil.</li>
        <li>Complete lack of humus.</li>
        <li>A reddish brown colour given by the
oxides of iron, aluminium and
manganese.</li>
    </ul>
</ol>
</li>
        <li>Chernozem soil: zonal soil in a semi-arid
climate. Horizon ‘A’ is rich in humus.
Horizon ‘B’ is rich in bases. Generally
acidic. It is found in Ukraine, central USA,
central Africa, South America and
Australia; it is highly productive for small
grain crops like wheat, oat, barley etc.</li>
        <li>Prairies soil: similar to chernozem. But it
lacks the excess calcium carbonate of the
chernozems. Extremely productive. Maize
and wheat are the main crops associated
with it.</li>
        <li>Chestnut soil: it is the zonal soil of midlatitude grasslands that occurs in drier
region. It has considerably low content of
organic materials. Its parent material is
generally loess. The chestnut soil occurs in
south Ukraine, the great plains of the U.S.A.
and South African veldts.</li>
        <li>Hydrographic soil: it is associated with
marshes, swamps, bogs or poorly drained
flat uplands. They are all intra-zonal soils.
"Bog" soils are formed under bog vegetation
in regions of cool continental climate.</li>
        <li>Desert soil: It is grey in colour in temperate
region and red in hot deserts of tropic. The
cold desert soil is found in mid-latitude cold
desert region and lack in humus. It has one of
the best cotton producing regions of the world.</li>
        <li>Tundra soils: It develops in such regions
where summer is short (3 months) and
winter is long (9 months). Plant growth is
restricted. Even percolated water is frozen
during the winter. No chemical and
biological action takes place for over nine
months. Wherever frozen ice melts, marshy
soil is developed. Canada and the erstwhile
U.S.S.R. have this type of soil.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Points to remember</i></strong></div>
<ol>
    <ul>
        <li>The “soil profile” refers to the arrangement
of the soil into horizons of differing texture,
colour and consistency.</li>
        <li>“Brunizem” soils are also termed as
“prairie” soil.</li>
        <li>“Calcification” is a pedogenic regime of
climate in which evaporation on the
average exceeds precipitation.</li>
        <li>The deposition of colloids and bases in the
underlying B horizon is a process known
as “illuviation”.</li>
        <li>The grey-brown podzolic soil differs from
podzols in that leaching is less intense and
contains more of the important base than
the podzols.</li>
        <li>The pedogenic regime of “gleization” is
characteristic of poorly drained environments
under a moist and cool cold climate.</li>
        <li>The dark-coloured soil absorbs more of
sun’s heat than the light coloured one.</li>
        <li>Most matured soils have a layered
arrangement of strata called “horizon”.</li>
        <li>“Podzolisation” commonly occurs in a
typical regime of coniferous forest regions.</li>
        <li>“Leaching” is a process of removal of
minerals in solution from the upper layers
to the lower layers of soils e.g. “Podzol” type of soil.</li>
    </ul>
</ol>


