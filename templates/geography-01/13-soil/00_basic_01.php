<h3>Soil</h3>


Soil may be defined as a thin layer of earth's
crust which serves as a natural medium for
growth of plants. It is the unconsolidated
mineral matter that has been subjected to, and
influenced by, genetic and environmental
factors-- parent material, climate, organisms and
topography all acting over a period of time. Soil
differs from the parent material in the
morphological, physical, chemical and biological
properties. Also, soils differ among themselves
in some or all the properties, depending on the
differences in the genetic and environmental
factors. Thus some soils are red, some are black;
some are deep and some are shallow; some are
coarse textured and some are fine-textured. They
serve as a reservoir of nutrients and water
for crops, provide mechanical anchorage and
favourable tilth. The components of soil are
mineral matter, organic matter, water and air,
the proportions of which vary and which
together form a system for plant growth; hence
the need to study the soils in perspective.
Soils are derived from consolidated parent
rocks by the process of weathering followed by
pedogenesis. Weathering refers to the physical
and chemical disintegration and decomposition
of rocks which are not under equilibrium under
temperature, pressure and moisture conditions
on the earth's surface. In the beginning, weathering precedes soil formation, more
so in hard rocks. In other words, weathering
creates the parent material over which soil formation takes place. Later, weathering, soil formation and development proceed simultaneously. The product of weathering is called regolith (small particle of rock). Under the influence of pedogenic processes, it finally develops
into mature soil.
<br>
<br>
Factors affecting soil formation other than
the regolith are:
<ol>
    <ul>
        <li>Climate</li>
        <li>Vegetation</li>
        <li>Relief</li>
        <li>Parental materials</li>
        <li>Living organism</li>
        <li>Time</li>
    </ul>
</ol>


