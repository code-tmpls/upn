<h3>Natural Vegetation</h3>

<ol>
    <ul>
        <li>
        <div class="subheading"><strong><i>Equatorial Evergreen Forest</i></strong></div>    
        <ol>
            <ul>
                <li>These forests are located close to the equator- Amazon and Congo basins, Malaysia, 
Coastal Burma, Cambodia, Vietnam,
Indonesia, New Guinea etc. - where the
rainfall is heavy. Example of trees - Ebony,
Mahogany, Rosewood, Rubber etc.</li>
                <li>Consists of tall, closely set trees. Their
crown form continuous canopy of foliage.</li>
                <li>Trees are smooth-barked and unbranched in the lower two-third part.</li>
                <li>Leaves are large and evergreen so called
"Broadleaf Evergreen Forest".</li>
                <li>Thick, woody lianas are common with
tendrils or suckers to climb.</li>
                <li>Epiphytes are numerous and they include fern, orchid, mosses and lichens.</li>
                <li>Trees are not found in a single stand.
Thus considerable labour is involved in
economic activities.</li>
                <li>Due to high temperature there is a rapid
consumption of dead plant matters by
bacterial action which results in the
absence of humus on the soil-surface.</li>
                <li>In the absence of cold and dry season
plant growth goes on continuously
throughout the year. Individual species
have their own seasons of leaf-sheding.</li>
            </ul>
        </ol>
 </li>
        <li>
        <div class="subheading"><strong><i>Tropical Rain Forest</i></strong></div>    
        <ol>
            <ul>
                <li>Quite similar in structure to the equatorial variety and extends in the tropical
gone of 10° to 25°N along the windward coast of trade winds.</li>
                <li>The trade wind littoral climate in which
the tropical rainforest thrives has a short
dry season.</li>
                <li>Epiphytes are abundant because of continued exposure to humid air.</li>
                <li>Area: Caribbean Land, SE-Florida, South
and South-east Asia, Eastern Brazil coast,
Madagascar coast, North-east Australian
coast.</li>
            </ul>
        </ol>
</li>
        <li>Tropical Monsoon Forest
1. They are located in Burma, Thailand,
Cambodia, Laos, North Vietnam, parts
of India, North Australia etc. Species -
Teak, Rubber, Bamboo, Mango, Sal, Sandalwood, Acacia, Eucalyptus etc.
2. More open tree growth than Equatorial
and Tropical Rain Forests.
3. Less competition among trees for light
so greater development of vegetation in
lower layers.
4. Trees trunks are massive with rich and
rough bark.
5. Most of the trees are deciduous, and
sheds their leaves in long dry season.
6. Teakwood trees are representative example of monsoon forest.
7. Clumps of bamboo are the important
part of vegetation.</li>
        <li>
        <div class="subheading"><strong><i>Temperate Evergreen Forests</i></strong></div>    
        <ol>
            <ul>
                <li>Located chiefly on the eastern sides of
landmasses in warm temperate latitudes
- South China, South Japan, Southeast
Australia, South Brazil etc. Examples of
trees - Evergreen Oak, Magnolia (China
& U.S.A.), Camphor and Bamboo
(China), Eucalyptus (Australia) etc.</li>
                <li>Unlike Equatorial and Tropical Rain
forest it has relatively few species of trees.</li>
                <li>Leaves tend to be smaller and more
leathery; the leaves canopy is also less
dense.</li>
                <li> Have a well developed lower stratum of
vegetation. Lianas and epiphytes are
abundant.</li>
                <li>The annual range of temperature is small
or moderate and rainfall is abundant
and well distributed throughout the year.</li>
            </ul>
        </ol>
</li>
        <li>
        <div class="subheading"><strong><i>Mediterranean Forest</i></strong></div>    
        <ol>
            <ul>
                <li>This type occurs on the Western sides of
land masses in the warm temperate latitudes: low lands around the Mediterranean
Sea, South-West Australia, Southwest Africa, Central Chile and Central California.
Examples of trees: Evergreen Oak, Olive,
Grape, Eucalyptus, Redwood etc.</li>
                <li>Consists of low trees with small, hard
leathery leaves.</li>
                <li>Today large areas consist of dense scrub,
locally known as "Maquis".</li>
                <li>In the Californian coast it is known as
"Chaparral" and in Australia as
"Sclerophyll Forest".</li>
            </ul>
        </ol>
</li>
        <li>
        <div class="subheading"><strong><i>Cool Temperate Forest</i></strong></div>    
        <ol>
            <ul>
                <li>Deciduous trees predominant.</li>
                <li>Regions include - West and Central Europe, Eastern U.S.A., North China,
North Japan New Zealand etc. Examples
of tree - Maple, Birch, Ash, Alm, Oak,
Beech, Chestnut, Walnut etc.</li>
                <li>Dominated by tall, broadleaf trees.</li>
                <li>Trees provide dense canopy in summer
but shed their leaves completely in the
winter.</li>
                <li>It is almost entirely limited to the midlatitude landmass of the N-Hemisphere.</li>
                <li>This forest represents a response to a
continental climate in which rainfall is
adequate throughout the growth season.</li>
                <li>Rainfall in greater in summer months
and the soil water demand is high.</li>
            </ul>
        </ol>
</li>
        <li>
        <div class="subheading"><strong><i>Temperate Mixed Forests:</i></strong></div>    
        <ol>
            <ul>
                <li>Between temperate deciduous and temperate coniferous forests.</li>
                <li>Trees: Aspen, Birch, Alder etc.</li>
            </ul>
        </ol>
</li>
        <li>
        <div class="subheading"><strong><i>Coniferous Forests or Taiga</i></strong></div>
            <ol>
                <ul>
                    <li>This type of forest is most extensive in
high latitudes and on high mountains.</li>
                    <li>This is a forest of evergreen, cone-bearing trees.</li>
                    <li>Examples of trees: Spruce, Blue Pine,
Hammock, Larch.</li>
                    <li>These conifers are extremely important
for their soft wood required for the paper, match and synthetic fibre industry,
found mainly in Northern Canada and
Northern Eurasia.</li>
                    <li>The trees have straight trunk, conical
shape with short branches and small
needlelike leaves.</li>
                    <li>In N. America, Europe and western Siberia it is known as "Boreal Forest".</li>
                    <li>In Canada it bears the hygrophytic vegetation as forming a bog succession and
leading to large thick peat accumulation
known as "Muskeg".</li>
                </ul>
            </ol>
</li>
        <li>
        <div class="subheading"><strong><i>Tropical Grasslands</i></strong></div>    
        <ol>
            <ul>
                <li>Tropical grasslands are located mainly
in the continental regions of tropical
latitudes where rain occurs in the hot
season which lasts for about 5 months.</li>
                <li>Important regions - north and south of
Zaire Basin, West Africa and east Africa plateau, parts of Brazil, Guiana
Highlands and part of Deccan plateau
in India.</li>
                <li> These are known by different names in
different regions:
    <br>
Campos - Brazil
<br>
Llanos - Guiana Highlands
<br>
Savanna - Africa and Australia</li>
            </ul>
        </ol>
</li>
        <li>
        <div class="subheading"><strong><i>Temperate Grasslands</i></strong></div>    
        <ol>
            <ul>
                <li>These grasslands are almost treelessthus contrasting with tropical grasslands.</li>
                <li>They are best developed in continental
interiors of temperate latitudes.</li>
                <li>Important temperate grasslands of the
world include:
<br>
Steppe - Eurasia
<br>
Prairie - North America
<br>
Pampas- Argentina
<br>
Veldt- South Africa
<br>
Downs- Australia</li>
            </ul>
        </ol>
</li>
            <li>
            <div class="subheading"><strong><i>Tropical Desert</i></strong></div>
                <ol>
                    <ul>
                        <li>These are mostly situated between 15º -
30º N and S on the western sides of land
masses.</li>
                        <li>The chief regions are: Sahara (North Africa),
Arabia, parts of Iran, Iraq, Syria, Jordan and
Israel, parts of Pakistan, Central Australia,
Namib Desert (South West Africa), Atacama
(coastal Peru and North Chile).</li>
                        <li>The most common plants are cacti, thorn
bushes and coarse grasses.</li>
                    </ul>
                </ol>
</li>
            <li>
            <div class="subheading"><strong><i>Mid-latitude Deserts</i></strong></div>
                <ol>
                    <ul>
                        <li>These are situated in the interior of Asia
and North America between 300 and
350 latitudes.</li>
                        <li>Aridity and a great annual temperature
with extremes of winter cold mark the
region.</li>
                        <li>In North America these deserts are
found in basins surrounded by the
Rockies.</li>
                        <li>In South America the Patagonia desert
lying to the east of the Andes is a typical example.</li>
                    </ul>
                </ol>
</li>
            <li>
            <div class="subheading"><strong><i>Tundra</i></strong></div>    
            <ol>
                <ul>
                    <li>This type of vegetation is chiefly confined to the northern hemisphere, fringing the Arctic “ocean in the continents
of Eurasia, North America and
Greenland Coast.</li>
                    <li>Important vegetation includes - mosses,
lichens end a few small shrubs</li>
                </ul>
            </ol>
</li>
    </ul>
</ol>


<div class="subheading"><strong><i>World Classification of Vegetation</i></strong></div>  

<b>Trophophyte:</b> Tropical deciduous
vegetation and grass.
<br>
<b>Hygrophyte:</b> Humid areas i.e. Equatorial
hot wet forests.
<br>
<b>Hydrophyte:</b> Vegetation of watery areas.
<br>
<b>Xerophyte:</b> Tropical Deciduous desert
vegetation.
<br>
<b>Mesophyte:</b> Temperate areas vegetation.
<br>
<b>Cryophyte:</b> Vegetation of Tundra and
cold regions.
<br>
<b>Halophyte:</b> Salty areas vegetation
(Mangrove).
<br>
<b>Lithophyte:</b> Vegetation of Rocks & Stones.


<div class="subheading"><strong><i>Points to remember</i></strong></div>
<ol>
    <ul>
        <li>Maple, Walnut, Mulberry, Magnolia and
Camphor trees are found in temperate
evergreen forests.</li>
        <li>The “Mediterranean” type of vegetation occurs in central California and central Chile.</li>
        <li>The “desert type” of vegetation is found in
coastal Peru and southern California.</li>
        <li>Alternate wet and dry seasons cause the
growth of distinctive vegetation called
“Tropical Savannah”.</li>
        <li>The tropical evergreen forest consists of the
trees like lions, broad-leaved evergreens,
flowering and fruit plants and their leaves
fall all at the same time.</li>
        <li>The occurrence of wet winter and dry summer is unique among Mediterranean climate
types and results in distinctive natural vegetation known as “Sclerophyll forest”.</li>
        <li>In tropical monsoon type of climate, the
rainfall is seasonal and generally occurs in
summer and may be as high as 300 cm in
favourable location.</li>
        <li>In “Tropical Savanna” climate, the rainfall
is 160 cm and temperature is about 23°c.</li>
        <li>The “Steppe” are the areas of comparatively lower temperature and slightly more
precipitation.</li>
        <li>The “China-type” climate is a type of humid
“mesothermal climate” and characterized by
warm summer and cool-winter. Its average temperature is 19°c and annual rainfall is “120 cm”.</li>
        <li>The “highlands type of climate” is found in
the Mountainous region of Himalayas, Tibet plateau, Rockies, Andes and Alps, which
have high diurnal range of “temperature”.</li>
        <li>The “alpine” forests are found from “2880 to 3600” m height on Himalayas.</li>
    </ul>
</ol>


