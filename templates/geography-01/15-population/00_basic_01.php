<div class="subheading"><strong><i>Races of the World</i></strong></div>
<ol>
    <ul>
        <li><b>Caucasoid:</b> The Caucasoids are numerically
one of the largest groups and it includes not
only white Europeans and people of
European origin living elsewhere, but also
Arabs and most of the people of the Indian
sub-continent. It accounts for 33% of the
world population. Caucasoids are also
divided into Nordic (Northern Europe),
Alpine (central Europe) and Mediterranean
people (Arabs, Jews and People of Indian
sub-continent).</li>
        <li><b>Mongoloids:</b> Mongoloids are represented by
the Chinese. Amerinds (native American
Indians) are perhaps an early offshoot while
the Polynesians are a sub-group of the
Mongoloids with a great deal of racial
intermixture. They constitute 43% of the world
population.</li>
        <li><b>Negroids:</b> The Negroids are represented by
the African people.</li>
        <li><b>Australoids:</b> Mostly tribal people are
represented by these races.</li>
        <li><b>Hottentots and Bushmen:</b> Africa tribe of
West coast and Atacama desert.</li>
    </ul>
</ol>


<b>Population Terms Birth Rate:</b> Number of the live births per
year per 1,000 of the population.
Death Rate: Number of deaths per year per
1,000 of the population.
<br>
<br>
<b>Infant Mortality:</b> Number of deaths of children
below 1 year of age per 1,000 of the population.
Life Expectancy: The average age at which
people die. It does not mean the age at which
most people die.
<br>
<br>
<b>Migration:</b> Migration is broadly defined as
permanent or semi-permanent shifting of residence.
<br>
<br>
<b>Natural Increase:</b> Excess of births over
deaths per 1,000 of population. This does not
include increase in population due to
immigration.
<br>
<br>
<b>Net Reproduction Ratio:</b> Rate at which
women are replaced by daughters who will have
children.
<br>
<br>
<b>Optimum Population:</b> A country is said to
have optimum population when the number of
people is in balance with the available resources.



