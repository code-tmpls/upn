<h3>Population</h3>



Human geography embraces the study of
human race, the growth of human numbers, the
movements and density of population, etc. Thus
human geography is a science which studies the
relationship between man and environment.
<br>
<br>
Factors Influencing Population Distribution
<ol>
    <ul>
        <li><b>Accessibility:</b> Man was unable to reach
inaccessible areas of forest, islands,
mountains for a long time, so such areas
have low density as in the Amazon basin,
S. American Plateaus, etc.</li>
        <li><b>Relief:</b> Steep gradients, high mountains,
rugged-terrain restrict settlement because
of hindrances in movement. Similarly rivers
may exert either a positive or negative
effects. Most attract settlement but some
are liable to flooding, change of coarse and
so hinder settlements.</li>
        <li><b>Altitude and Latitude:</b> There are very few
settlements above 5500 m in Andes and
Himalayas. 'La Paz' (Bolivian Capital) is at
the height of 3640 m. Low latitude high
plateau areas provide positive advantage.</li>
        <li><b>Climate:</b> Extreme heat, cold, humidity and
aridity deter settlement. Success of crops
also depends on climate. But no specific
climate is optimum for settlement as obvious
from the fact that two of the world primary
concentration of population lie in middle
latitude and the third is located in the
tropics. For example Java Island and
Amazon Basin have the same climate but
population density of Java is over 500
whereas Amazon basin has less than 1.</li>
        <li><b>Soil:</b> Deltaic and alluvial soils attract settlement
while laterites and podzols repel. All ancient
civilizations evolved in alluvial soil regions.
Higher density of Jawa as compared to
Sumatra is also due to soil fertility.</li>
        <li><b>Natural Vegetation:</b> It may also exert
positive as well as negative effects on
settlement.</li>
        <li><b>Mineral and Energy Resources:</b> Population
map of W. Europe is more or less identical
to distribution of coalfields and other
industries. S. African Rand, Appalachian
Coalfield, Donetz Basin, W-Australia also
show its effects.</li>
        <li><b>Economic Factor:</b> Density of population is
directly proportional to technological and
economic advancement. Migration of
Indian labourers to Mauritius, Trinidad and
Fiji under colonial rule shows this pattern.</li>
        <li><b>Political Factors:</b> Unlike communist countries
in the western world various inducements
may be offered to encourage migration to
new towns. Mass migration of Asian from
Uganda in 1972 is another example.</li>
        <li><b>Historical Factor:</b> Relatively recent settlement
of Australia is the basic reason for its low
density of 2. While high density of India is
liable to be explained in terms of its long
history of civilization and occupancy.</li>
    </ul>
</ol>



