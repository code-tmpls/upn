<h3>Human Settlement</h3>


The functional typology of human settlements is shaped, among others, by their political? administrative function. Its distinctive place
is determined by subjective factors, such as the
political administrative decisions, which have
changed the course of some settlements to the
benefit of others, or reverted them from their
normal, natural evolution. That means outside
involvement in space organization to the detriment of self?organization, the latter being the
outcome of the permanent tendency of territorial systems to rebalance from exogenous
factors?induced dysfunctions.
<br>
<br>
Settlements are classified on the basis of size
and function into URBAN and RURAL.
<ol>
    <ul>
        <li>
        <div class="subheading"><strong><i>Urban settlements:</i></strong></div>    
        <ol>
            <ul>
                <li>These types of settlement are nodal in
character and have secondary and tertiary activities.</li>
                <li>The chief occupation of the people of urban areas is non-agricultural i.e. industry, 
trade and services.</li>
                <li>The major function of an urban area are
trades and commerce, transport and
communication, mining and manufacturing, defence, administration, cultural
and recreational activities.</li>
                <li>Population density is high and the settlement size is large.</li>
            </ul>
        </ol>
</li>
        <li>
        <div class="subheading"><strong><i>Rural settlements:</i></strong></div>    
        <ol>
            <ul>
                <li>These settlements are chiefly concerned
with primary activities such as agriculture, mining, fishing, forestry etc.</li>
                <li>Most of the people of rural settlement are
engaged in agricultural work.</li>
                <li>The major function of rural settlement is
agriculture and each settlement specializes in various activities.</li>
                <li>Population density is small and the settlement size is small.</li>
            </ul>
        </ol>
</li>
    </ul>
</ol>

Settlements can also be classified on the
basis of shape and pattern into:
<ol>
    <ul>
        <li><b>Compact settlements:-</b>
        <ol>
            <ul>
                <li>In these settlement houses are built very
close to each other.</li>
                <li>Such settlements are found in river valleys and fertile plains.</li>
                <li>The people are closely tied and share
common occupations.</li>
            </ul>
        </ol>
</li>
        <li>
            
        <b>Dispersed settlements:-</b>
        <ol>
            <ul>
                <li>In these settlements houses are built far
apart from each other.</li>
                <li>These settlements consist of one or two
houses and cultural feature such as a
church or a temple binds the settlement
together.</li>
                <li>Such settlements are found over hills, plateau and highlands.</li>
            </ul>
        </ol>
</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Rural Settlements</i></strong></div>

<b>Rural Settlements:</b> Rural settlements are most
closely and directly related to land. They are dominated by primary activities 
such as agriculture, animal husbandry, fishing etc. The settlements size is
relatively small. Types of the settlement are determined by the extent of the 
built-up area and interhouse distance. The three factors are:
<ol>
    <ul>
        <li>
        <div class="subheading"><strong><i>Physical factors</i></strong></div>    
        <ol>
            <ul>
                <li><b>Nature of terrain:-</b> Dispersed type of
settlements is found in remote jungles,
small hills of Himachal Pradesh. Compact
settlements are found in highly productive alluvial plains of Punjab.</li>
                <li><b>Altitude:-</b> Dispersed settlements are
found in hills of Meghalaya and clustered
and semi-clustered settlements are found
in Gujarat plains.</li>
                <li><b>Climate:-</b> due to frequent droughts settlement may 
                become hamleted.</li>
                <li><b>Availability of water:-</b> Scarcity of water
in Rajasthan has resulted in development
of compact settlements.</li>
            </ul>
        </ol>
</li>
        <li>
        <div class="subheading"><strong><i>Cultural and ethnic factors</i></strong></div>
             <ol>
                <ul>
                    <li><b>Caste and tribal structure:-</b> due to ethnic factors 
settlement may become fragmented and Hamleted e.g. Chhattisgarh.</li>
                    <li><b>Religion:-</b> people of same religion prefer
to live together making a settlement large
or small.</li>
                </ul>
             </ol>
</li>
        <li>
        <div class="subheading"><strong><i>Security factors</i></strong></div>
            <ol>
                <ul>
                    <li>Defence from invasions and Wild animals:- due to defence from dacoits, wild
animals or fear settlements may cluster
and form compact settlements.
<br>
On the basis of the type of rural settlements found in India are:
<ol>
    <ul>
        <li>Clustered, agglomerated and nucleated
settlement: -
<ol>
    <ul>
        <li>In this type of settlement the built-up area
is compact and inter-house distance is
small.</li>
    </ul>
</ol>
</li>
    </ul>
</ol>
</li>
                    <li>In this type of village the general living
area is distinct and separatedfrom the
surrounding farms.</li>
                    <li>Such settlements are found in highly productive alluvial plains (Punjab), in the
valleys of Shiwaliks (Deheradun) and in
north eastern states.</li>
                    <li>Such settlements are also formed due to
security and defence reasons (e.g.
Madhya Pradesh) or scarcity of water or
cultivable land (Rajasthan).</li>
                </ul>
            </ol>

            <div class="subheading"><strong><i>Semi-clustered settlements: -</i></strong></div>
            <ol>
                <ul>
                    <li>In this type of settlement the built-up area
is less compact as compared to the clustered settlement.</li>
                    <li>It may result from segregation or fragmentation of 
                        a large compact village.</li>
                    <li>Some sections of a village society choose
or is forced to live a little away from the
main cluster or village.</li>
                    <li>The land-owning and dominant community occupies the central part of the main
village, whereas people of lower status of
settle on the outer flanks of the village.</li>
                    <li>Such settlements are found in Gujarat
plain and parts of Rajasthan.</li>
                </ul>
            </ol>

            <div class="subheading"><strong><i>Hamleted settlement:-</i></strong></div>
            <ol>
                <ul>
                    <li>When a large settlement gets fragmented
into several smaller units physically separated from each other but bears a 
common name it forms hamleted settlement.</li>
                    <li>It occurs due to social and ethnic factors.</li>
                    <li>These small units of settlements are
known as panna, para, palli, nagla, dhani
etc.</li>
                    <li>Such settlements are found in Ganga
plains, lower valleys of Himalayas.</li>
                </ul>
            </ol>

            <div class="subheading"><strong><i>Dispersed settlement:-</i></strong></div>
            <ol>
                <ul>
                    <li>When a settlement has a few isolated huts
it is called dispersed settlement.</li>
                    <li>These types of settlements are found in
remote jungles, small hills with a few
farms and pastures on the slope.</li>
                    <li>It results from extremely fragmented and
small resource support.</li>
                    <li>They are found in Meghalaya,
Uttaranchal, Himachal Pradesh and
Kerala.</li>
                </ul>
            </ol>
</li>
    </ul>
</ol>

On the basis of forms or shapes of the settlements:
These may be a number of geometrical forms
and shapes such as:
<ol>
    <ul>
        <li><b>Linear Pattern:</b> It is commonly found along
main roads, railways, streams, etc. It may
have a single row of houses arranged along
the main artery. For example rural settlements 
found along the sea coast, river valley, mountain ranges etc.</li>
        <li><b>Rectangular Pattern:</b> This is a very common
type which develops around the rectangular
shape of agricultural fields as it is common to
find a system of land measurement based on
square units. Village paths and cart tracks also
confirm to the rectangular field patterns and
run through the village in north-south and
east-west directions. Accessibility to farms and
fields and connectivity to other settlements lead
to rectangular shape of settlements. The settlements of coastal Maharashtra and Andhra
Pradesh and either side of Aravali hills, etc.
may be cited for examples.</li>
        <li><b>Square Pattern:</b> This is basically a varient
of rectangular type. Such a pattern is associated with villages lying at the crossing of
cart tracks or nroads and also related to
features restricting the extension of the village outside a square space. These features
may include an old boundary wall, thick
orchards, a road or a pond.</li>
        <li><b>Circular Pattern:</b> In the upper Doab and
Trans - Yamuna districts, Malwa region,
Punjab and Gujarat, large villages are 
characterized by a very high degree of 
compactness. The outer walls of dwellings 
adjoin each other and present a continuous
front so that when viewed from outside,
the villages look like a walled and fortified
enclosure pierced by a few openings. The
round form was a natural outcome of
maximum aggregation for the purpose of
defence during the past.</li>
        <li><b>Radial Pattern:</b> In this type, a number of
streets converge on one centre which may
be a source of water (pond, well), a temple
or mosque, a centre of commercial activity
or simply an open space. Thus, the streets
seem to be radiating from a common centre. Examples are settlements near
Gurushikar, Mount Abu in Rajasthan,
Vindhyachal in Uttar Pradesh, etc.</li>
        <li><b>Checker Board Pattern:</b> This is a type of
settlement found generally at the junction
of two roads. The village streets meet each
other at an angle or are parllel to each
other. This is because of the tendency to
align the dwellings along cardinal axes. This
pattern is common in the northern plains.</li>
        <li><b>Elongated Pattern:</b> Such settlement occurs as
a result of elongation of the rectangular pattern 
due to influence of site features. For instance, 
in the Ganga plains, in areas liable to
inundation, the rectangular pattern becomes
unusually elongated along the high ground.
Even otherwise the advantage offered by riverside location forces such a pattern.</li>
        <li><b>Fan Shaped Pattern:</b> This is seen where some
focal points or line is situated at one end of
the village. A focal object may be a tank a
riverside, a road, an orchard, a well or even
a place of worship. Such patterns are common in the delta region where the dwellings
simply follow the fan shaped profile of the
delta as in the case of Mahanadi, Godavari,
Krishna, Cauvery, etc. Such patterns are also
common in the Himalyan foothills.</li>
    </ul>
</ol>


Major problem of rural settlements are:
<ol>
    <ul>
        <li>Rural settlements in the developing countries 
            have poor infrastructure facilities.</li>
        <li>Supply of water to rural settlements in
developing countries is not adequate.
People in villages, particularly in mountainous and arid 
areas have to walk long distances to fetch drinking water.</li>
        <li>Water borne diseases such as cholera and jaundice are common 
problem because of lack of
safe drinking water and unhygienic conditions.</li>
        <li>Villages are adversely affected by the conditions of 
drought and flood. This in turn affects the crop cultivation.</li>
        <li>The absence of toilet and garbage disposal
facilities cause health related problems.</li>
        <li>The houses made up of mud, wood and thatch
get damaged during heavy rains and floods.</li>
        <li>Most houses have no proper ventilation.</li>
        <li>Unmetalled roads and lack of modern communication network causes difficulties in
providing emergency services during floods.</li>
        <li>It is also difficult to provide adequate health
and educational infrastructure for large
rural population. The problem is particularly serious where houses are scattered
over a large area.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Urban Settlements</i></strong></div>

According to the census of India urban areas are those which satisfy the conditions given
below.
<ol>
    <ul>
        <li>All places with a municipality corporation,
cantonment board or notified town area
committee etc.</li>
        <li>All other places which satisfy the following criteria:
            <ol>
                <ul>
                    <li>a minimum population of 5000</li>
                    <li>at least 75 percent of male working
population engaged in non-agricultural
sector, and</li>
                    <li>a density of population of at least 4,000
persons per square kilometer.</li>
                </ul>
            </ol>
        </li>
    </ul>
</ol>

Therefore, there are two broad groups of
town or urban settlement. The places which satisfy the conditions mentioned 
in category (a) are known as statutory town and the conditions mentioned in 
category (b) are known as census towns.
<br>
<br>
Urban agglomeration may consist of any one
of the three combinations given below:
<ol>
    <ul>
        <li>a town and its adjoining urban outgrowth;</li>
        <li>two or more contiguous towns with or
without their outgrowths; and</li>
        <li>a city and one or more adjoining towns
with their outgrowths together forming
contiguous streatch.</li>
    </ul>
</ol>

Examples of urban outgrowths are university campus, cantonment area, port area-seaport
and air port, railway colonies, etc.
Functional Classification of urban settlements
This is the most popular and widely accepted
classification of urban places in India as well as
in other parts of the world.
<br>
The cities are divided as:
<ol>
    <ul>
        <li><b>Administrative Towns:-</b> National capitals,
which have headquarters of the administrative offices of Central Government, are
called administrative towns, such as new
Delhi, Canberra, Moscow, and Washington.</li>
        <li><b>Defence Towns:-</b> Centres of military activities are known as defence towns. They
are of three types: Fort towns, Garrison
towns and Naval bases. Jodhpur is a fort
town; Mhow is a garrison town; and Kochi
is a naval base.</li>
        <li><b>Cultural Towns:-</b> towns famous for religious, educational or recreational functions
are called cultural towns. Places of pilgrimage, such as Jerusalem, Mecca, Jagannath
Puri and Varanasi etc. are considered as
religious towns. There are also recreational
towns such as Las Vegas in the USA.</li>
        <li><b>Industrial Towns:-</b> Mining and manufacturing regions. 
Dhanbad and Khetri are examples of mining towns. Towns which have
developed due to setting up of industries such
as Jameshdpur are called industrial towns.</li>
        <li><b>Trading and Commercial Towns:-</b> Many
old towns were famous as trade centres such
as Lahore in Pakistan, Baghdad in Iraq and
Agra in India. Some towns have developed
as transport towns such as Rotterdam in
the Netherlands, Aden in Yemen and
Mumbai in India are port towns.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Classification on the basis of size:</i></strong></div>

    Depending on the size and the services available and functions rendered, urban centres are
designated as town, city, million city,
conurbation, and megalopolis.
<ol>
    <ul>
        <li><b>Town:</b> Population size in town is higher
than the village. Functions such as, manufacturing, retail and wholesale trade, and
professional services exist in towns.</li>
        <li><b>City:</b> A city may be regarded as a leading
town. Cities are much larger than towns
and have a greater number of economic
functions. They tend to have transport terminals, major financial institutions and
regional administrative offices. When the
population crosses the one million mark it
is designated as a million city.</li>
        <li><b>Conurbation:</b> The term conurbation was
coined by Patrick Geddes in 1915 and applied to a large area of urban development
that resulted from the merging of originally
separate towns or cities. Greater London,
Greater Mumbai, Manchester, Chicago and
Tokyo are examples.</li>
        <li><b>Megalopolis:</b> This Greek word meaning
"great city", was popularised by Jean
Gottman (1957) and signifies 'super- metropolitan' region extending, as union of
conurbations. The urban landscape stretching from Boston in the north to south of
Washington in U.S.A. is the best known example of a megalopolis.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Problems in urban settlements:</i></strong></div>

The town and cities grow in size and number as the urban population expands. The rapid
rate of Urbanisation in developed and developing countries brings a host of urban problems due to a large increase in the number of
urban dwellers.
<br>
The problems are:
<ol>
    <ul>
        <li>Housing- lack of house and can't afford of
proper housing which leads to development
of squatter settlement.</li>
        <li>Water supply- shortage of water supply
due to the demand of water supply and
poor piping system.</li>
        <li>Transport- too many cars in major cities.
Poor public transport system</li>
        <li>Pollution- domestic and industrial waste
contribute to land pollution in urban areas.
Some steps that can be taken to overcome
these problems are:
<ol>
    <ul>
        <li>Provide low cost housing to relocate slum
dwellers and squatter.</li>
        <li>Improve the living condition of slum and
squatter settlements instead of removing them
by providing piped water and sanitation.</li>
        <li>Control the rural urban migration, it can
reduce the overcrowding in the city</li>
        <li>increase water supply by building more
reservoirs.</li>
        <li>more treatment plants should be built and
old rusty pipes must be replaced to increase
water quality.</li>
        <li>educate the public in the virtues of water
conservation.</li>
        <li>to ease congestion in the developed countries
road and railways network have to extended.</li>
        <li>Build extensive expressways and wider
roads to ensure smooth flow of traffic.</li>
        <li>Encourage to use public transport to reduce traffic congestion such as MRT.</li>
        <li>Increase awareness of health, hygiene and
ills of pollutions.</li>
    </ul>
</ol>
</li>
    </ul>
</ol>



