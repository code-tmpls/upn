        <div class="subheading"><strong><i>Rice</i></strong></div>
            
Rice is normally grown as an annual plant,
although in tropical areas it can survive as a
perennial crop for up to 30 years. The rice plant
can grow to 1-1.8 m tall, occasionally more depending on the variety and soil fertility.
<br>
<br>
Conditions of growth
<ol>
    <ul>
        <li>Hot and humid climate - high degree of
temperature ranging between 20°-27°C and
minimum rainfall 120 cm. It also requires
water logging particularly in the early parts
of its growth and deep clayey loams.</li>
        <li>Rice needs a level surface to ensure annual
flooding of the fields. It's most ideal habitat
is therefore, the great riverine flood-plains
of the world.</li>
    </ul>
</ol>
<br>
<br>
<b>Distribution:</b>
Monsoonal low lands of South-east Asia is the
most outstanding region for rice growing in the
world-accounting for 85% of the total rice acreage of the world and produce 90% of the world
output. The noted rice producing countries in the
world include China, India, Indonesia, Bangladesh,
Thailand, Vietnam, Myanmar, Italy, Spain, USA
and Brazil are other producing countries.
<br>
<br>
<b>China</b>
China is also the largest producer of rice in
the world. Sichuan basin is the largest producer
followed by Red Basin, riverine flood plain of
Yangtze basin, Yunnan and Kweichow provinces, Fukien, Kwanghing and Kwangsi and
Hunan, Kiangsi and Chungking province. The soil
fertility, use of manures and unified management
are responsible for higher productivity.
India is the second longest producer of rice
in the world.
<br>
<br>
<b>Bangladesh</b> is the third largest producer of
rice, though rice is grown everywhere Dacca,
maymansingh, Bakharganj, Faridpur, Barisal
districts account bulk of the country's output.
<br>
<br>
<b>Japan:</b> rice is grown throughout the country; per 
hectare production is very high. But high
domestic demand and limited land forces the
country to import rice.
<br>
<br>
The fertile alluvial plains of Irrawady in
Myanmar, the Red Basin of North Vietnam, the
Mekong Basin of South Vietnam, and the Menam
Basin of Thailand are also notable in rice production. Rice is also grown in Nile valley and
delta of Egypt, S.E. Brazil and Gulf of Mexico in
the USA and Po river valley in Italy.
<br>
<br>
<b>Export:-</b>
Very meager amount of rice (2 to 5%) come
to the international market. The major rice exporting countries are China, Myanmar, USA,
Thailand, Brazil, India and Italy. Major importing countries are Indonesia Korea, Hongkong,
Srilanka and Bangladesh.</li>
