<h3>Agriculture</h3>

<div class="subheading"><strong><i>Different Agricultural Types</i></strong></div>

<ol>
    <ul>
        <li>On the basis of supply of land: Intensive
agriculture and Extensive agriculture
<ol>
    <ul>
        <li>Intensive method is practiced where the
supply of land is limited and density of
population is high. China, Japan, India,
UK, Holland, Germany and Belgium
practice this method.</li>
        <li>Extensive method is practiced in sparsely
populated area - where per man land
area is higher and where there is scope
for bringing additional land under cultivation e.g. USA, Russia, Australia, 
Argentina and Brazil.</li>
    </ul>
</ol>
</li>
        <li>On the basis of supply of moisture: Humid farming, Irrigation farming and Dry
farming.
<ol>
    <ul>
        <li>Humid farming is practiced where there
is no dearth of rainwater for the
production of crops. Problems of water
logging and drainage and soil erosion are
present in the heavy rainfall areas.</li>
        <li>Irrigation farming is practiced in those
areas where rainfall is seasonal and the
amount is not satisfactory for crop
production. In the river valleys of the
world this farming is practiced.</li>
        <li>Dry farming: areas having very little
rainfall less than 50cm and very little
irrigation facilities practice dry farming.
Crops which can bear the high cost of
production e.g. cotton and wheat is
grown under this method.</li>
    </ul>
</ol>
</li>
    </ul>
</ol>

