        <li>
        <div class="subheading"><strong><i>Wheat</i></strong></div>    
        
Wheat is the dominant grain of world commerce and is the staple food of millions of people.
It is also an important part of the daily diet of
many millions more. The world wheat market is
enormous. Annual global wheat consumption
is in excess of 550 million tonnes (20 billion bushels). Approximately two-thirds of the wheat produced in the world is used for human food and
about one-sixth is used for livestock feed. Industrial uses, seed requirements, and post-harvest
losses account for the remaining withdrawals
from the world wheat granaries. The geographical concentration of wheat is found between 30-
55°N latitude in the Northern hemisphere and
between 20-40°S in Southern hemisphere.
<br>
<br>
<b>Conditions of Growth</b>
<ol>
    <ul>
        <li>A minimum temperature of 16°C and bright
sunshine for ripening, and 100 frost free days.</li>
        <li>A mild moist season with annual rainfall
ranging between 50 cm and 100 cm.</li>
        <li>A relatively stiff, preferably loamy and nonacidic soils.</li>
        <li>Level or slightly rolling lands to facilitate
mechanical methods of farming.</li>
    </ul>
</ol>

In terms of net output four countries viz.
Russia, U.S.A., China and India contribute over
50% of world production. But temperate countries like France, Spain, Argentina, and Australia are also important contributing most (20%)
of world's wheat.

