        <div class="subheading"><strong><i>Coffee</i></strong></div>
            
Coffee is a brewed drink prepared from
roasted seeds, commonly called coffee beans, of
the coffee plant. They are seeds of coffee cherries that grow on trees in over 70 countries. Coffee is usually propagated by seeds. The traditional
method of planting coffee is to put 20 seeds in
each hole at the beginning of the rainy season
Conditions of growth
<ol>
    <ul>
        <li>Warm climate conditions annual range of
