        <div class="subheading"><strong><i>Maize</i></strong></div>    
        
Maize is widely cultivated throughout the
world, and a greater weight of maize is produced
each year than any other grain. The United States
produces (42.5%) of the world's harvest; other top
producing countries includes China, Brazil,
Mexico, Argentina, India and France.
<br>
<br>
<b>Conditions of growth:</b>
 No other cereal is cultivated under such diverse climatic conditions and no other cereal crop
is so widely distributed both in tropical and
warm temperate latitudes. Even then favorable
conditions are:
<ol>
    <ul>
        <li>Temperature 20-25°C</li>
        <li>Long and warm summer with considerable
rainfall followed by little rainfall of autumn
and cold winters is ideal and optimum
conditions</li>
        <li>Rainfall 7-15 cm per month and during
season of 130 to 170 days.</li>
        <li>Deep rich soils of the sub-tropical latitudes
with high nitrogen content well drained
plain lands are most commonly preferred
for maize growing.</li>
    </ul>
</ol>
<br>
<br>
<b>Distribution:</b>
USA, Brazil, Mexico, China and Russia account
for 65% of the total world maize production.
USA accounts for half of the world's maize
production. The Corn Belt extends from central
Ohio to Central Nebraska. Iowa, Illinois, Indiana, Minnesota, Pennsylvania and Nebraska are
the principal maize producing states of the USA.
In this belt maize is grown as rotation crop.
In Argentina, Corn belt lies in the humid Pampas region, wet of the Parana river. 
Despite contributing only 3.5% in world production Argentina exports more than 
half of the total export.
<br>
<br>
<b>China</b> is the second largest producer.
<br>
<b>Brazil</b> - Minas Gerais, Sao Paolo, Rio Grande
are important maize producing zone. The production is almost double of Argentina but consumed locally.
<br>
<br>
<b>Export:</b> 16 per cent of the total global output
enters world market is sale. Argentina, USA,
France and South Africa are major exporting countries. Japan is the 
largest buyer, followed by Netherlands, Italy, Spain, UK, Germany and Denmark.</li>
