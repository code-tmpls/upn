mori reared in captivity. The birth place of silk is
China as from there it spread to Japan, India,
Southern Europe and Western Asia.
Commercial production of silk involves four
different stages: (a) growing of mulberry trees
(b) rearing of silk (c) tendering of cocoons (d)
reeling of silk.
<br>
<br>
The temperature should be between 20-30°C
each cocoon has 762 to 915 meters of filament. For
the ideal growth of cocoon, high temperature and
heavy rainfall is required. The mulberry trees can
be grown on poor acid and infertile soils. This is
why they are relegated to the rugged lands or hilly
tracts which are not available for food production.
<br>
<br>
<b>Distribution:</b>
