<h3>Fisheries</h3>


The principal fishing grounds of the world
are located on the plankton rich banks of the
continental shelves especially in the cool water
of the northern hemisphere in comparatively
high latitudes. In terms of production, Japan is
para-mount followed by the CIS, China, Norway
and USA.

<div class="subheading"><strong><i>Locational Considerations</i></strong></div>
<ol>
    <ul>
        <li><b>Supply of Plankton:</b> Important conditions
for its presence are (a) Shallow water above
continental shelf (b) Cool Water- Plentiful
in polar water where cold & warm currents
meet. (c) Land- derived minerals which
nourishes planktons.</li>
        <li><b>Cool Climate:</b> Best developed where temp.
is below 20°C; tropical water is too warm
so fishes are of less commercial value while
in the temperate latitude water is cool so
most suitable; fish cannot be kept long in
hot, moist tropical conditions. While the
temperate land of cold winters originally
provides natural ice for preservation of fish.</li>
        <li><b>Physical & Environmental Influences :</b> Both
Atlantic and Pacific ocean coast-lines of
middle and high latitude in N-Hemisphere
are very much indented and are backed by
strong relief. There are sheltered inlets &
estuarine coasts that make ideal sites for
fishing ports & villages. The rugged
mountains & short growing period in
Norway, Hokkaido, Iceland, Alaska restricts
agricultural activities & people take to the
sea to enrich their diet. In S-Hemisphere
fishing activities are hampered by lack of
harbours or of labour. In Argentina &
Australia meat & other foodstuffs are so
plentiful that it has not been necessary to
develop a fishing industry.</li>
        <li><b>Moderate or Large Population:</b> It is a labour
intensive industry, small scale fishing in wellpopulated areas as China, Japan, etc.</li>
        <li></li>
        <li></li>
        <li></li>
    </ul>
</ol>

<div class="subheading"><strong><i>Types of Fish</i></strong></div>
<ol>
    <ul>
        <li><b>Salt Water Fish:</b> They spend their entire life in
the oceans and seas.</li>
        <li><b>Fresh Water Fish:</b> They are found in inland
streams, rivers and lakes.</li>
        <li><b>Anadromous Fish:</b> They are spawned in the
inland rivers but spend most of their life in the
seas and only return to the rivers to spawn
and die, e.g. Salmon.</li>
        <li><b>Pelagic Fish:</b> They live at or near the surface
of water like herring.</li>
        <li<b>>Demersal Fish:</b> They live at the bottom like
cod, haddock.</li>
    </ul>
</ol>

<ol>
    <ul>
        <li><b>Pelagic:</b> Light loving & found near the
ocean surface and form schools e.g.
Herring: most important catch of N.
Atlantic region & also the chief fish caught
in Japan & China.
<br>
<br>
<b>Mackerel:</b> generally found south of the main
herring areas, e.g. in the Mediterranean Sea,
Off- South Scandinavia, Off- Cornwall (UK),
Off- Carolina (USA), Yellow sea.
<br>
<br>
<b>Sardines:</b> temperate water fish;
Mediterranean sea, Bay of Biscay, Coastal
New England States are its main regions.
<br>
<br>
<b>Brisling:</b> temperate water fish; off-south
Norway.
<br>
<br>
<b>Anchovies:</b> temperate water fish, OffEurope, Peru (90% of Peru catch).
<br>
<br>
<b>Menhaden:</b> off-USA (from Newfoundland
to Caribbean Sea); due to oily nature
unsuitable for human, generally used as
animal feeding, fertilizer, soap, etc.
<br>
<br>
<b>Capelin:</b> Like: Menhaden, caught in the
N.E-Atlantic.</li>
        <li><b>Demersal:</b> not light loving, on ocean
bottom, also known as ‘White Fish’ e.g.
Cod: in terms of value more important than
Herring; North Sea, Off- Norway &
Iceland, Grand Bank off Newfoundland.
<br>
<b>Haddock:</b> N. Atlantic Coast.
<br>
<b>Halibut:</b> Off- British Columbia, N.W-USA,
Hake and Flat fishes like Plaice, Sole,
Flounder.
<br>
<b>Tuna:</b> Japan, Mediterranean Sea, Indian
Ocean, California.</li>
    </ul>
</ol>


<b>Fresh Water Fish (10%):</b> They rarely swim
in large shoals & their fishing areas are
rather restricted. Inland fishing is important
in former USSR & E. Asia.
Trout, Perch, Pike, Salmon: N.America &
Europe.
<br>
<br>
<b>Sturgeon:</b> Caspian Sea, Black sea, Volga,
Danube, Dnieper, St. Lawrence.
<br>
<b>Carp:</b> China & Japan.
<br>
<b>Eels:</b> Denmark, Netherlands, USA.
<br>
<br>
<b>Anadromous Fish:</b> Both fresh water& salt
water fish.
<br>
<b>Salmon:</b> N. America (from Alaska to
Oregon on Pacific coast)-Young salmons
live in sea but after 2-5 yrs they return to
the stream where they were born to lay
their eggs & die. Today Salmon’s greatest
haul is in Alaska.

<div class="subheading"><strong><i>Major Fishing Regions</i></strong></div>
<ol>
    <ul>
        <li><b>North - West Pacific Region:</b> It extends
from Pacific Bering Sea in north to
Philippines Sea in the South. In this Region
Japan is the leading producer of fish.</li>
        <li><b>North-East Pacific Region :</b> It extends from
Alaska to California along the western coast
of North America Salmon, Pilchard, Tuna,
Sardines and Halibut are the important
catch of this region.</li>
        <li><b>North Atlantic Region:</b> It extends from the
Arctic circle in the north to the borders of
the Mediterranean Sea along the West
European Coasts.</li>
        <li><b>North-West Atlantic Region:</b> It extends
mainly between long island and Newfound
land and to a little extent in its
surroundings.</li>
        <li></li>
        <li></li>
    </ul>
</ol>

