<h3>Minerals</h3>

<div class="subheading"><strong><i>Minerals generally occur in any of the following
formations:</i></strong></div>
<ol>
    <ul>
        <li>Minerals found in sedimentary beds or
horizontal strata are mainly coal and some
grades of Iron ores. Other minerals of this
origin are gypsum, potash and salt.
Petroleum also belongs to this category.</li>
        <li>Alluvial Deposits contain those minerals
which are resistant to erosion e.g. gold,
platinum and tin.</li>
        <li>Some metallic minerals like tin, silver, copper,
zinc and lead are obtained from cracks,
crevices, faults or joints in rocks. The smaller
varieties of such occurrences are called
veins and the larger lodes.</li>
        <li>Deep weathering products also contain some
of the mineral resources and bauxite, the
ore of aluminium is formed by this process.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Classification of Minerals</i></strong></div>

<b>Industrial Metallic Mineral:</b> Iron.
Non-Metallic Minerals: Salt, Sulphur,
Potash, Nitrates and precious stones like
Diamond.
<br>
<b>Non Ferrous Metallic Minerals:</b>
Aluminum, Copper, tin.
Ferro-alloy Metallic Minerals: Manganese,
Vanadium, Cobalt, Molybdenum, Nickel.
<br>
<b>Power Minerals:</b> Coal, Petroleum and Gas.
Precious metallic minerals: Gold, Silver
and Platinum.


