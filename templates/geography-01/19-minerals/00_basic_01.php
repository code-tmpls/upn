<div class="subheading"><strong><i>Aluminium</i></strong></div>

Most abundant metal; 8% of the Earth’s crust.
Ores: Bauxite (in the form of oxide),
Cryotite (found only in Greenland),
Corundum, Kaolin,
Commercial Bauxite has 50% alumina &
7% silica, so it is the most wan-ted ore,
mostly mined in tropical region but
aluminium is manufactured in developed
countries where cheaper electricity is
available. World’s first Bauxite mine was
in the village ‘Les-Baux’ in France from
which the ore name bauxite is derived.
Process: Hall-Herault Process, Bayer
Process.
