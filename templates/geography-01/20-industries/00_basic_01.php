New York, Philadelphia, Baltimore districtsfor textiles, steel, leather goods and chemical
industries; Detroit district (the greatest
automobile centre of the world) for
automobiles.</li>
        <li>Southern Industrial Region- Extends over
a wide area from Texas to North Carolina.
20 per cent of the country’s labour force is
engaged in various industries of this region.
Cotton textile, goods, aircrafts, aircraft
industry and petroleum refining are
important industries. Charlotte, Columbia,
August, Atlanta, Knoxville, Chattanooga
and Birmingham are important industrial
centers.</li>
        <li>Pacific coast industrial region- Extends
over the states of Washington, Oregon and
California along the Pacific coast in the
West. About 10% of the country’s labour
force is engaged in manufacturing industries
of this area. This is the smallest industrial
region of the country. Los Angeles, San
Francisco, Seattle, San Diego are important
industrial centers.</li>
    </ul>
</ol>

