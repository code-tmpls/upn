<h3>Industries</h3>


Industry refers to an economic activity that
is concerned with production of goods, extraction of minerals or the provision of services. There
are several industries like iron and steel industry (production of goods), coal mining industry
(extraction of coal) and tourism industry (service provider). Industries can be classified on the
basis of raw materials, size and ownership.

<div class="subheading"><strong><i>Types of Industries</i></strong></div>
<ol>
    <ul>
        <li>On the basis of Raw Materials they use,
industries can be divided into:-
<ol>
    <ul>
        <li>Agro based Industries: use plant and
animal based products as their raw materials. Example - 
Food processing, vegetable oil, cotton textile, dairy products
and leather industries.</li>
        <li>Mineral based industries: use mineral
ores as their raw materials. Example -
heavy machinery, building materials and
railway coaches.</li>
        <li>Marine based industries: use products
from the sea and oceans as raw materials. Example - industries processing sea
food or manufacturing fish oil.</li>
        <li>Forest based industries: Utilize forest
produce as raw materials. For example:
pulp and paper, pharmaceuticals, furniture and buildings.</li>
    </ul>
</ol>
</li>
        <li>Size of an industry refers to the amount
of capital invested, number of people employed and the volume of production.
Based on size, industries can be classified
into:-
<ol>
    <ul>
        <li>Small-scale Industries: Cottage or
household industries like basket weaving, pottery and other handicrafts, silk
weaving and food processing.</li>
        <li>Large-scale Industries: automobiles and
heavy machinery industries.</li>
    </ul>
</ol>
</li>
        <li>On the basis of ownership industries can
be divided into:-
<ol>
    <ul>
        <li>Private Sector Industries: owned and
operated by individuals or a group of
individuals e.g. Tata Motors, Reliance
Industries, Bajaj Group etc.</li>
        <li>Public Sector Industries: owned and
operated by the government. Exp -
Hindustan Aeronautics Limited and Steel
Authority of India Limited.</li>
        <li>Joint Sector Industries: owned and operated by the state and individuals or a
group of individuals. Exp - Maruti
Udyog Limited.</li>
        <li>Cooperative Sector Industries: owned
and operated by the producers or suppliers of raw materials, workers or both.
Exp - Anand Milk Union Limited and
Sudha Dairy.</li>
    </ul>
</ol>
</li>
        <li>Other Classification:
            <ol>
                <ul>
                    <li>Raw Material based industries: Iron
and Steel, Sugar, Cement, Fish, Meat,
Milk and Food processing, Rayon etc.</li>
                    <li>Power based industries: Aluminium.</li>
                    <li>Skilled labour based industries:
Jewellery, Diamond cutting, Watch, Toys
and electronics in Japan, Jagadhari.</li>
                    <li>Transportation based: Trans- Siberian
Railway region industries, Industries of
Great Lake waterways region of U.S.A
and Canada, industries of coastal areas
of Japan, refineries in coastal areas, Jute
mills on the bank of river Hugli, industries of Perth. Sydney railway region of
Australia etc.</li>
                    <li>Capital based industries: Many industries of developing countries of colonial
period established by ruler nations during
their colonial period e.g. industries of Zaire
and Guinea were developed by Belgium.</li>
                    <li>Market based industries: Textile, Refineries, Electronics, Bakery, 
Ice-Cream, Biscuits, Hosiery and other consumer goods.</li>
                    <li>Footloose industries: Industries that can
be established both in the market as well
as in the raw material source region e.g.
Paper industry.</li>
                </ul>
            </ol>
</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Factors Influencing Industrial Location</i></strong></div>

The factors affecting the location of industries are:
<ol>
    <ul>
        <li>the availability of raw material,</li>
        <li>land</li>
        <li>water</li>
        <li>labour</li>
        <li>power</li>
        <li>capital</li>
        <li>transport</li>
        <li>Market</li>
    </ul>
</ol>

Industries are situated where some or all of these
factors are easily available. Sometimes, the government provides incentives like subsidized power,
lower transport cost and other infrastructure so that
industries may be located in backward areas.

