<div class="subheading"><strong><i>USA</i></strong></div>

<ol>
    <ul>
        <li>New England Region- The region comprises
six states viz. Connecticut, Rhode Island,
Massachusetts, Vermont, New Hampshire
and Maine. The nucleus of this region is
Boston. Major industries in the region are
electrical machinery, textiles, engineering,
