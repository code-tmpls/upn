<h3>Energy Resources</h3>


Most of the world's energy resources are
from the sun's rays hitting earth. Some of that
energy has been preserved as fossil energy; some
is directly or indirectly useable; for example, via
wind, hydro- or wave power.

<div class="subheading"><strong><i>Coal</i></strong></div>

Coal is the most abundant fossil fuel found in
sedimentary source most of which has been formed
during Carboniferous period. This was the fuel that
launched the industrial revolution and has continued to grow 
in use; China, which already has many
of the world's most polluted cities, is building about
two coal-fired power plants every week. Coal is the
fastest growing fossil fuel and its large reserves
would make it a popular candidate to meet the energy 
demand of the global community, short of global warming concerns and other pollutants.

