<div class="subheading"><strong><i>Natural Gas</i></strong></div>

Natural gas primarily consists of methane. It is
found associated with fossil fuels, in coal beds, as
methane clathrates, and is created by methanogenic
organisms in marshes, bogs, and landfills. It is an
important fuel source, a major feedstock for fertilizers, and a potent greenhouse gas.
The world's largest proven gas reserves are
located in Russia. Russia is also the world's largest natural gas producer, through the Gazprom
Company. Major proven resources are Russia,
Iran, Qatar, Saudi Arabia and United Arab
Emirates.
<br>
<br>
The world's largest gas field is Qatar's offshore North Field, estimated to have 25 trillion
cubic metres of gas in place—enough to last more
than 200 years at optimum production levels.
The second largest natural gas field is the South
Pars Gas Field in Iranian waters in the Persian
Gulf. Generally natural gas occurs together with
mineral oil in the anticlines of sedimentary rocks.
<br>
<br>
