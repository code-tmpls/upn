<div class="subheading"><strong><i>Hydel Energy</i></strong></div>

Hydroelectricity is electricity generated by
hydropower, i.e., the production of power
through use of the gravitational force of falling
or flowing water. It is the most widely used form
of renewable energy. Once a hydroelectric complex is constructed, the project produces no direct waste, and has a considerably lower output
level of the greenhouse gas carbon dioxide (CO2)
than fossil fuel powered energy plants. Worldwide, an installed capacity of 777 GWe (Giga
watt - Electric) supplied 2998 TWh (tera watt
hours) of hydroelectricity in 2006. This was
around 20% of the world's electricity and about
88% of electricity from renewable sources.
<br>
<br>
<b>Distribution:</b>
<br>
In the Scottish Highlands of United Kingdom,
there are examples at Kinlochleven and Lochaber,
constructed during the early years of the 20th
century. The Grand Coulee Dam, switched to
support Alcoa aluminum in Bellingham, Washington, United States for 
irrigation and power (in
addition to aluminum power).
In Surinam, the Brokopondo Reservoir was
constructed to provide electricity for the Alcoa
aluminium industry. New Zealand's Manapouri
Power Station was constructed to supply electricity to the aluminium 
smelter at Tiwai Point.
As of 2007 the Kárahnjúkar Hydropower Project
in Iceland remains controversial.
<br>
<br>
<b>Failure Hazard:</b>
Dam failures have been some of the largest
man-made disasters. Also, good design and construction are not an 
adequate guarantee of safety.
For example, the Banqiao Dam failure in
Southern China resulted in the deaths of 171,000
people and left millions homeless. Also, the creation of a dam in a 
geologically inappropriate
location may cause disasters like that of Vajont
Dam in Italy, where 2000 people died in 1963.
Smaller dams and micro hydro facilities create less risk, but can form continuing hazards
even after they have been decommissioned. For
example, the Kelly Barnes small hydroelectric
dam failed in 1967, causing deaths with the
Toccoa Flood, ten years after its power plant was
decommissioned in 1957.


