<div class="subheading"><strong><i>Solar Energy</i></strong></div>

It is common knowledge that solar radiation
is unevenly distributed, and that it varies in intensity from one geographic 
location to another depending upon the latitude, season, and time of day.
Until recently, valid records for solar radiation have
been very scanty in the vast majority of the developing countries. In the 
absence of such useful information as a guide for the proper exploitation of
solar energy, only general hints can be offered regarding the geographic areas 
with favourable conditions for solar energy applications.
For convenience and simplicity, the geographic distribution of total solar 
radiation on a global scale is divided in terms of intensity into
four broad belts around the earth. These are illustrated in Figure 2, and also 
described briefly thereunder with respect to the northern hemisphere, 
with the understanding that the same
conditions apply to the corresponding belts in
the southern hemisphere:

<ol>
    <ul>
        <li><b>Most Favourable belt</b>
        <br>
        <br>
 This belt, lying between latitudes 15°N, and
35°N, embraces the regions that are naturally
endowed with the most favourable conditions
for solar energy applications. These semi-arid
regions are characterized by having the greatest
amount of solar radiation, more than 90% of
which comes as direct radiation because of the
limited cloud coverage and rainfall (less than 250
mm per year).</li>
        <li><b>Moderately Favourable belt</b>
 This belt lies between the equator and latitude 15°N, and is 
 the next most favourable region for the purpose previously mentioned. 
 Because the humidity is high, and cloud cover is
frequent, the proportion of scattered radiation
is quite high. There is a total of about 2,500 hours
of sunshine per year. The solar intensity is almost 
uniform throughout the year as the seasonal variations are only slight.</li>
        <li><b>Less Favourable belt</b>
 This belt lies between latitude 35°N and
45°N. Although the average solar intensity is
roughly about the same as for the other two belts,
there are marked seasonal variations in both radiation intensity and daylight hours. 
During the winter months solar radiation is relatively lower
than in the rest of the year.</li>
        <li><b>Least Favourable belt</b>
The regions in this belt lie beyond latitude
45°N. They include the USSR, and the greater
parts of northern Europe and North America.
Here, about half of the total radiation is diffuse radiation, with a higher proportion in
winter than in summer primarily because of
the rather frequent and extensive cloud coverage.</li>
    </ul>
</ol>


