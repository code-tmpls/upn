<div class="subheading"><strong><i>Air Transport</i></strong></div>

Air routes can be broadly classified into (i)
Intercontinental (ii) Continental (iii) National and
(iv) Regional air routes.
U.S.A has the largest number of Airports.
London’s Heathrow airport is the busiest airport
in the world. The main national airways include
British airway, Lufthansa (Germany), KLM
(Dutch), Air Italia, Air France, SAS (Norway,
Sweden, Denmark), Qantas (Australia),
Aeroflot (Russia), Japan airlines and United 
airlines, TWA, Pan-American airlines (all USA) etc.

