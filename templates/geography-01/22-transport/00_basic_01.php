<div class="subheading"><strong><i>Ocean Transport</i></strong></div>

The main oceanic routes of the world are:
<ol>
    <ul>
        <li>North Atlantic route: It is the busiest trade
route of the world and connects ports of
Western Europe with ports on the eastern
coast of North America.</li>
        <li>The panama route: It connects the ports of
the pacific with the ports in the Atlantic ocean.</li>
        <li>The Mediterranean:Suez-Asiatic route. It
connects the western European countries
with the Asian countries.</li>
        <li>The cape route: It connects the western
and southern sections of Africa with western Europe.</li>
        <li>South Atlantic route: It links Europe with
the Caribbean islands and the eastern countries of south America.</li>
        <li>The pacific route: It connects the western
seaboard of North America with eastern
part of Asia.</li>
