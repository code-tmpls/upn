<h3>National Income Concept And Accounting</h3>

National income accounting is a branch
of macroeconomics of which estimation of
national income and related aggregates is a part.
Some definitions used in national income
concept:
<ol>
    <ul>
        <li>
        <div class="subheading"><strong><i>What is Economic territory?</i></strong></div>    
        
Economic territory is the geographical
territory administered by a government within
which persons, goods and capital circulate
freely.
<br>
<br>
Those parts of the political frontiers of a
country where the government of India, does
not enjoy the above “freedom” are not to be
included in economic territory of that country.
One example is embassies. Government of
India does not enjoy the above freedom in the
foreign embassies located within India. So,
these are not treated as a part of economic
territory of India. They are treated as part of
the economic territories of their respective
countries.
<br>
<br>
For example the U.S. embassy in India is
a part of economic territory of the U.S.A.
Similarly, the Indian embassy in Washington is
a part of economic territory of India.
Based on ‘freedom’ criterion, the scope of
economic territory is defined to cover:
<ol>
    <ul>
        <li>Political frontiers including territorial
waters and air space.</li>
        <li>Embassies, consulates, military bases, etc
located abroad, but excluding those
located within the political frontiers.</li>
        <li>Ships, aircrafts etc, operated by
the residents between two or more
countries.</li>
        <li>Fishing vessels, oil and natural gas rigs,
etc operated by the residents in the
international waters or other areas over
which the country enjoys the exclusive
rights or jurisdiction.</li>
    </ul>
</ol>
</li>
        <li>
        <div class="subheading"><strong><i>What is the difference between citizen
and resident?</i></strong></div>
            
Citizenship is basically a legal concept
based on the place of birth of the person or
some legal provisions allowing a person to
become a citizen. On the other hand
residentship is basically an economic concept
based on the basic economic activities performed
by a person.
<br>
<br>
A resident, whether a person or an
institution, is one whose centre of economic
interest lies in the economic territory of the
country in which he lives. That is NRI are the
citizens of India but their economic interest lies
in other countries ( a engineer working in US
thus its economic interest in US) thus NRI are
known as Non Resident Indian.</li>
        <li>
        <div class="subheading"><strong><i>What is the difference between National
and Domestic Product?</i></strong></div>
            
National income and related aggregates
are basically measures of production activity.
There are two categories of national income
aggregates: domestic and national, or domestic
product and national product.
<br>
<br>
Production activity of the production units
located within the economic territory is
domestic product irrespective of whether
carried out by the residents or non-residents
i.e. irrespective of whether performed by citizen
of India or not.
National product includes production
activities of residents irrespective of whether
performed within the economic territory or
outside it (such as workers working in Gulf
countries).
<br>
<br>
National product is derived in the following
way:
National product = Domestic product +
residents contribution to production outside the
economic Territory - non-residents contribution
to production inside the economic territory.
In practical estimates the resident’s
contribution outside the economic territory is
called “factor income received from abroad”.
The non-residents’ contribution inside the
economic territory is called “factor income paid
to residents”.
<br>
<br>
Therefore, National product = Domestic
product + Factor income received from abroad
- Factor income paid to abroad.</li>
        <li>
        <div class="subheading"><strong><i>What is the difference between Gross
and Net?</i></strong></div>
            
Gross means total value of products
received from the buyer i.e. if I bought a laptop
and pay 1000 rupees then it is gross and net is
what is received by company after deducting
the depreciation cost, excise duty, taxes etc i.e.
finally company may receive 600 rupees after
deducting all this. Then it is known as Net.</li>
        <li>
        <div class="subheading"><strong><i>What is the difference between market
price and Factor cost?</i></strong></div>
            
If, I buy a bike of 50000 rupees this is the
Gross amount (as discussed above) and when
taxes and depreciation are deducted it becomes
Net.</li>
        <li></li>
    </ul>
</ol>
Some companies even get subsidies for
their products.
Thus Market price is 50000 rupees and
Factor cost is market price – indirect taxes +
Subsidies.
<br>
<br>
This is what is actually available to
production units for distribution of income
among the owners of production house.
Thus National income is defines as the Net
National Product at Factor Cost.
Methods for estimation of National Income
The flow of production, income and
expenditure never stops. It is a circular flow
without a beginning or an end. Production
generates income; income generates demand for
goods and services. And demand leads to
expenditure on the goods and services
produced, so that, the circle of production,
income and expenditure always continues.
Production aspects point to the flow of goods
and services in the economy or the process of
value adding. Income or distribution aspects
points to the generation of income in terms of
the wages, interest, rent and profit and,
expenditure or disposition aspect indicates
disposal of income in terms of consumption
expenditure or investment expenditure.
National income of a country is measured at
three different levels:
    <ol>
        <ul>
            <li><b>Output or Production Method:</b> This
method is also called the value-added
method. This method approaches national
income from the output side. Under this
method, the economy is divided into
different sectors such as agriculture,
fishing, mining, construction,
manufacturing, trade and commerce,
transport, communication and other
services. Then, the gross product is found
out by adding up the net values of all the
production that has taken place in these
sectors during a given year.
In order to arrive at the net value of
production of a given industry,
intermediate goods purchased by the
producers of this industry are deducted
from the gross value of production of that
industry. The aggregate or net values of
production of all the industry and sectors
of the economy plus the net factor income
from abroad will give us the GNP. If we
deduct depreciation from the GNP we get
NNP at market price. NNP at market price
– indirect taxes + subsidies will give us
NNP at factor cost or National Income.
The output method can be used where a
census of production for the year is
calculated. The advantage of this method
is that it reveals the contributions and
relative importance and of the different
sectors of the economy.</li>
            <li><b>Income Method:</b> This method approaches
national income from the distribution side.
According to this method, national
income is obtained by summing up of the
incomes of all individuals in the country.
Thus, national income is calculated by
adding up the rent of land, wages and
salaries of employees, interest on capital,
profits of entrepreneurs and income of selfemployed people.
This method of estimating national income
has the great advantage of indicating the
distribution of national income among
different income groups such as landlords,
capitalists, workers, etc.</li>
            <li><b>Expenditure Method:</b> This method arrives
at the national income by adding up all
the expenditure made on goods and
services during a year. Thus, the national
income is found by adding up the
following types of expenditure by
households, private business enterprises
and the government: -
<ol>
    <ul>
        <li>Expenditure on consumer goods and
services by individuals and households
denoted by C. This is called personal
consumption expenditure denoted by C.</li>
        <li>Expenditure by private business
enterprises on capital goods and on
making additions to inventories or stocks
in a year. This is called gross domestic
private investment denoted by I.</li>
        <li>Government’s expenditure on goods and
services i.e. government purchases denoted
by G.</li>
        <li>Expenditure made by foreigners on goods
and services of the national economy over
and above what this economy spends on
the output of the foreign countries i.e.
exports – imports denoted by (X – M).
Thus, GDP = C + I + G + (X – M).</li>
    </ul>
</ol>
</li>
        </ul>
    </ol>

    <div class="subheading"><strong><i>Limitations of National Income Estimation in
India</i></strong></div>    

There are limitations in the estimation of
National Income in India. These are:
<ol>
    <ul>
        <li>The output of the non-monetized sector is
totally excluded. A difficulty arises in
finding out the imputed value of the
produce of the non-monetized sector.</li>
        <li>Non-availability of data about the income
of small producers or household
enterprises.</li>
        <li>Lack of differentiation in economic
functions.</li>
        <li>Absence of data on income distribution.</li>
        <li>The illegal income remains unreported.</li>
    </ul>
</ol>

<b>Brief history of National income concept</b>
<br>
In 1876, DadaBhai Naoroji was the first
person to prepare estimates of national income
and per capita income for the year 1867-68.
DadaBhai Naoroji estimated national income
at Rs 340 crore and per capita income of Rs 20.
Professor V.K.R.V. Rao estimated National
Income for the year 1931-32 at Rs 1689 crore
and per capita income at Rs 62. Ministry of
Commerce (Govt. of India) estimated national
income at Rs 6234 crore and per capita income
at Rs 198 for the year 1945-46. Presently
Central Statistical Organization measures the
national income of the country.
<br>
<br>
According to UN “an under developed
country is one in which per capita real income
is low when compared to that of US, Canada,
Australia and western Europe.
But a comprehensive measure of
development must take into account the
potential of an economy to use its human,
natural and capital resources to raise the level
of living of its population.



