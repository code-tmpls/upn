<div class="subheading"><strong><i>CONCEPTS IN CAPITAL MARKET</i></strong></div>

<div class="subheading"><strong><i>Derivatives</i></strong></div>

It derives from an underlying assetsecurities, shares, debt instruments,
commodities etc. Derivative is a financial
instrument. The price of the derivative is directly
dependent upon the value of the underlying
asset in the present and the projected future
trends. Futures and options are the two classes
of derivates.

<div class="subheading"><strong><i>Futures</i></strong></div>

Futures are financial instruments bases on
a physical underlying (commodity, equities etc).
A futures contract is an agreement between
two parties to buy or sell an asset at a certain
time in the future for a certain price.

<div class="subheading"><strong><i>Options</i></strong></div>

Options are a class of futures where the
buyer or seller has the option whether to buy
or not. Put option is the right but not the
obligation to sell. Call option is right but not
the obligation to buy.

<div class="subheading"><strong><i>Buyback of Shares</i></strong></div>

Buyback of shares is the process of a
corporation’s repurchase of stock or bonds it
has issued. In the case of stocks, this reduces
the number of shares outstanding, giving each
remaining shareholder a larger percentage
ownership of the company. This is usually
considered a sign that the company’s
management is optimistic about the future and
believes that the current share price is
undervalued.

<div class="subheading"><strong><i>Rolling Settlement</i></strong></div>

Rolling Settlements is a mechanism of
settling trades. In Rolling Settlements, trades
done on a single day are settled separately from
the trades of other day on the basis of Trade
day + 2 days (T+2). Such netting of trades is
done only for the day. As such, in Rolling
Settlement, is carried out on a daily basis.

<div class="subheading"><strong><i>FMC</i></strong></div>

Forward Markets Commission (FMC)
headquartered at Mumbai is a regulatory
authority, which monitors and disciplines the
working of the exchanges. It recognizes an
exchange or can withdraw such recognition. It
collects and whenever the Commission thinks
it necessary publishes information regarding the
trading conditions in respect of goods. It makes
inspection of the accounts and other documents
of any recognized association or registered
association or any member of such association
whenever it considerers it necessary.

<div class="subheading"><strong><i>Mutual Fund</i></strong></div>

Mutual fund is a financial intermediary
that mops up money from a group of investors
to invest in capital market so as to generate
returns for the investors.
Global Depository Receipts (GDR)
Indian companies are allowed to raise
equity capital in the international market
through the issue of Global Depository Receipt
(GDRs). GDRs are designated in dollars/euros.
They are also called euroissues. The proceeds
of the GDRs can be used for financing capital
goods imports, capital expenditure including
domestic purchase/installation of plant,
equipment and building and investment in
software development, prepayment or
scheduled repayment of earlier external
borrowings, and equity investment in JVs in
India.

<div class="subheading"><strong><i>American Depository Receipts (ADR)</i></strong></div>

American depository receipts are issued
to US retail and institutional investors. They
are entitled to bonus, stock split and dividend.
They are listed either on Nasdaq or NYSE. They
help raise equity capital in forex for various
benefits like expansion, acquisition etc.

<div class="subheading"><strong><i>Participatory Notes (PN)</i></strong></div>

Participatory Notes are instruments used
for making investments in the stock markets.
In India, Foreign Institutional Investors use
these instruments for facilitating the
participation of overseas funds like hedge funds
and others who are either not interested or not
eligible for participating directly in the Indian
stock market.

<div class="subheading"><strong><i>Clearing House</i></strong></div>

An organization which registers, monitors,
matches and guarantees the trades of its
members and carries out the final settlement of
all futures transactions. The National Securities
Clearing Corporation is the clearing house for
the NSE.

<div class="subheading"><strong><i>Share</i></strong></div>

Share is a certificate representing
ownership of the company that issued it. Shares
can yield dividends and entitle the holder to
vote at general meetings. The company may be
listed on a stock exchange. Shares are also
known as stock or equity.

<div class="subheading"><strong><i>Bond</i></strong></div>

A debt instrument issued for a period of
more than one year with the purpose of raising
capital by borrowing.

<div class="subheading"><strong><i>Debenture</i></strong></div>

Debt not secured by a specific asset of the
corporation, but issued against the issuer’s
general credit, that is, it is unsecured debt.

<div class="subheading"><strong><i>Bear Market</i></strong></div>

A sustained period of falling stock prices
usually preceding or accompanied by a period
of poor economic performance.

<div class="subheading"><strong><i>Bull Market</i></strong></div>

A stock market characterized by rising
prices over a long period of time. The time span
is not precise, but it represents a period of
investor optimism, lower interest rates and
economic growth.

<div class="subheading"><strong><i>Market capitalization</i></strong></div>

It is the market’s total valuation of a public
company. Price per share multiplied by the total
number of shares outstanding gives market
capitalization of a company.

<div class="subheading"><strong><i>Insider Trading</i></strong></div>

Insider trading occurs when any one with
information related to strategic and priceinfluencing information purchases or sells stocks
so as to make speculative profits.
National Securities Depository Limited (NSDL)
The enactment of Depositories Act in 1996
led to the establishment of NSDL, the first
depository in India. It holds more than 85% of
the securities held in dematerialized mode in
India. Promoted by IDBI, UTI and NSE, NSDL
has established a national infrastructure that
handles most of the securities held and settled
in dematerialized form in the Indian capital
markets.



