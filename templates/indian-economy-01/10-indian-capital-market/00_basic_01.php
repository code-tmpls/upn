<h3>Indian Capital Market</h3>

The capital market provides an alternative
mechanism for allocating resources; channelises
household savings to the corporate sector and
allocates funds among firms. In this process it
allows both firms and households to share risk.
The capital market enables the valuation of firms
on an almost continuous basis and it plays an
important role in the governance of the
corporate sector. The reforms in the capital
market are aimed at enhancing the efficiency,
safety, integrity and transparency of the
market.
<br>
<br>
Capital market is the market for long term
funds, just as the money market is the market
for short term funds. Capital market refers to
all the facilities and the institutional
arrangement for borrowing and lending
medium and long term funds. It is concerned
with the raising of money/capital for
investment purpose. Private sector
manufacturing industries, agriculture and the
government predominantly make demand of
long term capital for purpose of investment.
The central and state governments require
substantial sums from the capital market and
they invest not only on economic overheads as
transport, irrigation and power development
but also on basic industries and sometimes even
consumer goods industries. Individual savers,
corporate savings, banks, insurance companies,
specialized financing agencies and government
are important supplier of funds for the capital
market. Among institutions, commercial banks
are important investors, but are largely
interested in government securities and to a
small extent on investment in debentures of
companies; LIC and GIC are of growing
importance in the Indian capital market though
their major interest is still in government
securities; Private funds constitute a major
medium of savings but their investments too
are mostly in government securities; and special
institutions, set up since independence—IFCI,
ICICI, IDBI, UTI etc.—all these aim at providing
long term capital to the private sector.
<br>
<br>
Gilt-edged market and the industrial
securities market are two prominent
constituents of the Indian capital market. The
gilt-edged market refers to the market for
government and semi-government securities
backed by the Reserve Bank of India.
Structure of the Capital Market
The capital market can be divided into two
constituents, the financial institutions and the
securities market. The financial institutions
provide long term and medium term loans. The
securities market is divided into (a) the gilt
edged market (or the market for government
securities), and (b) the corporate or industrial
securities market.

