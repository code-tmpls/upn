        <div class="subheading"><strong><i>Gilt-Edged market</i></strong></div>
            
The gilt-edged market is also known as
the securities guaranteed (both principal and
interest) by the government apart from
government securities. The government
securities are risk free because the government
can’t default on its payment obligations and
are hence known as gilt-edged (which means
'of the best quality'). The important
characteristics of the government securities are:
<ol>
    <ul>
        <li>It is without risk and returns are
guaranteed. There is no place for
speculation and manipulation of the
market as also no uncertainties regarding
yield, payment on time, etc.</li>
        <li>Government securities market consists of
the new issues market and the secondary
market. R.B.I. is responsible for all the new
issues of government loans, as it manages
entirely the public debt operations of both
the central and state governments. The
secondary market deals in old issues.</li>
        <li>The investors in the gilt-edged market are
predominantly institutions—commercial
banks, Life Insurance Corporation,
General Insurance Corporation and the
provident funds—which are required
statutorily to invest a certain portion of
their funds in government securities. These
institutions mobilise the savings of the
people through their various scheme and
invest a certain proportion in government
securities.</li>
        <li>Government securities are the most liquid
debt instruments.</li>
        <li>The transactions in the government
securities market are large.
Many private sector mutual funds have
entered this market in a considerable way in
recent times and have floated various gilt-edge
funds for this purpose on account of the riskfree returns guaranteed by the government
securities market and its high liquidity. Many
risk-averse individual investors ( particularly
those belonging to fixed-income groups) have
welcomed this opportunity as investment in
guilt-edged funds is better than investment in
bank fixed deposits due to better liquidity and
exemption from tax on dividends.</li>
    </ul>
</ol>
</li>
        <li>
            
        <div class="subheading"><strong><i>Corporate Securities Market</i></strong></div>
            
Securities issued by firms (i.e. shares,
bonds and debentures) can be bought and sold
freely in corporate securities market. It
comprises the new issues market (the primary
market) and the secondary market (stock
exchanges).</li>
        <li>
        <div class="subheading"><strong><i>The New Issues Market/Primary Market</i></strong></div>    
        
The new issues market is concerned with
the issue of new securities—bonds debentures,
shares and so on. Funds are often raised by the
public limited companies from the primary
market for setting up or expanding their
business. However, the company has to fulfill
various requirements and decide upon the
appropriate timing and method of issue for
selling its securities. The various methods
through which capital can be raised are:
<ol>
    <ul>
        <li>By Prospectus: Capital can be raised from
the general public by the issue of
prospectus which is an invitation to the
general public for subscribing to the
capital. The prospectus contains various
details regarding particulars of the
company, its financial position etc.</li>
        <li>By Offer for Sale: A method similar to
the prospectus where initially shares are
taken up by a third party in bulk. The
peculiar feature of this arrangement is that
while company already receives the
money, any premium received from the
public goes to this third party rather than
to the company.</li>
        <li>By Private Placing: The shares are sold to
individuals or institutions directly by
making a private appeal to them. The cost
of raising capital in this method becomes
far lesser than the cost of raising capital
via other methods.</li>
        <li>By offering Rights Issue: By this,
companies raise capital from the existing
shareholders. Under this method of
issuance, the shareholders have the right
to a certain number of shares in
proportion to the shares held by them.</li>
    </ul>
</ol>
</li>
        <li>
        <div class="subheading"><strong><i>The Secondary Market/ Stock Exchange</i></strong></div>
            
The stock exchange or the secondary
market is a highly organized market for the
purchase and sale of second-hand quoted or
listed securities (quoting or listing of a particular
security implies incorporating that security in
the register of the stock exchange so that it can
be bought and sold there). A stock exchange is
an association, organization or body of
individuals, whether incorporated or not,
established for the purpose of assisting,
regulating and controlling business in buying,
selling and dealing in securities, as has been
defined by the securities Contracts (Regulation)
Act, 1956.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>STOCK EXCHANGES IN INDIA</i></strong></div>

With over 25m shareholders, India has the
third largest investor base in the world after
the USA and Japan. Over 9,000 companies are
listed on the stock exchanges, which are services
by approximately 7,500 stockbrokers. The India
capital market is significant in terms of the
degree of development, volume of trading and
its tremendous growth potential.
Stock exchanges provide an organised
market for transactions in securities and other
securities. There are 23 stock exchanges in the
country, 21 of them being regional ones with
allocated areas. Three others set up in the
reforms era, viz., National Stock Exchange
(NSE), the Over the Counter Exchange of India
Limited (OTCEI) and Inter-connected Stock
Exchange of India Limited (ISE). Important
Stock Exchanges in India are Bombay Stock
Exchange, popularly known as BSE and
National Stock Exchange located in Mumbai.
<br>
<br>
The stock exchanges in India are located at
Ludhiana, New Delhi, Jaipur, Merrut, Ahmedabad, Rajkot, Indore, Vadodara, Bombay, Pune,
Hyderabad, Mangalore, Bangalore, Ernakulam,
Coimbatore, Madras, Patna, Kanpur,
Bhubaneshwar, Calcutta and Guwahati.

<div class="subheading"><strong><i>Bombay Stock Exchange (BSE)</i></strong></div>

The Bombay Stock Exchange or BSE is the
oldest stock exchange in Asia. The Bombay
Stock Exchange was established in 1875. There
are around 4,800 Indian companies listed with
the stock exchange. As of 2008, it is among the
five biggest stock exchanges in the world in
terms of transactions volume. In 2005, the status
of the exchange changed from an Association
of Persons (AoP) to a full fledged corporation
under the BSE (Corporatization and
Demutualization) Scheme, 2005 and its name
was changed to the Bombay Stock Exchange
Limited. The BSE Sensex (Sensitive Index), also
called the BSE 30, is a widely used market index
in India and Asia.
<br>
<br>
Sensex or Sensitive Index is a valueweighted index composed of 30 companies with
the base 1978-1979 = 100. It consists of the 30
largest and most actively traded blue chip
stocks, representative of various sectors, on the
Bombay Stock Exchange. Inclusion of the
company is basically on the basis of market
capitalization. The 30 companies in the index
are revised periodically – some are replaced by
others and new sectors may find representation
as the economy evolves.
