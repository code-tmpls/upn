<div class="subheading"><strong><i>WHAT ARE ADR, GDR AND IDR?</i></strong></div>

A depositary receipt (DR) is a type of
negotiable (transferable) financial security that
is traded on a local stock exchange but
represents a security, usually in the form of
equity that is issued by a foreign publicly listed
company. The DR, which is a physical
certificate, allows investors to hold shares in
equity of other countries.
<br>
<br>
The increasing demand for Depositary
Receipts is driven by the desire of individual
and institutional investors to diversify
their portfolios, reduce risk and invest
internationally in the most efficient manner
possible. While most investors recognize the
benefits of global diversification, they also
understand the challenges presented when
investing directly in local trading markets. These
obstacles can include inefficient trade
settlements, uncertain custody services
and costly currency conversions. But
Depositary Receipts offer cost benefits and
conveniences.

<div class="subheading"><strong><i>How Does the DR Work?</i></strong></div>

The DR is created when a foreign
company wishes to list its already publicly
traded shares or debt securities on a
foreign stock exchange. Initial public
offerings, however, can also issue a Depository
Receipt.
<br>
<br>
To allow creation of DRs, the shares of
the foreign company, which the DRs represent,
are first of all delivered and deposited with the
custodian bank of the depository through
which they intend to create the DR. On
receipt of the delivery of shares, the custodial
bank creates DRs and issues the same to
investors in the country where the DRs are
intended to be listed. These DRs are then listed
and traded in the local stock exchanges of that
country.

<div class="subheading"><strong><i>American Depository Receipt</i></strong></div>

ADRs allow U.S. investors to invest in nonU.S. companies and give non-U.S. companies
easier access to the U.S. capital markets.
The first ADR was created in 1927 by a
U.S. bank to allow U.S. investors to invest in
shares of a British department store. Today,
there are more than 2,000 ADRs available
representing shares of companies located in
more than 70 countries.
ADRs may be “sponsored” or
“unsponsored.” Sponsored ADRs are those in
which the non-U.S. company enters into an
agreement directly with the U.S. depositary
bank to arrange for recordkeeping, forwarding
of shareholder communications, payment of
dividends, and other services. An unsponsored
ADR is set up without the cooperation of the
non U.S. Company and may be initiated by a
broker dealer wishing to establish a U.S. trading
market.

<div class="subheading"><strong><i>Global Depository Receipt</i></strong></div>

A global depository receipt is a certificate
issued by a depository bank, which purchases
shares of foreign companies and deposits it on
the account. GDRs represent ownership of an
underlying number of shares. Global depository
receipts facilitate trade of shares, and are
commonly used to invest in companies from
developing or emerging markets.
<br>
<br>
The typical GDR structure offers DRs in
Europe or other non-US markets pursuant to
Regulation S (Reg S) promulgated under the
US Securities Act of 1933. GDRs are listed on
a European stock exchange such as London or
Luxembourg and clear through the
Euromarkets clearing systems, Euroclear and
Clearstream. GDRs may also be listed on other
exchanges, such as Dubai and Singapore. The
ability of retail investors to purchase GDRs
will depend on the type and location of
the listing. In general, however, GDR offerings
are aimed at institutional investors and
depending on the exchange, they can then be
purchased by retail investors in the secondary
market.

<div class="subheading"><strong><i>Indian Depository Receipt</i></strong></div>

An Indian Depository Receipt (IDR) is an
instrument denominated in Indian Rupees in
the form of a depository receipt created by a
Domestic Depository (custodian of securities
registered with the SEBI) against the underlying
equity of issuing company to enable foreign
companies to raise funds from the Indian
Securities Markets.
<br>
<br>
The foreign company IDRs will deposit
shares to an Indian Depository. The depository
would issue receipys to investors in India
against these shares. The benefit of underlying
shares (like bonus, dividends etc.) would accrue
to the depository receipt holders in India
Standard Chartered PLC became the first
global company to file for an issue of Indian
depository receipts in India.
