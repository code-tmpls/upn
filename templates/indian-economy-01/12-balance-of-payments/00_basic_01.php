<h3>Balance of Payments</h3>

The balance of payments of a country is a
systematic record of all economic transactions
between the residents of the reporting country
and residents of foreign countries during a given
period of time.
<br>
<br>
The balance of payment record is
maintained in a standard double-entry bookkeeping method. International transactions are
entered into the record as credit or debit. The
payments received from foreign countries are
entered as credit and payments made to other
countries as debit.
<br>
<br>
A complete BoP account comprises the
following two broad accounts: (a) Current
Account; and (b) Capital and Financial
Account.

<div class="subheading"><strong><i>Current Account Transactions</i></strong></div>

The current account deals with the trade
of goods and services between two countries.
An export is a good (or service) that is sent from
the domestic country and purchased abroad.
An import is a foreign produced good that is
imported for domestic consumption. The
monetary value of exports from a country and
imports into a country are measured in the
current account. If the value of a country's
exports exceeds the value of the goods and
services it imports, then that country has a trade
surplus otherwise a trade deficit.
<br>
<br>
In brief current account includes:
<ol>
    <ul>
        <li>Payments related to foreign trade, current
business and services.</li>
        <li>Short term banking and Credit
facilities.</li>
        <li>Payments due as interest on loans and
dividends.</li>
        <li>Foreign travel, education, medical
expenses.</li>
        <li>Remittances i.e. money sent by individuals
working abroad back at home.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Capital account Transactions</i></strong></div>

It includes those transactions which are
undertaken by a resident of India such that
his/her assets or liabilities outside India are
altered (either increased or decreased). For
example: - (i) a resident of India acquire an
immovable property outside India or acquire
shares of a foreign company. This way his/her
overseas assets are increased; or (ii) a resident
of India borrows from a non-resident
through External commercial Borrowings
(ECBs). This way he/she has created a liability
outside India.

<div class="subheading"><strong><i>It includes:</i></strong></div>

Foreign direct investment (FDI) refers to
long term capital investment such as the
purchase or construction of machinery,
buildings or even whole manufacturing
plants. If foreigners are investing in a country,
that is an inbound flow and counts as a surplus
item on the capital account. If a nation's
citizens are investing in foreign countries,
that's an outbound flow that will count as a
deficit.
Portfolio investment refers to the purchase
of shares and bonds in Indian share market by
the foreigners.
Other investment includes capital flows
into bank accounts or provided as loans. Large
short term flows between accounts in different
nations are commonly seen when the market is
able to take advantage of fluctuations in interest
rates and/or the exchange rate between
currencies.
<br>
<br>
Reserve account, the reserve account
is operated by a nation's central bank i.e.
RBI to buy and sell foreign currencies; it
can be a source of large capital flows
to counteract those originating from the
market.

<div class="subheading"><strong><i>WHAT ARE EXTERNAL
COMMERCIAL BORROWINGS?</i></strong></div>

External Commercial Borrowings (ECB)
refer to commercial loans [in the form of bank
loans, buyers’ credit, suppliers’ credit,
securitised instruments (e.g. floating rate notes
and fixed rate bonds)] availed from non-resident
lenders with minimum average maturity of 3
years. Even loans from Foreign Equity Holders
are considered as ECBs.
Thus ECBs mean foreign currency loan
raised by residents from recognised lenders.
ECB can be accessed under two routes,
viz., (i) Automatic Route and (ii) Approval
Route
<ol>
    <ul>
        <li>
        <div class="subheading"><strong><i>AUTOMATIC ROUTE</i></strong></div>    
        
Eligible borrowers</li>
        <li>Corporates (registered under the
Companies Act, except financial
intermediaries (such as banks, financial
institutions (FIs), housing finance
companies and NBFCs) are eligible to raise
ECB.</li>
        <li>Individuals, Trusts and Non- Profit making
Organisations are not eligible to raise ECB.</li>
        <li>Units in Special Economic Zones (SEZ) are
allowed to raise ECB for their own
