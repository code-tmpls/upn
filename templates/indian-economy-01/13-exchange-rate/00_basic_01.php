<h3>Exchange Rate</h3>

The exchange rate expresses the national
currency's quotation in respect to foreign ones.
For example, if one US dollar is worth 50
rupees, then the exchange rate of dollar is 50.
If something costs Rs. 150, it automatically costs
3 US dollars as a matter of accountancy. Thus,
the exchange rate is a conversion factor, a
multiplier or a ratio, depending on the direction
of conversion.
<br>
<br>
Each country, through varying
mechanisms, manages the value of its currency.
As part of this function, it determines
the exchange rate regime that will apply to
its currency. For example, the currency
may be free-floating, pegged or fixed, or a
hybrid.
<br>
<br>
When the exchange rate can freely move,
assuming any value that private demand and
supply jointly establish, it is called "flexible"
exchange rate.
If the central bank timely and significantly
intervenes on the currency market, then a
"managed floating exchange rate regime" takes
place.
<br>
<br>
In "freely" and "managed" floating
regimes, a loss in currency value is
conventionally called”depreciation", whereas
an increase of currency's international
value will be called "appreciation". If the
dollar rises from 50 rupees to 60, then it
has shown depreciation i.e. value of
Indian currency is decreasing with respect to
Dollar.
<br>
<br>
But central banks can also declare a fixed
exchange rate, offering to supply or buy
any quantity of domestic or foreign currencies
at that rate. Under this regime, a loss of
value, usually forced by market or a purposeful
policy action, is called”devaluation", whereas
an increase of international value is a
"revaluation".

<div class="subheading"><strong><i>Merits and demerits of fixed and flexible
foreign exchange rates:</i></strong></div>

<b>Merits of fixed exchange rate:</b>
<ol>
    <ul>
        <li>It ensures stability in exchange rate. The
exporters and importers have not to
operate under uncertainty about the
exchange rate. Thus it promotes foreign
trade.</li>
        <li>It promotes capital movements. Fixed
exchange rate system attracts foreign
capital because a stable currency does not
involve any uncertainties about exchange
rate that may cause capital loss.</li>
        <li>Stable exchange rate prevents capital
outflow.</li>
        <li>It prevents speculation in foreign exchange
market.</li>
        <li>It forces the government to keep inflation
in check. In case of fixed exchange rate
system, inflation causes balance of
payments deficit resulting in depletion of
foreign exchange reserves.</li>
    </ul>
</ol>

<b>Demerits of fixed exchange rate:</b>
<ol>
    <ul>
        <li>It contradicts the objective of having free markets</li>
        <li>Under this system, countries with deficits
in balance of payment run down this stock
of gold and foreign currencies. This can
create serious problem for them. They
may be forced to devalue their currency.
On the other hand countries with surplus
in balance of payments will face the
problem of inflation.</li>
        <li>There may be undervaluation or
overvaluation of currency. If the fixed
exchange rate is at a level which is lower
then the market level i.e. at which demand
for foreign currency far exceeds its supply,
it will result in deficit in balance of
payment. If it is higher than the market
level i.e. at which the supply exceeds
demand then it may create inflationary
pressure because of balance of payments
surplus. It is difficult to fix a rate that
may prove to be equilibrium rate
Merits of flexible exchange rate system.</li>
        <li>It eliminates the problem of overvaluation
or undervaluation of currencies, Deficit or
surplus in balance of payments is
automatically corrected under this system.</li>
        <li>It frees the government from problem of
balance of payments.</li>
        <li>There is no need for the government to
hold any reserves.</li>
        <li>It enhances the efficiency in the economy
by achieving optimum resource allocation.</li>
    </ul>
</ol>

<b>Demerits of flexible exchange rate system</b>
<ol>
    <ul>
        <li>It creates situations of instability and
uncertainty. Wide fluctuations in
exchange rate are possible. This hampers
foreign trade and capital movements
between countries.</li>
        <li>It encourages speculation which may lead
to larger uncertainties and fluctuations.</li>
        <li>The uncertainty caused by currency
fluctuations can discourage international
trade and investment.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>RUPEE DEPRECIATION CONCEPT</i></strong></div>

Exchange rate is defined as rate or price
at which one country’s currency is exchanged
for another country’s currencies. It depends on
the comparative trade advantages and
economic strengths of the countries. Higher the
demand the stronger it becomes. However for
currencies like Rupee which are not traded on
exchanges, the value depends on capital inflows
in the country.
<br>
<br>
Rupee depre-ci-a-tion means that rupee
has become less valu-able with respect to dol-lar.
If the rupee moves upward from 30 per dol-lar
to 40 per dol-lar then rupee is said to
depre-ci-ate. It means that rupee is now cheaper
than what it used to be earlier.

<div class="subheading"><strong><i>Causes for depreciation</i></strong></div>
<ol>
    <ul>
        <li>
        <div class="subheading"><strong><i>Stock market performance</i></strong></div>    
        
It is a known fact that Indian stock market
is dominated by overseas investors. When
the economy is performing well and stock
market is performing better than other
countries, overseas investors will become
heavy investors here. To invest here, they
require rupee. This will increase the
demand for rupee and will result in higher
value for rupee. On the other hand, when
these investors are pulling money out of
Indian stock market, rupee will be
depreciated. Indian markets are in a bad
shape for the last 1 year. The sentiments
after the US downgrade and the European
crisis etc. resulted in overseas investors
selling in India and buying dollars.
In a bad performing market, when there
is depreciation in rupee, it will bring down
the overseas investors real returns. So they
will start selling, which will again deepen
the situation. Very high prices for gold
have created panic among investors and
fearing a bubble there, investors started
moving towards dollar. This demand in
dollar is also causing depreciation of rupee.</li>
        <li>
        <div class="subheading"><strong><i>Current account deficit</i></strong></div>    
        
Current account deficit occurs when a
country’s total import exceeds the total
exports. This makes the country, a net
debtor to the rest of the world. This is not
good for the country because, the country
needs to buy more foreign currency. More
demand for the foreign currency will
reduce the value of that country’s
currency. India’s current account deficit
is more than the expected level now and
this also contributes to the depreciation of
Indian rupee.</li>
        <li>
        <div class="subheading"><strong><i>Corruption and Political paralysis</i></strong></div>
            
In the last 1 year, lot of corruption issues
came to limelight thus affecting investor
sentiments globally.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Rupee depreciation: who are affected?</i></strong></div>

<ol>
    <ul>
        <li>Foreign investment: Any investment
abroad, whether in stocks or real estate,
would become dearer. For instance, to buy
stocks worth a dollar, now a person
would be required to pay around Rs. 50
compared with Rs.45 few months ago.
However, since different countries have
different currencies, the actual impact
would depend upon the fluctuations of
currency of a country in which a person
is investing. Normally, the rupee is first
converted into dollars, which in turn is
converted into the currency of the third
country. Hence, the actual impact would
vary according to the level of depreciation
in rupee vis-a-vis the third currency.</li>
        <li>Foreign education (for aspiring
students): Students who plan for next year
admissions will end up paying more for
the forms of exams such as Test of English
as a Foreign Language (TOEFL), Graduate
Record Exam (GRE) and Graduate
Management Admission Test (GMAT).
These are entrance exams that students
take to prove their eligibility for studying
abroad in terms of basic language, skills
and IQ. Moreover, the application forms
of foreign universities, which cost $50-
1,000, will be more expensive for Indian
students.</li>
        <li>Foreign education (for existing
students): The overall budget will be
affected as every item in the budget list
will witness a change, as apart from the
application fee, examination fee and the
tuition fee, even the cost of living will be
higher for the students. This will have an
impact on students who have delayed
paying their fees to the universities for the
July-September 2011 batch. Students may
now have to pay Rs. 50,000-1 lakh over
the fee which they were to pay earlier
this year.</li>
        <li>Foreign travel: The depreciating rupee
will increase the cost of foreign travelling.</li>
    </ul>
</ol>

