<div class="subheading"><strong><i>Effect on Export and Import</i></strong></div>

For equity investors, the rupee depreciation
would have a twin impact. Some sectors that are
primarily export oriented stand to gain from rupee
depreciation while sectors dependent on import may
be affected adversely. These include petroleum
products, metals, capital goods and power.
A number of steps have been taken
recently to stimulate capital inflows and curb
speculation in foreign exchange market to
stabilize the value of the rupee.

