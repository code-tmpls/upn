<div class="subheading"><strong><i>FOREIGN EXCHANGE
MANAGEMENT ACT (FEMA)</i></strong></div>

When a business enterprise imports goods
from other countries, exports its products to
them or makes investments abroad, it deals in
foreign exchange. Foreign exchange means
'foreign currency' and includes: - (I) deposits,
credits and balances payable in any foreign
currency; (II) drafts, travellers' cheques, letters
of credit or bills of exchange, expressed or
drawn in Indian currency but payable in any
foreign currency; and (III) drafts, travellers'
cheques, letters of credit or bills of exchange
drawn by banks, institutions or persons outside
India, but payable in Indian currency.
In India, all transactions that include
foreign exchange were regulated by Foreign
Exchange Regulation Act (FERA), 1973.
<br>
<br>
The main objective of FERA was
conservation and proper utilization of the
foreign exchange resources of the country. It
also sought to control certain aspects of the
conduct of business outside the country by
Indian companies and in India by foreign
companies. It was a criminal legislation
(punishable with imprisonment as per code of
criminal procedure) which meant that its
violation would lead to imprisonment and
payment of heavy fine. It had many restrictive
clauses which deterred foreign investments.
In the light of economic reforms and the
liberalized scenario, FERA was replaced by a
new Act called the Foreign Exchange
Management Act (FEMA), 1999. The Act
applies to all branches, offices and agencies
outside India, owned or controlled by a person
resident in India.
<br>
<br>
FEMA emerged as an investor friendly
legislation which is purely a civil legislation in
the sense that its violation implies only payment
of monetary penalties and fines. However,
under it, a person will be liable to civil
imprisonment only if he does not pay the
prescribed fine within 90 days from the date of
notice but that too happens after formalities of
show cause notice and personal hearing. FEMA
also provides for a two year sunset clause for
offences committed under FERA which may
be taken as the transition period granted for
moving from one 'harsh' law to the other
'industry friendly' legislation.
Broadly, the objectives of FEMA are:
<ol>
    <ul>
        <li>To facilitate external trade and payments;</li>
        <li>To promote the orderly development and
maintenance of foreign exchange market.</li>
    </ul>
</ol>

The Act has assigned an important role to
the Reserve Bank of India (RBI) in the
administration of FEMA. The rules, regulations
and norms pertaining to several sections of the
Act are laid down by the Reserve Bank of
India, in consultation with the Central
Government. The Act requires the Central
Government to appoint as many officers of the
Central Government as Adjudicating
Authorities for holding inquiries pertaining to
contravention of the Act.
<br>
<br>
There is also a provision for appointing
one or more Special Directors (Appeals) to hear
appeals against the order of the Adjudicating
authorities. The Central Government also
establishes an Appellate Tribunal for Foreign
Exchange to hear appeals against the orders of
the Adjudicating Authorities and the Special
Director (Appeals).
<br>
<br>
The FEMA provides for the establishment,
by the Central Government, of a Director of
Enforcement with a Director and such other
officers or class of officers as it thinks fit for
taking up for investigation of the contraventions
under this Act.
<br>
<br>
The Act deals with two types of foreign
exchange transactions – Capital Account
Transactions and Current Account
Transactions.
FEMA permits only authorized persons to
deal in foreign exchange or foreign security.
Such an authorized person, under the Act,
means authorized dealer, money changer, offshore banking unit or any other person for the
time being authorized by Reserve Bank.


