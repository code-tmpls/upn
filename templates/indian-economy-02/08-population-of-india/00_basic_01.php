<h3>Population Of India</h3>

<div class="subheading"><strong><i>SOME IMPORTANT CENSUS
CONCEPTS</i></strong></div>

<div class="subheading"><strong><i>Rural-Urban Areas</i></strong></div>

The data in the table on Final Population
Totals census 2011 are presented separately for
rural and urban areas. The unit of classification in this regard is 'town' for urban areas and
'village' for rural areas. In the Census of India
2011, the definition of urban area adopted is as
follows: (a) All statutory places with a municipality, corporation, cantonment board or notified town area committee, etc. (b) All other
places satisfying the following three criteria simultaneously:
<ol>
    <ul>
        <li>a minimum population of 5,000;</li>
        <li>at least 75 per cent of male 
            working population engaged in non-agricultural pursuits;</li>
        <li>a density of population of at least 400 per
sq. km. (1,000 per sq. mile).</li>
    </ul>
</ol>

For identification of places which would qualify
to be classified as 'urban' all villages, which, as
per the 2001 Census had a population of 4,000
and above, a population density of 400 persons
per sq. km. and having at least 75 per cent of
male working population engaged in non-agricultural activity were considered. To work out
the proportion of male working population referred to above against b)(ii), the data relating to
main workers were taken into account.
<br>
<br>
An Urban Agglomeration is a continuous urban spread constituting a town and its adjoining urban outgrowths (OGs) or two or more
physically contiguous towns together and any
adjoining urban outgrowths of such towns.
Examples of OGs are railway colonies, university campuses, port areas, etc., that may come
up near a city or statutory town outside its
statutory limits but within the revenue limits of
a village or villages contiguous to the town or
city. Each such individual area by itself may
not satisfy the minimum population limit to
qualify it to be treated as an independent urban unit but may deserve to be clubbed with
the town as a continuous urban spread.
For the purpose of delineation of Urban Agglomerations during Census of India 2011, following criteria are taken as pre-requisites: (a)
The core town or at least one of the constituent
towns of an urban agglomeration should necessarily be a statutory town; and (b) The total
population of all the constituents (i.e. towns
and outgrowths) of an Urban Agglomeration
should not be less than 20,000 (as per the 2001
Census). With these two basic criteria having
been met, the following are the possible different situations in which Urban Agglomerations
would be constituted: (i) a city or town with
one or more contiguous outgrowths; (ii) two or
more adjoining towns with their outgrowths;
and (iii) a city and one or more adjoining towns
with their outgrowths all of which form a continuous spread.

<div class="subheading"><strong><i>City</i></strong></div>

Towns with population of 1,00,000 and
above are called cities.

<div class="subheading"><strong><i>Scheduled Castes & Scheduled Tribes</i></strong></div>

Article 341 of the Constitution provides that
the President may, with respect to any State or
Union territory, specify the castes, races or tribes
or parts of or groups within castes, races or tribes
which shall for the purposes of the Constitution
be deemed to be Scheduled Castes in relation to
that State or Union territory. Similarly, Article
342 provides for specification of tribes or tribal
communities or parts of or groups within tribes
or tribal communities which are deemed to be for
the purposes of the Constitution the Scheduled
Tribes in relation to that State or Union territory.
In pursuance of these provisions, the list of Scheduled Castes and / or Scheduled Tribes are notified for each State and Union territory and are
valid only within the jurisdiction of that State or
Union territory and not outside.
<br>
<br>
It is important to mention here that under
the Constitution (Scheduled Castes) Order,
1950, no person who professed a religion different from Hinduism was deemed to be a
member of a Scheduled Caste in addition to
every member of the Ramdasi, Kabirpanthi,
Majhabi or Sikligar caste resident in Punjab or
Patiala and East Punjab States Union were in
relation to that State whether they professed
the Hindu or the Sikh religion. Subsequently, in
September, 1956, by an amendment, the Presidential Order of 1950 and in all subsequent
Presidential Orders relating to Scheduled Castes,
the population professing the Hindu and the
Sikh religions were placed on the same footing
with regard to their inclusion as Scheduled
Castes. Later on, as per the amendment made
in the Constitution (Scheduled Castes) Order
1990, the Hindu, the Sikh and the Buddhist
professing population were placed on the same
footing with regard to the recognition of the
Scheduled Castes.
<br>
<br>
For finalizing the list of Schedule Castes/
Scheduled Tribes notified in each state/union
territory, all the constitutional amendments that
have taken place prior to the conduct of 2011
census were taken into account. Since there is
no Scheduled Castes list for the state of Nagaland
and the Union territories of Andaman & Nicobar
Islands and Lakshadweep; and no Scheduled
Tribes list for the States of Delhi, Haryana and
Punjab and the Union territories of Chandigarh
and Puducherry, the Scheduled Castes and
Scheduled Tribes population figures are furnished for only the relevant category in respect
of these States and Union territories.

<div class="subheading"><strong><i>Literates</i></strong></div>

A person aged 7 years and above who can
both read and write with understanding in any
language has been taken as literate. It is not
necessary for a person to have received any
formal education or passed any minimum educational standard for being treated as literate.
People who were blind and could read in Braille
are treated to be literates.
<br>
<br>
A person, who can neither read nor write or
can only read but cannot write in any language,
is treated as illiterate. All children of age 6 years
or less, even if going to school and have picked
up reading and writing, are treated as illiterate.
Sex Ratio
Sex ratio has been defined as the number of
females per 1000 males in the population. It is
expressed as 'number of females per 1000 males'.
<br>
<br>
<b>Sex-ratio =</b>Number of females/Number of males × 1000

<div class="subheading"><strong><i>Literacy Rate</i></strong></div>

Literacy rate of population is defined as the
percentage of literates to the total population
of age 7 years and above.
<br>
<br>
<b>Literacy rate</b> = Number of Literates 1000/Population aged 7+ × 1000

<div class="subheading"><strong><i>Work Participation Rate</i></strong></div>

Work participation rate is defined as the percentage of total workers (main and marginal)
to total population.
<br>
<br>
<b>Work participation rate</b> = Total Workers (Main+Marginal)/Total Population × 100

<div class="subheading"><strong><i>Definition of Slum</i></strong></div>

Slums have come to form an integral part of
the phenomena of urbanization in India. Comprehensive information on the slums being essential for formulation of effective and coordinated
policy for their improvement. Formation and identification of slum enumeration blocks prior to the
conduct of 2011 Census has made it possible to
compile and repare special tables for slums. It is
for the first time in the history of census in the
country that the slum demography is being presented on the basis of the actual count. The systematic delineation of slums for collection of primary data on their population characteristics
during population enumeration itself may perhaps be the first of its type in the world.
For the purpose of Census of India, 2011, the
slum areas broadly constitute of :-
<ol>
    <ul>
        <li>All specified areas in a town or city notified as 'Slum' by State/Local Government
and UT Administration under any Act including a 'Slum Act'.</li>
        <li>All areas recognized as 'Slum' by State/
Local Government and UT Administration,
Housing and Slum Boards, which may
have not been formally notified as slum
under any act;</li>
        <li>A compact area of at least 300 population
or about 60-70 households of poorly built
congested tenements, in unhygienic environment usually with inadequate infrastructure and lacking in proper sanitary
and drinking water facilities.</li>
    </ul>
</ol>

