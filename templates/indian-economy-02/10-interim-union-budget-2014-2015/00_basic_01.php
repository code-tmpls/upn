<h3>Interim Union Budget 2014-2015</h3>

<div class="subheading"><strong><i>INTERIM UNION BUDGET 2014-2015</i></strong></div>

In an election year, Finance Minister
presented an Interim Budget short of rhetoric
and stuck to highlighting the Government’s
achievements of the last 10 years. Faced with a
massive economic slowdown the Finance
Minister tinkered with excise duty to make cars,
two-wheelers and mobiles cheaper, announced
the implementation of the long standing One
Rank, One Pension for defence forces and
expressed hope that the worst of the slowdown
is over.

