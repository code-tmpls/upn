<div class="subheading"><strong><i>Northern Mountains</i></strong></div>

The mountain ranges and plateaus of the
north Kashmir, the Himalayas and the hill ranges
of Nagaland, East Assam, Manipur and Mizoram
from a conspicuous mountain system bordering
the country in the North.

<div class="subheading"><strong><i>HIMALAYAS</i></strong></div>
<ol>
    <ul>
        <li><b>The Greater Himalayas/Himadri/ Inner
Himalayas:</b> They comprise the northern most
ranges having an average height of 6000 m with
breadth ranging from 120 to 190 km. Conforming
the syntaxial bend to the underlying rocks, it is
an asymmetrical mountain throwing very few
spurs southward.
Important peaks of this system include Mt.
Everest (8,848 m) at Kathmandu in Nepal,
Kanchanjunga (8,598 m), Dhaulagiri (8,172 m)
Nanda Devi (7,817m) Nanga Parbat (8,126m).
Important passes are Bara Lacha La and Shipki
La (in Himachal Pradesh), Thaga La Niti Pass and
Lipulekh (Uttarakhand); Nathu La and Jelep La,
(Sikkim); and Burzil pass and Zojila pass
(Kashmir).</li>
        <li><b>The Lesser/ Middle Himalayas:</b> They
have an average height of about 3,500 to 5,000
metres and average width of 60 to 80 km. It
extends in the North of Siwalik range. Important
ranges include the Dhauladhar, the Pir Panjal and
the Musoorie range. Hills and resorts are Shimla,
Musoorie, Nainital, Almora, Darjeeling etc.</li>
        <li><b>The Outer Himalayas/Shiwaliks:</b> They
are the foothills running parallel to the
Himalayas. Average height is 1,000 to 1,500
metres. It is a chain of low-lying hills formed
through fluvial deposits like sand, clay and
pebbles. Dehradun valley; Udhampur and Kotli
valleys have developed into good human
settlements.</li>
    </ul>
</ol>

Siwalik or Outer Himalayas is a hogback
structure formed of river born deposits in the
latest phase of Himalayas uplift in Middle
Miocene to lower Pleistocene age. It descends
northwards to flat-floored structured valleys
called “Duns”. It is covered with thick tropical
wet deciduous forests in the east.


<div class="subheading"><strong><i>TRANS-HIMALAYAS</i></strong></div>

They comprise Karakoram and the Kailash
mountain systems. Mt. K2
 (Godwin Austin -
8,611m) which is now in Pakistan occupied
Kashmir is located in the Karakoram. Another
important peak is Hidden Peak (8,068m). The
Siachen glacier (72km) - largest in India is situated
in Nubra valley in this region. Other glaciers are
Baltoro (60 km), Biafo (60 km) and Hispar (62 km).


<div class="subheading"><strong><i>PURVANCHAL OR EASTERN HILLS</i></strong></div>

After crossing the Dihang gorge in the east,
the Himalayas bend towards South forming a
series of hills with a north-south direction, convex
side facing the west. These hills are made up of
sedimentary rocks and are highly dissected. Their
heights vary from 500m to 3,000m. The ‘Patkai
Bum’ forms the boundary between India
(Arunachal Pradesh) and Burma. Further South
is the Naga Hills, boundary between Nagaland
and which forms Burma. Saramati (3,826m) is the
highest peak. Garo, Khasi, Jaintia, Mikir, Dapha,
Mini and Abor are other important hills of the
north-eastern region. Mawsynram with
maximum rainfall in the world is located on the
Khasi hills in Meghalaya.

