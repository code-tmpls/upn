    <div class="subheading"><strong><i>Drainage System</i></strong></div>

    <div class="subheading"><strong><i>Important Concepts</i></strong></div>

<b>Tributary:</b> A river or stream which
contributes its water to a main river. For example,
the Yamuna is the tributary of the Ganga.
<br>
<b>Distributary:</b> A branch or outlet which leaves
a main river and does not rejoin it, carrying its
water to the sea or a lake.
<br>
<b>Delta:</b> A triangular shaped alluvial tract,
formed at the mouth of a river. For instance the
delta of the Ganges is the largest delta in the
world.
<br>
<b>Doab:</b> The alluvial tract of land between two
adjacent rivers, e.g. the plain between the Ganges
and the Yamuna.
<br>
<b>Catchment Basin (Drainage Area):</b> The
region which drains all the river water that falls
on it into a river or stream.
<br>
<b>Breakwater:</b> A barrier built into the sea in
order to break the force of the waves and thus to
serve as a protection against them.
<br>
<b>Estuary:</b> The mouth of a river where tidal
effects are felt and where fresh water and sea
water mix.
<br>
<b>Drainage Patterns:</b> River and its tributaries
drain an area, which is called a ‘river basin’. Its
boundary formed by the crest line of the
surrounding highland is the watershed of the basin.
A river system usually develops a pattern
which is related to the general structure of the
basin.
<br>
<br>
A dendritic pattern develops in a region made
of rocks which offer some resistance to erosion
and which has a uniform structure. A trellis
drainage pattern develops in a region made up
of alternate belts of hard and soft rocks all of
which dip in the same direction and which lie at
right angles to the general slope, down which the
river flows. A radial pattern develops on a dome
or volcanic cone.
<br>
<br>
Classification of Drainage System: Over 90%
of India’s land surface drains into the Bay of
Bengal and almost all the remaining area drains
into the Arabian Sea. Only a very small area in
Rajasthan has an inland drainage.
 Our river system can be classified into:
<ol>
    <ul>
        <li>The Himalayan River System</li>
        <li>The Peninsular River System
The Himalayan rivers fall into four broad groups:
<ol>
    <ul>
        <li>Pre-Himalayan Rivers: Arun, Indus, Satluj
and Brahmaputra.</li>
        <li>Great-Himalayan Rivers: Ganga, Kali,
Ghaghra, Gandak, Tista etc.</li>
        <li>Lesser -Himalayan Rivers: Beas, Ravi,
Chenab, Jhelum etc.</li>
        <li>Siwalik Rivers: Hindan, Sonali etc.
The peninsular rivers fall into two categories,
viz., the coastal rivers and the inland rivers. The
former are comparatively small streams. The
west-coast rivers are of great importance.</li>
    </ul>
</ol>
</li>
    </ul>
</ol>

Although only 3 percent of the areal extent of the
basins of India is drained by these rivers, as much
as 14 percent of the country’s water resources are
contained by them.
<ol>
    <ul>
        <li><b>Rivers rising from the Western Ghats:</b> the
Godavari, the Krishna, the Cauvery, the
