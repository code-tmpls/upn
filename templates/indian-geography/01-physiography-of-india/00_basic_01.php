<h3>Physiography Of India</h3>


India is the seventh largest and second most
populous country in the world. Its area is 2.4% of
the total world area but about 16% of the entire
human races reside in its fold. In population, only
the mainland china exceeds that of India.
<br>
<br>
India, Pakistan, Bangladesh, Nepal and
Bhutan form the well-defined realm of south Asia
often referred to as the Indian sub-continent.
Lying entirely in the northern hemisphere
(tropical zone), the Indian mainland extends
between the latitude - 8°
4' N to 37°
6’N and
longitude - 68°
7' E to 97°
25’E. The southernmost
point in the Indian territory, the Indira Point, is
situated at 6°
30' north in the Andaman and
Nicobar islands. The tropic of cancer passes
through the centre of India.
<br>
<br>
India covers an area of 3,287 sq. Km. and
measures about 3,214 km. from north to south and
about 2,933 Km east to west. The total length of
the mainland coastland is nearly 6,200 km and
land frontier about 15,200 km.
The boundary line between India and China
is called the McMahon line. To the north-west,
India, shares a boundary mainly with Pakistan
and to the east with Myanmar and Bangladesh.
The Indian Ocean lies in the south. In the south,
on the eastern side, the Gulf of Mannar and the
Palk Strait separate India from Sri Lanka. The
Andaman and Nicobar Islands in the Bay of
Bengal and the Lakshadweep islands in the
Arabian Sea are parts of the Indian territory.
India’s relief is marked by a great variety.

