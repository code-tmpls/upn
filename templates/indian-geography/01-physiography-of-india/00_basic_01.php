<div class="subheading"><strong><i>Great Northern Plains</i></strong></div>

The great plains are an aggradational surface
of great extent formed after the Himalayas. These
are formed by the Indus, the Ganga and
Brahmaputra rivers. The Great Plains stretch in
the east-west direction between the Himalayas
in the north and the peninsular plateau in the
south. Width varies from 500 km in PunjabRajasthan to 200 km. in Bihar. It covers an area of
over 7 lakh sq. km. Important characteristics
include - Bhabar (unsorted sediments), Terai
(Marshy tract) Bhangar (older alluvium) and
Khadar (newer alluvium)
<br>
<br>
The Great Northern plains consist of the
following regions:
<ol>
    <ul>
        <li>The Punjab-Haryana Plains: They are
drained by the tributaries of river Indus, Ravi,
Beas and Satluj. These plains are flat with an
average elevation of 200 to 240 metres. These
fertile plains include the Bari Doab (between Ravi
and Beas rivers) and Bist Doab (between Beas and
Satluj)</li>
        <li>The Rajasthan Plains: They cover an area
of 1.75 lakh sq. km. in Rajasthan and its adjoining
states. It includes the Marusthali of Thar desert
and bagar tract west of the Aravallis. The region
is one of the dry river beds (Saraswati) and
shifting sand dunes. River Luni is an inland
drainage river. Sambhar, Kuchaman, Didwana
and Pachbhadra are the salt lakes of this region.</li>
        <li>The Ganga Plains : The Ganga plains of
U.P., Bihar and West Bengal occupy an area of
3.57 lakh sq. km. Important rivers of this region
are the Ganga, the Yamuna, the Gandak, the Son
and the Kosi. The Bengal basin is mainly
composed of the Ganga delta.</li>
        <li>The Brahmaputra Plains: It contains
alluvial deposits of the Brahmaputra and its
tributaries the Subansiri, Dibang and the Lohit.
It is a flood prone area because the slope of the
valley is too gentle to drain away the large volume
of rainfall in monsoon season.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Peninsular Plateau</i></strong></div>

The Peninsular plateau, which covers an area
of about 16 lakh sq. km., is the largest
physiographic division of the country. It has
senile topographical features. The Aravallis form
its boundary in the north-west, Rajmahal Hills in
the north and north-east. South of about 220
N
latitude, the Sahyadris (Western Ghats) and the
Eastern Ghats form the western and eastern
boundaries respectively.
<br>
<br>
On the basis of variation in relief, further
divisions of the Peninsular Plateau may be made
as follows:
<ol>
    <ul>
        <li><b>The Aravalli Range:</b> It is one of the oldest
fold mountains of the world. The highest peak is
Gurushikhar (1,722m) in Mount Abu Hills. The
Delhi Ridge marks the northern end of the range.</li>
        <li><b>The Central Plateau:</b> It consists of Plateau
of Rajasthan and Madhya Pradesh. Average
height is 250-300m. The Malwa Plateau in
Madhya Pradesh lies in the north of Vindhyas.
The Plateau is largely broken. The Chambal
ravine is an important feature. The area between
the Yamuna and Vindhyan Plateau is called
Bundelkhand Plateau. It lies in the east of Maikal
ranges and south of river Son. Important rivers
of the region are the Rihand and Son.</li>
        <li><b>The Vindhya Range:</b> It is an escarpment
running in east-west direction. Kaimur Hills form
its eastern portion.</li>
        <li><b>The Satpura Range:</b> It lies between
Narmada and Tapti (Tapi) valleys latitudinally.
The plateau is formed of lava. The highest point
of Satpura range is Dhupgarh in Panchmarhi
(1,350m). The eastern part of the range is Maikal
Plateau.</li>
        <li><b>The Eastern Plateau and the Chhotanagpur
Plateau:</b> East of the Maikal Hills lies the
Baghelkhand Plateau. South of the Baghelkhand
is Chhattisgarh Basin. Further south is Bastar
Plateau or Dandakaranya.</li>
The Chotanagpur Plateau lies in the east of
Baghelkhand Plateau. It has an average height of
700m. It has a radial drainage. The Damodar river
originates near Tori in Palamau. Damodar valley
project is located here. The Chotanagpur Plateau
ends in Rajmahal Hills.
        <li><b>The Kathiawar and Kutch Peninsula:</b>
They have an Archaean structure but are surfaced
by tertiary rocks. It tapers into the Arabian Sea.</li>
        <li><b>The Deccan Plateau:</b> It is located within
the Satpura-Mahadeo-Maikal ranges in the north,
the Eastern Ghats in the East and the Western
Ghats in the west. It is slightly titled towards
south-east. The peninsula is triangular in shaperising 
from 500 to 1,000m. Amarkantak (1,057m)
and Dhupgarh (1,350m) are important peaks.</li>
        <li><b>The Western Ghats or the Sahyadris:</b>
They form the western edge of the Deccan
Plateau. Starting from Khandesh, south of Tapi
and running southward parallel along the
western coast for 1600 km, it reaches
Kanyakumari and joins the Eastern Ghats at
Nilgiri Hills. Kalsubai (1,646m), Mahabaleshwar
(1,438m) and Harishchandra are among the
highest peaks in this part of the ghats. Thalghat
and Bhorghat are important passes through
which roads and railways run between the
Deccan Plateau and the Konkan plains.
Kudremukh (1,894m), Pushpagiri (1,714m) and
Brahmagiri peak lie south of Coorg.
The highest peak of the Nilgiri Hills is
Dodabeta (2,637m) situated near Ootacamund.
Udyogmandal (Ooty) is an important hill resort.
<ol>
    <ul>
        <li><b>Palghat Gap:</b> It connects Tamil Nadu and
Kerala and is located south of the Nilgiri,
separating the Cardamom Hills from the Nilgiris.
Further south of the Palghat Gap, there are the
Annamalai Hills and the Palni Hills in the north
east. The culmination point of these hills is the
Anai Mudi Peak (2,695m) with the distinction of
being the highest peak of South India.</li>
        <li><b>The Eastern Ghats:</b> They form the eastern
boundary of the Deccan Plateau. They are broken
as compared to the Western Ghats. The highest
point (1,680m) is in the Visakhapatnam district,
Mahendragiri (1,501m).</li>
    </ul>
</ol>
The most important hill in the Cuddapah
Range is Nallamalla Hills. The Southern part of
Nallamalla is called Palkonda Range. Javadi Hills
in North Arcot and Ginjee Hills in South Arcot
and Pachaimalai in Tamil Nadu, form the others
hills of the Eastern Ghat.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Coastal Plains</i></strong></div>

It occurs along the shores of the Bay of Bengal
and the Arabian Sea. The Plains are narrower
along the west coast than the east coast. A number
of deltas occur on the east coast because the
gradient is less steep.
<ol>
    <ul>
        <li><b>The West Coastal Plains:</b> It spreads its
structure from the Rann of Kutch to Kanyakumari
for about 1,500 km. The Gujarat plain is formed
by the Sabarmati, Mahi and several other rivers.
The Karnataka coast extends south of Goa for
about 525 km. The Sharavati is an important river.
The region has several rocky cliffs. The Kerala
plain extends from south of Cannanore to Cape
Comorin for about 500 km.</li>
        <li><b>The East Coastal Plains:</b> Extends from the
mouth of Subarnarekha to Kanyakumari for about
1,100 km. Its average width is 120 km. The East
Coast is much wider and contains many deltas.
The Krishna and Godavari form large delta.
Kolleru Lake is located between Krishna and
Godavari. The Pulicat Lake is a lagoon (salt water
lake) separated from the sea by Sriharikota Island.
The Utkal plains include the Mahanadi delta.
Chilka Lake, south of Mahanadi delta is an
important Lagoon.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>The Islands</i></strong></div>

India has in all 247 islands of which 204 lie in
the Bay of Bengal and remaining in the Arabian
Sea and Gulf of Mannar between India and Sri
Lanka.
<br>
<br>
<b>The Andaman and Nicobar</b> are two major
groups of Islands in the Bay of Bengal. These are
extensions of the Arakan Yoma of Burma.
The islands in the Arabian Sea have a coral
origin and are surrounded by fringing reefs.
Lakshadweep (32 sq. km. area) is important for
coral reefs. Further south of it is Minicoy islands.
It is the largest with an area of 4.5 sq. km.
Rameshwaram Island which lies just off the Tamil
Nadu coast, between India and Sri Lanka, is also
of coral origin.


