<h3>Natural Vegetation</h3>


Natural vegetation may be defined as that part
of plant communities which has been remaining
undisturbed over a long period of time. Here the
individual species adopt them selves to certain
soil and climatic conditions and proliferate
though natural process.
<br>
<br>
On the basis of appearance of the plant
community with respect to form and other
characteristics, the vegetation of India is
commonly identified as forest, grasslands and
shrubs. The climate, specially the sun shine and
precipitation, deter mines the type of plant species
that can survive in a particu lar region.
On the basis of climatic condition, the
vegetation has been classified as: - Tropical
Evergreen forests, Monsoon forests, Temperate
Forests, Grasslands, Tundra, Savanna, Prairie,
Alpine and so on.


<div class="subheading"><strong><i>Basic terms:</i></strong></div>
<ol>
    <ul>
        <li><b>Flora:</b> It refers to the plants of a particular
region or period, listed by species and
considered as a group.</li>
        <li><b>Vegetation:</b> It refers to the assemblage
of plant species living in association with
each other in a given environment -
often termed ecological frame.</li>
        <li><b>Forests:</b> Forest is a large tract covered by
trees and shrubs. It consists of forests,
grassland and scrub.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Forest Area</i></strong></div>

Forests constitute about 23% of India’s total
land surface and occupy 746 lakh hectares of land.
The Chhattisgarh and MP covers 7217 hectare,
Arunachal Pradesh 5110 hectares, Orissa - 2857
hec, Andhra Pradesh- 2724 hec, Maharashtra2724 hec and Uttaranchal - 1887 hectares.


<div class="subheading"><strong><i>Major Vegetation Regions</i></strong></div>
<ol>
    <ul>
        <li><b>Tropical Evergreen or Rain Forests:</b> These
forests occur in areas where the rainfall exceeds
200 cm, the average annual temperature is
between 20°C to 27°C and average annual
humidity exceeds 77 per cent. The trees are
evergreen and dense and forests have a three
storied appearance. These forests are found in
Western parts of Western Ghats, eastern part of
subtropical Himalayas (Terai), north east India
comprising Lushai, Cachar, Khasi, Jaintia and
Garo hills and most of Andaman and Nicobar
Islands.
<br>
<br>
These forests may be sub divided into the
following sub-types:
<ol>
    <ul>
        <li><b>Tropical Wet Evergreen Forests:</b> They
cover 4.5 m. ha area and are found along the
western side of the Western Ghats, in a strip
running south-west from Arunachal Pradesh,
upper Assam, Meghalaya, Nagaland, Manipur,
Tripura and Andaman and Nicobar Islands. Here
the rainfall exceeds 300 cm. The forests are lofty,
dense, evergreen and multistoried.
The main species of trees found are Poon,
toon, chaplas, rosewood, ebony, Sissoo,
ironwood, Gurjan, pila champa etc. The
undergrowth consists of canes, bamboo, ferns,
climbers etc. Due to the dense undergrowth and
lack of transport these forests have not been
exploited.</li>
        <li><b>Tropical Semi-evergreen Forests:</b> Where
the rainfall is somewhat less than 200 cm, the
mean annual temperature between 24°C to 27°C
and humidity percentage is 80, the evergreen
forests degenerate into semi-evergreen forests.
These cover 1.9 m. ha area. These forests are found
on the western coast, in upper Assam, lower
slopes of eastern Himalayas, Orissa and in
Andaman and Nicobar islands. The forests have
evergreen trees mixed with deciduous types. The
important species include Aini, Semul, Kadam,
irul etc.</li>
        <li><b>Tropical Moist Deciduous Forests:</b> Such
forests occur in areas of low annual rainfall of 100
cm to 150 cm. The main annual temperature is
between 26°C to 27°C, and humidity percent is
60 to 80. These forests are found in a belt running
north-south on eastern slopes of western ghat,
central plateau including Chotanagpur, Upper
Mahanadi Valley and hills of Madhya Pradesh,
Himalayan foothills, eastern ghats in Tamil Nadu
and Andaman and Nicobar islands.
These forest trees shed their leaves and are the
most important forests source of commercial
timber. The species of trees include Sal, teak,
Arjun, jarul, laurel, Andaman paduk, ebony,
mulberry, Kusum, kanju, ber, gular, palas, haldu,
siris, mahua, Semul, har, sandalwood, jamun etc.
Most of these forests have been cleared from level
land for cultivation.</li>
        <li><b>Littoral and Swamp Forests:</b> These forests
cover 6 lakh ha and occur in and around tidal
creeks and river deltas. They are found in thickets
on western coast at a few places but on the eastern
coast they form a continuous belt on the fringe of
deltas of Ganga, Mahanadi, Godavari, Krishna
and Cauvery. They are densest in Sunderbans,
where Sundri trees predominate.</li>
    </ul>
</ol>
</li>
        <li><b>Dry Tropical Forests:</b> These forests occur
in areas having annual rainfall between 75 cm to
125 cm, mean annual temperature of around 23°C
to 27°C and humidity between 51 to 58 per cent.
They are divided into the following sub-types.
<ol>
    <ul>
        <li><b>Tropical Dry Deciduous:</b> These forests are
found on a very large area in an irregular wide
strip running north south from the foothills of
Himalayas to Kanyakumari except in Rajasthan,
Western Ghats and West Bengal. The important
trees include teak, tendu, sal, bijasal, rosewood,
palas, bel, lendi, axlewood, anjair, harra, khair etc.</li>
        <li><b>Tropical Thorn Forests:</b> They are
restricted to areas where rainfall is very low i.e.
between 50 cm to 75 cm, the mean annual
temperature is between 25°C to 27°C and
humidity is less than 47 per cent. They are found
in Kutch, neighbouring parts of Saurashtra, a
large strip in south western Punjab, western
Haryana, western and northern Rajasthan, Upper
Ganga plains, Deccan plateau and lower
peninsular India. Here thorny trees especially
acacias predominate.</li>
        <li><b>Tropical Dry Evergreen Forests:</b> These
forests occur in areas where the mean annual
rainfall is about 100 cm, mean annual temperature
is about 28°C and mean annual humidity is 74
per cent. These forests are found in the east coast
of the peninsula. The important species of trees
include khirni, jamun, kokko, ritha, neem, palm, etc.</li>
    </ul>
</ol>
</li>
        <li><b>Riparian Forests:</b> Where the rainfall is less
than 50 cm, short trees and grass predominate.
These forests are found along banks of rivers and
wet lands. Deciduous vegetation like neem,
Shisham, pipal, mango, jamun, khair are usually
found. Kans and munj grass are found in
abundance.</li>
        <li><b>Subtropical Broad Leaved Hill Forests:</b>
The forests are found between 915 to 1830 m
height above sea level where the mean annual
rainfall is between 75 cm to 125 cm, mean annual
temperature is between 18°C to 21°C and
humidity percent is 80. These forests are found
in high lands of Bastar, Panchmarhi,
Mahabaleshwar, Nilgiris, Palni and Khasi hills
and lower slopes of Himalaya in West Bengal and
Assam. Such forests are called ‘Shola’ in South
India.</li>
        <li><b>Montane Wet Temperate Forests:</b> These
forest occur at a height of 1800 to 3000 m above
sea level in areas where annual rainfall is between
150 to 300 cm, annual temperature is between
11°C to 14°C and humidity per cent is 83. These
forests are found in hills of Tamilnadu, Kerala,
Eastern Himalayas, higher hills of West Bengal,
Assam and Arunachal Pradesh. The main trees
found are deodar, Indian Chestnut, magnolia,
birch, plum, blue pine, oak, hemlock etc.</li>
        <li><b>Montane Moist Temperate Forests:</b> They
occur in temperate eastern and western
Himalayas between the pine and alpine forests
in Kashmir, Himachal Pradesh, Punjab, Uttar
Pradesh, Darjeeling and Sikkim between 1600 to
3500 meters. The forests are predominantly
coniferous forests and include trees like pine,
deodar, spruce, silver fir, oak, beach, birch, polar,
elm, chestnut, maple, rhododendrons etc.</li>
        <li><b>Alpine Forests:</b> They occur in the Alpine
areas of the Himalayas beyond the limit of tree
growth i.e. between 2900 to 3500 m and consist of
dwarf shrubs of juniper, fir, honey suckle, birch,
rhododendrons etc. At still higher altitudes,
shrubs of low herbs are the only vegetation found.</li>
        <li><b>Grasslands:</b> These grasslands are divided
into three types (i) Hilly or upland grassland.
They are found in Himalayas above 100 m and in
Deccan hills (ii) low land grasslands - They occur
in plains of Punjab, Haryana, Uttar Pradesh, Bihar
and north western parts of Assam (iii) Riverine
grasslands. They are found in riverine tracks of
northern India especially in the bhabhar tracks.</li>
    </ul>
</ol>


