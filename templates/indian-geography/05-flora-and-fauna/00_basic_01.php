<h3>Flora And Fauna</h3>


Owing to a wide range of climatic condition,
India can boast of a rich and varied vegetation.
In the remote hilly tracts of the Himalayas and
Deccan mountains, a large number of endemic
flora i.e. Plants that have grown there for millions
of years and are not found to grow naturally
elsewhere in the world, is found here.
In recent years, many of these endemic plants
are facing extinction because of ecological
disturbance. There are eight floristic regions of
India:
<ol>
    <ul>
        <li>The Western Himalayas</li>
        <li>The Eastern Himalayas</li>
        <li>Assam</li>
        <li>The Indus plain</li>
        <li>The Ganga plain</li>
        <li>Deccan</li>
        <li>Malabar</li>
        <li>Andamans</li>
    </ul>
</ol>

India has the forest cover of 22 per cent while
the actual forest cover is only about 11 per cent of
the total country area. Coniferous forest cover
only 6 per cent and the broadleaf deciduous
forests comprise about 94 per cent of the total
forest cover.
