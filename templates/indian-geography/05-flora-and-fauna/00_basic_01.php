India has a great variety of fauna, with about
500 species of mammals and 2,100 species of birds
and reptiles. Some rare and extinctive species are
found in certain pockets in India important
among them are the Asiatic lion, now confined
to the Gir forest; the one horned rhinoceros, a
vanishing species in Assam and the great Indian
bustards, now rarely seen in Rajasthan.
<br>
<br>
Some plant and animal species are protected
under various schemes and a number of wildlife
sanctuaries have been planned for the
conservation of animal species. The National
wildlife action plan was adopted in 1983. It
provides the framework of strategy as well as
program for wild life conservation. The wildlife
& reserves in India can be classified into ‘National
Parks’ and ‘Wildlife Sanctuaries. The National
parks protect the entire ecosystem, where as
wildlife sanctuaries have the special purpose of
preserving animals and birds. There are, at
present, 68 national parks including marine parks,
high altitude parks and parks in protected areas
of Andaman and Nicobar and 367 wild life
sanctuaries.
<br>
<br>
The Red Panda project was started in 1996 in
the Padmja Naidu Himalayan Zoological Park.
The Manipur Bow Antler deer project was started
in 1977 in Kaibul Lamjoa in Manipur near the
Loktak Lake. The Gir Lion sanctuary project was
launched by the Gujarat government in the Gir
wildlife sanctuary in 1972. The Himalayan musk
deer project was started in the Kedarnath
sanctuary in Uttarakhand. The crocodile project
was started in 1975 in Orissa. Later Uttar Pradesh,
Rajasthan, West Bengal, Tamil Nadu, Andhra
Pradesh, Gujarat, Kerala, Madhya Pradesh, Bihar,
Andaman and Nicobar Islands and Nagaland
were included in this project. Project Hangul was
started in 1970 in the Dachigam national park in
Kashmir.
<br>
<br>
The national bird of India, the peacock, is
found along with many species of birds
throughout India. The only ape found in India is
the Hoolock Gabon in Assam.


