<div class="subheading"><strong><i>Points to Remember</i></strong></div>
<ol>
    <ul>
        <li>The Wild Life Protection Act - 1972 adopted
by all states except Jammu and Kashmir and
India is signatory to the convention on
International Trade in endangered Species
of Wild Flora and Fauna under which the
commercial exploitation of the species is
prohibited.</li>
        <li>India posses about 8% of the world’s known
living organism while it has only 2 percent
of the world’s land mass.</li>
        <li>At present about 15,000 plant species and
75,000 animal species are facing extinction.</li>
        <li>In the state of Maharashtra, Madhya Pradesh
and Andhra Pradesh, the elephant has
almost all disappeared.</li>
        <li>The North-east India has over 600 different
orchid species which are now facing extinction.</li>
        <li>The idea of biosphere reserves was initiated
by UNESCO in 1973-74 and the first reserve
in the Nilgiri was established in 1986.</li>
        <li>The “Man and Biosphere” (MAB)
programme was launched by the
government of India for the conservation of
the biological diversity.</li>
        <li>Asiatic lion, one of the rarest and most
important wild animals in India is found in
only two ports of the country: the famous
Gir National Park in Gujarat and the
Chandraprabha wildlife sanctuary of
Uttar Pradesh.</li>
        <li>The one horned rhinoceros is also vanishing
but now it is found in Assam’s Kaziranga
National Park and Manas wildlife sanctuary.</li>
        <li>The Dachigam National Park of Kashmir
protects the hangul or Kashmir Stag.</li>
        <li>The largest areas reserved for sanctuaries
are in Madhya Pradesh, Andhra Pradesh,
Gujarat and Karnataka, but the proportion
of sanctuary area to forest area is highest in
Gujarat.</li>
        <li>The word “National Park” refers to relatively
large area of one or more ecosystem that
has not materially altered by human
exploitation and occupation. The place is
especially meant for plants, animals and
habitats for special scientific education.</li>
        <li>The concept of “Wildlife-Sanctuary” is more
or less similar to the national park but it is
chiefly meant to protect wildlife and
conserve species. Its boundary is not fixed
by the legislation.</li>
        <li>The concept of “Biosphere Reserves” refers
to the multipurpose protected area to
preserve the genetic diversity of the
ecosystem. Its main objectives are; to
conserve integrity and diversity of plants,
animals and micro-organism, to provide
facilities for education, awareness and
training and to promote research on
ecological conservation.</li>
        <li>The “Operation Tiger” was launched by
WWF in 1973 to protect the tiger from
further extinction.</li>
        <li>Of the total 27 tiger reserves in India,
Nagarjunasagar Tiger reserve of Andhra
Pradesh is the largest with an area of
3,568 sq km.</li>
        <li>The National wildlife Action plan was
adopted in 1983.</li>
    </ul>
</ol>





