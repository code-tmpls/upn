<br>
<b>Arecanut:</b> Kerala ranks first in the production
followed by Karnataka, Assam & T.N.
<br>
<b>Ginger:</b> The chief producers are Kerala,
Meghalaya, Orissa, Arunachal Pradesh & H.P.</li>
        <li>
        <div class="subheading"><strong><i>Coconut</i></strong></div>    
        
In terms of number of nuts India ranks second
