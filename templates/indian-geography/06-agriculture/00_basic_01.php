ranks third after Indonesia and Philippines.
In terms of area under coconut Kerala also
ranks first followed by Tamil Nadu, Karnataka,
Andhra Pradesh, Orissa and Goa.
<br>
<br>
In terms of production Kerala also ranks first
followed by Tamil Nadu, Karnataka, Andhra
