<h3>Agriculture</h3>


Indian agriculture provides livelihood to
about 70% of the work-force, contributes nearly
35% of net national product and accounts for
sizeable share of total value of country’s exports.
It also supplies bulk of wage goods required by
non - agricultural sector and raw material for a
large section of industry.


<div class="subheading"><strong><i>Cropping Season in India</i></strong></div>

India has many growing seasons due to
prevalence of high temperature through a long
period. Different crop seasons are:
    <ol>
        <ul>
            <li><b>Kharif:</b> Crops are sown at the beginning
of the South -West monsoon and harvested at the
end of the South -West monsoon.
Important crops: Jowar, bajra, rice, maize,
cotton, Jute, groundnut, sugarcane, tobacco etc.</li>
            <li><b>Rabi:</b> Crops need relatively cool climate
during the period of growth but warm climate
during the germination of their seed and
maturation.
<br>
Sowing season- (October -December) and
harvesting season (February - April).
Important Crops: Wheat, barley, gram,
linseed, mustard, masur, peas and potatoes.</li>
            <li><b>Zaid:</b> Crops which are being raised
throughout the year due to artificial irrigation.
<ol>
    <ul>
        <li><b>Zaid Kharif:</b> Sown in August-September
and harvested in December-January. Important
crops include rice, jowar, rapeseed, cotton and
oilseeds.</li>
        <li><b>Zaid Rabi:</b> Sown in February - March and
harvested in April -May. Important crops are
watermelon, cucumber, leafy and other
vegetables.</li>
    </ul>
</ol>
</li>
        </ul>
    </ol>

Total geographical area of India: 328.7 m. ha.
Total New sown Area: 140.27 m. ha. (46.12%)
Total Net Irrigated Area: 38.80 m. ha (27.66%
of net sown area).
In Eastern India, east of 80° East meridian and
in the coastal lowland (West coastal lowland
south of Surat) rice is the dominant crop. Tea and
jute are distinctive crops of eastern India. West
of 80° East meridian and north of Surat, wheat is
the dominant crop. 100 cm Isohyet is the major
dividing line.
<br>
<br>
Jowar, bajra, pulse, groundnut and cotton are
the chief crops in the Indian Plateau and wheat,
various pulses, cotton, mustard, jowar, bajra, etc.
in the alluvial plains of Uttar Pradesh, Haryana
and Punjab.


<div class="subheading"><strong><i>Types of Farming</i></strong></div>
<ol>
    <ul>
        <li><b>Shifting Cultivation:</b> Prevalent in the forest
areas; cultivation is done over the burnt forests
which are abandoned when fertility dwindles. It
has different name indifferent states. Main crops:
dry paddy, wheat, maize, sugar cane etc.</li>
        <li<b>Sedentary or Settled Cultivation:</b> Mainly
confined to plateau and highland areas. Main
crops: Sugarcane, Oilseeds, Cotton.</li>
        <li></b>Capitalist Farming:</b> Practised on large
holdings called, farm estates. It is highly capital
intensive type of agriculture. Main crops:
plantation crops.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Description of crops</i></strong></div>
<ol>
    <ul>
        <li>
        <div class="subheading"><strong><i>Rice</i></strong></div>    
        
It covers about 25% of the total gross cropped
area of the country.
<br>
<br>
<b>Climatic Conditions:</b> Rain fall more than 125
cm.; clayey loam soil is best suited; average
monthly temperature should not fall below 21°C
as in Orissa, West Bengal, Bihar, Assam and South
India.
<br>
<br>
<b>Area:</b> Coastal India (south of Bombay on the
western coast), Eastern India, Chhattisgarh Plain,
Wainganga valley, alluvial plain of West Bengal,
Mahanadi delta, eastern Assam valley, Cauvery
delta.
<br>
<br>
In West Bengal rice is grown in 3 different
agronomic seasons-
<ol>
    <ul>
        <li>Aus, sown in Feb-March,</li>
        <li>Aman, sown in March-April</li>
        <li>Boro, sown in June.</li>
    </ul>
</ol>

In terms of production as % of total rice
production, West Bengal ranks first followed by
T.N., U.P., Andhra Pradesh, Punjab & Bihar.
In terms of yield (kg/ha), Tamil Nadu ranks
one followed by Punjab, Andhra Pradesh and
Karnataka.</li>
        <li>
        <div class="subheading"><strong><i>Wheat</i></strong></div>    
        
It covers about 14% of total cropped area of
the country.
<br>
<br>
<b>Climatic Conditions:</b> Cool climate; 30 cm.
rainfall during the growing period; clayey alluvial
is the best suited soil; raised mainly in area of
rainfall annually less than 100 cm.
<br>
<br>
<b>Crops:</b> (1) Common bread wheat-Most
common in the Indo-Gangetic Plains; the major
part of total wheat produced in India. (2) Durum/
Marcaroni wheat-Susceptible to yellow rust; most
common in the north-western India and the black
soils of Indian Plateau. (3) Emmer/Triticum
Dicocum wheat-Produced in Karnataka, Tamil
Nadu and Maharashtra. (4) Indian dwarf wheatProduced in limited areas of M.P. and U.P.
About 70% of total wheat area in India is
under irrigation.
<br>
<br>
About 40% of total wheat area in India is rainfed.
In terms of area as a percentage of total area
under wheat, UP ranks one followed by MP,
Punjab, Rajasthan, Bihar and Haryana.
In terms of production as % of total wheat
production in India, U.P. ranks one followed by
Punjab, Haryana, Bihar.
<br>
<br>
In terms of yield (kg/ha) Punjab ranks one
followed by Haryana, West Bengal, Rajasthan,
Uttar Pradesh.</li>
        <li>
        <div class="subheading"><strong><i>Barley</i></strong></div>    
        
For the growth of barley the climatic
conditions are the same as for wheat. However it
can well thrive even in more cool climate as its
potential area is much wider than the wheat.
Barley is used for the production of malt
which again is used for brewing beer and other
alcoholic products.
<br>
<br>
In terms of production as % of total barley
production in India, Uttar Pradesh ranks first
followed by Rajasthan, Haryana, Punjab.</li>
        <li>
        <div class="subheading"><strong><i>Maize</i></strong></div>
            
Climatic Conditions: Area of 4.5 months free
from frost; temperature during growing period
of 21°C to 27°C but should not fall below 13°C;
rainfall between 50 to 100 cm.; well drained loamy
soils rich in nitrogen; grown as kharif crop in all
the states.
<br>
<br>
<b>Area:</b> Mainly north of a line connecting Surat
with Kolkata (excluding Kutch, West Rajasthan
and Ladakh), in the North East (Meghalaya,
Nagaland, Manipur, Mizoram), northern districts
of Andhra Pradesh and the adjoining districts of
Maharashtra and M.P.
<br>
<br>
In terms of area as % of total area under maize
U.P. ranks first followed by Rajasthan, M.P., Bihar
and Gujarat.
In term of production as % of total production
in India U.P. also ranks first followed by former
Bihar, Karnataka, M.P., Rajasthan.</li>
        <li>
        <div class="subheading"><strong><i>Jowar/Sorghum</i></strong></div>    
        
Climatic Conditions: Annual rainfall below
100 cm, needs 30 cm rainfall during the growing
season; mean monthly temperature between 20°
and 33°C, clayey deep regur and alluvium for
better field.
<br>
<br>
<b>Area:</b> On Indian Plateau, west of 80°E
meridian; south of Pune 80% agricultural land
under it; in Andhra Pradesh, Maharashtra and
Karnataka as rabi & as kharif elsewhere.
In terms of area as % of total area under it
Maharashtra ranks first followed by Karnataka,
M.P., Andhra Pradesh and Rajasthan. In terms of
production as % of total production Maharashtra
also ranks first followed by Karnataka, M.P., and
Andhra.</li>
        <li>
        <div class="subheading"><strong><i>Other Coarse Cereals</i></strong></div>     
        
Beside maize and jowar a variety of coarse
cereals are grown in India. These are mostly rain
fed crops like bajra, ragi, etc.
<br>
<br>
Coarse cereals altogether accounts 28-30% of
the total area under food crops and about 18.5%
of total annual food production.
Though they have little commercial value
they are liable to be grown in even ecologically
unfriendly areas and have been adopted by the
poor section of the society.
In terms of area as % of total area under bajra
Rajasthan ranks first followed by Maharashtra,
Gujarat, U.P and Haryana.
<br>
<br>
In terms of production as % of total
production of bajra Rajasthan ranks first followed
by Gujarat, U.P. Maharashtra and M.P.</li>
        <li>
        <div class="subheading"><strong><i>Pulses</i></strong></div>    
        
Kharif Pulses: Arhar/ Tur, Moong, Urad,
Moth, etc; grown throughout India.
Rabi Pulses: Gram, Peas, Khesari, Masur,
Urad, etc.; grown in north India.
Pulses occupy about 13% of total Indian
cropped area while 90% area under it is rainfed.
In terms of area as % of total area under pulses
M.P. ranks first followed by Rajasthan,
Maharashtra, U.P. and Orissa.
In terms of production as % of total
production: Haryana ranks first followed by
Punjab, M.P. and U.P.

<div class="subheading"><strong><i>Oil Seeds</i></strong></div>
<ol>
    <ul>
        <li><b>Groundnut:</b> India ranks first in the world
both in area and production.
<br>
<b>Climate:</b> Highly susceptible to frost so not
grown in the winter season north of Tropic of
Cancer. Well drained sandy soil is more suitable
for it
<br>
<b>Area:</b> Raised as kharif throughout India but
as Rabi in Tamil Nadu, Karnataka, Andhra
Pradesh, Orissa. Kharif crop covers more than
90% area.
<br>
<br>
In terms of area Gujarat ranks first followed
by Andhra Pradesh, Tamil Nadu, Karnataka and
Maharashtra.
In terms of production Gujarat also ranks first
followed by Andhra Pradesh, Tamil Nadu,
Karnataka and Maharashtra.</li>
        <li><b>Rapeseed & Mustard
Climate:</b> Like wheat they thrive only in cool
climate and frost damages it. It is grown as Rabi
crop.
<br>
<br>
U.P. and Rajasthan are the chief producer and
together they account for 60% of the country
production.
<br>
<br>
In terms of area U.P. ranks first followed by
Rajasthan, Haryana and M.P.
In terms of production Rajasthan ranks first
followed by U.P., Haryana and M.P.</li>
        <li><b>Sesamum/ Til</b>:
India shares about one-third of world
production and area wise first in the world.
Climate-Raised throughout India on light soil
as kharif in the northern India and as Rabi crop
in south India.
The main producing states are Orissa,
Karnataka and Gujarat.</li>
        <li><b>Linseed</b>:
India accounts 10% of world production and
area wise stands first in the world.
Main producing states are M.P., U.P.,
Maharashtra, Bihar and Rajasthan.</li>
        <li><b>Niger Seed:</b>
Area wise first in the world;
Used for making soap.
Main producing states are M.P., Orissa,
Maharashtra and Bihar.</li>
        <li><b>Sunflower:</b>
Area wise first in the world raised as rabi crop.
<br>
<br>
Maharashtra alone shares about 75% of the
country production.</li>    
</ul>
</ol>

</li>
        <li>
        <div class="subheading"><strong><i>Cotton</i></strong></div>    
        
<b>Climate:</b> A sub-tropical crop; temperatureabove 21°C and 200 frost free days; rainfall- 75
cm. during the growing season; regur soil in the
rainfed area and heavy alluvial soil in the irrigated
area are most suited.
<br>
<b>Area:</b> Mainly in area west of 80°E; W. Punjab,
Haryana, SE-Rajasthan, black cotton soil of Malwa
plateau, Nagpur plain, Kathiawar and Gujarat
plain, N-Karnataka and neighbouring Andhra
Pradesh, Maharashtra and W-T.N.
<br>
<br>
In terms of area under cotton Maharashtra
also ranks first followed by Gujarat, Andhra
Pradesh, Punjab.
In terms of production, Maharashtra also
ranks first followed by Gujarat, Punjab, Andhra
Pradesh and Rajasthan.
In terms of yield, Punjab ranks first followed
by Rajasthan and Tamil Nadu.</li>
        <li>
        <div class="subheading"><strong><i>Jute</i></strong></div>  
        
India ranks first both in area and production
in the world but Bangladesh stands first in export.
<br>
<br>
<b>Climate :</b> Rainfall between 150 and 300 cm.;
humidity over 80%; temperature between 24°C
and 35°C during the growing period; preferred
to land of annual flooding.
<br>
<b>Area:</b> Lower Brahmaputra Valley, West
Bengal plain, Mahanadi delta, NE-Bihar,
In terms of area West Bengal ranks first
followed by Bihar, Assam and Andhra Pradesh.
In terms of production West Bengal also ranks
first followed by Bihar, Assam and Orissa.</li>
        <li>
        <div class="subheading"><strong><i>Tobacco</i></strong></div>    
        
<b>Climate :</b> Rainfall about 60 cm during the
growing period; humidity about 8%, well drained
sandy loam soil rich in potash is most favourable.
<br>
<b>Area:</b> Ganga plain N. Bihar; Godavari and
Krishna deltas of Andhra Pradesh; Madurai,
Belgaum, Baroda, Satara and Kaira near Mumbai.
<br>
<br>
In terms of area under tobacco Andhra
Pradesh ranks first followed by Gujarat,
Karnataka, Uttar Pradesh and Bihar.
In terms of production Gujarat ranks first
followed by Andhra Pradesh, Uttar Pradesh,
Karnataka and Bihar.</li>
        <li>
        <div class="subheading"><strong><i>Sugarcane</i></strong></div>    
        
India has the largest are in sugarcane
production in the world and also leads in
production.
<br>
<br>
<b>Climate:</b> Temperature about 25°C; hot dry
wind is inimical; annual rainfall about 50 to 75
cm. plus limited irrigation; well drained reddish
loamy soil is most suited; cool dry weather during
its maturing period promotes the sucrose content.
<br>
<b>Area:</b> South India provides the best suited
tropical conditions with longer crushing period.
North India produces the sugar cane in subtropical condition. The main area lies in the Ganga
plain west of Patna.
<br>
<br>
In terms of area under sugarcane production
U.P. ranks first followed by Maharashtra, Tamil
Nadu, Karnataka and Andhra Pradesh.
In terms of production U.P. also ranks first
followed by Maharashtra, Tamil Nadu, Karnataka
and Andhra Pradesh.
In terms of yield Tamil Nadu ranks first
followed by Maharashtra.</li>
        <li>
        <div class="subheading"><strong><i>Rubber</i></strong></div>    
        
In 1902, first of all the rubber plantation was
introduced on the bank of Periyar river
India is the fourth largest natural rubber
producer in the world only after Thailand,
Indonesia and Malaysia.
<br>
<br>
<b>Area:</b> More than 90% area is concentrated in
Kerala alone and the rest is spread over in Tamil
Nadu, Karnataka, Andaman and Nicobar islands.
Both in respect of acreage and production
Kerala stands first.</li>
        <li>
        <div class="subheading"><strong><i>Tea</i></strong></div>    
        
India accounts about 30% of global output
followed by China, Kenya, Sri Lanka. However,
Srilanka and Kenya have overtaken India in terms
of export.
<br>
<br>
<b>Climate:</b> Hot climate with the temperature
between 21°C and 32°C; rainfall more than 150
cm; deep loam soil rich in humus and virgin forest
soil with very little calcium but enough iron are
best suited.
<br>
<b>Area:</b> NE-India contributes about 80% of the
total production and the rest 20% is shared by S.
India.
<br>
<br>
<b>Assam:</b> Either sides of Brahmaputra river and
south of Assam hills in Cachar.
<br>
<b>W. Bengal:</b> Darjeeling and Jalpaiguri. It has
good flavour so fetches high price.
<br>
<b>S. India:</b> Annamalai hills; Hassan and
Chikmanglur districts of Karnataka; Kottayam,
Quilon and Trivandrum districts of Kerala.
<br>
<b>Uttarakhand:</b> Dehradun district.
Himachal Pradesh: Dharmashala in Kangra
district.
<br>
<br>
In terms of area under tea Assam ranks first
followed by W.Bengal, Tamil Nadu, Kerala.
In terms of production Assam also ranks first
followed by W.Bengal, Tamil Nadu and Kerala.
In terms of productivity/yield Tamil Nadu
ranks first followed by Kerala.</li>
        <li>
        <div class="subheading"><strong><i>Coffee</i></strong></div>    
        
Arabica and Robusta varieties of coffee are
grown in India accounting for 49% and 51% of
area under cultivation respectively.
<br>
<br>
<b>Area:</b> Eastern slopes of Western Ghats in
Kerala, Karnataka and Tamil Nadu. Karnataka
(Chikmanglur, Shimoga and Hassan), Tamil
Nadu (Nilgiri and Palani Hills).
<br>
<br>
In terms of area under coffee plantation
Karnataka ranks first followed by Kerala and
Tamil Nadu.
In terms of production Karnataka ranks first
followed by Kerala, Tamil Nadu and Andhra
Pradesh.</li>
        <li>
