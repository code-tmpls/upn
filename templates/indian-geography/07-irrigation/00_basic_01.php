For the better utilization of the total potential
the irrigation schemes have been divided into :
<ol>
    <ul>
        <li>Major Projects: Cultivable command area
of more than 10,000 hectare, (including
canal irrigation)</li>
        <li>Medium Project: Cultivable command area
between 2000 and 10000 hectare.</li>
        <li>Minor Projects: Cultivable command area
less than 2000 hectare, include mainly wellirrigation.</li>
        <li>Micro Projects: Drip irrigation and the use
of sprinklers.</li>
    </ul>
</ol>

In Punjab 75% of the net sown area is irrigated,
in Haryana & U.P. this ratio is of about 57%, in
TN. About 49% in U.P. about 38%, in Maharashtra
only 9% are irrigated.


