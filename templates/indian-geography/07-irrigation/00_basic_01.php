<div class="subheading"><strong><i>SUGGESTIONS FOR SUSTAINABLE WATER RESOURCE UTILISATION</i></strong></div>

<ol>
    <ul>
        <li>Greater emphasis on ground water
utilisation as a part of comprehensive land
use policy and afforestation programme.</li>
        <li>Introducing water harvesting technology for
optimum utili- sation of rain water and surface runoff to sustain use of ground water.
Greater emphasis of small storage schemes.</li>
        <li>Involvement of local people by community
participation.</li>
        <li>Restoring original cropping pattern to
prevent any drought type situation due to
introduction of HYVs.</li>
        <li>Judicious use of water involves:
            <ol>
                <ul>
                    <li>Drip irrigation</li>
                    <li>Low energy precision application</li>
                    <li>Reusing urban wastewater</li>
                    <li>Saving water used in industries</li>
                    <li>Municipal water conservation managing
both supply and demand of water.</li>
                </ul>
            </ol>
</li>
        <li>Development of National Water Management policy not only to improve supply but
also to manage demand better.</li>
    </ul>
</ol>


