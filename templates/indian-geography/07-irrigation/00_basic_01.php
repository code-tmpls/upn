<h3>Irrigation</h3>


The process of supplying water to the crops
by artificial means such as canals, wells, tubewells 
from the source of water such as river, tanks,
ponds or ground water is called irrigation. The
monsoon rainfall in India is uncertain & highly
variable. The temporal & spatial variability of
rainfall accentuates the need for artificial supply
of water to the crops. Hence irrigation in India
becomes indispensable.
<br>
<br>
Apart from vagaries of monsoon, the cropping pattern in 
India also enhances the need for irrigation in the areas with sufficient rainfall.
Certain crops like rice, sugarcane, jute, cotton,
chalks require irrigational water. It is estimated
that production of irrigation crops is 50 to 100
percent higher than that of unirrigated crops
under similar geographical conditions. Introduction of HYV seeds and green revolution have
further increased the need for irrigation.

<div class="subheading"><strong><i>Reasons for Water Scarcity</i></strong></div>
<ol>
    <ul>
        <li>The rainfall is highly variable.</li>
        <li>18% of water available is polluted and unfit
for drinking.</li>
        <li>Almost all parts of India are affected either
by flood or drought.</li>
        <li>Demand for water is increasing due to rapid
increase in population.</li>
        <li>The management of available water resources in not proper.</li>
        <li>The storage facilities are very poor.</li>
        <li>Over-exploitation of ground water of- ten
result in the intrusion of saline sea water in
coastal areas.</li>
        <li>Cultivation of hybrid varieties of paddy,
wheat, cotton, sugarcane and tobacco which
consume more water.</li>
        <li>Evaporation loss of water stored in large
reservoirs and loss by seepage in long canal
systems.</li>
        <li>Siltation of water bodies.</li>
        <li>Dumping of various types of pollutants into
water bodies reduce its usability.</li>
    </ul>
</ol>

 The source of irrigation depends upon the
water availability, topography, relief,
availability of soil and moisture,
requirements of crop. Important sources of
irrigation in India include:
<ol>
    <ul>
        <li><b>Wells:</b> Wells are the most basic sources of
irrigation in India. It depends upon the availability of ground 
water. The Great Plains, the deltaic regions of the Mahanadi, Godavari,
Krishna and Cauvery are important regions
of well irrigation.</li>
        <li><b>Tanks:</b> Located in the rocky region of the
Peninsula. The tanks are non - perennial in
nature and additional water is needed in
summer. About 11% of the net irrigated area
in India is under tank irrigation. The
Karnataka plateau, coastal Maharashtra,
Andhra Pradesh, Tamil Nadu have this kind
of irrigation.</li>
        <li><b>Canals:</b> India has one of the world’s largest
areas under canal irrigation. About 40% of
the net irrigated area comes under canal
irrigation. Uttar Pradesh, Punjab, Haryana,
Rajasthan, Bihar, Andhra Pradesh are the
main regions.</li>
    </ul>
</ol>

