        <div class="subheading"><strong><i>Sheep</i></strong></div>    
        
India stands sixth in sheep population in the
world after Australia, Russia, China, Argentina
and New Zealand. More than 4 per cent of the
world’s sheep are reared in India, most of the
sheep are raised in regions which are too dry, too
stony or too mountainous to be too good for agricultural or 
for cattle rearing. Most of the India
sheep are of poor quality yielding inferior wool
in less quantity. Their yield of mutton is also very
low. However, some of the good breeds are found
in the northern temperate region. By virtue of
their sheer number, sheep occupy an important
place in our economy because they provide us
with wool, mutton and skins.
<br>
<br>
The distribution of sheep may be properly
studied by dividing the sheep areas into following regions:
Temperate Himalayan Region: It comprises
Jammu and Kashmir, Himachal Pradesh and
Uttarakhand. The entire region has temperate climate which is quite 
suit- able for good quality sheep. The excellent pastures exist on the hill
slopes. India’s best quality sheep are reared in the
Kula, Kangra, Chamba and Kashmir valley at altitudes varying from 2000 to 3000 m.
Dry North-Western Region: This region includes Rajasthan and neighbouring parts of
Punjab, Haryana, Western Uttar Pradesh, Gujarat
and Madhya Pradesh. There are more than 13
million sheep contributing to about half of the
total wool production of India, however the wool
is of comparatively inferior quality and the yield
of wool per sheep is lower than that of the Himalayan. Marwari breed of 
Rajasthan is famous for its wool.
<br>
<br>
<b>Semi-arid Southern Region:</b> This region,
com- prising Maharashtra, Karnataka, Andhra
Pradesh, Tamil Nadu and parts of Madhya
Pradesh, supports half the total number of sheep
found in India. In spite of the largest numbers of
ships about 50 percent of the sheep of this region
are raised for mutton and produce no wool.
Humid Eastern Region: This region comprising Bihar, West Bengal, Assam and Orissa, has
humid climate which is not favourable for sheep
rearing. The sheep are mainly reared for producing mutton. In terms of sheep population
Rajasthan ranks first followed by Andhra
Pradesh, Tamil Nadu and Karnataka.</li>
        <li>
        <div class="subheading"><strong><i>Goat</i></strong></div>    
        
Goat is called the poor man’s cow because it
can be cheaply reared on meagre grass of poor
quality. It is the major supplier of mutton along
with milk, hair and skins. Goats are found in larger
number as compared to sheep and are next only
to cattle. About one-sixth of the world’s goat is
reared in India. Although goats are found in almost of 
parts of country their major concentration
is in Bihar (17.31 mil- lion), West Bengal (14.17
million), and Uttar Pradesh (13.11 million). These
states account for more than half goats of India.
There are some outstanding breeds which are
found in some specific areas. The Himalaya or
Angora goat which is also known as the Chamba,
Gaddi, Chegu or Kashmiri breed is reared in
Kashmir and Himachal Pradesh. It produces soft
warm hair. The Pashmina reared in Kashmir and
Kullu valley is world renowned for its pashmina
fur known as Mohair.
<br>
<br>
The Jamunapuri is the breed found between the
rivers Yamuna and the Chambal. It is dual purpose
breed providing meat and milk. Among the other
breeds are the Beetal of Punjab, of the Marwari,
Mehsana, Kathiawar and Zalwadi of Rajasthan
Gujarat and Madhya Pradesh and the Barari, Surti
and Deccani of the Peninsular India. Several important foreign 
breeds such as Alpine, Nubian, Saanen,
Toggenberg and Angora have been used for cross
breeding with the local breeds.</li>
        <li>
        <div class="subheading"><strong><i>Pigs</i></strong></div>
            
There are about 100 lakh pigs providing about
5 per cent of India’s meat production in the form
of pork. In a poor and thickly populated country
like India, pig rearing is an important activity
because pig provides rich meat at low cost. Pig
farming plays in important role in improving the
socio-economic status of sizeable section of
weaker rural communities especially in northeastern 
states where every rural family rears pigs
for meat.</li>
