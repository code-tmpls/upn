<div class="subheading"><strong><i>Fisheries</i></strong></div>    

India is the world’s 7th largest producers of
fish. Production of fish in India is small, being
only about 2.4% of the total world production.
72% of the total catch is brought by non - mechanized boats. 
Marine fisheries account for 2/3rd
and inland fisheries for 1/3rd of India’s fish production. 
The present per capita consumption of
fish in India is only 4kgs/year against a desired
consumption level of 31kgs/year.

