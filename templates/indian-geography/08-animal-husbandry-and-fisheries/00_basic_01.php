<h3>Animal Husbandary & Fisheries</h3>


India has the largest and most varied animal
resources in the world and it has about 1/6th of the
cattle, 1/2 of the buffalo and 1/5th of the goat population in the World. India has a good number of
milch breeds. Sahiwal, Red Singhi and Deoni are
some of the outstanding breeds. India has gone for
the world’s largest and most ambitious milk
programmes called ‘operation flood’. Drought
breeds are poor in milk yields, but the bullocks are
excellent draught animals. About 42% of the cattle
in the country are draught animals.
<br>
<br>
India has 3% of the world’s sheep population
and ranks 6th among the sheep-breeding countries
of the world. Rajasthan has the largest (24%) of
India’s total stock. India owns one of the largest
livestock populations in the world. It accounts for
16% of the cattle population and 57% of buffalo
population. India has become the largest producer
of milk in the world.

