<div class="subheading"><strong><i>Marine Fisheries</i></strong></div>

It includes coastal fisheries, off-shore fishers
and deep sea fisheries.
<ol>
    <ul>
        <li><b>Coastal Fisheries:</b> The coastal zone extends
upto 25m depth. Fish of this belt comprises
of pelagic fishes, like sardines, Mackerel,
Lesser Sardines, bottom species like Bombayduck, silver bellies, Ships, etc.
Herrings, Sardines and Anchovies together
accounts for 65% catch.
<br>
<br>
Tuna, Bonitos, Mackerels, Crustaceans, Shark,
Rays, Skates, Flounders, Halibuts account the
remaining 35% of total catch.
71% of total production of marine fish is along
the west coast of Kerala, Maharashtra,
Karnataka, Gujarat, Goa. Only about 29% of
the total marine fish catch comes from the east
coast of India.</li>
        <li><b>Off-shore and Deep Sea Fisheries:</b> It is not
fully developed in India. Large concentrations
of Sardines and Mackerel have been located
along the Thiruvanantapuram Goa coast.
<ol>
    <ul>
        <li>
        <div class="subheading"><strong><i>Inland Fisheries</i></strong></div>    
        
Quick growing species with non-precious
feeding habit are generally selected for cultivation in ponds, lakes and reservoirs.
Some famous inland species are Cirrhinusmarigala, Cirrhinus -cirrhosa, Puntius,
Cornaticus, Katla, Labeo-rohita, etc.
In brackish water estuarine fisheries the fish
found are mostly marine species like Anchovies, Cat-fish, Perches, Pearl-spot, etc.</li>
        <li>
        <div class="subheading"><strong><i>West Coast Fishing</i></strong></div>    
        
Here the fishing season is from September to
February-March.
Here both the continental self and the water
have more pronounced seasonal cycle and
higher Phosphatic and nitrate contents, so
more availability of plankton.
Major fish species are Sardines, Mackarel,
Prawn, etc.</li>
        <li>
        <div class="subheading"><strong><i>East Coast Fishing</i></strong></div>    
        
Here the fishing season is from July to October on the Andhra coast and September to
April on the Coromandel Coast.
Here the circulation of water is less pronounced.
Estuarine Fisheries: Chilka and Pulicat Lake.
Per capita fish consumption is one of the highest in West Bengal but the state production is
sufficient to meet only 20% of total demand.</li>
        <li>
        <div class="subheading"><strong><i>Pearl Fisheries</i></strong></div>    
        
East coasts are more extensive and productive than the west coast and extended from
Cape-Camorin to Kilkarai with Tuticorin as
centre. Excellent quality of Oriental Pearls or
Lingna pearls is found here.
On the West coasts Gulf of Kutch, to the north
of Halar district of Saurashtra and near
Jamnagar are the productive regions.
The principal pearl fishing areas in India are Gulf
of Mannar, Gulf of Kutch and the Palk Bay.
In southern India pearl-oysters are harvested
by divers but in Kutch and Saurashtra they
are exposed to low-water spring tides and
hence are easily collected by hand.
Central Marine Fisheries Research Institute is
located at Mandapam Camp.</li>
