<div class="subheading"><strong><i>Manganese Ore</i></strong></div>

It is primarily used for making iron and steel
and it acts as basic raw material for manufacturing
its alloy. It is also used in manufacturing of
bleaching powder, insecticides, paints, batteries
and China clay. In India it occurs in the form of
sedimentary stratified metamorphic deposits of
Dharwar System of Peninsular India.
<br>
<br>
<b>Ore Types:</b> Psilomelane, Braunite, Pyrolusite.
About 78% of total reserve is found in Nagpur
and Bhandara districts of Maharashtra and
Balaghat district of M.P. Singhbhum-KeonjharBonai area holds the 11% of total reserve while
the north Kanara shares 6%.
In terms of reserve M.P. (including
Chhattisgarh) ranks first followed by
Maharashtra, Orissa and Karnataka.
In terms of production Orissa ranks first
followed by Karnataka, M.P. (including
Chhattisgarh), Maharashtra and Andhra Pradesh.
Ferro-Manganese Plants: (1) Rayagada (2) Joda
(both in Orissa) (3) Tumsar (4) Kamptee (both in
Maharashtra) (5) Bhadravati (6) Dandeli (both in
Karnataka) (7) Garividi (Andhra Pradesh).

<div class="subheading"><strong><i>Chromite</i></strong></div>

It is used for producing “Disodium” used for
the manufacturing stainless steel.
Orissa ranks first in the production by
contributing 90% of the national production,
followed by Karnataka, Maharashtra, Jharkhand
and Tamil Nadu.
<br>
<br>
About 96% production of Tamil Nadu is
exported to Japan and the rest to Australia.
The production of chromite was of 1324
thousand tonnes in 1999-2000.

<div class="subheading"><strong><i>Nickel</i></strong></div>

Cuttack and Mayurbhanj districts of Orissa
is famous nickel reserve and about 80% Orissa
reserve is only concentrated in Kausa block.
India imports nickel to fulfill its domestic
demand.

<div class="subheading"><strong><i>Bauxite</i></strong></div>

These deposits are mainly associated with
laterite soils.
<br>
<br>
Upto 1988 India was an important importer
of aluminium but in 1993 India has become a
major exporter of alumina, mainly to Italy,
Germany, U.K., Japan, etc.
In terms of reserves Orissa ranks first followed
by Andhra Pradesh, M.P. (including
Chhattisgarh), Bihar (including Jharkhand).
In terms of production Orissa also ranks first
followed by Jharkhand, Maharashtra, Gujarat and
M.P. (including Chhattisgarh).

<div class="subheading"><strong><i>Copper</i></strong></div>

Copper ores in India are found as sulphides
(Chalcopyrite, Chalcocite, Bronite), oxides
(Cuprite) and carbonates (Malachite and Azurite).
They generally occur in veins in Peninsular
India in highly metamorphosed rocks.
In terms of reserve Rajasthan ranks first
followed by Bihar (including Jharkhand), M.P.
(including Chhattisgarh), Karnataka and Andhra
Pradesh.

<div class="subheading"><strong><i>Gold</i></strong></div>

Generally found in quartz veins and
sometimes associated with iron and copper
sulphides.
<br>
<br>
Karnataka has been leading producer, since
1871 when the mining first started in Ooregum
mines, Champion in Mysore. Other mines are
Hutti, Topuldedi, Wondalli etc.
Ramagiri Gold field of Anantapuram district
of Andhra Pradesh is another producer
The alluvial gold is found in the beds of Garranadi, South Koel, Sanjay, 
Subarnarekha river of Singhbhum district of Jharkhand.
Bharat Gold mines Ltd.: Founded in 1972 as
a Public Sector which manages all the three
operating mines of KGF area.
Hindustan Copper Ltd: Recover gold as a byproduct along with other metals from its Khetri
deposits.

<div class="subheading"><strong><i>Silver</i></strong></div>

Generally in the form of Galena, Proustite mixed
with copper, zinc or lead. Produced as by-product
during the smelting of galena or produced from the
lead ore of Kurnool, Cuddapah and Guntur of
Andhra Pradesh; Singhbhum and Ranchi of
Jharkhand; Vadodara of Gujarat. Quartzites of
Mysore Gold field and cupriferous pyrites of
Chitradurg also yield some amount of silver.
Diamond
<br>
<br>
The diamond is mostly available in Panna
(Madhya Pradesh), Rammallakota in Andhra
Pradesh and also in Krishna river basin. Presently
diamonds are being excavated from only Panna
mines.

<div class="subheading"><strong><i>Mica</i></strong></div>

In India the two important ores of micaMuscovite and Biotite - are found.
In terms of production Bihar (including
Jharkhand) ranks first followed by Andhra
Pradesh and Rajasthan.
Kodarma in Jharkhand is the world’s largest
mica mine.
<br>
<br>
Ruby mica and Bengal mica, which is of high
quality, is found in Bihar and Jharkhand.
Green Mica: (lightest of all types), also called
as Electrical Mica; found in Andhra Pradesh.
In Rajasthan green or pink colour high quality
mica are found.
<br>
<br>
A large quantity of mica is also exported. But
now India is facing stiff competition with Brazil.

<div class="subheading"><strong><i>Limestone</i></strong></div>

Consumption of limestone in India: Cement
Industry-67%, Iron & steel industry -16% chemical
Industry - 4%
In terms of overall production of limestone
M.P. (including Chhattisgarh) ranks first followed
by Andhra Pradesh, Rajasthan, Gujarat and
Karnataka.
Cement grade Lime-stone are mainly found
in Andhra Pradesh, Karnataka, Gujarat and
Rajasthan.
Flux grade limestone is mainly produced in
MP (including Chhattisgarh), Meghalaya,
Andhra Pradesh, Gujarat, Orissa, Jharkhand and
Karnataka.

<div class="subheading"><strong><i>Asbestos</i></strong></div>

In India Amphibole and Crysolite varieties of
asbestos are found.
Asbestos has the fibrous structure and has
great economic importance as it has the capacity
to be separated quickly into fine filaments of high
tensile strength and its great resistance to fire.
In terms of production Rajasthan ranks first
followed by Andhra Pradesh and Karnataka.

<div class="subheading"><strong><i>Fire Clay</i></strong></div>

It is used in making bricks for furnace.
These reserves are mostly available in
Gondwana coal regions and basins. Bihar,
Gujarat, Madhya Pradesh, Orissa, Tamil Nadu,
Rajasthan, West Bengal and Andhra Pradesh are
the states where fire clay is abundantly available.

<div class="subheading"><strong><i>Gypsum</i></strong></div>

Gypsum is a colourless or white mineral which
contains calcium sulphate. Gypsum is used in the
production of cement and plaster of Paris.
Most of its deposits are found in Rajasthan,
Tamil Nadu, Jammu & Kashmir, Himachal
Pradesh and Uttar Pradesh. Some deposits are
also found in Gujarat.

<div class="subheading"><strong><i>Rare-Earths</i></strong></div>

In the south-west tip of India, on the Kerala
and Tamil Nadu coast, an extremely rich minerals
like Ilmenite and Monazite are found.
Ilmenite: From Quilon to Kanyakumari.
Ilmenite and Rutile are the by product in the
by-products in the extraction of monazites.

<div class="subheading"><strong><i>Salt</i></strong></div>

About 75% of total salt produced in India is
manufactured from saline sea water by the
process of solar evaporation.
Sea-Salt: Gujarat (Mithapur, Jamnagar,
Dharsana, Okha, Bulsar), Maharashtra
(Bhandrup, Uran, Bhayandar), Tamil Nadu
(Madras and Tuticorin).
<br>
<br>
<b>Salt Lake:</b> Sambhar, Didwana, Pachbhadra,
Lankasara in Rajasthan.
Rock-Salt: Mined at present in Mandi district
Drang and Guna in Himachal Pradesh.
In terms of production Gujarat ranks followed
by Tamil Nadu and Rajasthan.

<div class="subheading"><strong><i>Kyanite</i></strong></div>

India has the largest reserves of Kyanite in
the world. The distribution is as follows:
Jharkhand: A belt extending from Lapsa Buru
to Kharasawan in Saraikela with the important
mines at Lapsa-Buru, Ghagidih, Bachia- Bakro &
Mauyaluka.
Maharashtra: Pahargaon & Pipalgaon in
Sakohi Tehsil and Gorkha- Buranga and
Asvalpain in Bhandara districts.

<div class="subheading"><strong><i>Off-Shore Mineral Wealth</i></strong></div>

“Polymetallic Nodules”, rich in some metals like copper, cobalt, nickel, manganese, are
located on the deep sea floor of the Indian
Ocean especially on the Central Indian Ocean
basin.
<br>
<br>
Indian Institute of Oceanography- Goa is
the prime organisation to explore these metals. India has accorded “Pioneer Investor”
status in deep sea bed exploration by the U.N.
in 1982.
The International Seabed Authority registered India’s claim for deep seabed mining in
August 1987 and allotted a site of 1.5 lakh sq.
cm. for further exploration in the central Indian
Ocean.

