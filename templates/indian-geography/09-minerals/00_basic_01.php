<h3>Minerals</h3>


Minerals are the naturally occurring materials
which when exploited economically are called
ores. Their exploitation is possible when they occur
in concentrated form and near the surface. India is
endowed with a rich variety of minerals. The
ancient archaic landmass contains both metallic
and nonme- tallic minerals.

<div class="subheading"><strong><i>Distribution of Minerals</i></strong></div>

Though natural endowment in terms of
mineral is quite high, its spatial distribution is
highly uneven. The important areas are:
<ol>
    <ul>
        <li><b>North-Eastern Peninsular Belt:</b> This is an
ancient shield area of peninsular block. It
comprises of Chhotanagpur Plateau, Orissa
Plateau and Chhattisgarh. It contains large
quantity of coal, iron ore, manganese, mica,
bauxite, copper, etc. It is known as mineral
heart of India. It has 100% Kyanite, 93% iron
ore, 84% coal, 70% chromite.</li>
        <li><b>Central Belt:</b> The region consists of MP,
Chhattisgarh, Andhra Pradesh and
Maharashtra. It is the second largest mineral belt of India. 
Large deposits of manganese, baux- ite, coal, iron ore, graphite and
limestone are found.</li>
        <li><b>The South- Western Belt:</b> It includes south
Karnataka and Goa. It has iron ore, granite
and clay deposits.</li>
        <li><b>The North-Western Belt:</b> It extends along the
Aravallis and adjacent Gujarat. It is important
for non-ferrous min- eral (copper, lead, zinc),
uranium, mica, steatite, etc. It has mineral oil
source in the Gujarat plains.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Iron-ore</i></strong></div>

India has the vast resources of iron-ore, about
20% of total world reserves.
Ore-Types: The ore-types are the following:
<ol>
    <ul>
        <li>Haematite: It has the iron content up to 86%;
mainly concentrated in the Dharwar and
Cuddapah system of the peninsular India. 
It is also called as “Red Ores” which contributes about 85% of total national production.</li>
        <li>Magnetite: It has the iron content of 60%;
mainly concentrated in the Dharwar and
Cuddapah system of Indian peninsula. It is
also called “Black Ores” and contributes
about 8% of total production.</li>
        <li>Limonite: It has 30-50% iron content and is
the prominent constitute of laterite. It contributes about 7% of total production.</li>
    </ul>
</ol>



