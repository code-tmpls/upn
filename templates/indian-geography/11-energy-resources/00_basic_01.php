Sub-Himalayan area of Punjab, Haryana, J&K and
Arunachal Pradesh.</li>
        <li>Rajasthan-Saurashtra-Kutch Basin: West
of Aravalli</li>
        <li>Northern Gujarat Basin & Southern
Gujarat Basin.</li>
        <li>Ganga Valley Basin: U.P. and Bihar.</li>
        <li>Coastal Tamil Nadu, Andhra Pradesh &
Kerala: Mahanadi, Godavari, Cauvery & Krishna
deltas.</li>
        <li>Andaman and Nicobar Coastal region.
Main production area: Assam-Arakan region,
Cambay basin of Gujarat.</li>
    </ul>
</ol>
In Cambay region oil was discovered in
Bombay High in 1974 and in R-12, B-37, B-38 and
N-Bassein recently.
<br>
<br>
