The modern petro-chemical industries,
cement and fertilizer industries and locomotive
all so heavily depend on this mineral liquid that
possession of oil-wells alone makes a country or
a region economically well off. Petroleum is
always found in pre-existing shallow basins or
geosynclines characterized by marine life
sedimentation and subduction.
In India most of the petroleum occurrences
are associated with anticlines and faults traps in
the rocks formation of Tertiary times. However
most of the areas in India are of Pre-Cambrian
age which is regarded as highly unfavourable
regions for oil-field.
Major Petro-rocks area in India can be
identified as:
<ol>
    <ul>
        <li>Upper Assam Basin: Brahmaputra valley
