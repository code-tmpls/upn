<div class="subheading"><strong><i>Road Transport</i></strong></div>

India’s road network is one of the largest in
the world; the total length of the roads being more
than 33 lakh km at present. Karnataka, with a total
road length of about 64,000 km, leads followed
by Madhya Pradesh and Uttar Pradesh. Roads are
most suitable for short and medium distance.
Other advantages include flexibility, reliability,
speed and door to door service. For the purpose
of maintenance and construction, roads are
classified into:
    <br>
    <br>
National Highways, State Highways, Village
Roads, Border Roads, International Highways.
There are about 30 National Highways
connecting state capitals and have been
constructed by the Central government. The
present National Highway system includes a total
length of 38517 km. It constitutes only 2% of total
road length and carries nearly 40% of the road
traffic. 
<br>
<br>
The National highways No-7 is the longest
highways of India. At present India has 5 express
highways. They are:
<ol>
    <ul>
        <li>Western</li>
        <li>Eastern</li>
        <li>Between Calcutta and Dumdum</li>
        <li>Between Sukinda mines and Paradeep</li>
        <li>Between Durgapur and Calcutta</li>
    </ul>
</ol>

The Plan of Super National Highways: After
the National Highways, there is a plan under the
consideration of Central Road Transport Ministry
to create Super National Highways. Through
these Super National Highways of about 14,000
km length, there is a plan to link big sea ports of
the country with important cities. In the building
of these Highways, the role of Private Sector will
be important. This will be done on the basis of
BOT (Build Operate Transfer) by Private Sector.
The National Highway Authority of India
(NHAI) was constituted under the National
Highway Authority of India Act 1988 and was
made operational in February 1995. Initially it was
entrusted with the task of implementing five
externally aided NH improvement projects.
Subsequently it has been mandated to implement
the National Highways Development Project
(NHDP) comprising 4/6 Lanning of 13252 km of
national highways having two components:
<ol>
    <ul>
        <li>The Golden Quadrilateral connecting
Delhi-Mumbai-Chennai-Kolkata-Delhi</li>
        <li>North-South and East-West corridors
(7300 km.), connecting Srinagar to Kanya Kumari
and Silchar to Porbandar, respectively.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Ports</i></strong></div>


There are 11 major ports and 148 minor
working ports in India. Major ports are the direct
responsibility of the Central government while
minor ports including the intermediate ports fall
in the concurrent list of the Indian Constitution
and are managed and administered by the
respective maritime state governments.
<br>
<br>
<b>Major Ports on the Western Coast :</b> Kandla
(Gujarat); Mumbai (Maharashtra); Marmagao
(Goa); New Mangalore (Karnataka); Cochin
(Kerala); Jawaharlal Nehru Port (Nhava Sheva,
Maharashtra).
<br>
<b>Major Ports on the Eastern Coast:</b> Tuticorin
(Tamil Nadu); Chennai (T.N.); Vishakhapatnam
(Andhra Pradesh); Paradeep (Orissa) and
Calcutta -Haldia (West Bengal).
<br>
<br>
Among major ports, Mumbai is the biggest.
Kandla is a tidal port. Marmagao enjoys the
second position by value of the tonnage of traffic,
bulk of which is export of iron ore.
Visakhapatnam is the deepest, land-locked and
protected port. Chennai has an artificial harbour.
Calcutta is a riverine port. Haldia has a fully
equipped containerized berth.
<br>
Shipping: Overseas shipping has an extremely
important role to play in India’s international
trade. The country has the largest merchant
shipping fleet among developing countries and
ranks 17th in the world in shipping tonnage.


<div class="subheading"><strong><i>Air Transport</i></strong></div>

The major international airports are Delhi
(Indira Gandhi International Airport - Palam);
Kolkata (Dum Dum, now Netaji Subhash
Chandra Bose International Airport); Mumbai
(Santa Cruz, now Chhatrapati Shivaji
International Airport); Chennai (Meenambakam);
Amritsar International Airport; Guwahati
(Lokpriya Gopinath Bordoloi International
Airport); Ahmedabad (Sardar Vallabhbhai Patel
International Airport); Hyderabad (Rajiv Gandhi
International Airport); Cochin International
Airport; Bengaluru International Airport; Goa
International Airport and Trivandrum
International Airport (Thiruvana nthapuram).
Besides, there are 87 aerodromes and 20 civil
enclaves maintained by Civil Aviation
Department. The Civil Aviation Centre in
Fursatganj near Allahabad provides, among other
things, ground training to the pilots.
Air India established in 1953 is having
bilateral air services agreement with 90 countries
as on Jan 1, 1998.
<br>
<br>
Indian Airlines started its operations from 1st
August, 1953, with a fleet of 99 aircraft and was
the outcome of the merger of seven former
independent airlines, namely Deccan Airways,
Airways-India, Bharat Airways, Himalayan
Aviation, Kalinga Air Lines, Indian National
Airways and Air Services of India. After being
granted permission from the Government of
India, on 15 July 2007, Indian Airlines and Air
India merged and started to operate as a single
entity. A new company called National Aviation
Company Ltd was registered by the government
to amalgamate the two airlines along with their
low-fare subsidiaries- Air India Express (of Air
India) and Alliance Air (of Indian). Post-merger
the new airline will be renamed as Air Indian.
<br>
<br>
Pawan Hans Limited basically provides
helicopter support services to oil sector, hill
station and remote areas. It also provides air
support services to several customers which
includes ONGC, Punjab, M.P., Lakshadweep
Administration, GAIL, BSF and also caters to
private sector.
<br> 
<br>
<b>Private Air Taxi:</b> The liberalization process in
civil aviation took wing in April 1990 with the
cargo open skies policies.


<div class="subheading"><strong><i>National Waterways</i></strong></div>

The Inland Waterways Authority of India was
set up on October 27, 1986. This statutory body
has the responsibility of development,
maintenance and regulation of national
waterways.
<br>
<br>
The Government has identified 10 important
waterways for consideration to declare them as
National waterways. The followings have so far
been declared as National Waterways and the
same are being developed for navigation by
Inland Waterways Authority of India.

<ol>
    <ul>
        <li>The Ganga between Allahabad and Haldia
(1620 km) on October 27, 1986,</li>
        <li>The Sadia-Dhubri-stretch of river Brahmaputra
(891 km) on October 26, 1988</li>
        <li>The Kollam-Kottapuram stretch of west
coast canal (168 km) along with Champakara
canal (14 km) and Udyogmandal canal (22
km) in Kerala</li>
        <li>Kakinada - Puducherry stretch of canals and
the kalu velly Tank.</li>
        <li>Talcher - Dhamra stretch of river Brahmani.</li>
        <li>Lakhipur to Bhanga of river Barak</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Posts & Telegraphs</i></strong></div>


The first Indian postal stamp was issued in
1852 in Karachi. The Postal department was set
up in 1854 when nearly 700 post offices were
already functioning. Today, there are about
1, 53,454 post offices. For the efficient and correct
handling of the volume of mail, a numerical postal
address code, known as the Postal Index Number
(PIN), was introduced with digits which help to
identify and locate every departmental delivery
post offices excluding branch post offices.

<div class="subheading"><strong><i>Telecommunication</i></strong></div>

Telecommunication services were introduced
in India soon after invention of Telegraph and
telephone. First telegraph line between Calcutta
and Diamond Harbor was opened for traffic in
1851. By March 1884, telegraph messages could
be sent from Agra to Calcutta.




