<h3>Transport & Communication</h3>

Transport and Communication facilities are
necessary for the healthy growth of country. Road
and rail transport are well developed in India
with ample scope for the development of water
transport. Ocean waterways are already well
developed. Inland waterways require
improvement.


<div class="subheading"><strong><i>Railways</i></strong></div>

Trains are the most important means of
transport. They account for nearly three-fourth
of the passenger traffic and four-fifth of the freight
traffic in India. Indian railways system is the
largest in Asia and the fourth largest in the world.
It is the biggest departmental public undertaking
in the country. It is also the world’s second largest
railway system under a single management. The
first train in India steamed off from Bombay to
Thane, a stretch of 34 km. in 1853. The network
of railway has increased upto 63,465 km.
<br>
<br>
The Indian railways operate in three different
gauges mainly-Broad Gauge, Metre Gauge and
Narrow Gauge. The broad gauge accounts for
nearly 50% followed by metre gauge 43% of the
total route length. Railways are divided into 17
zones, headed by a General Manager who is
responsible to the Railway Board for operation,
maintenance and financial matters. Out of the 17
zones, Northern Railways having length 10,995
km is the longest route.
<br>
<br>
Container Service: Indian Railway has
introduced a new marketing strategy of container
services in 1980 with 7 container depots which in
1996 has expanded to 32 locations. The Container
Corporation of India (CONCOR), a public sector
undertaking, provide door to door services for
domestic users, transportation in bulk for small
customers and International transport in
International Standards Organisation (ISO)
containers.

