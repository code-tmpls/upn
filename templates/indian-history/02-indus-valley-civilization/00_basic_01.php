<li>
    
<div class="subheading"><strong><i>Kalibangan</i></strong></div>    

Kalibangan was an important Harappan city. The
word Kalibangan means ‘black bangles’. A ploughed
fi eld was the most important discovery of the early
excavations. Later excavations at Kalibangan made
the following specifi c discoveries:
    <ol>
        <ul>
            <li>A wooden furrow</li>
            <li>Seven fi re altars in a row on a platform suggesting
the practice of the cult of sacrifice.</li>
            <li>Remains of a massive brick wall around both the
citadel and the lower town (the second Harappan
site after Lothal to have the lower town also
walled.</li>
            <li>Bones of a camel.</li>
            <li>The skull of a child found suffering from
hydrocephalus.</li>
            <li>A tiled fl oor which bears intersecting designs of
circles.</li>
            <li>A human head with long oval eyes, thick lower
lips, receding forehead and straight pointed nose.</li>
            <li>Evidences of two types of burials:
 Burials in a rectangular grave and
 Burials in a circular grave.</li>
        </ul>
    </ol>
</li>
<li>
    
<div class="subheading"><strong><i>Kot-Diji</i></strong></div>

Kot-Diji is known more as a pre-Harappan site.
It gives the impression of a pre-Harappan fortifi ed
settlement. Houses were made of stone. The remains
of Kot-Diji suggest that the city existed in the fi rst
half of the third millennium B.C. Excavations at the
site suggest that the city was destructed by force.</li>
<li>
<div class="subheading"><strong><i>Lothal/i></strong></div>    

Lothal was an important trade centre of the Harappan culture. The town planning in Lothal was different from that of Harappa and Mohenjodaro. The city
was divided into six sections. Each section was built
on a wide platform of unripe bricks. Each platform
was separated by a road with width ranging from
12 feet to 20 feet. Excavations at Lothal led to some
specifi c discoveries which include:
<ol>
    <ul>
        <li>Remains of rice husk (the only other Harappan city
where the rice husk has been found is Rangpur near
Ahmedabad).</li>
        <li>An artifi cial dock yard.</li>
        <li>Evidence of a horse from a doubtful terracotta
fi gurine.</li>
        <li>Impressions of cloth on some of the seals.</li>
        <li>Evidence of direct trade contact with
Mesopotamia.</li>
        <li>Houses with entrances on the main street (the houses
of all other Harappan cities had side entries).</li>
<li>A ship designed on a seal.</li>
<li>A terracotta ship.</li>
<li>A painting on a jar resembling the story of the
'cunning fox' and the 'thirsty crow' narrated in
Panchatantra.</li>
<li>Evidence of double burial (burying a male and
a female in a single grave) found in three graves
whereas in Kalibangan one such grave has been
found.</li>
<li>Evidence of games similar to modern day chess
and</li>
<li>An instrument for measuring 180, 90, 45 degree
angles (the instrument points to modern day
compass).</li>
    </ul>
</ol>
</li>
<li> 
<div class="subheading"><strong><i>Amri</i></strong></div>    
<ol>
    <ul>
        <li>Amri also gives evidence of pre-Harappan
settlement. However it lacks the fortifi cation plan
of the pre-Harappan phase. A spectacular feature
of Amri is that it gives the impression of existence
of transitional culture between pre and post
Harappan culture.</li>
        <li>Important findings at Amri include the actual
remains of rhinoceros, traces of Jhangar culture in
late or declining Harappan phase and fi r altars.</li>
    </ul>
</ol>
</li>
<li>
    
<div class="subheading"><strong><i>Chanhu-daro</i></strong></div>    
<ol>
    <ul>
        <li>Excavations at Chanhu-daro have revealed three
different cultural layers from lowest to the top
being Indus culture, and the pre-Harappan Jhukar
culture and the Jhangar culture.</li>
        <li>The site is especially important for providing
evidences about different Harappan factories.
These factories produced seals, toys and bone
implements.</li>
        <li>Through excavations, the evidence of bead maker's
shops has come to light.</li>
        <li>It was the only Harappan city without a citadel.</li>
        <li>Some remarkable fi ndings at Chanhu-daro include
bronze fi gures of bullock cart and ekkas, a small
pot suggesting an inkwell, footprints of an elephant
and a dog chasing a cat</li>
    </ul>
</ol>
</li>
<li>
    
<div class="subheading"><strong><i>Ropar</i></strong></div>    
<ol>
    <ul>
        <li>Ropar is a Harappan site from where remains of
pre-Harappan and Harappan cultures have been
found.</li>
<li>Buildings at Ropar were made mainly of stone and
soil.</li>
<li>Important findings at the site include pottery,
ornaments, copper axes, chert blades, terracotta
blades, one inscribed steatite seal with typical Indus
pictographs, several burials interred in oval pits
and a rectangular mud brick chamber.</li>
<li>There is also an evidence of burying a dog below
the human burial (Though the practice was
prevalent in Burzahom in Kashmir, it was late in
the Harappan context.</li>
    </ul>
</ol>
</li>
<li>
    
<div class="subheading"><strong><i>Banawali</i></strong></div>
<ol>
    <ul>
        <li>Situated in Hissar district of Haryana, Banawali
has provided two phases of culture during its
excavations: the pre-Harappan (Phase I) and the
Harappan (Phase II).</li>
        <li>Though Phase II belonged to the Harappan period,
chess board or grid pattern of town planning was
not always followed as in other Harappan sites.</li>
        <li>The roads were not always straight nor did they
cut at right angles.</li>
        <li>It also lacked another important feature of the
Harappan civilization - a systematic drainage
system.</li>
        <li>High quality barley has been found in
excavations.</li>
<li>Other important material remains include ceramics,
steatite seal and a few terracotta sealing with typical
Indus script, ear rings shaped like leaves of a peepal
tree and terracotta bangles.</li>
    </ul>
</ol>
 </li>
