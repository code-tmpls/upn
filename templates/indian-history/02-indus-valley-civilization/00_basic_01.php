<h3>Indus Valley Civilization</h3>

<div class="subheading"><strong><i>Origin and Phases</i></strong></div>

<ol>
    <ul>
        <li>The Indus Valley civilization was an ancient
civilization thriving along the Indus river and the
Ghaggar-Hakra river in what is now Pakistan and
north-western India. Among other names for this
civilization is the Harappan civilization in reference
to the fi rst excavated city of Harappa.</li>
<li>An alternative term for the culture is SaraswatiSindhu civilization based on the fact that most of
the Indus Valley sites have been found along the
Ghaggar-Hakra river.</li>
<li>R.B. Dayaram Sahni first discovered Harappa
(on Ravi) in 1921. R.D. Banerjee discovered
Mohenjodaro or 'Mound of the Dead' (on Indus)
in 1922. Sir John Marshal played a crucial role in
both these.</li>
<li>Harappan civilization forms part of the proto
history of India i.e. the script is there, but it cannot
be deciphered and belongs to the Bronze Age.</li>
<li>The Indus valley civilization gradually developed
to a full-fledged civilization which has been
established through a continuous sequence of strata
named as Pre-Harappan, Early Harappan, Mature
Harappan and Late Harappan stages or phases.</li>
<li>The long term indigenous evolution of this
civilization which obviously began on the periphery
of the Indus Valley in the hills of eastern Baluchistan
and then extended so far into the plains, can be
documented by an analysis of four sites which have
been excavated in recent years: Mehargarh, Amri,
Kalibangan and Lothal which refl ect the sequence
of the four important phases or stages in pre and
proto history in the north-west region of the Indian
sub-continent.</li>
<li>The sequence begins with the transition of
nomadic herdsmen to settled agriculturists in
eastern Baluchistan (First Phase), continues with
the growth of large villages and the rise of towns
in the Indus Valley (Second Phase), leads to the
emergence of the great cities (Third Phase), and
fi nally, ends with their decline (Fourth Phase).</li>
<li>Mediterranean, Proto-Australoid, Mongoloids and
Alpines formed the bulk of the population, though
the fi rst two were more numerous.</li>
<li>More than 100 sites belonging to this civilization
have been excavated.</li>
<li>According to radio-carbon dating, it spread from
the year 2500-1750 B.C.</li>
<li>Copper, bronze, silver and gold were known but
not iron.</li>
    </ul>
</ol>



<div class="subheading"><strong><i>Geographical Extent</i></strong></div>
<ol>
    <ul>
        <li>Covered parts of Punjab, Sindh, Baluchistan,
Gujarat, Rajasthan and some parts of Western U.P.
It extended from Manda in Jammu in the north
to Daimabad in the south and from Alamgirpur
in western U.P. to Sutkagendor in Baluchistan in
the west.</li>
        <li>Major sites in Pakistan are Harappa (on river
Ravi in west Punjab), Mohenjodaro (on Indus),
Chanhu-Daro (Sindh), etc. In India the major
sites are Lothal, Rangpur and Surkotda (Gujarat),
Kalibangan (Rajasthan), Banawali (Hissar) and
Alamgirpur (western U.P.)</li>
        <li>The largest and the latest site in India is Dholavira
in Gujarat. Dr. J.P. Joshi and Dr. R.S. Bisht were
involved in it.</li>        
    </ul>
</ol>

<div class="subheading"><strong><i>Town Planning</i></strong></div>

<ol>
    <ul>
        <li>The Indus Valley people were primarily urban
people. Elaborate town-planning following the
Grid System. Roads were well cut dividing the
town into large rectangular or square blocks. Lamp
posts at intervals indicate the existence of street
lightening. Flanking the streets, lanes and by-lanes
were well-planned houses. The streets were quite
broad varying from 9 feet to 34 feet in breadth.</li>
        <li>Burnt bricks of good quality were used for building
material except in Rangpur and Kalibangan.
Elsewhere in the contemporary world mud bricks
were used. No pottery-kiln was allowed to be built
within the four walls of the city.</li>
        <li>Houses were often of two or more storey, of
varying sizes but were quite monotonous - a square
courtyard around which were a number of rooms.
The windows faced the streets and the houses had
tiled bathrooms. It is especially noteworthy that
almost every house had its own wells, bathrooms,
courtyards, drains and kitchens.</li>
        <li>There was a good drainage system and drains were
made of mortar, lime and gypsum and covered 
with large brick slabs for easy cleaning which
shows a developed sense of health and sanitation.
Every house had its own soak-pit which collected
all the sediments and allowed only the water to
fl ow into the street drain. House drains emptied
themselves into the main drains which ran under
the main streets and below many lanes. There were
special trenches constructed outside every city for
the rubbish to be thrown in them.
</li>
<li>The towns were divided into two parts: Upper
part or Citadel and the Lower part. The Citadel
was an oblong artifi cial platform some 30-50 feet
high and about some 400-200 yards in area. It was
enclosed by a thick (13 m in Harappa) crenellated
mud brick wall. The Citadel comprised of public
buildings whereas the lower part comprised of
public dwellings.</li>
<li>In Mohenjodaro, a big public bath (Great Bath)
measuring 12 m by 7 m and 2.4 m deep has been
found. Steps led from either end to the surface,
with changing rooms alongside. The Great Bath
was probably used for ritual bathing.</li>
<li>Lamp posts at intervals indicate the existence of
street lighting.</li>
<li>There were special series constructed for the
travelers and a system of watch and word at night
also existed.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Political Organization/
Municipalities</i></strong></div>

<ol>
    <ul>
        <li>There is no clear idea of the political organization
of the Indus valley people. Perhaps they were more
concerned with commerce and they were possibly
ruled by a class of merchants.</li>
        <li>Also there was an organization like a municipal
corporation to look after the civic amenities of the
people.</li>
</ul>
</ol>


<div class="subheading"><strong><i>Economic Life
Agriculture</i></strong></div>

<ol>
    <ul>
        <li>The Indus people sowed seeds in the fl ood plains
in November, when the fl ood water receded, and
reaped their harvests of wheat and barley in April,
before the advent of the next fl ood which indicated
agriculture and knowledge of calendar reading.</li>
        <li>The people grew wheat, barley, rai, peas, sesamum,
mustard, rice (in Lothal), cotton, dates, melon, etc.
The Indus people were the fi rst to produce cotton
in the world.</li>
        <li>In Kalibangan, fi elds were ploughed with wooden
ploughs.</li>
        <li>Domestication of animals was done on a large scale.
Besides the cattle, sheep, pigs, camels, cats and dogs
were domesticated. Horses weren't in regular use
but elephant was. Remains of horse at Surkotda
and dogs with men in graves in Ropar have been
discovered.</li>
        <li>Produced suffi cient to feed themselves. There was
no exchange of foodgrains/export or import.</li>
        <li>Food grains were stored in granaries. Eg. In
Harappa and Mohenjodaro.</li>

    </ul>
</ol>

    

<div class="subheading"><strong><i>Trade and Commerce</i></strong></div>

<ol>
    <ul>
        <li>Well-knit external and internal trade. There was no
metallic money in circulation and trade was carried
through Barter System.</li>
        <li>Weights and measures of accuracy existed in
Harappan culture (found at Lothal). The weights
were made of limestone, steatite, etc. and were
generally cubical in shape.</li>
        <li>16 was the unit of measurement (16, 64, 160, 320).</li>
        <li>Flint tool-work, shell-work, bangle-making
(famous in Kalibangan), etc. were practiced. Raw
materials for these came from different sources:
gold from north Karnataka, silver and Lapis Lazuli
from Afghanistan and Iran, copper from Khetri and
Baluchistan, etc.</li>
        <li>Bead making factories existed in Chanu daro and
Lothal. They were items of export.</li>
        <li>A dockyard has been discovered at Lothal.
Rangpur, Somnath and Balakot functioned as
seaports. Sutkagendor and Sutkakoh functioned
as outlets.</li>
        <li>The inland transport was carried out by bullock
carts.</li>
<li>Every merchant or mercantile family probably
had a seal bearing an emblem often of a religious
character, and a name or brief description, on one
side. The standard Harappa seal was a square or
oblong plaque made of steatite stone. The primary
purpose of the seal was probably to mark the
ownership of property, but they may have also
served as amulets.</li>
<li>The Mesopotamian records from about 2350 B.C.
onwards refer to trade relations with Meluhha,
the ancient name of the Indus region. Harappan
seals and other material have been found at
Mesopotamia. There were also instances of trade
with Sumer, Babylonia, Egypt, etc.
Art and Craft</li>
<li>The Harappan culture belongs to the Bronze Age
and bronze was made by mixing tin and copper.
Tools were mostly made of copper and bronze. For
making bronze, copper was obtained from Khetri
in Rajasthan and from Baluchistan and tin from
Afghanistan.</li>
<li>The people of this culture were not acquainted with
iron at all.</li>
<li>The Indus Valley people had achieved a great skill
in drawing the fi gures of men, animals and various
other objects of nature and were fully conversant
with the art of craving with fi gures on ivory, soapstone, leather, metal and wood proving their artistic
acumen.</li>
<li>Cotton fabrics were quite common and woolens
were popular in winter.</li>
<li>One male fi gure or a statue shows that generally
two garments were worn and the female dress was
more or less like that of a male.</li>
<li>The Indus valley people were very fond of
ornaments (of gold, silver, ivory, copper, bronze
and precious stones) and dressing up. Ornaments
were worn by both men and women, rich or poor.
Women wore heavy bangles in profusion, large
necklaces, ear-rings, bracelets, fi gure-rings, girdles,
nose-studs and anklets. The Harappans were expert
bead makers.</li>
<li>They were fully conscious of the various fashions of
hair-dressing and wore beards of different styles.
Harappan Seals</li>
<li>Potter's wheel was in use. The Indus Valley
Pottery was red or black pottery and the people
indulged in dice games, their favorite pastime
being gambling.</li>
<li>The Harrapans most notable artistic achievement
was their seal engravings especially those of
animals. The red sandstone torso of a man is
particularly impressive for its realism. However
the most impressive of the fi gurines is the bronze
image of a dancing girl (identifi ed as a devdassi)
found at Mohenjodaro.</li>
<li>Maximum number of seals discovered is made of
steatite with the unicorn symbol being discovered
on the maximum number of seals.</li>
<li>For their children, the Harappans made cattle-toys
with moveable heads, model monkeys which could
slide down a string, little toy carts and whistles
shaped like birds all of terracotta.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Religious Life</i></strong></div>

<ol>
    <ul>
<li>The main object of worship was the Mother Goddess
or Shakti. But the upper classes preferred a God
- nude with two horns, much similar to Pashupati
Shiva. Represented on the seal is a fi gure with
three horned heads in a yogic posture, surrounded
by an elephant, a tiger, a rhinoceros and below
his throne is a buffalo. Near his feet are two deer.
Pashupatinath represented the male deity.</li>
<li>The elaborate bathing arrangement marking the
city of Mohenjodaro would suggest that religious
purifi cation by bath formed a feature of the Indus
Valley people.</li>
<li>Phallus (lingam) and yoni worship was also
prevalent.</li>
<li>Many trees (pepal), animals (bull), birds (dove,
pigeon) and stones were worshipped. Unicorns
were also worshipped. However no temple has
been found though idolatry was practiced.</li>
<li>At Kalibangan and Lothal fi re altars have been
found.</li>
<li>Although no definite proof is available with
regard to disposal of the dead, a broad view is that
there were probably three methods of disposing
the dead - complete burial (laid towards north),
burial after exposure of the body to birds and
beasts, and cremation followed by burial of the
ashes. The discovery of cinerary urns and jars,
goblets or vessels with ashes, bones or charcoal
may however suggest that during the fl ourishing
period of the Indus valley culture, the third method
was generally practiced. In Harappa, there is one
place where evidence of coffi n burial is there. The
people probably believed in ghosts and spirits as
amulets were worn.</li>
<li>Dead bodies were placed in the north-south
orientation.</li>
<li>It appears from excavations that the people of
this culture were well-versed with surgery. For
example, some evidences have come from both
Kalibangan and Lothal hinting at head surgery.
Otherwise, they used to take recourse to black
magic, amulets etc.</li>


    </ul>
</ol>




<div class="subheading"><strong><i>Script</i></strong></div>
<ol>
    <ul>
<li>The script is not alphabetical but pictographic
(about 600 undeciphered pictographs).</li>
<li>The script has not been deciphered so far, but
overlaps of letters show that it was written from
right to left in the fi rst line and left to right in the
second line. This style is called 'Boustrophedon'.
End/Decay</li>
<li>The Harappan culture lasted for around 1000
years.</li>
<li>The invasion of the Aryans, recurrent fl oods (7
fl oods), social breakup of Harappans, Earthquakes,
successive alteration in the course of the river
Indus and the subsequent drying up of the areas
in and around the major cities, etc. are listed as
possible causes for the decline of the Indus Valley
Civilization.</li>

    </ul>
</ol>


<div class="subheading"><strong><i>Important Sites of the Indus Valley
