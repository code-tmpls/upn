<li>
<div class="subheading"><strong><i>Harappa</i></strong></div>    

 Harappa is situated in Montogomery district of 
 Punjab (Pakistan). Excavations at the site have led
to the following specifi c findings:
<ol>
    <ul>
        <li>Two rows of six granaries with brick platforms; 12
granaries together had the same area as the Great
Granary at Mohenjodaro.</li>
        <li>Working fl oors, consisting of rows of circular brick
platforms lay to the south of granaries and were
meant for threshing grain.</li>
        <li>Evidence of coffi n burial and cemetery 'H' culture.
• The dead were buried in the southern portion of
the fortifi ed area, called cemetery R-37.</li>
        <li>Single room barracks just below the walls of the
citadels for the labourers and factory workers.</li>
        <li>It has been identifi ed with Hari-Yupiya which is
mentioned in the Rigveda.</li>
        <li>Evidence of direct trade and interaction with
Mesopotamia.</li>
        <li>Discovery of a red sandstone male torso and</li>
        <li>Stone symbols of female genitals.</li>
        <li>Almost 36% of the total seals excavated in the Indus
Civilization are excavated from Harappa alone.</li>
        <li>Other discoveries include Bronze image of an 'ekka'
(vehicle) and a seal with the representation of the
sign of 'swastika' on it.</li>
    </ul>
</ol>
</li>
<li>

<div class="subheading"><strong><i>Mohenjo-daro</i></strong></div>

Also known as the ‘Mound of the dead’, it lies
in Larkana district of Sind (Pakistan). Some of the
specifi c fi ndings during the excavations of Mohenjodaro include:

<ol>
    <ul>
        <li>A college, a multi-pillared assembly hall.
• The Great Bath</li>
        <li>A large granary (the largest building of Mohenjodaro)
which suggests extreme centralization as the ruling
authorities must have fi rst brought the agricultural
produce here and then redistributed it.</li>
        <li>A piece of woven cotton alongwith spindle whorls
and needles.</li>
        <li>Superfi cial evidence of a horse or an ass.
• A pot-stone fragment of Mesopotamian origin.
• Evidence of direct trade contact with
Mesopotamia.</li>
        <li>A bronze dancing girl.</li>
        <li>Evidence of violent death of some of the inhabitants
(discovery of human skeletons put together).</li>
<li>A seal representing Mother Goddess with a plant
growing from her womb, and a woman to be
sacrifi ced by a man with a knife in his hand.</li>
<li>A bearded man</li>
<li>A seal with a picture suggesting Pashupati
Mahadev.</li>
<li>This city is also an extreme example of conservatism,
as despite having been fl ooded almost nine times,
they never tried to shift to a safer place. Rather, they
came back to the original site whenever the water
table receded. Nor did they ever try to build strong
embankments to protect themselves from fl oods.</li>

    </ul>
</ol>
</li>
<li>
    
<div class="subheading"><strong><i>Alamgirpur</i></strong></div>
<ol>
    <ul>
        <li>The famous Harappan site is considered the eastern
boundary of the indus culture. Although the caves
found here resemble those at other Harappan sites,
other fi ndings suggest that Alamgirpur developed
during the late-Harappan culture.</li>
<li>The site is remarkable for providing the impression
