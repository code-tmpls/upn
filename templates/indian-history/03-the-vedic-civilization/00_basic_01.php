<h3>The Vedic Civilization</h3>

We know about the Aryans in India from the
various Vedic texts, especially the Rig Veda, which
is the earliest specimen of the Indo-European
language and the chief source of information on
the history of this period.

<ol>
    <ul>
        <li>Many historians have given various theories
regarding the original place of the Aryans.
However, the Central Asian Theory given by Max
Muller, is the most accepted one. It states that the
Aryans were semi-nomadic pastoral people around
the Caspian Sea in Central Asia.</li>
        <li>They entered India probably through the Khyber
Pass (in the Hindukush Mountains) around 1500
B.C.</li>
        <li>The holy book of Iran 'Zend Avesta' indicates entry
of Aryans to India via Iran.</li>
        <li>The early Aryans did not have to look routes
to Indian sub-continent; for the Harappans had
crossed the high passes of the Hindukush and
reached the middle course of the Amu Darya where
they had set up a trading post at Shortughai.</li>
        <li>In the Rigvedic period, the nobles were advised to
eat from the same vessel as the vis for success.</li>
        <li>Metal came to be known as Ayas and Iron as
Krishanayas (Black Metal).</li>
        <li>The Vedic texts may be divided into two broad
chronological strata: the Early Vedic (1500-1000
B.C.) when most of the hymns of the Rig Veda were
composed and the Later Vedic (1000-600 B.C.) when
the remaining three Vedas and their branches were
composed.</li>
        
    </ul>
</ol>

<div class="subheading"><strong><i>Early Vedic or Rigvedic Period
(1500-1000 B.C.)</i></strong></div>
<ol>
    <ul>
    <li>The Rig Veda is a collection of prayers offered to
Agni, Indra, Varuna and other gods by various
families of poets and sages.</li>
    <li>From Rigveda, we come to know that there were
33 gods that time who were divided into three
categories viz., heavenly gods, atmospheric god,
and earthly gods. Varuna, Surya, Aditi, Savitri
were heavenly gods. Indra, Rudra, Maruts etc. were
atmospheric gods. Agni, Soma, and Prithvi were
earthly gods.</li>
    <li>Four rivers of Afghanistan are clearly described
in the Rigveda. These are: Kubha, Krumu, Gomati
(Gomal), Suvastu (swat).</li>
    <li>It consists of ten Mandala or books of which Book
II to VII is the easiest portion. Book I and X seem
to have been the latest additions.</li>
    <li>In the Rigvedic period, the dead man's soul is said
to depart to the waters of the plants.</li>
    <li>Since the Aryans came through the mountains,
which were considered the dwelling places of
their gods, these are repeatedly mentioned in the
Rigveda. Meru, a mountain beyond the Himalayas,
is a happy divine abode in the Mahabharata and
the Puranas.</li>
    <li>The Rig Veda has many things in common with
the Avesta, which is the oldest text in the Iranian
language. The two texts use the same names for
several Gods and even for social classes.</li>
<li>The history of the later Vedic period is based mainly
on the Vedic texts which were compiled after
the age of the Rig Veda. These include the three
Vedas - Samveda, Yajurveda, Atharvaveda and
the Brahamanas, the Aranyakas, the Upanishads
and the Sutras.</li>
<li>The collection of the Vedic hymns or mantras were
known as Samhitas.</li>
<li>For purposes of singing, the prayers of the Rigveda
were set to tune and this modifi ed collection was
known as the Samveda Samhita.</li>
<li>The Yajurveda contains not only the hymns but
also the rituals which have to accompany their
recitation.</li>
<li>The Atharvaveda is completely different from the
other three Vedas. It contains charms and spells
to ward off evils and diseases. Its contents throw
light on the beliefs and practices of the non-Aryans.
Atharvaveda is the most valuable of the Vedas after
the Rig Veda for the history and sociology.</li>
<li>All the Vedic literature is together called the Shruti
and they include apart from the four Vedas, the
Brahamanas, the Aranyakas and the Upanishads.</li>
<li>The Brahamanas are a series of texts that followed
the Vedic samhitas. Each Veda has several
bhramanas attached to it. These are ritual texts.</li>
<li>Brahamanas attached to the Rigveda are Aitareya,
Kaushitaki (composed by Hotri priest). Brahamanas 
attached to Samveda are Jamini, Tandyamasha,
Panchavis, Chhandogya (composed by Udgatri
priest). Brahamanas attached to Yajurveda are
Satpatha Brahmana (composed by Adhvaryu
priest). Brahamanas attached to Atharvaveda are
Gopatha Brahamana.
</li>
<li>The Brahamanas throw light on the socio-political
life of the Aryans and form a sort of explanation of
their religion, especially sacrifi ce. They also contain
ritualistic formulae for the respective Vedas and
its priests.</li>
<li>The Aranyakas are forest books that are treaties
on mysticism and philosophy and are concluding
portion of the Brahamanas. They explain the
metaphysics and symbolism of sacrifi ce. They lay
emphasis not on sacrifi ce but on meditation. They
are infact opposed to sacrifi ce and many of the
ritualistic practices. Their stress is on moral virtues.
They form a bridge between the way of the works
(karma-marga, advocated by the Brahamanas) and
the way of knowledge (gyan-marga, advocated
by the Upanishads). Some important Aranyakas
are Aitreya Aranyaka, Kaushitaki Aryanka and
Taittiriya Aranyaka.</li>
<li>The Upanishads contain philosophical speculations.
They are generally called Vedanta which means the
end of the Vedas. One reason is that they came at
the end of the Vedic period or that they were taught
at the end of the Vedic instruction. These texts were
compiled around 600 B.C. and criticized the rituals
and laid stress on the values of right belief and
knowledge. They emphasized that the knowledge
of the self and the atma should be acquired and the
relation of atma with Brahma should be properly
understood.</li>
<li>The ten Upanishads are: Ishopanishat,
Kenopanishat, Kathopanishat, Parshnopanishat,
Mandukopanishat, Koushikopanishat ,
Thaittariyopanishat, Chandogyopanishat and
Brihadaranyaopanishat. These are commentaries
appended to the Aranyakas and deal mainly with
philosophy and religion.</li>
<li>The Puranas are 18 in number, of which the
Bhagawat Purana and Vishnu Purana are the most
important.</li>
    </ul>
</ol>

