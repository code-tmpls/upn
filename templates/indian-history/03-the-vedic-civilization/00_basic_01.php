means formula. Grihasutra contained social
rituals including sixteen sanskaras through
which individual had to pass from conception to
cremation.</li>
<li>Woman was now gradually losing her position of
importance in the religious and social sphere. The
king and the nobility had now begun to marry more
than one wife and the birth of a daughter was now
regarded as source of misery.</li>
<li>Higher education was, however, imparted to
women. The re-marriage of a widow was prevalent
