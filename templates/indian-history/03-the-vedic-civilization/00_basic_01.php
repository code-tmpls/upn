<li>Throughout the Vedic period, education was
imparted orally. Unlike the Harappans, the Aryans
do not seem to have a system of writing.</li>
<li>The Aryans loved music and played the flute,
lute and harp. There are references to singing and
dancing girls. People also delighted in gambling. 
They enjoyed chariot racing. Both men and women
wore ornaments.</li>
