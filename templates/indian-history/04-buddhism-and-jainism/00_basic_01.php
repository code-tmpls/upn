<h3>Buddhism and Jainism</h3>

<div class="subheading"><strong><i>Causes for the Growth of Buddhism
and Jainism</i></strong></div>
<ol>
    <ul>
        <li>The Vedic rituals were expensive and the sacrifi ces
prescribed were very complicated and had lost
their meaning.</li>
        <li>Supremacy of the Brahmins created unrest.</li>
        <li>All the religious texts were in Sanskrit, which was
not understandable to the masses.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Buddhism</i></strong></div>

Buddhism is the middle way of wisdom and compassion.
It stands for three pillars:
<ol>
    <ul>
        <li>Buddha - its founder</li>
        <li>Dhamma - his teachings</li>
        <li>Sangha - order of Buddhist monks and nuns.</li>
        
    </ul>
</ol>

<div class="subheading"><strong><i>Five Great Events of Buddha's Life and
their Symbols</i></strong></div>
<ol>
    <ul>
        <li>Birth - Lotus and bull</li>
        <li> Great renunciation - Horse</li>
        <li>Nirvana - Bodhi tree</li>
        <li>First Sermon - Dharmachakra or wheel</li>
        <li>Parinirvana or death - Stupa</li>

    </ul>
</ol>

<div class="subheading"><strong><i>The Buddha</i></strong></div>
<ol>
    <ul>
        <li>Also known as Sakyamuni (the Sage of the Sakyas),
Jina (the Victorious) or Tathagata (one who has
reached the truth).</li>
        <li>Born in 563 B.C. on the Vaishakha Poornima Day
at Lumbini (near Kapilavastu) in Nepal.</li>
        <li>His father Suddhodana was the Saka ruler.</li>
        <li>His mother (Mahamaya, of Kosala dynasty) died
after 7 days of his birth and he was brought up by
his stepmother Gautami.</li>
        <li>Buddha was married at 16 to Yashodhara and
enjoyed married life for 13 years and had a son
named Rahula.</li>
        <li>After seeing an old man, a sick man, a corpse and
an ascetic, he decided to become a wanderer.</li>
<li>Left his palace at 29 (with Channa, the charioteer
and his favourite horse, Kanthaka) in search of
truth (also called 'Mahabhinishkramana' or the
Great renunciation) and wandered for 6 years.</li>
<li>He fi rst meditated with Alara Kaiama. But he was
not convinced that man could obtain liberation
from sorrow by mental discipline and knowledge.
His next teacher was Udraka Ramputra. He then
joined forces with fi ve ascetics - Kondana, Vappa,
Bhadiya, Mahanama and Assagi, who were
practicing the most rigorous self-mortification
in the hope of wearing away their karma and
obtaining fi nal bliss.</li>
<li>For six years he tortured himself until he was
nothing but a walking skeleton. But after six years
he felt that his fasts and penance had been useless
so he abandoned these things and the fi ve disciples
also left him.</li>
<li>Attained Nirvana or Enlightenment at 35 at
Uruvela, Gaya in Magadha (Bihar) under the Pipal
tree.</li>
<li>Delivered the fi rst sermon at Sarnath at Deer Park
where his fi ve disciples had settled. His fi rst sermon
is called 'Dharmachakrapravartan' or 'Turning of
the Wheel of Law'.</li>
<li>Attained Mahaparinirvana at Kushinagar (identical
with village Kasia in Deoria district of U.P.) in
483 B.C. at the age of 80 in the Malla republic. His
death is said to have been caused by a meal of pork
(sukramad-dava), which he had taken with his lay
disciple Chunda at Pavapuri.</li>
<li>His last words were: “All composite things decay.
Strive diligently!” This was his “fi nal blowing out”
(Parinirvana). His body was cremated and his ashes
were divided among the representatives of various
tribal societies and King Ajatshatru of Magadha.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>The Dhamma</i></strong></div>

<div class="subheading"><strong><i>The Four Great Truths</i></strong></div>
<ol>
    <ul>
        <li>Dukkha - The world is full of sorrow and misery.</li>
        <li>The cause of all pain and misery is desire and
attachment.</li>
        <li>Pain and misery can be ended by killing or
controlling desire.</li>
        <li>Desire can be controlled by following the Eight Fold
Path.</li>
        
    </ul>
</ol>


<div class="subheading"><strong><i>The Eight Fold Path</i></strong></div>
The central theme of Buddha's teachings is the
eight-fold path (Astangamarga) prescribed by him
which consist of:
<ol>
    <ul>
        <li>Wisdom - Right Faith, Right Thought,</li>
        <li>Moral Discipline - Right Action, Right Livelihood,
Right Speech,</li>
        <li> Mental Discipline - Right Effort, Right Remembrance and Right Concentration.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Belief in Nirvana</i></strong></div>

<ol>
    <ul>
        <li>When desire ceases, rebirth ceases and nirvana is
attained i.e. freedom from the cycle of birth and
death by following the eight-fold path.</li>
        <li>According to Buddha the soul is a myth.</li>
      
    </ul>
</ol>
<div class="subheading"><strong><i>Belief in Ahimsa</i></strong></div>
One should not cause injury to any living being, animal
or man.

<div class="subheading"><strong><i>Law of Karma</i></strong></div>
Man reaps the fruits of his past deeds.

<div class="subheading"><strong><i>The Sangha</i></strong></div>

<ol>
    <ul>
        <li>Consists of monks (Bhikshus and Shramanas) and
nuns.</li>
        <li>Bhikshus acted as torch bearer of the Dhamma.</li>
        <li>Apart from Sangha, the worshippers were called
Upasakas.</li>

    </ul>
</ol>



<div class="subheading"><strong><i>Buddhist Councils</i></strong></div>


The monks gathered four times after the death of
the Buddha and the effect of these events had their
effect on Buddhism.
<ol>
    <ul>
        <li>First Council – At Rajgriha, in 483 B.C. under
the Chairmanship of Mehakassaapa (king was
Ajatshatru). Divided the teachings of Buddha into
two Pitakas – Vinaya Pitaka and Sutta Pitaka. Upali
recited the Vinaya Pitaka and Ananda recited the
Sutta Pitaka.</li>
        <li>Second Council – At Vaishali, in 383 B.C. under
Sabakami (king was Kalasoka of Shishunaga
Dynasty). Followers were divided into
Sthavirmadins and Mahasanghikas.</li>
        <li>Third Council – At Pataliputra, in 250 B.C. under
Mogaliputta Tissa (king was Ashoka). In this the
third part of the Tripitaka was coded in the Pali
language.</li>
        <li>Fourth Council – At Kashmir (Kundalvan) in
72 A.D. under Vasumitra (king was Kanishka).
Vice Chairman was Ashwagosha. Divided into
Mahayana and Hinayana sects.</li>
        <li>In Mahayana, idol worship is there. It became
popular in China, Japan, Korea, Afghanistan,
Turkey and other South East countries.</li>
        <li>Hinayana became popular in Magadha and Sri
Lanka. It believed in individual salvation and not
in idol worship.</li>
        <li>Apart from these two there is third vehicle called
Vajrayana, which appeared in the 8th century and
grew rapidly in Bihar and Bengal. They did not
treat meat, fi sh, wine, etc. as a taboo in the dietary
habits and freely consumed them.</li>
        <li>Fifth Council - In Mandalay, Burma (now
Myanmar) in 1871 and was presided over by
Theravada monks in the reign of King Mindon.</li>
        <li>Sixth Council - In Kaba Aye in Yangoon in 1954,
was sponsored by the Burmese Government.
Buddhism after Buddha</li>
        <li>Of all the religious remains of between 200 B.C.
and 200 A.D. so far discovered in India, those
of Buddhism far outnumber those of any other
religion viz., Hinduism, Jainism etc together.</li>
        <li>Hieun Tsang (the Chinese traveler), in the 7th
century A.D. found that the Lesser Vehicle or
Hinayanism is almost extinct in most of India, and
only fl ourishing in a few parts of the west.</li>
        <li>From Nalanda , the missionary monk
Padmasambhava went forth to convert Tibet to
Buddhism in the 8th century A.D.</li>
        <li>In the 6th century AD, the Huna King Mihirkula
destroyed monasteries and killed monks.</li>
        <li>A fanatical Shaivite king of Bengal, Sashanka, in the
course of an attack on Kannauj in the beginning of
the 7th century A.D., almost destroyed the Tree of
Wisdom at Gaya.</li>
        <li>As late as the 5th century A.D., written scriptures
were rare and the pilgrim Fa-hien was hard put to
fi nd a copy of the Vinay Pitaka.</li>

    </ul>
</ol>



<div class="subheading"><strong><i>Buddhist Literature</i></strong></div>
Buddhist literature in Pali language is commonly
referred to as Tripitakas i.e. 'Threefold Basket'.
<ol>
    <ul>
        <li>Vinaya Pitaka are the rules of discipline in Buddhist
monasteries.</li>
        <li>Sutta Pitaka is the largest and contains collection
of Buddha's sermons.</li>
        <li>Abhinandan Pitaka is the explanation of the
philosophical principles of the Buddhist religion.</li>
<li>Mahayana and Deepvamsa are other Buddhist
texts. They provide information about the then
Sri Lanka.</li>
<li>Jatakas are the fables about the different births of
Buddha.</li>
<li>The fundamentals of Buddhist teachings are
contained in the “Dhammacakka-Pavattana Sutta
(Sermon of the Turning of the Wheel of Law).
Buddha fi rst taught this to his fi rst disciples at
Benaras. This contains the Four noble truths and
the Noble eight-fold path, which are accepted as
basic categories by all Buddhist sects.</li>
<li>Among the chief Mahayana texts is the Lalitvistara,
a fl owery narrative of the life of Buddha. This test
was utilized by Sir Edwin Arnold for The Light
of Asia, a lengthy poem on the life of Buddha,
which enjoyed much popularity at the end of the
last century.</li>
    </ul>
</ol>



<div class="subheading"><strong><i>Causes of Decline of Buddhism</i></strong></div>

<ol>
    <ul>
        <li>It succumbed to the Brahmannical rituals and
ceremonies such as idol worship, etc. which
Buddhism had earlier denounced.</li>
        <li>Revival of reformed Hinduism with the preaching
of Shankaracharya from ninth century onwards.</li>
        <li>Use of Sanskrit, the language of intellectuals in place
of Pali, the language of the common people.</li>
        <li>Deterioration in the moral standards among the
monks living in the Buddhist monasteries.</li>
        <li> Attacks of Huna king Mihirkula in the sixth century
and the Turkish invaders in the 12th century A.D.
which continued till the 13th century A.D.</li>
    </ul>
</ol>



<div class="subheading"><strong><i>Jainism</i></strong></div>

<ol>
    <ul>
        <li>Founded by Rishabhanath, the fi rst Tirthankara.</li>
        <li>There were 24 Tirthankara (Prophets or Gurus)
and all of them were Kshatriyas. Rishabhanath's
reference is also there in the Rigveda. But there is
no historical basis for the fi rst 22 Tirthankaras. Only
the last two are historical personalities.</li>
        <li>The 23rd Tirthankara Parshwanath (symbol: snake)
was the son of King Ashvasena of Benaras. His
main teachings were: Non-injury, Non-lying, Nonstealing, Non-possession.</li>
        <li>The 24th and the last Tirthankara was Vardhman
Mahavira (symbol: lion). He added celibacy to his
main teachings.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Vardhman Mahavira</i></strong></div>

<ol>
    <ul>
        <li>He was born in Kundagram (district Muzaffarpur,
Bihar) in 599 B.C.</li>
        <li>His father Siddhartha was the head of Jnatrika clan.
His mother was Trishla, sister of Lichchhavi Prince
Chetak of Vaishali.</li>
        <li>Mahavira was related to Bimbisara.</li>
        <li>Married to Yashoda and had a daughter named
Priyadarsena, whose husband Jamali became his
fi rst discipline.</li>
        <li>At 30, after the death of his parents he became an
ascetic.</li>
        <li>In the 13th year of his asceticism, outside the town
of Jrimbhikgrama, he attained supreme knowledge
(Kaivalya).</li>
        <li> From now on he was called Jaina or Jitendriya and
Mahavira and his followers were named Jains. He
also got the title of Arihant i.e. worthy.</li>
        <li>At the age of 72, he attained death at Pava, near
Patna in 527 B.C. Mahavira survived the death of
his chief rival Gosala and probably also that of the
Buddha, and died of self-starvation at the age of 72
in the little town of Pava, near the Magadhan capital
Rajagriha. It is said that at the time of Mahavir's
death at Pavapuri, the kings of the two clans viz.,
the Mallas and the Lichchhavis, celebrated the lamp
festival in his honour.</li>
        <li>After the death of Mahavira, during the reign of
King Chandragupta, a severe famine led to the great
exodus of Jain monks from the Ganga valley to the
Deccan, where they established important centres
of their faith. This migration led to a great schism
in Jainism. Bhadrabahu who led the emigrants
insisted on the retention of the rule of nudity which
Mahavira had established. Sthulabhadra, the leader
of the monks who remained in the north allowed
his followers to wear white garments, owing to the
hardships and confusion of the famine. Hence arose
two sects of the Jains: the Digambaras (sky-clad i.e.
naked) and the Svetanbaras (white-clad).</li>
        <li>Our knowledge of Jainism after Mahavira is
meager. There were eleven ‘Gandharas’ but only
one of them named Sudharman survived the
master and became the fi rst pontiff. He died 20
years after Mahavira.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Teachings of Mahavira</i></strong></div>

<ol>
    <ul>
        <li>Rejected the authority of the Vedas and do not
attach any importance to the performance of the
sacrifices.</li>
        <li>He believed that every object even the smallest
particle possesses a soul and is endowed with
consciousness. That is why they observe strict nonviolence.</li>
        <li>The Jains reject the concept of the Universal soul
or a Supreme power as the creator or sustainer of
the Universe.</li>
        <li>Jainism does not deny the existence of Gods but
refuses to give Gods any important part in the
universal scheme. Gods are placed lower than the
Jina.</li>
        <li>Universal brotherhood (non-belief in the caste
system).</li>
        <li>In Jainism, three Ratnas (Triratnas) are given and
they are called the way to Nirvana. They are Right
Faith, Right Knowledge and Right Conduct.
</li>

    </ul>
</ol>


<div class="subheading"><strong><i>Jain Councils</i></strong></div>

<ol>
    <ul>
         <li>According to a Jain tradition, an oral sacred
literature had been passed down from the
 days of Mahavira, but Bhadrabahu was the last
person to know it perfectly. On Bhadrabahu's
death, Sthulabhadra called a Great Council at
Pataliputra which was the First Jain Council held
in the beginning of third century B.C. It resulted in
the compilation of 12 Angas to replace the former
14 Purvas. The Digambaras rejected this canon and
declared that the original one was lost. Thus, there
was a great urgency to devise new scriptures.</li>
         <li>The Second Council was held at Vallabhi (Gujarat)
in the 5th century A.D. under the leadership of
Devridhigani. It resulted in fi nal compilation of 12
Angas and 12 Upangas.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Other Important Facts</i></strong></div>

<ol>
    <ul>
        <li>Jainism reached the highest point in Chandragupta
Maurya's time. In Kalinga it was greatly patronized
by Kharavela in the fi rst century A.D.</li>
        <li>Various factors were responsible for the decline of
Jainism in India. They took the concept of Ahimsa
too far. They advised that one should not take
medicine when one fell sick because the medicine
killed germs. They believed that there was life in
trees and vegetables and so refrained from harming
them. Such practices could not become popular
with the common man.</li>
        <li>Lack of patronage from the later kings.
• Jain literature is in Ardh-Magadhi and Prakrit
dialects.</li>
        <li>Due to the infl uence of Jainism, many regional
languages emerged out, like Sauraseni, out of
which grew the Marathi, Gujarati, Rajasthani and
Kannada languages.</li>

    </ul>
</ol>


