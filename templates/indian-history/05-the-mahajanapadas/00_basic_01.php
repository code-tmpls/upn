<h3>The Mahajanapadas</h3>

<ol>
    <ul>
        <li>From the 16th century onwards, the widespread use
of Iron in eastern U.P. and western Bihar created
conditions for the formation of large territorial
states.</li>
<li>The new agricultural tools and implements enabled
the peasants to produce more and the extra produce
was collected by princes to meet military and
administrative needs.</li>
<li>With this the 'Janapadas' started giving way
to 'Mahajanapadas' and the land between the
Himalayas and the Narmada was divided into
16 Mahajanapadas which are mentioned in the
Buddhist literature 'Anguttar Nikaya'.</li>
<li>These are Kamboj, Gandhara, Kuru, Panchal,
Chedi, Avanti, Matsya, Sursena, Koshla, Vatsa,
Malla, Vajjis, Anga, Magadha, Kashi, Asmaka.
Of these, Magadha, Kosala or Avadh, Vatsa and
Avanti were more important.</li>
<li>Some of these were ruled by hereditary monarch
but others were republican or oligarchial states,
ruled either by representative of the people as a
whole or by nobility.</li>
<li>Of the non-monarchial clans, the most important
was the Vajjis confederacy of eight clans, the most
powerful of which were the Lichchavis ruling from
their capital at Vaishali.</li>
<li>There were matrimonial relations between the
rulers of Magadha, Kosala, Vatsa and Avanti, but
they did not prevent them from fi ghting with one
another for supremacy.</li>
<li>Ultimately the Kingdom of Magadha emerged as
the most powerful and succeeded in founding in
empire.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Magadha Empire (6-4 B.C.)</i></strong></div>

<ol>
    <ul>
        <li>Magadha embraced the former districts of Patna,
Gaya and parts of Shahabad and grew to be the
leading state of the time.</li>
        <li>It's success was attributed to its geographical
position i.e. proximity to rich iron deposits which
yielded effective weaponry and the benefi ts of the
fertile Ganga soil.</li>
        <li>Also elephants were fi rst used in war.</li>
        <li>Archaeologically 6th century B.C. marks the
beginning of the NBPW (Northern Black Polished
Ware); a glossy, shining type of pottery. This
marked the beginning of the Second Urbanization
in India.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Haryanka Dynasty</i></strong></div>  

The Haryanka Dynasty was originally founded
in 566 B.C. by the grandfather of Bimbisara, but the
actual foundation in the true sense is credited to
Bimbisara.





<div class="subheading"><strong><i>Bimbisara (544 B.C.-492 B.C.)</i></strong></div>  
<ol>
    <ul>
        <li>A contemporary of Buddha, he conquered Anga
(east Bihar) to gain control over the trade routes
with the southern states.</li>
        <li>His capital was Rajgir (Girivaraja) and he
strengthened his position by matrimonial alliances
with the ruling families of Kosala, Vaishali and
Modra (3 wives).</li>
        <li>The earliest capital of Magadha was at Rajgir, which
was called 'Giriraja' at that time. His capital was
surrounded by 5 hills, the openings of which were
closed by stone walls on all sides. This made Rajgir
impregnable.</li>

    </ul>
</ol>



