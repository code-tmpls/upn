<div class="subheading"><strong><i>Foreign Invasions and Persian
Conquests of India</i></strong></div>  

<ol>
    <ul>
        <li>During 6th century B.C. northwestern India
had been isolated from the developments in
the rest of India and closer connections with
Persian Civilization, being politically a part of the
Achaemenied Empire.</li>
        <li>A little before 530 B.C., Cyrus (the Achaemenid
emperor of Persia) crossed the Hindukush
mountains and received tributes from the tribes of
Kamboja, Gandhara, and the trans-Indus region.</li>
        <li>During the lifetime of Buddha, the powerful
Achaemenian emperor of Persia Darius I (522-486
B.C.) captured a portion of Punjab and Sindh. The
Behistun Inscription of 519 B.C. states that Gadara
(Gandhara) was a province which sent teak.</li>
        <li>Herodotus, the famous Greek historian, considered
as father of history, mentions Gandhara as the
20th satrapy or province, counted amongst the
most populous and wealthy in the Achaemmenid
Empire.</li>
        <li>The Indian provinces provided mercenaries for the
Persian armies fi ghting against the Greeks in the
years 486-465 B.C.</li>
<li>Alexander came to India in order to reach the
easternmost parts of Darius's empire, to the
'problem of ocean', the limits of each were a puzzle
to Greek geographers and to add this fabulous
country to his list of conquests.</li>
<li>Herodotus mentions about a naval expedition
dispatched by Darius under Skylax (517 B.C.) to
explore the Indus. Herodotus says: “the population
of the Indians is by far the greatest of all the
people that we know; and they paid a tribute
proportionately larger than the rest”.</li>
<li>Xerxes utilized his Indian provinces to secure an
Indian contingent to fi ght his battles in Greece.</li>
<li>There were 'Gandharians' as well as 'Indians' in his
contingent. The former bore bows of reed and short
spears for fi ght at close quarters, while the latter,
clad in cotton also bore similar bows and arrows
tipped with iron. These Indian troops were the fi rst
Indians to fi ght in Europe.</li>
<li>The Persian Empire set the model for Mauryas as
far as Imperial pretensions are concerned. The
prevalence in the North-West of Kharosthi script
which is only a localized adaptation of Aramaic
and written from the right was perhaps a vestige
of Persian rule.</li>

    </ul>
</ol>


<div class="subheading"><strong><i>Alexander's Invasion</i></strong></div>  

<ol>
    <ul>
<li>Alexander was the son of Philip of Macedonia
(Greece) who invaded India in 326 B.C. At that time
North-west India was split up into a number of small
independent states like Taxila, Punjab (kingdom of
Porus), Ghandara, etc. Except Porus who fought
the famous Battle of Hydaspas (on the banks of
Jhelum) with Alexander, all other kings submitted
meekly. Ambhi (Omphis), the king of Taxila,
submitted to Alexander in about the same time.
Later, impressed by Porus, Alexander reinstated
him in power. Then Alexander captured the tribal
republic of Glauganikai (Glachukayanaka) with
its 37 towns.</li>
<li>When Alexander reached Beas his army refused
to go further, forcing him to retreat. To mark the
farthest point of his advance, he erected 12 huge
stone altars on the northern banks of the Beas. 
Remaining in India for 19 months, Alexander
finally died in Babylon in 323 B.C.
</li>
<li>Alexanders invasion opened up four distinct lines
of communication (3 by land and 1 by sea) thus
exposing India to Europe.</li>
<li>Due to this cultural contact, a cosmopolitan
school of art came up in Gandhara which was
characterized by sensuous art and continued till
the Gupta Age.</li>
<li>It also paved the way for the unifi cation of north
India under Chandragupta Maurya by weakening
the small states.</li>
<li>But the immediate effect of this expedition was
the destruction of tribes, which had survived from
earlier times.</li>
<li>The earliest instance of 'Jauhar' in recorded history
occurred when Alexander encountered the Sibis
(a rude tribe clad in skins) and the Agalassoi
(Agrasrenis). The latter suffered terribly for daring
to resist the invader. The people of one town to the
number of 20,000 men, women, and children set
their dwellings ablaze and threw themselves into
the flames.</li>
    </ul>
</ol>






