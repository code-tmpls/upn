<h3>The Mauryans</h3>


<div class="subheading"><strong><i>Rise of the Mauryans</i></strong></div>  

<ol>
    <ul>
        <li>Closeness to the source of natural resources like
iron ores enabled the Magadhan princes to equip
themselves with weapons.</li>
        <li>Agricultural tools of iron, which increased
production and added to royal taxation alongwith
the alluvial soil of Gangatic plains and suffi cient
rainfall which were very conducive for agricultural
practices.</li>
        <li> Rise of towns and use of metallic money boosted
trade and commerce, which increased royal
revenue.</li>
        <li>Use of elephants on a large scale in its war supplied
by the eastern part of the country added to the
military power.</li>
        <li>The unorthodox character of the Magadhan Society
as a result of racial admixture.</li>
        <li></li>
        <li></li>
    </ul>
</ol>


<div class="subheading"><strong><i>Historical Sources</i></strong></div>  

<ol>
    <ul>
        <li>The history of Mauryas, unlike that of the earlier
ruling houses, is rendered reliable by a variety
of evidences drawn from such sources as the
Buddhist and the Jain traditions; the Kalpasuta of
Jains and the Jatakas, Dighanikaya, Dipavamsa and
Mahavamsa of Buddhists respectively.</li>
        <li>The Arthashastra of Kautilya, the Greek accounts,
the first decipherable inscription of Ashoka
(deciphered by James Princip in 1837) and the
archaeological remains.</li>
        <li>The Puranas and Mudrarakshasa of Vishakhadutta
though belong to a later date, throw light on the
history of the Mauryans alongwith Patanjali's
Mahabhashya.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Chandragupta Maurya</i></strong></div>  

<ol>
    <ul>
        <li>Chandragupta Maurya was the founder of
Mauryan dynasty. Also known as Sandrocottus
(kind towards friends) by Greek scholars.</li>
        <li>Brahmanical sources (Mudrarakshasa) say that the
name Maurya was derived from Mura; a Shudra
woman in the court of Nandas, and Chandragupta
was son or grandson of the woman.</li>
        <li>Vishnu Purana also mentions him of low origin
i.e. a Shudra. But the Buddhist and Jain sources
ascribed him a Kshatriya status.</li>
        <li>His early career is shrouded in mystery. According
to Justin, a Greek writer, he overthrew Nandas
between 325-322 B.C. According to Plutarch, he met
Alexander in Punjab and implicitly invited him to
attack Nandas but offended him by his boldness
of speech.</li>
        <li>Chandragupta occupied Magadhan throne in 321
B.C. with the help of Chanakya (Kautilya).</li>
        <li>He had allied with a Himalayan chief Parvartaka.</li>
        <li>He defeated Seleucus Nicator, then Alexander's
governor in 305 B.C. who ceded to Chandragupta
the three rich provinces of Kabul, Kandahar and
Heart in return for 500 elephants.</li>
        <li>Seleucus probably gave one of his daughters
to Chandragupta and sent his ambassador,
Megasthenes to the Mauryan Court, who wrote
an account (Indica) not only of the administration
of the city of Pataliputra but also of the entire
Mauryan Empire.</li>
        <li>The Greek writer Justin calls Chandragupta's army
as a “Dacoits gang”.</li>
        <li>According to the Jain work Parishista-parvan,
Chandragupta converted to Jainism in the end years
of his life and went to south near Sravanbelgola
with his Guru Bhadrabahu. It is said that he starved
himself to death here.</li>
        <li>According to the same text, Chanakya made
Chandragupta enter into an alliance with
Paravartaka (king of Himvatakuta) and the allied
armies besieged Pataliputra.</li>
        <li>Vishakhadatta wrote a drama Mudrarakshasa
(describing Chandragupta's enemy) and Debi
Chandraguptam in 6th century A.D.</li>        
    </ul>
</ol>



<div class="subheading"><strong><i>Bindusara</i></strong></div>  

<ol>
    <ul>
        <li>Bindusara was the son of Chandragupta and was
known as Amitraghata (slayer of foes), besides
the master of the land between the two seas
- Arabian Sea and the Bay of Bengal. Succeeded
Chandragupta in 297 B.C.</li>
        <li>He continued friendly links with Syrian king
Antiochus I and is stated to have requested him for
a present of fi gs and wine together with a sophist
to which Antiochus sent fi gs and wine but replied
that Greek philosophers were not for export.</li>
        <li> He also received a Greek ambassador Daimachos
from Antiochus I. Ptolemy II Philadelphus of Egypt also sent an envoy
Dionysius to Bindusara's court.</li>
        <li>History credits him with the suppression of a revolt
and further for the redressal of grievances against
the misrule of wicked bureaucrats (dustanatyas).</li>
        <li>According to Tibetan Lama Taranath and
Jain legends, Chanakya was the minister of
Bindusara.</li>
        <li>There was a council of ministers of 500 members
in the court of Bindusara, which was headed by
Khallatak.</li>
<li>Bindusara did not make any territorial conquest
and towards the time of his death he joined the
Ajivika sect.</li>

    </ul>
</ol>



<div class="subheading"><strong><i>Ashoka</i></strong></div>  

<ol>
    <ul>
        <li>Ashoka (273-232 B.C.) had served as governor of
Taxila and Ujjain previously.</li>
        <li>Ashoka is called 'Buddhashakya and Ashok'
in Maski edict and 'Dharmasoka' in Sarnath
inscription. He was also known as 'Devampriya' i.e.
beloved of the Gods and 'Piyadassi' i.e. of pleasing
appearance.</li> 
        <li>His empire covered the whole territory from
Hindukush to Bengal and extended over to
Afghanistan, Baluchistan and the whole of India
with the exception of a small area in the farthest
south comprising of Kerela. Kashmir and Valleys
of Nepal were also included and was the first
empire to do so. Assam was not included in his
dominion.</li>
        <li>The Kalinga War fought in 261 B.C. and mentioned
in XIII Rock Edict changed his attitude towards life
and he became a Buddhist.</li>
        <li>In the 14th year of his reign he started the
institution of Dhamma Mahamatras (the offi cers
of righteousness) to spread the message of
Dhamma.</li>
        <li> During his reign the policy of Bherighosha (physical
conquest) was replaced by that of Dhammaghosha
(cultural conquest).</li>
        <li>In course of his second tour in the 21st year of his
reign he visited Lumbini, the birth place of Buddha
and exempted the village from Bali (tribute) and
the Bhaga (the royal share of the produce) which
were reduced to one eighth.</li>
        <li>He organized a network of missionaries to preach
the doctrine of Buddhism both in his kingdom
and beyond. He sent them to Ceylon, Burma (sent
his son Mahindra and daughter Sangamitra to
Ceylon) and other South-east Asian regions notably
Thailand.</li>
        <li>Ashoka's Hellenistic contemporaries were
Antiochus II of Syria, Ptolemy II Philadelphus of
Egypt, Magas of Cyrene, Antigonus Gonatas of
Macedonia and Alexander of Epirus. These are
mentioned in his thirteenth Rock Edict.</li>
        <li>Ashoka was the fi rst Indian king to speak directly
to the people through his inscriptions, which seem
to be the earliest specimens of Prakrit language in
India.</li>
        <li>They are mostly engraved on rocks and found not
only in Indian subcontinent but also in Afghanistan.
These inscriptions communicate royal orders. These
inscriptions were composed in Prakrit and were
written in Brahmi script throughout the greater
part of the empire. But in the north-western part
they appear in Aramaic and Kharoshthi script.</li>
        <li>In his inscriptions following languages have been
used: Brahmi, Kharoshthi, Aramaic, Greek, etc.</li>
        <li>The Ashokan inscriptions were generally placed
on ancient highways and threw light on the career
of Ashok's policies and the extent of his empire.</li>
        <li>Tarai pillars show Ashoka's respect for
Buddhism.</li>
        <li>Ashoka in his fi fth rock edict mentions that he
had several brothers and sisters. Two of these
brothers are named in Divyavadana as Susima
and Vuigatasoka, whom the Sinhalese chronicles,
name as Sumana and Tishya. The former was
step-brother of Ashoka. Ashoka's mother was
Subhadrangi.</li>
        <li>Ashoka does not call himself by his personal name
Ashoka in any of his inscriptions except two: these
are Maski and Gujarra inscriptions.</li>
        <li>Ashoka died in 232 B.C. and with him departed the
glory of Mauryan Empire.</li>
    </ul>
</ol>




<div class="subheading"><strong><i>Ashokan Edicts</i></strong></div>  

<ol>
    <ul>
        <li>
        
        <div class="subheading"><strong><i>Major Rock Edicts</i></strong></div>  
        
        These are related to administration and ethics.
<b>1st Rock Edict:</b> It puts prohibition on animal
sacrifi ces in festive gatherings. Interestingly, only
three animals (2 peacocks and 1 deer) could be used
for the royal kitchen as well instead of hundreds of
them used earlier.
<br>
<b>2nd Rock Edict:</b> It mentions about the medical
missions sent everywhere for both men and animals
by Ashoka. It mentions Chola, Chera, Pandaya and
Satyaputra and has also a list of herbs and trees to
be planted in different areas.
<br>
<b>3rd Rock Edict:</b> In the 12th year of Ashoka's
inauguration the edict enjoins a quenquennial humiliation.
<b>4th Rock Edict:</b> In the 12th year of Ashoka's reign 
compares the past condition of the kingdom with
that of the present. 
<br>
<b>5th Rock Edict:</b> It, for the fi rst time, mentions about
the appointment of the Dhamma-mahamatras to look
after propagation of Dhamma. They were appointed
in the 13th year of Ashoka's consecration.
<br>
<b>6th Rock Edict:</b> It shows his concern for the people's grievances for round 
the clock consultations or
any type of appeal and that the mahamattas should
communicate to him all the matters concerning public
business even if he is in his harem. It announces the
appointment of pativedakas, custodies morum and
criminal magistrates.
<br>
<b>7th Rock Edict:</b> It contains the kings desire to
obliterate diversities of religious opinions and tells
us that Ashoka, after ten years since his consecration, visited Bodhi tree, ended all pleasure tours and
instead, concentrated on the Dhamma tours.
<br>
<b>8th Rock Edict:</b> It contrasts the carnal enjoyments
of former rajas with the harmless enjoyments of the
king - visits to holy places, almsgiving, respect to
elders, etc.
<br>
<b>9th Rock Edict:</b> It shows the uselessness of all
other ceremonies except the Dhamma as it includes
ethical concepts within its fold. It basically continues
the Dhamma discourse.
<br>
<b>10th Rock Edict:</b> In this edict, Ashoka shows the
lack of any worldly desire except the desire to propagate Dhamma 
and to see people following it.
<br>
<b>11th Rock Edict:</b> It suggests to people that the gift
of Dhamma is the best gift or the chiefest of charitable
donations as it brings gain in this world and merit
in the next. It is at Dhauli and Girnar.
<br>
<b>12th Rock Edict:</b> It expresses Ashoka's concern
for the well-being of all other sects. In this he prefers to advance the essence of all the doctrines. He
also requests all the offi cers to internalize this basic
philosophy behind propagation of Dhamma.
<br>
<b>13th Rock Edict:</b> In this edict, Ashoka shows his
remorse for the devastation caused by his Kalinga
War. The killing of so many families made Ashoka
take resort to cultural conquest (Dhammavijaya)
rather than even think in the future about any war
and aggrandizement. It is incomplete.
<br>
<b>14th Rock Edict:</b> It states that this inscription of
Dhamma was engraved at the command of the beloved
of the Gods, the king Piyadassi. It exists in abridged,
medium length and extended versions for each classes
has not been engraved everywhere. It summarises
the preceeding and is complete in itself.
        </li>
        <li>
        <div class="subheading"><strong><i>Separate Edicts</i></strong></div>  
        
<b>First Separate Edict (Dhauli and Jaugada):</b> Addressed to offi cers of Tosali and 
Samapa. One royal officer will tour every fi ve years to see that men are
never imprisoned or tortured without good reason.
The prince of Ujjain shall send out a similar group
of offi cers, but at intervals not exceeding three years,
similarly at Taxila.
<b>Second Separate Edict:</b> Addressed to the prince
at Tosali and the offi cials at Samapa, it states that
the offi cers shall at all times attend to the conciliation of the people of the frontiers and to promoting
Dhamma among them.
        </li>
        <li>
        
        <div class="subheading"><strong><i>Minor Inscriptions</i></strong></div>  
        
<b>Queen's Edict:</b> On the Allahabad pillar, the gift
of the second queen, the mother of Tivara, Karuvaki
for dispensing charity or any other donation.
Barabar Cave Inscription:
<ol>
    <ul>
        <li>In 12th year the Banyan cave given to Ajivikas.</li>
        <li>In 12th year cave in Khalitika mountain given to
Ajivikas.</li>
        <li>The king Piyadassi, consecrated since nineteen
years.</li>    
</ul>

</ol>
<b>Kandhar Bilingual Rock Inscription:</b> Greek version - king refrains from eating meat and his hunters
and fi shermen have stopped hunting. Aramic version
- very few animals were killed by Ashoka. Fishing
prohibited.
<br>
<b>Bhabru Inscription:</b> The king of Magadha, Piyadassi shows deep respect for the faith in Buddha,
the Dhamma and the Sangha. This edict confi rms
Ashoka's conversion to Buddhism.
<br>
<b>Rummindei Pillar Inscription:</b> In 20th year Piyadassi visited Lumbini and here exempted people
from land tribute (udbalike) and fixed contribution
at 1/8 (atthabhagiya).
<br>
<b>Nigalisagar Pillar Inscription:</b> On 14th year the
stupa of Buddha Kanakamuni was enlarged to double
in size.
<br>
<b>Schism Edict:</b> At Kaushambi (Allahabad pillar),
Sanchi and Sarnath. All dissenting monks and nuns to
be expelled and made to wear robes and the laymen
and offi cials are to enforce this order on confession
(upostha) days addressed to offi cials of Kausambi
and Pataliputra.
        </li>
    </ul>
</ol>
 


<div class="subheading"><strong><i>Pillar Edicts</i></strong></div>  

<b>1st:</b> On 27th regional year. His principle is to
protect thorough Dhamma to administer according
to Dhamma, to please the people with Dhamma to
guard the empire with Dhamma.
<br>
<b>2nd:</b> Dhamma is good and what is Dhamma? It
is having few faults and many good deeds: mercy,
charity, truthfulness and purity.
<br>
<b>3rd:</b> One only notices one's good deeds, does not 
notice one's wicked deeds, one should notice this and
think. Cruelty, harshness, anger, pride and many are
indeed productive of sin.
<br>
<b>4th:</b> In the 26th year, appointment of Rajukas over
hundreds and thousands, with independent authority over judgement, there should be uniformity in
judicial procedure and punishment. Men who are
imprisoned or sentenced to death are to be given
three days respite.
<br>
<b>5th:</b> In the 26th year, prohibition of killing specific
animals and burning forest; cattle and horses are
not to be branded. Twenty fi ve releases of prisoners
have been made.
<br>
<b>6th:</b> Mention of major rock edicts, which have
been issued in 12th year, to honour all sects.
<br>
<b>7th:</b> Only in the Delhi-Topara pillar, Rajuka,
Ajivikas and Nirgrantha (Jainas) were mentioned in
this edict. Dhamma is better advanced by persuasion
than by legislation.


<div class="subheading"><strong><i>The Mauryan Empire after Ashoka</i></strong></div>  

<ol>
    <ul>
        <li>Vishnu Purana gives the names of his seven
successors but with no details, probably the empire
was divided into two eastern and western parts.</li>
    <li>The western being ruled by Kunal and later for
sometime by Samprati where Indo-Greeks began to
make early inroads, and until 180 B.C. had virtually
supplanted the later Mauryas.</li>
    <li>The eastern part being ruled by Brihadratha from
Pataliputra. He was the seventh king in succession
from Ashoka.</li>
    <li>He was killed by his commander in chief
Pushyamitra Sunga, who ascended the throne
in 187 B.C. The royal dynasty founded by him is
known as Sunga Dynasty.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Mauryan Administration</i></strong></div>  

<ol>
    <ul>
        <li>A vast and highly centralized bureaucratic rule
with the king as fountain head of all powers.
The king claimed no divine rule, rather it was
paternal despotism. Kautilya called the king
Dharmapravartaka or promulgator of social
order.</li>
    <li>The highest functionaries at the centre called tirthas
and were paid fabulously. They were Mantri,
Purohita, Senapati and Yuvaraja. Besides the
two chief offi cers at the Centre were Sannidhata
(treasurer) and Samaharta (tax collector).</li>
    <li>Kautilya again and again emphasized the
importance of Mantriparishad.</li>
    <li>Kautilya mentions 27 superintendents (adhyakshas)
mostly to regulate economic activities. The famous
were as follows.
<ol>
    <ul>
        <li><b>Sitadhyaksha:</b> Super-intendent of crown land.</li>
        <li><b>Panyadhyaksha:</b> Super-intendent of Commerce.</li>
        <li><b>Pautavadhyaksha:</b> Super-intendent of weight and
measures.</li>
        <li><b>Sulkadhyasha:</b> Superintendent of tolls.</li>
        <li><b>Samsthadhyaksha:</b> Super-intendent of market.</li>
        <li><b>Akaradhyaksha :</b> Super-intendent of mines.</li>
        <li><b>Rajuka :</b> Superintendent to look after Justice.</li>
    </ul>
</ol>
</li>
<li> Except the capital Pataliputra, the whole empire
was divided into four provinces controlled by a
viceroy either a prince or a member of the royal
family. Provinces were sub-divided into districts
and had three main officers.</li>
<li>Pradesika responsible for the overall administration
of the district. Rajuka was responsible for revenue
administration and later judicial particularly in
rural areas and was under Pradesika.</li>
<li>Sub-district consisted of a group of villages
numbering 5 to 10 and was administered by 'Gopa'
(accountant) and 'Sthanika' (tax collector). The
villages were administered by the village head man
who was responsible to the Gopas and Sthanikas.</li>
<li>The administration of capital Pataliputra has been
described by six boards consisting of fi ve members
each being entrusted with matters relating to
industrial arts, care of foreigners, registration
of births and deaths, regulation of weights and
measures, public sale of manufactured goods and
the last with collecting toll on the articles sold, this
being one tenth of the purchase price.</li>
<li>Mauryans had a big army and there is no evidence
of its reduction even by peace loving Ashoka.</li>
<li>According to Pliny, Chandragupta maintained
600,000 foot soldiers, 30,000 cavalry and 900
elephants.</li>
<li>According to Megasthenese, the army was
administered by six committees consisting of fi ve
members each taken from a board of 30 members.
The six committees or the wings of army were: the
army, the cavalry, the elephants, the chariots, the
navy and the transport.</li>
<li>Spies operated in the guise of sanyasis, wanderers,
beggars, etc. and were of two types 'Sanstha'
and 'Sanchari'. The former worked by remaining
stationed at a public place and later by moving
from place to place. These spies were integral
to the Mauryan administration. They collected
intelligence about foreign enemies and kept an eye
on numerous offi cers. The 'prativedikas' were the
special reporters of the king.</li>
<li>Land revenue was the main source of income of the
state. Peasants paid ¼ of the produce as Bhaga and
extra tax Bali tribute. According to Arthashastra, 
the land belonged to the king, irrigation tax was
also levied by the government.</li>
<li>Besides other taxes like Pindakara (assessed on
group of villages), 'Kara' (levied on fruits and
fl ower gardens), Hiranya (paid only in cash) were
also collected.</li>
<li>Industrial arts and crafts proliferated as a result
of swift communication through a network of
good and long roads and incentives given by the
government.</li>
<li>A striking social development was the employment
of slaves in agricultural operation on a large scale.</li>
<li>The sale of merchandise was supervised.</li>
<li>No banking system but usury prevailed. It seems
that the punch-marked silver coins, which carry the
symbols of peacock and hill and regent formed the
imperial currency of the Mauryas.</li>
<li>Megasthenes noticed the absence of slavery. But it
is contradicted by Indian sources.</li>
<li>Kautilya recommends the recruitment of Vaishyas
and Shudras in the army, but their actual enrolment
is extremely doubtfull.</li>
<li>In addition to the four regular castes, he refers not
less than fi ve mixed castes by the general name
Antyavasayin who lived beyond the pale of Aryan
society.</li>
<li>The position of Shudra improved somewhat for
hitherto agricultural laborers and domestic slaves.
They could now own land.</li>
<li>In the Mauryan period, stone culture emerged as
the principal medium of Indian arts.</li>
<li>Tamralipti was one of the most important maritime
trading centres during the Mauryan times. It was
situated on the Eastern coast.</li>
<li>The animals, which are carved on the Mauryan
pillars, are: Bull, Lion, Elephant.</li>
<li>According to Arthashastra, a man could be slave
either by birth, by voluntarily selling oneself, by
being captured in war or as a result of judicial
punishment. Megasthenese did not fi nd slaves in
India.</li>
<li>Puranas have called Kautilya as 'dvijarshabh' i.e.
superior brahmana. Chanakya spent last days
of his life doing meditation in the forests near
Magadha.</li>
<li>'Sishtas' were learned men during Mauryan times.</li>
<li>The trade links between India and Egypt were so
developed that Ptolemy had established a port
named Bernis on the Red sea. India exported turtle
skin, pearls, precious and semi-precious stones,
cotton and costly wood to Egypt.</li>
    </ul>
</ol>

