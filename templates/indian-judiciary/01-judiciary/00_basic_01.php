</ul>
</ol>

<div class="subheading"><strong><i>High Courts</i></strong></div>

In each state there is a High Court, which
exercises powers within the territorial jurisdiction
of the state concerned. A High Court consists of
a Chief Justice and such other judges as the
President may, from time to time, determine.
Since the number of the judges of the State High 
courts has not been fixed by the constitution, it
varies from court to court. Appointments of the
judges of the High Court shall be made by the
President by warrant under his hand and seal.
The President consults the Chief Justice of India
and the Governor of the state and the Chief Justice
of the High Court concerned in making the
appointment. In case, of the appointment of the
Chief Justice of the High Court, the President will
consult the Chief Justice of India and the Governor
of the state concerned.
  
<div class="subheading"><strong><i>Qualifications and Conditions of Service</i></strong></div>

A person shall not be qualified for appointment
as a judge of High court unless he is a citizen of
India and has either held for at least ten years a
judicial office in the territory of India or has for
at least ten years been an advocate of a High court
in any state. The salary allowance and other
amenities enjoyed by the judges of High Courts
cannot be modified to their disadvantage during
their tenure of office. However, the President is
authorised to reduce their salaries during the
proclamation of financial emergency. As the
salaries and other expenses of the judges and the
maintenance of state High Courts are charged to
the Consolidated Fund of the state, they are to
subject to the vote of the state legislature.
<br>
A judge shall hold office till he attains the age
of sixty two years. However, he may resign his
office by writing to the President. He can be
removed from his office by the President, in the
manner provided for the removal of a judge of
the Supreme Court. A judge can be removed from
his office before the expiry of his term on grounds
of proved misbehaviour or incapacity. However,
such an action can be taken by the President only
if both the House of Parliament pass a resolution
by a two thirds majority of the members present
in each House which should also be the majority
of the total membership of the House.

<div class="subheading"><strong><i>How a Judge of the Supreme Court or
High Courts can be removed?</i></strong></div>

A complex procedure for removal of judges is
usually laid down in the constitution. The framers
of Indian Constitution were conscious of the value
of an independent judiciary and made elaborate
provisions to secure the same.
Article 124 of the Constitution provides for the
establishment and Constitution of the Supreme
Court of India. The article lays down that the
Supreme Court shall consist of Chief Justice of
India and not more than such number of judges
as Parliament may by law prescribe. It further
provides that every judge shall be appointed by
the President by warrant under his hand and seal
after consultation with such of the judges of the
Supreme Court and of the High courts in the
States as the President may deem necessary for
the purpose and shall hold office until he attains
the age of 65 years. The Supreme Court judge may
resign his office by writing in his hand addressed
to the President. Clause (4) of article 124 provides
that a judge of Supreme court shall not be
removed from his office except by an order of the
President passed after an address by each House
of Parliament supported by a majority off total
membership of that House and by a majority of
not less than two-third of the Members of that
House present and voting presented to the
President in the same session for such removal on
the ground of proved misbehaviour or incapacity.
Provision to article 217(1) and article 218 indicate
that provisions of clauses (4) and (5) of article 124
equally apply for the removal of a judge of the
High Court.
Clause (5) of article 124 empowers Parliament
to regulate the procedure for the presentation of
an address and for the investigation and proof of
the misbehaviour or incapacity of a judge by law.
A suggestion in the Constituent Assembly that the
matter may first be investigated by a judicial
committee was rejected. Article 124 is an attempt
to make the judiciary totally independent of the
executive or legislature so that the judiciary can
adjudicate constitutional and legal questions
fearlessly and impartially.

<div class="subheading"><strong><i>Jurisdiction of High Court</i></strong></div>

The High Court of a state enjoys the following
powers:-
<ol>
    <ul>
        <li><b>Original:</b> The original criminal jurisdiction of the High Court has been completely
taken away by the criminal procedure
Code, 1973. The original civil jurisdiction
has been retained by the courts in respect
of actions of higher value.</li>
<li><b>Appellate:</b> The appellate jurisdiction of
the High Court is both civil and criminal.
Appeals from the decisions of the District
Judges and from those of Subordinate
Judge in case of a higher value, lie direct
to the High Court. On questions of fact as
well as law when any court subordinate
to the High Court decides an appeal from
the decision of an inferior court, a second
appeal lies to the High Court from the
decisions of the lower appellate court but
only on question of law and procedure.
The criminal appellate jurisdiction of the
High Court extends to appeals from the
decisions of a Sessions Judge or an Additional Sessions Judge, where the sentence
of imprisonment exceeds seven years and
from the decisions of an Assistant Session
Judge.</li>
<li><b>Power of Superintendence:</b> According to
Article 227, every High Court has the
power of superintendence over all courts
and tribunals, except those dealing with
the armed forces functioning with its territorial jurisdiction. Interpreting the scope
of this power, the Supreme Court said that
all types of tribunals including the election
tribunals operating within a state are subject to the superintendence of the High
Courts and further, that the "superintendence is both judicial and administrative.</li>
<li><b>Control over Subordinate Court:</b> As the
head of the judiciary in the state, the High
Court has got an administrative control
over the subordinate judiciary in the state
in respect of certain matters, besides its
appellate and supervisory jurisdiction over
them. Article 228 empowers the High
Court to transfer constitutional cases from
lower courts. Thus, if the Court is satisfied
that a case pending in one of its subordinate courts involves a substantial question
of law as to the interpretation of the
constitution, the determination of which is
necessary for the disposal of the case, it
shall then withdraw the case and may
either dispose of the case itself or determine the constitutional question and then
send the case back to the court where from
it was withdrawn.</li>
<li><b>The Writ Jurisdiction of High Court:</b>
Every High Court shall have power
throughout the territories in relation to
which it exercises jurisdiction to issue to
any person or authority including the
Government within those territories, orders or writs including writs in the nature
of habeas corpus, mandamus, prohibition,
quo warranto and certiorari or any of
them for enforcement of the fundamental
rights guaranteed by the constitution, and
for any other purposes (Article 226). The
power may also be exercised by any High
Court exercising jurisdiction in relation to
the territories within which the cause of
action, wholly or in part, arises for the
exercise of such power, notwithstanding
that the seat of such government or authority or the residence of such person is
not within those territories.</li>
<li>Power of Appointment: According to
Article 229, the Chief Justice of the High
Court, is empowered to appoint officers
and servants of the Court. The Governor
may in this respect require the Court to
consult the Public Service Commission of
the state. He also consults the High Court
in the appointment, posting, and promotion of district judges and along with the
state Public Service Commission in appointing person to the judicial service of
the state. The Chief Justice is also
authorised to regulate the conditions of
service of the staff subject to any law made
by the state legislature in this respect. The
power of the Chief Justice to appoint any
member of the staff of the High Court also
includes his power to dismiss any such
member form the service of the court. The
powers of posting and promotions and
grant of leave to persons belonging to the
judicial service is also vested in the High
Court. The constitution also provides for
charging all the administrative expenses of
the High Court on the Consolidated Funds
of the State.</li>
</ul>
</ol> 


<div class="subheading"><strong><i>Subordinate Courts</i></strong></div>

As mentioned earlier, the judiciary of a state
consists of a high court and a hierarchy of 
subordinate courts, also known as lower courts.
The subordinate courts are so called because of
their subordinate to the state high court. They
function below and under the high court at
district and lower levels.
Articles 233 to 237 in Part VI of the Constitution
make the following provisions to regulate the
organisation of subordinate courts and to ensure
their independence from the executive.
<ol>
    <ul>
        <li>
        <div class="subheading"><strong><i>Appointment of District Judges</i></strong></div>    
        
The appointment, positing and promotion of
district judges in a state are made by the governor
of the state in consultation with the high court.
A person to be appointed as district judge
should have the following qualifications:
    <ol>
        <ul>
            <li>He should not already be in the service of the
Central or the state government.</li>
<li>He should have been an advocate or a
pleader for seven years.</li>
<li>He should be recommended by the high
court for appointment.</li>
        </ul>
    </ol>
The expression 'district judge' includes judge
of a city civil court, additional district judge, joint
district judge, assistant district judge, chief judge
of a small cause court, chief presidency magistrate,
additional chief presidency magistrate, sessions
judge, additional sessions judge and assistant
sessions judge.</li>
<li>
<div class="subheading"><strong><i>Appointment of other Judges</i></strong></div>    

Appointment of person (other than district
judges) to the judicial service of a state are made
by the governor of the state after consultation with
the State Public Service Commission and the high
court.
The expression 'judicial service' means a service
consisting exclusively of persons intended to fill
the post of district judge and other civil judicial
posts inferior to the post of district judge.</li>
<li>
<div class="subheading"><strong><i>Control over Subordinate Courts</i></strong></div>    

The control over district courts and other
subordinate courts including the posting,
promotion and leave of person belonging to the
judicial service of a state and holding any post
inferior to the post of district judge is vested in the
court. This control is vested in the high court to
secure the independence of the subordinate
judiciary.</li>
    </ul>
</ol>
The Constitution also empowers the governor
to apply the above provisions regarding
subordinate courts to any class or classes of
magistrate in the state.


