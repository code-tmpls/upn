<h3>Judiciary</h3>

<div class="subheading"><strong><i>The Supreme Court</i></strong></div>
The Constitution of India has the following
provisions regarding the Supreme Court of India:

<div class="subheading"><strong><i>Article 124:</i></strong></div>
<b>Establishment and constitution of Supreme
Court</b>
<ol>
    <ul>
        <li>There shall be a Supreme Court of India
consisting of a Chief Justice of India and,
until Parliament by law prescribes a larger
number, of not more than seven.</li>
        <li>Every Judge of the Supreme Court shall be
appointed by the President by warrant
under his hand and seal after consultation
with such of the Judges of the Supreme
Court and of the High Courts in the States
as the President may deem necessary for
the purpose and shall hold office until he
attains the age of sixty-five years:
Provided that in the case of appointment
of a Judge other than the Chief Justice, the
Chief Justice of India shall always be
consulted. Provided further that
<ol>
    <ul>
        <li>a Judge may, by writing under his hand
addressed to the President, resign his
office;</li>
<li>a Judge may be removed from his office
in the manner provided in clause (4).
The age of a Judge of the Supreme Court shall
be determined by such authority and in such
manner as Parliament may by law provide.</li>
    </ul>
</ol>
</li>
        <li>A person shall not be qualified for appointment as a Judge of the Supreme
Court unless he is a citizen of India and-
<ol>
    <ul>
        <li>has been for at least five years a Judge
of a High Court or of two or more
such Courts in succession; or</li>
<li>has been for at least ten years an
advocate of a High Court or of two or
more such Courts in succession; or</li>
<li>is, in the opinion of the President, a
distinguished jurist.</li>
    </ul>
</ol> </li>
<li>Parliament may by law regulate the procedure for the presentation of an address
and for the investigation and proof of the
misbehaviour or incapacity of a Judge
under clause (4).</li>
<li>Every person appointed to be a Judge of
the Supreme Court shall, before he enters
upon his office, make and subscribe before
the President, or some person appointed in
that behalf by him, an oath or affirmation
according to the form set out for the
purpose in the Third Schedule.</li>
<li>No person who has held office as a Judge
of the Supreme Court shall plead or act in
any court or before any authority within
the territory of India.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Article 125:</i></strong></div>
<b>Salaries, etc., of Judges.</b>
<ol>
    <ul>
        <li>There shall be paid to the Judges of the
Supreme Court such salaries as may be
determined by Parliament by law and,
until provision in that behalf is so made,
such salaries as are specified in the Second
Schedule.</li>
        <li>Every Judge shall be entitled to such privileges and allowances and to such rights in
respect of leave of absence and pension as
may from time to time be determined by
or under law made by Parliament and,
until so determined, to such privileges,
allowances and rights as are specified in
the Second Schedule:</li>
</ul>
</ol>   
Provided that neither the privileges nor the
allowances of a Judge nor his rights in respect of
leave of absence or pension shall be varied to his
disadvantage after his appointment.
   
<div class="subheading"><strong><i>Article 126:</i></strong></div>

<b>Appointment of acting Chief Justice.</b>
When the office of Chief Justice of India is
vacant or when the Chief Justice is, by reason of
absence or otherwise, unable to perform the duties
of his office, the duties of the office shall be
performed by such one of the other Judges of the
Court as the President may appoint for the
purpose.

<div class="subheading"><strong><i>Article 127:</i></strong></div>
<b>Appointment of ad hoc Judges.</b>
<ol>
    <ul>
        <li>If at any time there should not be a
quorum of the Judges of the Supreme
Court available to hold or continue any
session of the Court, the Chief Justice of
India may, with the previous consent of
the President and after consultation with
the Chief Justice of the High Court concerned, request in writing the attendance
at the sittings of the Court, as an ad hoc
Judge, for such period as may be necessary, of a Judge of a High Court duly
qualified for appointment as a Judge of the
Supreme Court to be designated by the
Chief Justice of India.</li>
<li>It shall be the duty of the Judge who has
been so designated, in priority to other
duties of his office, to attend the sittings of
the Supreme Court at the time and for the
period for which his attendance is required, and while so attending he shall
have all the jurisdiction, powers and privileges, and shall discharge the duties, of a
Judge of the Supreme Court.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Article 128:</i></strong></div>

<b>Attendance of retired Judges at sittings of the
Supreme Court.</b>
Not with standing anything in this Chapter,
the Chief Justice of India may at any time, with
the previous consent of the President, request any
person who has held the office of a Judge of the
Supreme Court or of the Federal Court 2[or who
has held the office of a Judge of a High Court and
is duly qualified for appointment as a Judge of the
Supreme Court] to sit and act as a Judge of the
Supreme Court, and every such person so requested
shall, while so sitting and acting, be entitled to
such allowances as the President may by order
determine and have all the jurisdiction, powers
and privileges of, but shall not otherwise be
deemed to be, a Judge of that Court:
Provided that nothing in this article shall be
deemed to require any such person as aforesaid
to sit and act as a Judge of that Court unless he
consents so to do.

<div class="subheading"><strong><i>Article 129:</i></strong></div>
<b>Supreme Court to be a court of record.</b>
The Supreme Court shall be a court of record
and shall have all the powers of such a court
including the power to punish for contempt of
itself.

<div class="subheading"><strong><i>Article 130:</i></strong></div>

<b>Seat of Supreme Court.</b>
The Supreme Court shall sit in Delhi or in such
other place or places, as the Chief Justice of India
may, with the approval of the President, from time
to time, appoint.

<div class="subheading"><strong><i>Article 131:</i></strong></div>

Original jurisdiction of the Supreme Court.
Subject to the provisions of this Constitution,
the Supreme Court shall, to the exclusion of any
other court, have original jurisdiction in any
dispute-
<ol>
    <ul>
        <li>between the Government of India and one
or more States; or</li>
<li>between the Government of India and any
State or States on one side and one or more
other States on the other; or</li>
<li>between two or more States, if and in so far
as the dispute involves any question (whether
of law or fact) on which the existence or
extent of a legal right depends:</li>
    </ul>
</ol>

Provided that the said jurisdiction shall not
extend to a dispute arising out of any treaty,
agreement, covenant, engagement, sanad or other
similar instrument which, having been entered
into or executed before the commencement of this
Constitution, continues in operation after such
commencement, or which provides that the said
jurisdiction shall not extend to such a dispute

<div class="subheading"><strong><i>Article 132:</i></strong></div>
<b>Appellate jurisdiction of Supreme Court in appeals
from High Courts in certain cases</b>
<ol>
    <ul>
        <li>An appeal shall lie to the Supreme Court
from any judgment, decree or final order
of a High Court in the territory of India,
whether in a civil, criminal or other proceeding, if the High Court certifies under
article 134A that the case involves a substantial question of law as to the interpretation 
of this Constitution.</li>
<li>Where such a certificate is given, any
party in the case may appeal to the Supreme Court on the ground that any such
question as aforesaid has been wrongly
decided.</li>
    </ul>
</ol>
Explanation.- For the purposes of this article,
the expression "final order" includes an order
deciding an issue which, if decided in favour of
the appellant, would be sufficient for the final
disposal of the case.

<div class="subheading"><strong><i>Article 133:</i></strong></div>

<b>Appellate jurisdiction of Supreme Court in appeals
from High Courts in regard to civil matters.-</b>
<ol>
    <ul>
        <li>An appeal shall lie to the Supreme Court
from any judgment, decree or final order
in a civil proceeding of a High Court in the
territory of India 7 [if the High Court
certifies under article 134A.
<ol>
    <ul>
        <li>that the case involves a substantial
question of law of general importance;
and</li>
<li>that in the opinion of the High Court
the said question needs to be decided
by the Supreme Court.</li>
    </ul>
</ol>
</li>
<li>Notwithstanding anything in article 132,
any party appealing to the Supreme Court
under clause (1) may urge as one of the
grounds in such appeal that a substantial
question of law as to the interpretation of
this Constitution has been wrongly decided.</li>
<li>Notwithstanding anything in this article,
no appeal shall, unless Parliament by law
otherwise provides, lie to the Supreme
Court from the judgment, decree or final
order of one Judge of a High Court.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Article 134:</i></strong></div>
<b>Appellate jurisdiction of Supreme Court in regard
to criminal matters.</b>
<ol>
    <ul>
        <li>An appeal shall lie to the Supreme Court
from any judgment, final order or sentence in a criminal proceeding of a High
Court in the territory of India if the High
Court-
<ol>
    <ul>
        <li>has on appeal reversed an order of
acquittal of an accused person and
sentenced him to death; or</li>
<li>has withdrawn for trial before itself any
case from any court subordinate to its
authority and has in such trial convicted
the accused person and sentenced him
to death; or</li>
<li>8[certifies under article 134A] that the
case is a fit one for appeal to the
Supreme Court:</li>
    </ul>
</ol></li>
    </ul>
</ol>

Provided that an appeal under sub-clause (c)
shall lie subject to such provisions as may be made
in that behalf under clause
<ol>
    <ul>
        <li>of article 145 and to such conditions as the
High Court may establish or require.</li>
<li>Parliament may by law confer on the
Supreme Court any further powers to entertain and hear appeals from any judgment, final order or sentence in a criminal
proceeding of a High Court in the territory
of India subject to such conditions and
limitations as may be specified in such law.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Article 135:</i></strong></div>

<b>Jurisdiction and powers of the Federal Court
under existing law to be exercisable by the
Supreme Court.-</b>
Until Parliament by law otherwise provides,
the Supreme Court shall also have jurisdiction and
powers with respect to any matter to which the
provisions of article 133 or article 134 do not apply
if jurisdiction and powers in relation to that matter
were exercisable by the Federal Court immediately
before the commencement of this Constitution
under any existing law.

<div class="subheading"><strong><i>Article 136:</i></strong></div>
<b>Special leave to appeal by the Supreme Court.</b>
<ol>
    <ul>
        <li>Notwithstanding anything in this Chapter, the Supreme Court may, in its 
            discretion, grant special leave to appeal from
any judgment, decree, determination, sentence or order in any cause or matter
passed or made by any court or tribunal
in the territory of India.</li>
<li>Nothing in clause (1) shall apply to any
judgment, determination, sentence or order passed or made by any court or tribunal constituted 
by or under any law relating to the Armed Forces.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Article 137:</i></strong></div>

Review of judgments or orders by the Supreme
Court.
Subject to the provisions of any law made by
Parliament or any rules made under article 145,
the Supreme Court shall have power to review
any judgment pronounced or order made by it.

<div class="subheading"><strong><i>Article 138:</i></strong></div>
<b>Enlargement of the jurisdiction of the Supreme
Court.</b>
<ol>
    <ul>
        <li>The Supreme Court shall have such further jurisdiction and powers with respect
to any of the matters in the Union List as
Parliament may by law confer.</li>
<li>The Supreme Court shall have such further jurisdiction and powers with respect
to any matter as the Government of India
and the Government of any State may by
special agreement confer, if Parliament by
law provides for the exercise of such jurisdiction and powers by the Supreme Court.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Article 139:</i></strong></div>

<b>Conferment on the Supreme Court of powers to
issue certain writs.</b>
Parliament may by law confer on the Supreme
Court power to issue directions, orders or writs,
including writs in the nature of habeas corpus,
mandamus, prohibition, quo warranto and
certiorari, or any of them, for any purposes other
than those mentioned in clause (2) of article 32.

<div class="subheading"><strong><i>Article 140:</i></strong></div>

Ancillary powers of Supreme Court.
Parliament may by law make provision for
conferring upon the Supreme Court such
supplemental powers not inconsistent with any of
the provisions of this Constitution as may appear
to be necessary or desirable for the purpose of
enabling the Court more effectively to exercise the
jurisdiction conferred upon it by or under this
Constitution.

<div class="subheading"><strong><i>Article 141:</i></strong></div>

Law declared by Supreme Court to be binding
on all courts.
The law declared by the Supreme Court shall
be binding on all courts within the territory of
India.

<div class="subheading"><strong><i>Article 142:</i></strong></div>

Enforcement of decrees and orders of Supreme
Court and orders as to discovery, etc.
<ol>
    <ul>
        <li>The Supreme Court in the exercise of its
jurisdiction may pass such decree or make
such order as is necessary for doing complete justice in any cause or matter pending before it, and any decree so passed or
order so made shall be enforceable throughout the territory of India in such manner
as may be prescribed by or under any law
made by Parliament and, until provision
in that behalf is so made, in such manner
as the President may by order1 prescribe.</li>
<li>Subject to the provisions of any law made
in this behalf by Parliament, the Supreme
Court shall, as respects the whole of the
territory of India, have all and every power
to make any order for the purpose of
securing the attendance of any person, the
discovery or production of any documents,
or the investigation or punishment of any
contempt of itself.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Article 143:</i></strong></div>

<b>Power of President to consult Supreme Court.</b>
<ol>
    <ul>
        <li>If at any time it appears to the President
that a question of law or fact has arisen,
or is likely to arise, which is of such a
nature and of such public importance that
it is expedient to obtain the opinion of the
Supreme Court upon it, he may refer the
question to that Court for consideration
and the Court may, after such hearing as
it thinks fit, report to the President.</li>
<li>The President may, notwithstanding anything in the proviso to article 131, refer a
dispute of the kind mentioned in the 3[said
proviso] to the Supreme Court for opinion
and the Supreme Court shall, after such
hearing as it thinks fit, report to the President its opinion thereon.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Article 144:</i></strong></div>

<b>Civil and judicial authorities to act in aid of the
Supreme Court.</b>
All authorities, civil and judicial, in the territory
of India shall act in aid of the Supreme Court.

<div class="subheading"><strong><i>Article 145:</i></strong></div>
<b>Rules of Court, etc.</b>
<ol>
    <ul>
        <li>Subject to the provisions of any law made
by Parliament, the Supreme Court may
from time to time, with the approval of the
President, make rules for regulating generally the practice and procedure of the
Court including
<ol>
    <ul>
        <li>rules as to the persons practising before
the Court;</li>
<li>rules as to the procedure for hearing
appeals and other matters pertaining to
appeals including the time within which
appeals to the Court are to be entered;</li>
<li>rules as to the proceedings in the Court
for the enforcement of any of the rights
conferred by Part III;</li>
<li>rules as to the entertainment of appeals
under sub-clause (c) of clause (1) of
article 134;</li>
<li>rules as to the conditions subject to
which any judgment pronounced or
order made by the Court may be
reviewed and the procedure for such
review including the time within which
applications to the Court for such review
are to be entered;</li>
<li>rules as to the costs of and incidental to
any proceedings in the Court and as to
the fees to be charged in respect of
proceedings therein;</li>
<li>rules as to the granting of bail;</li>
<li>rules as to stay of proceedings;</li>
<li>rules providing for the summary
determination of any appeal which
appears to the Court to be frivolous or
vexatious or brought for the purpose of
delay;</li>
<li>rules as to the procedure for inquiries
referred to in clause (1) of article 317.</li>

    </ul>
</ol>
</li>
<li>Subject to the rules made under this article
may fix the minimum number of Judges
who are to sit for any purpose, and may
provide for the powers of single Judges
and Division Courts.</li>
<li>The minimum number of Judges who are
to sit for the purpose of deciding any case
involving a substantial question of law as
to the interpretation of this Constitution or
for the purpose of hearing any reference
under article 143 shall be five:
Provided that, where the Court hearing an
appeal under any of the provisions of this Chapter
other than article 132 consists of less than five
Judges and in the course of the hearing of the
appeal the Court is satisfied that the appeal
involves a substantial question of law as to the
interpretation of this Constitution the
determination of which is necessary for the disposal
of the appeal, such Court shall refer the question
for opinion to a Court constituted as required by
this clause for the purpose of deciding any case
involving such a question and shall on receipt of
the opinion dispose of the appeal in conformity
with such opinion.</li>
<li>No judgment shall be delivered by the
Supreme Court save in open Court, and
no report shall be made under article 143
save in accordance with an opinion also
delivered in open Court.</li>
<li>No judgment and no such opinion shall be
delivered by the Supreme Court save with
the concurrence of a majority of the Judges
present at the hearing of the case, but
nothing in this clause shall be deemed to
prevent a Judge who does not concur from
delivering a dissenting judgment or opinion.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Article 146:</i></strong></div>

<b>Officers and servants and the expenses of the
Supreme Court.</b>
<ol>
    <ul>
        <li>Appointments of officers and servants of
the Supreme Court shall be made by the 
Chief Justice of India or such other Judge
or officer of the Court as he may direct:
Provided that the President may by rule require
