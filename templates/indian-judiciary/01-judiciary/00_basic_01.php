</ul>
</ol>

<div class="subheading"><strong><i>High Courts</i></strong></div>

In each state there is a High Court, which
exercises powers within the territorial jurisdiction
of the state concerned. A High Court consists of
a Chief Justice and such other judges as the
President may, from time to time, determine.
Since the number of the judges of the State High 
courts has not been fixed by the constitution, it
varies from court to court. Appointments of the
judges of the High Court shall be made by the
President by warrant under his hand and seal.
The President consults the Chief Justice of India
and the Governor of the state and the Chief Justice
of the High Court concerned in making the
appointment. In case, of the appointment of the
Chief Justice of the High Court, the President will
consult the Chief Justice of India and the Governor
of the state concerned.
  
<div class="subheading"><strong><i>Qualifications and Conditions of Service</i></strong></div>

A person shall not be qualified for appointment
as a judge of High court unless he is a citizen of
India and has either held for at least ten years a
judicial office in the territory of India or has for
at least ten years been an advocate of a High court
in any state. The salary allowance and other
amenities enjoyed by the judges of High Courts
cannot be modified to their disadvantage during
their tenure of office. However, the President is
authorised to reduce their salaries during the
proclamation of financial emergency. As the
salaries and other expenses of the judges and the
maintenance of state High Courts are charged to
the Consolidated Fund of the state, they are to
subject to the vote of the state legislature.
<br>
A judge shall hold office till he attains the age
of sixty two years. However, he may resign his
office by writing to the President. He can be
removed from his office by the President, in the
manner provided for the removal of a judge of
the Supreme Court. A judge can be removed from
his office before the expiry of his term on grounds
of proved misbehaviour or incapacity. However,
such an action can be taken by the President only
if both the House of Parliament pass a resolution
by a two thirds majority of the members present
in each House which should also be the majority
of the total membership of the House.

<div class="subheading"><strong><i>How a Judge of the Supreme Court or
High Courts can be removed?</i></strong></div>

A complex procedure for removal of judges is
usually laid down in the constitution. The framers
of Indian Constitution were conscious of the value
of an independent judiciary and made elaborate
provisions to secure the same.
Article 124 of the Constitution provides for the
establishment and Constitution of the Supreme
Court of India. The article lays down that the
Supreme Court shall consist of Chief Justice of
India and not more than such number of judges
as Parliament may by law prescribe. It further
provides that every judge shall be appointed by
the President by warrant under his hand and seal
after consultation with such of the judges of the
Supreme Court and of the High courts in the
States as the President may deem necessary for
the purpose and shall hold office until he attains
the age of 65 years. The Supreme Court judge may
resign his office by writing in his hand addressed
to the President. Clause (4) of article 124 provides
that a judge of Supreme court shall not be
removed from his office except by an order of the
President passed after an address by each House
of Parliament supported by a majority off total
membership of that House and by a majority of
not less than two-third of the Members of that
House present and voting presented to the
President in the same session for such removal on
the ground of proved misbehaviour or incapacity.
Provision to article 217(1) and article 218 indicate
that provisions of clauses (4) and (5) of article 124
equally apply for the removal of a judge of the
High Court.
Clause (5) of article 124 empowers Parliament
to regulate the procedure for the presentation of
an address and for the investigation and proof of
the misbehaviour or incapacity of a judge by law.
A suggestion in the Constituent Assembly that the
matter may first be investigated by a judicial
committee was rejected. Article 124 is an attempt
to make the judiciary totally independent of the
executive or legislature so that the judiciary can
adjudicate constitutional and legal questions
fearlessly and impartially.

<div class="subheading"><strong><i>Jurisdiction of High Court</i></strong></div>

The High Court of a state enjoys the following
powers:-
<ol>
    <ul>
        <li><b>Original:</b> The original criminal jurisdiction of the High Court has been completely
taken away by the criminal procedure
Code, 1973. The original civil jurisdiction
has been retained by the courts in respect
of actions of higher value.</li>
<li><b>Appellate:</b> The appellate jurisdiction of
the High Court is both civil and criminal.
Appeals from the decisions of the District
Judges and from those of Subordinate
Judge in case of a higher value, lie direct
to the High Court. On questions of fact as
well as law when any court subordinate
to the High Court decides an appeal from
the decision of an inferior court, a second
appeal lies to the High Court from the
decisions of the lower appellate court but
only on question of law and procedure.
The criminal appellate jurisdiction of the
High Court extends to appeals from the
decisions of a Sessions Judge or an Additional Sessions Judge, where the sentence
of imprisonment exceeds seven years and
from the decisions of an Assistant Session
Judge.</li>
<li><b>Power of Superintendence:</b> According to
Article 227, every High Court has the
power of superintendence over all courts
and tribunals, except those dealing with
the armed forces functioning with its territorial jurisdiction. Interpreting the scope
of this power, the Supreme Court said that
all types of tribunals including the election
tribunals operating within a state are subject to the superintendence of the High
Courts and further, that the "superintendence is both judicial and administrative.</li>
<li><b>Control over Subordinate Court:</b> As the
head of the judiciary in the state, the High
Court has got an administrative control
over the subordinate judiciary in the state
in respect of certain matters, besides its
appellate and supervisory jurisdiction over
them. Article 228 empowers the High
Court to transfer constitutional cases from
lower courts. Thus, if the Court is satisfied
that a case pending in one of its subordinate courts involves a substantial question
of law as to the interpretation of the
constitution, the determination of which is
necessary for the disposal of the case, it
shall then withdraw the case and may
either dispose of the case itself or determine the constitutional question and then
send the case back to the court where from
it was withdrawn.</li>
<li><b>The Writ Jurisdiction of High Court:</b>
Every High Court shall have power
throughout the territories in relation to
which it exercises jurisdiction to issue to
any person or authority including the
Government within those territories, orders or writs including writs in the nature
of habeas corpus, mandamus, prohibition,
quo warranto and certiorari or any of
them for enforcement of the fundamental
rights guaranteed by the constitution, and
for any other purposes (Article 226). The
power may also be exercised by any High
Court exercising jurisdiction in relation to
the territories within which the cause of
action, wholly or in part, arises for the
exercise of such power, notwithstanding
that the seat of such government or authority or the residence of such person is
not within those territories.</li>
<li>Power of Appointment: According to
Article 229, the Chief Justice of the High
Court, is empowered to appoint officers
and servants of the Court. The Governor
may in this respect require the Court to
consult the Public Service Commission of
the state. He also consults the High Court
in the appointment, posting, and promotion of district judges and along with the
state Public Service Commission in appointing person to the judicial service of
the state. The Chief Justice is also
authorised to regulate the conditions of
service of the staff subject to any law made
by the state legislature in this respect. The
power of the Chief Justice to appoint any
member of the staff of the High Court also
includes his power to dismiss any such
member form the service of the court. The
powers of posting and promotions and
grant of leave to persons belonging to the
judicial service is also vested in the High
Court. The constitution also provides for
charging all the administrative expenses of
the High Court on the Consolidated Funds
of the State.</li>
</ul>
</ol> 


<div class="subheading"><strong><i>Subordinate Courts</i></strong></div>

As mentioned earlier, the judiciary of a state
consists of a high court and a hierarchy of 
subordinate courts, also known as lower courts.
The subordinate courts are so called because of
their subordinate to the state high court. They
function below and under the high court at
district and lower levels.
Articles 233 to 237 in Part VI of the Constitution
make the following provisions to regulate the
organisation of subordinate courts and to ensure
their independence from the executive.
<ol>
    <ul>
        <li>
        <div class="subheading"><strong><i>Appointment of District Judges</i></strong></div>    
        
The appointment, positing and promotion of
district judges in a state are made by the governor
of the state in consultation with the high court.
A person to be appointed as district judge
should have the following qualifications:
    <ol>
        <ul>
            <li>He should not already be in the service of the
Central or the state government.</li>
<li>He should have been an advocate or a
pleader for seven years.</li>
<li>He should be recommended by the high
court for appointment.</li>
        </ul>
    </ol>
The expression 'district judge' includes judge
of a city civil court, additional district judge, joint
district judge, assistant district judge, chief judge
of a small cause court, chief presidency magistrate,
additional chief presidency magistrate, sessions
judge, additional sessions judge and assistant
sessions judge.</li>
<li>
<div class="subheading"><strong><i>Appointment of other Judges</i></strong></div>    

Appointment of person (other than district
judges) to the judicial service of a state are made
by the governor of the state after consultation with
the State Public Service Commission and the high
court.
The expression 'judicial service' means a service
consisting exclusively of persons intended to fill
the post of district judge and other civil judicial
posts inferior to the post of district judge.</li>
<li>
<div class="subheading"><strong><i>Control over Subordinate Courts</i></strong></div>    

The control over district courts and other
subordinate courts including the posting,
promotion and leave of person belonging to the
judicial service of a state and holding any post
inferior to the post of district judge is vested in the
court. This control is vested in the high court to
secure the independence of the subordinate
judiciary.</li>
    </ul>
</ol>
The Constitution also empowers the governor
to apply the above provisions regarding
subordinate courts to any class or classes of
magistrate in the state.

<h3>Rule of Law</h3>

The Rule of Law- is a basic requirement for a
democratic government. And for the maintenance
of rule of law, there must be an independent and
impartial judiciary. It is embodied in the concept
of rule of law that equality before the law or equal
protection of the laws is ensured to all citizens,
and every citizen is protected from arbitrary
exercise of power by the State. Thus in a state
professing the rule of law, the aim should be to
provide for a system which secures to its citizens
adequate procedure for the redress of their
grievances against the state before forums which
are able to administer justice in an impartial
manner without any fear or favour.

<div class="subheading"><strong><i>Rule of Law in India</i></strong></div>

Each country has devised its own system to
ensure the maintenance of the rule of law. The
Indian Constitution embodies the modern concept
of the rule of law with the establishment of a
judicial system which should be able to work
impartially and free from all influences. The rule
of law pervades over the entire field of
administration and every organ of the State is
regulated by the rule of law. The concept of the
rule of law would lose its vitality if the
instrumentalities of the state are not charged with
the duty of discharging their function in a fair and
just manner. It has been held that the rule of law
pervades the Constitution as its basic feature and
cannot be taken away even by an amendment of
the Constitution.

<div class="subheading"><strong><i>Constitutional Provisions for Rule of Law</i></strong></div>

Fundamental Rights: Indian Constitution
enshrines the fundamental rights to individuals
which operate as limitations on the exercise of
powers by the government. If there is an
infringement of the Fundamental Rights of a
citizen, the rule of law requires that there should
be a proper forum for the address of his grievances.
For this purpose, it is provided that an aggrieved 
person may even move the Supreme Court directly
by appropriate proceedings for the enforcement of
his fundamental rights. The rule of law under the
Constitution thus serves the needs of the people.
It recognises the social reality and tries to adjust
itself to it from time to time avoiding the
authoritarian path.
It is specifically provided that the state shall
not deny to any person, equality before the law
or the equal protection of law. The absence of
arbitrary power is the first essential of the rule of
law upon which the Indian constitutional system
is based. In a system governed by the rule of law,
discretion, when conferred upon executive
authorities, must be confined within clearly defined
limits. This means that decision should be made
by the application of known principles and rules
and, in general, such decisions should be predictable
and the citizen should know where he stands.
Directive Principles: The Indian Constitution
lays down in Part IV the Directive Principles of
State Policy. It enjoins to bring about a social order
in which justice, social, economic and political,
shall prevail all the institutions of national life. It
directs it to work for an egalitarian society where
there is no concentration of wealth, where there
is plenty, where there is equal opportunity for all,
to education, to work, to livelihood, and where
there is social justice.
It is true that the representatives of the people
are charged with the responsibility of realising the
aims and objectives of the Constitution, but left to
themselves there is a possibility that uncontrolled
and unrestricted power might lead to an
authoritarian State. It is here that the DPSPs. come
into picture & serve as the beacon lights for both
the politicians as well as the judiciiary who is
responsible for ensuring that the govt. of the day
respects the rights of the people & acts as per rule
of law.

<h3>Judical Review</h3>

Judicial Review means the power of the courts
to declare a law made by legislature beyond the
powers given to it by the Constitution as ultravires
of the Constitution and hence void. Judicial review
is a powerful weapon to restrain any
unconstitutional exercise of power by the
Legislature and the Executive. The only check on
the exercise of power by the judiciary, however,
is the self-imposed discipline of judicial restraint.
The doctrine of judicial review does not permit the
court to direct or advise the executive in matters
of policy or to sermonize vis-Ã -vis any matter
which under the Constitution lies within the
sphere of Legislature or the Executive, provided
those authorities do no transgresses their
constitutional limits or statutory powers.

<div class="subheading"><strong><i>Judicial Review in India</i></strong></div>

The phrase "judicial review" is not anywhere
used in the Indian Constitution. But, the framers
of Indian Constitution intended this power to be
exercised by the courts while interpreting any law
passed by the Parliament or State-legislatures.
Till 1967 the Supreme Court had exercised the
power of judicial review with restraint. But in
sGolaknath case, 1967, the Supreme Court
overruled previous decisions and declared that the
Parliament could not amend the Constitution to
take away or abridge any of the Fundamental
Rights. This decision resulted in a public
controversy over the sovereignty of Parliament.
Then, came two other judgments of the Supreme
Court - the Bank Nationalization case and the
Privy Purses case. These cases also questioned the
supremacy of the parliament. Soon thereafter in
a newly elected Lok Sabha following the 1971
elections, the Parliament passed 24th , 25th and
26th constitution Amendment bills to neutralise
the effect of the three above-mentioned Supreme
Court judgments. In the famous Keshavanand
Bharati Case, 1973, the Supreme Court reversed
the Golaknath Case ruling and upheld Parliament's
right to amend the Constitution including the
Fundamental Rights, but not the 'basic structure
or framework' of the Constitution. The Supreme
Court holds the view that the Parliament is not
omnipotent to change the basic structure of the
Constitution.

<div class="subheading"><strong><i>Impact of the 42nd Amendment</i></strong></div>

The 42nd Amendment enacted during the
Emergency made far-reaching changes to curtail
the powers of the courts and to make the Parliament
sovereign. Firstly, the 42nd Amendment stated
that no amendment to the Constitution could be
questioned in a Court of Law. And "for the
removal of the doubts, it is hereby declared that
there shall be no limitation what ever on the

constituent power of Parliament to amend by way
of addition, variation or repeal the provisions of
this constitution." In this manner, through this
Amendment the Supreme Court's power to judicial
review of constitutional amendments was taken
away to establish the complete and total
sovereignty of Parliament. The Amendment stated
that:
<ol>
    <ul>
        <li>A High Court cannot pronounce invalid any
Central law,</li>
<li>The Supreme Court shall not pronounce a
State law as unconstitutional unless a Central
law has also been challenged.</li>

    </ul>
</ol>

Further, the minimum number of judges of the
Supreme Court who shall sit to determine the
constitutional validity of any Central or State law
shall be seven and in the case of High Court, five.
It was also stated that a majority of not less than
two- thirds of the judges hearing such a case must
agree before a law is declared invalid. But after
this the 43rd Amendment was passed which
restored the pre-emergency position of the Supreme
Court's power of judicial review over laws passed
by state legislatures and Parliament. As far as
Parliament's sovereignty with regard to amending
the Constitution is concerned, there is no change.
The power of Parliament to amend the Constitution
exists as under the 42nd Amendment, however
the judgment of the Supreme Court in the Minerva
Mills case in May 1980 was a setback to the
position of unlimited powers claimed by the
Parliament to amend any part of Constitution.
This judgment recognized only limited powers of
the Parliament to amend the Constitution without
altering the basic structure.

<div class="subheading"><strong><i>Constitutional Provisions</i></strong></div>

Article 32 of the Constitution makes the
Supreme Court the ultimate guardian of the
Fundamental Rights of the citizens and clothes it
with the power to issue writs for their enforcement.
Article 142 gives the Apex Court wide powers to
proonounce orders as are necessary in the interest
of justice for doing complete justice in any cause
or matter before it, in addition to its powers to
make decisions under article 131 to 136. Article
142 contains no words of limitation and has
enabled the court to intervene in a wide variety
of cases. Starting with Union Carbide Corpn. v.
Union of India, (1991) Supreme Court has made
significant strides to maintain the rule of law,
which is the bedrock of our Constitution.

<div class="subheading"><strong><i>Significance of Judicial Review</i></strong></div>

It is said that there is a shift from the traditional
judicial role to judicial activism, from passivity to
creativity, in that the courts are taking judicial
notice of the changing needs of the society and
evolving new tools for redressing public wrongs.
Public Interest Litigation based on the enlarged
concept of locus standi, has developed on account
of judicial activism. In several matters, the Courts
have provided relief through the 'judicial review',
such as, in cases concerning the deprived or
disadvantaged sections of the society, prisoners,
environmental degradation, closure of polluting
industries in Delhi, encroachments and
unauthorised constructions, immediate medical
aid by government hospitals to seriously injured
persons, reparations to riot-victims, professional
college admissions, contempt involving
disobedience or imperviousness to court orders,
corruption in high places, or malfeasance of public
servants (including Ministers) involving breach of
public trust, etc.

<div class="subheading"><strong><i>Conclusion</i></strong></div>

Undoubtedly, the maxim "the King can do no
wrong" or absolute immunity of the government
is not recognised in our legal system, rather
independence and impartiality which are the two
basic attributes essential for proper discharge of
judicial functions are emphasized upon. In fact,
"judicial activism" is nothing but Judiciary's
insistence that the rule of law must guide the
Legislature and the Executive in enacting or
enforcing the laws of the land. Judicial review is
a constitutionally embraced concept, which has
been streesed upon in cases like Minerva Mills
Ltd.v. Unin of India, S.P.Sampath Kumar v.
Union of India. Subhesh Sharma v. Union of
India,etc. Judicial review, in India is a power born
from the first principles of democratic
constitutionalism & is today an area of great
promise. It is an essential part of the rule of law.
However, it must also be kept in view that the
actual governance of the country is certainly the
sphere of the Executive which is accountable to
Parliament. Neither the Executive nor the Judiciary
should exceed their legitimate functions. Only 
then the two organs of the State can function
harmoniously. Self restraint is the key to the whole
issue.

