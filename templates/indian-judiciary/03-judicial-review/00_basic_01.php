<h3>Panchayat
& Municipalities</h3>

<div class="subheading"><strong><i>Rural Local Bodies</i></strong></div>

The Constitution (73rd Amendment) Act,
1992 has added a new Part IX consisting of 16
Articles and the Eleventh Schedule to the
Constitution. The 73rd Amendment Act
envisages the Gram Sabha as the foundation of
the Panchayati Raj System to perform functions
and powers entrusted to it by the State
legislatures. The amendment provide for a three
tier Panchayati Raj System at the village,
intermediate and district levels. Small states
with population below twenty lakh have been
given the option not to constitute the Panchayats
at the intermediate level. The Act provides that
the Panchayat bodies will have an assured
duration of five years, with elections mandatory
after this period. However, one thing is to be
noted that under the Amendment Act the
establishment of Panchayats and the devolution
of necessary powers and authority on the
Panchayati Raj institutions are vested in the
State Governments. In view of this it may be
said that the success of the Panchayati Raj
institutions as a unit of democracy and thereby
ushering an all around development of rural
areas will much depend on the intention and 
support of the State Governments. Without
honest intention, these institutions would be
misused by rural rich and poor and illiterate
masses will remain a mute supporters as it is
happening in Parliamentary and State Assembly
elections in the Country.
<br>
<b>The salient features of the Seventy-Third
Constitutional Amendment Act may be
summarised as follows:</b>

<b>Gram Sabha:</b> Article 243A provides that the
Gram Sabha may exercise such powers and
perform such functions at the village level as the
legislature of a State may by law provide. The
73rd amendment thus envisages the Gram Sabha
as the foundation of Panchayati Raj System.
'Gram Sabha' means a body consisting of persons
registered in the electoral rolls relating to village
comprised within the area of Panchayat at the
village level.
<br>
<b>Constitution of Panchayats:</b> Article 243B
visualises a three-tier Panchayati Raj System. It
provides that in every State there shall be
constituted Panchayats at the village,
intermediate and district levels. Small states
having a population not exceeding twenty lakhs
have been given an option not to constitute the
Panchayats at the intermediate level.
<br>
<b>Composition of Panchayats:</b> Article 243C
provides that, subject to the provisions of this
part the legislature of a State may by law make
provisions with respect to the composition of
Panchayats. However, the ratio between the
population of the territorial area of a Panchayat
at any level and the number of seats in such
Panchayats to be filled by election shall, so far
as practicable, be the same throughout the
State.
<br>
All the seats in a Panchayat shall be filled by
persons chosen by direct election from territorial
constituencies in the Panchayat area. For this
purpose each Panchayat area shall be divided
into territorial constituencies in such manner
that the ratio between the population of each
constituencies and the number of seats allotted
to it, so far as practicable, be the same throughout
the Panchayat area.
The legislature of a State may by law provide
for representation of following persons in
Panchayats :
<ol>
    <ul>
        <li>the Chairpersons (Chairman) of the
Panchayats at the village level, in the
Panchayats at the intermediate level or in
the case of a State not having Panchayats at
the intermediate level, in the Panchayats in
the district level;</li>
<li>the Chairpersons of the Panchayats at the
intermediate level, in the Panchayats at the
district level;</li>
<li>the members of the Lok Sabha and the
Legislative Assembly of the State
representing constituencies which comprise
wholly or partly a Panchayat area at the
level other than the village level, in such
Panchayats;</li>
<li>the members of the Rajya Sabha and
Legislative Council of the State where they
are registered as electors.</li>
    </ul>
</ol>

The Chairperson of a Panchayat and other
members of Panchayat whether or not chosen
by direct election from territorial constituencies
in the Panchayat area shall have the right to
vote in the meetings of Panchayat.
The Chairperson of a Panchayat at the
village level shall be elected in such a manner
as the legislature of a State may by law, provide.
The Chairperson of a Panchayat at the
intermediate level or district level shall be elected
by, and amongst, the elected members thereof.


<b>Disqualifications for Membership:</b> A
person shall be disqualified for being chosen as,
and for being a member of Panchayats:
<ol>
    <ul>
        <li>if he is so disqualified by or under any law
for the time being in force for the purposes
of elections to the legislature of the State
concerned;</li>
<li>if he is so disqualified by or under any law
made by the legislature of the State.
But no person shall be qualified on the
ground that he is less than 25 years of age,
under clause (a), if he has attained the age of
21 years.</li>
    </ul>
</ol> 

If any question arises as to whether a member
of a Panchayat has become subject to any of the
qualifications mentioned in clause (1) the
question shall be referred for the decision of
such authority and in such manner as the
legislature of a State may, by law, provide.


<b>Reservation of Seats in Panchayats:</b> Article
243D provides that in every Panchayats seats
shall be reserved for the Scheduled Castes and
Scheduled Tribes. The number of seats so
reserved shall be, as nearly as may be, in the
same proportion to the total number of seats to
be filled by direct election in that Panchayat as
the population of the SCs and STs in that
Panchayat area bears to the total population of
that area and such seats may be allotted by
rotation to different constituencies in a
Panchayat.
<br>
Out of total number of seats reserved under
clause (1) not less than 1/3 seats shall be
reserved from women belonging to the SCs and
STs. [Clause (2)] Out of total number of seats to
be filled by direct election in every Panchayat
not less than 1/3 (including the number of seats
reserved for SC's and ST's women) seats shall
be reserved for women. Such seats may be
allotted by rotation to different constituents in
a Panchayat. [Clause (3)]
The office of the Chairpersons in the
Panchayats at the village or any other level shall
be reserved for SCs, STs, and women in such
manner as the legislature of a State may, by law,
provide. But the number of offices of
Chairpersons reserved for the SCs and STs in
the Panchayats at each level in any State shall
be, as nearly as possible, in the same proportion
to the total number of such offices in the
Panchayats at each level in proportion of the
total population of the SCs and STs in the State.
However, not less than 1/3 of the total number
of the offices of Chairperson in the Panchayat
at each level shall be reserved for women. The
number of offices reserved under this clause
shall be allotted by rotation to different
Panchayats at each level.
The reservation of seats under clauses (1)
and (2) and the reservation of offices of
Chairperson (other than the reservation for
women) under clause (4) shall cease to have
effect on the expiration of the period specified
in Article 334. [Clause (5)]
<div>
<b>Reservation for Backward Classes :</b> The
legislature of a State is empowered under clause
(6) to make provision or reservation of seats in
any Panchayat or office of Chairpersons in the
Panchayats at any level in favour of backward
classes of citizens.
</div>
<b>Duration of Panchayats :</b> According to
Article 243E every Panchayat, unless sooner
dissolved under any law for the time being in
force, shall continue for five years from the date
appointed for its first meeting. No amendment
of any law in force shall have effect of causing
dissolution of a Panchayat at any level which
is functioning before such amendment till the
expiration of its normal period of five years.
An election to constitute a Panchayat must
be completed :
<ol>
    <ul>
        <li>before the expiry of its duration;</li>
        <li>before the expiration of a period of six
months from the date of its dissolution.</li>
    </ul>
</ol>

But where the remainder of the period for
which the dissolved Panchayat would have
continued in less than six months, it shall not
be necessary to hold any election under this
clause for constituting the Panchayat for such
period.
<b>Powers, Authority and Responsibility of
Panchayats :</b> Article 243G, provides that subject
to the provisions of this Constitution the
legislature of a State may, by law, endow the
Panchayats with such powers and authority as
may be necessary to enable them to function as
an institution of self government. Such law may
contain provision for the devolution of powers
and responsibilities upon Panchayats subject to
such conditions as may be specified therein,
with respect to :
<ol>
    <ul>
        <li>the preparation of plans for economic
development and social justice;</li>
<li>the implementation of schemes for social
development and social justice as may be
entrusted to them including those in relation
to the matters listed in the Eleventh Schedule.</li>
    </ul>
</ol>

The matters listed in the Eleventh Schedule
are as follows:
<ol>
    <ul>
        <li>Agriculture, including agricultural extension.</li>
        <li>Land improvement, implementation of
land reforms, land consolidation and soil
