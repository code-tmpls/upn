</ol>

<div class="subheading"><strong><i>Duration of Municipalities:</i></strong></div>
<ol>
    <ul>
        <li>Every Municipality, unless sooner dissolved
under any law for the time being in force,
shall continue for five years from the date
appointed for its first meeting and no
longer:
Provided that a Municipality shall be given
a reasonable opportunity of being heard before
its dissolution.</li>
<li>No amendment of any law for the time
being in force shall have the effect of
causing dissolution of a Municipality at
any level, which is functioning immediately before such amendment, till the expiration of its duration specified in class
(1).</li>
<li>An election to constitute a Municipality
shall be completed:
<ol>
    <ul>
        <li>Before the expiry of its duration
specified in clause (1).</li>
<li>Before the expiration of a period of six
months from the date of its dissolution:</li>
    </ul>
</ol>
Provided that where the remainder of the
period for which the dissolved Municipality
would have continued is less than six months,
it shall not be necessary to hold any election
under this clause for constituting the
Municipality of such period.
</li>    
<li>A Municipality constituted upon the dissolution of a
     Municipality before the expiration of its duration shall continue only 
     for the remainder of the period for which
the dissolved Municipality would have
continued under clause (1) had it not been
so dissolved.
    </li>
    </ul>
</ol>

<div class="subheading"><strong><i>Disqualification for Membership:</i></strong></div>
<ol>
    <ul>
        <li>A person shall be disqualified for being
chosen as, and for being, a member of a
Municipality:
<ol>
    <ul>
        <li>If he is so disqualified by or under any
law for the time being in force for the
purposes of elections to the Legislature
of the State concerned:
Provided that no person shall be disqualified
on the ground that he is less than twenty-five
years of age, if he has attained the age of
twenty-one years;</li>
<li>If he is so disqualified by or under any
law made by the legislature of the
State.</li>
    </ul>
</ol>
</li>
<li>If any question arises as to whether a
member of a Municipality has become
subject to any of the disqualifications
mentioned in clauses (1), the question shall
be referred to for the decision of such
authority and in such manner as the
legislature of a State may, by law, provide.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Powers, Authority and Responsibilities of
Municipalities:</i></strong></div>

Subject to the provisions of this Constitution,
the legislature of a State may, by law, endow:
<ol>
    <ul>
        <li>The Municipalities with such powers and
authority as may be necessary to enable
them to function as institutions of selfgovernment and such law may contain
provisions for the devolution of powers and
responsibilities upon Municipalities, subject
to such conditions as may be specified therein,
with respect to:
<ol>
    <ul>
        <li>The preparation of plans for economic
development and social justice;</li>
<li>The performance of functions and the
implementation of schemes as may be
entrusted to them including those in
relation to the matters listed in the
Twelfth Schedule;</li>
    </ul>
</ol>
</li>
<li>The Committees with such powers and
authority as may be necessary to enable
them to carry out the responsibilities
conferred upon them including those in
relation to the matters listed in the Twelfth
Schedule.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Power to impose taxes by, and Funds of, the
Municipalities:</i></strong></div>

The Legislature of a state may, by law,
<ol>
    <ul>
        <li>Assign to a Municipality such taxes, duties,
tolls and fees in accordance with such
procedure and subject to such limits;</li>
<li>Assign to a Municipality such taxes, duties
tolls and fees levied and collected by the
State Governments for such purposes and
subject to such conditions and limit;</li>
<li>Provide for making such grants-in-aid to the
withdrawal for such moneys therefrom, as
may be specified in the law,</li>
<li>Provide for constitution of such funds for
crediting all moneys received, respectively,
by or on behalf of the Muniscipalities and
also for the withdrawal for such moneys
therefrom, as may be speciefied in the law.</li>
    </ul>
</ol> 

