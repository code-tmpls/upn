<h3>Panchayat
& Municipalities</h3>

<div class="subheading"><strong><i>Rural Local Bodies</i></strong></div>

The Constitution (73rd Amendment) Act,
1992 has added a new Part IX consisting of 16
Articles and the Eleventh Schedule to the
Constitution. The 73rd Amendment Act
envisages the Gram Sabha as the foundation of
the Panchayati Raj System to perform functions
and powers entrusted to it by the State
legislatures. The amendment provide for a three
tier Panchayati Raj System at the village,
intermediate and district levels. Small states
with population below twenty lakh have been
given the option not to constitute the Panchayats
at the intermediate level. The Act provides that
the Panchayat bodies will have an assured
duration of five years, with elections mandatory
after this period. However, one thing is to be
noted that under the Amendment Act the
establishment of Panchayats and the devolution
of necessary powers and authority on the
Panchayati Raj institutions are vested in the
State Governments. In view of this it may be
said that the success of the Panchayati Raj
institutions as a unit of democracy and thereby
ushering an all around development of rural
areas will much depend on the intention and 
support of the State Governments. Without
honest intention, these institutions would be
misused by rural rich and poor and illiterate
masses will remain a mute supporters as it is
happening in Parliamentary and State Assembly
elections in the Country.
<br>
<b>The salient features of the Seventy-Third
Constitutional Amendment Act may be
summarised as follows:</b>

<b>Gram Sabha:</b> Article 243A provides that the
Gram Sabha may exercise such powers and
perform such functions at the village level as the
legislature of a State may by law provide. The
73rd amendment thus envisages the Gram Sabha
as the foundation of Panchayati Raj System.
'Gram Sabha' means a body consisting of persons
registered in the electoral rolls relating to village
comprised within the area of Panchayat at the
village level.
<br>
<b>Constitution of Panchayats:</b> Article 243B
visualises a three-tier Panchayati Raj System. It
provides that in every State there shall be
constituted Panchayats at the village,
intermediate and district levels. Small states
having a population not exceeding twenty lakhs
have been given an option not to constitute the
Panchayats at the intermediate level.
<br>
<b>Composition of Panchayats:</b> Article 243C
provides that, subject to the provisions of this
part the legislature of a State may by law make
provisions with respect to the composition of
Panchayats. However, the ratio between the
population of the territorial area of a Panchayat
at any level and the number of seats in such
Panchayats to be filled by election shall, so far
as practicable, be the same throughout the
State.
<br>
All the seats in a Panchayat shall be filled by
persons chosen by direct election from territorial
constituencies in the Panchayat area. For this
purpose each Panchayat area shall be divided
into territorial constituencies in such manner
that the ratio between the population of each
constituencies and the number of seats allotted
to it, so far as practicable, be the same throughout
the Panchayat area.
The legislature of a State may by law provide
for representation of following persons in
Panchayats :
<ol>
    <ul>
        <li>the Chairpersons (Chairman) of the
Panchayats at the village level, in the
Panchayats at the intermediate level or in
the case of a State not having Panchayats at
the intermediate level, in the Panchayats in
the district level;</li>
<li>the Chairpersons of the Panchayats at the
intermediate level, in the Panchayats at the
district level;</li>
<li>the members of the Lok Sabha and the
Legislative Assembly of the State
representing constituencies which comprise
wholly or partly a Panchayat area at the
level other than the village level, in such
Panchayats;</li>
<li>the members of the Rajya Sabha and
Legislative Council of the State where they
are registered as electors.</li>
    </ul>
</ol>

The Chairperson of a Panchayat and other
members of Panchayat whether or not chosen
by direct election from territorial constituencies
in the Panchayat area shall have the right to
vote in the meetings of Panchayat.
The Chairperson of a Panchayat at the
village level shall be elected in such a manner
as the legislature of a State may by law, provide.
The Chairperson of a Panchayat at the
intermediate level or district level shall be elected
by, and amongst, the elected members thereof.


<b>Disqualifications for Membership:</b> A
person shall be disqualified for being chosen as,
and for being a member of Panchayats:
<ol>
    <ul>
        <li>if he is so disqualified by or under any law
for the time being in force for the purposes
of elections to the legislature of the State
concerned;</li>
<li>if he is so disqualified by or under any law
made by the legislature of the State.
But no person shall be qualified on the
ground that he is less than 25 years of age,
under clause (a), if he has attained the age of
21 years.</li>
    </ul>
</ol> 

If any question arises as to whether a member
of a Panchayat has become subject to any of the
qualifications mentioned in clause (1) the
question shall be referred for the decision of
such authority and in such manner as the
legislature of a State may, by law, provide.


<b>Reservation of Seats in Panchayats:</b> Article
243D provides that in every Panchayats seats
shall be reserved for the Scheduled Castes and
Scheduled Tribes. The number of seats so
reserved shall be, as nearly as may be, in the
same proportion to the total number of seats to
be filled by direct election in that Panchayat as
the population of the SCs and STs in that
Panchayat area bears to the total population of
that area and such seats may be allotted by
rotation to different constituencies in a
Panchayat.
<br>
Out of total number of seats reserved under
clause (1) not less than 1/3 seats shall be
reserved from women belonging to the SCs and
STs. [Clause (2)] Out of total number of seats to
be filled by direct election in every Panchayat
not less than 1/3 (including the number of seats
reserved for SC's and ST's women) seats shall
be reserved for women. Such seats may be
allotted by rotation to different constituents in
a Panchayat. [Clause (3)]
The office of the Chairpersons in the
Panchayats at the village or any other level shall
be reserved for SCs, STs, and women in such
manner as the legislature of a State may, by law,
provide. But the number of offices of
Chairpersons reserved for the SCs and STs in
the Panchayats at each level in any State shall
be, as nearly as possible, in the same proportion
to the total number of such offices in the
Panchayats at each level in proportion of the
total population of the SCs and STs in the State.
However, not less than 1/3 of the total number
of the offices of Chairperson in the Panchayat
at each level shall be reserved for women. The
number of offices reserved under this clause
shall be allotted by rotation to different
Panchayats at each level.
The reservation of seats under clauses (1)
and (2) and the reservation of offices of
Chairperson (other than the reservation for
women) under clause (4) shall cease to have
effect on the expiration of the period specified
in Article 334. [Clause (5)]
<div>
<b>Reservation for Backward Classes :</b> The
legislature of a State is empowered under clause
(6) to make provision or reservation of seats in
any Panchayat or office of Chairpersons in the
Panchayats at any level in favour of backward
classes of citizens.
</div>
<b>Duration of Panchayats :</b> According to
Article 243E every Panchayat, unless sooner
dissolved under any law for the time being in
force, shall continue for five years from the date
appointed for its first meeting. No amendment
of any law in force shall have effect of causing
dissolution of a Panchayat at any level which
is functioning before such amendment till the
expiration of its normal period of five years.
An election to constitute a Panchayat must
be completed :
<ol>
    <ul>
        <li>before the expiry of its duration;</li>
        <li>before the expiration of a period of six
months from the date of its dissolution.</li>
    </ul>
</ol>

But where the remainder of the period for
which the dissolved Panchayat would have
continued in less than six months, it shall not
be necessary to hold any election under this
clause for constituting the Panchayat for such
period.
<b>Powers, Authority and Responsibility of
Panchayats :</b> Article 243G, provides that subject
to the provisions of this Constitution the
legislature of a State may, by law, endow the
Panchayats with such powers and authority as
may be necessary to enable them to function as
an institution of self government. Such law may
contain provision for the devolution of powers
and responsibilities upon Panchayats subject to
such conditions as may be specified therein,
with respect to :
<ol>
    <ul>
        <li>the preparation of plans for economic
development and social justice;</li>
<li>the implementation of schemes for social
development and social justice as may be
entrusted to them including those in relation
to the matters listed in the Eleventh Schedule.</li>
    </ul>
</ol>

The matters listed in the Eleventh Schedule
are as follows:
<ol>
    <ul>
        <li>Agriculture, including agricultural extension.</li>
        <li>Land improvement, implementation of
land reforms, land consolidation and soil
conservation.</li>
<li>Minor irrigation, water management and
watershed development.</li>
<li>Animal husbandry, dairying and poultry.</li>
<li>Fisheries.</li>
<li>Social forestry and farm forestry.</li>
<li>Minor forest produce.</li>
<li>Small-scale industries, including food processing industries.</li>
<li>Khadi, village and cottage industries.</li>
<li>Rural housing.</li>
<li>Drinking Water.</li>   
<li>Fuel and fodder.</li>
<li>Roads, culverts, bridges, ferries, waterways and other means of communication.</li>
<li>Rural electrification, including distribution of electricity.</li>
<li>Non-conventional energy sources.</li>
<li>Poverty alleviation programme.</li>
<li>Education, including primary and secondary schools.</li>
<li>Technical training and vocational education.</li>
<li>Audit and non-formal education.</li>
<li>Libraries.</li>
<li>Cultural activities.</li>
<li>Markets and fairs.</li>
<li>Health and sanitation, including hospitals,
primary health centres and dispensaries.</li>
<li>Family welfare.</li>
<li>Women and child development.</li>
<li>Social welfare, including welfare of the
handicapped and mentally retarded.</li>
<li>Welfare of the weaker sections, and in
particular, of the Scheduled Castes and
the Scheduled Tribes.</li>
<li>Public distribution system.</li>
<li>Maintenance of community assets.</li>
</ul>
</ol>

It is to be noted that Article 243G is subject
to the provisions of the Constitution. This means
that the normal distribution of powers under
Article 245 and 246 cannot be effected by the
State legislature while vesting with powers and
authorities upon the Panchayats.
<br>
<b>Powers to Impose Taxes and Funds of
Panchayats :</b> Article 243H empowers a State
legislature to make by law provision for imposing
taxes etc. by the Panchayats, such a law :
<ol>
    <ul>
        <li>authorize a Panchayat to levy, collect and
appropriate such taxes, duties, tolls and fees
in accordance with such procedure and
subject to such limits;</li>
<li>assign to a Panchayat such taxes, duties,
tolls and fees levied and collected by the
State Government for such purposes and
subject to such conditions and limits;</li>
<li>provide for making such grants-in-aid to the
Panchayats from the Consolidated Fund for
the State; and</li>
<li>provide for constitution of such funds for
crediting all moneys received, by or on behalf
of the Panchayats and also for the
withdrawal of such money therefrom.</li>
    </ul>
</ol>

<b>Finance Commission :</b> Article 243-I provides
for the establishment of a Finance Commission
for reviewing financial position of the
Panchayats. The Governor of a State shall within
one year from the commencement of the
Constitution (73rd Amendment) Act, 1992 and
thereafter at the expiration of every fifth year,
constitute a Finance Commission. The legislature
of the State may by law, provide for the
composition of the Commission, the qualifications
requisite for appointment of its members and
the manner in which they shall be selected.
It shall be the duty of the Finance Commission
to review the financial position of the Panchayats
and to make recommendations to the Governor
as to:
<ol>
    <ul>
        <li>the principles which should govern :
            <ol>
                <ul>
                    <li>the distribution between the State and the
Panchayats of the net proceeds of the
taxes, duties, tolls and fees leviable by the
State, which may be divided between
them under this Part and the allocation
between the Panchayats at all levels of
their respective shares of such proceeds;</li>
<li>the determination of the taxes, duties, tolls
and fees which may assigned to, or appropriated by, the Panchayats;</li>
<li>the grants-in-aid to the Panchayats from
the Consolidated Fund of the State;</li>
                </ul>
            </ol>
        </li>
        <li>the measures needed to improve the financial
position of the Panchayats;</li>
<li>any other matter referred to the Finance
Commission by the Governor in the interests
of sound finance of the Panchayats.</li>
    </ul>
</ol>

The commission shall determine its procedure
and shall have such powers in the performance
of its functions as the State legislature may, by
law, confer on it.

The Governor shall cause every
recommendation made by the Commission
together with an explanatory memorandum as
to the action taken thereon to be laid before the
legislature of the State.
“The Governor shall cause every
recommendation made by the Commission
together with an explanatory memorandum as
to the action taken thereon to be laid before the
legislature of the State
“The State Finance Commission under
Panchayatri Raj Law is designed to ensure
regional balance in the distribution of State and
Central Funds.”
<div>
<b>Audit of Accounts of Panchayats :</b> The
legislature of a State may, by law, make provision
with respect to the maintenance of accounts by
the Panchayats and the auditing of such
accounts.
</div>
<b>Elections to the Panchayats :</b> Under Article
243K the superintendence, direction and control
of the preparation of electoral rolls and conduct
of all elections to the Panchayats shall be vested
in a State Election Commission consisting of the
State Election Commissioner to be appointed by
the Governor. Subject to the provisions of any
law made by the State legislature, the conditions
of service and tenure of office of the State
Election Commissioner shall be such as the
Governor may by rule determine. The State
Election Commissioner shall not be removed
from his office except in like manner and on like
grounds as a Judge of a High Court. The
conditions of service of the State Election
Commissioner shall not be varied to his
disadvantage after his appointment.
The Governor of State shall, when so
requested by the State Election Commissioner,
make available to Commission such staff as may
be necessary for the discharge of its functions.
The State legislature may, subject to the
provisions of this Constitution, by law, make
provision with respect to all matters relating to
or in connection with elections to the Panchayats.
<br>
<b>Part not to apply to Certain Areas :</b> Article
243M provides that Part 9 shall not apply to the
following areas :
<ol>
    <ul>
        <li>the scheduled areas referred to in clause
(1) and tribal areas referred to in clause (2)
of Article 244;</li>
<li>the State of Nagaland, Meghalaya and
Mizoram;</li>
<li>the hill areas in the State of Manipur for
which District Councils exist under any
law for the time being in force;</li>
<li>to Panchayats at the district level of the hill
areas of the District of Darjeeling in the
State of West Bengal for which Darjeeling
Gorkha Hill Council exists under any law
for the time being in force;</li>
<li>shall affect the functions and powers of
the Darjeeling Gorkha Hill Council constituted under such law.
</li>
    </ul>
</ol>
Notwithstanding anything in this
Constitution:
<ol>
    <ul>
        <li>The State Legislature of Nagaland,
Meghalaya and Mizoram may, by law,
extend this Part to that State, except the
areas referred to in clause (1) if the Legislative
Assembly of that State passes a resolution to
that effect by a majority of the total
membership of that House and by a majority
of not less than two-third of the members of
that House present and voting;</li>
<li>Parliament may, by law, extend the provisions
of Part 9 to the Scheduled areas and Tribal
areas referred to in clause (1) subject to such
exceptions and modifications as may be
specified in such law, such law shall not be
deemed to be amendment of this Constitution
for the purposes of Article 368.</li>
    </ul>
</ol>


<b>Continuance of Existing Laws and
Panchayats :</b> Article 243N provides that not
withstanding anything in Part 9 or any provision
of any law relating to Panchayats in force in a
State immediately before the commencement of
the Constitution (73rd Amendment) Act, 1992,
which is inconsistent with the provisions of Part
9, shall continue to be in force until amended
or repealed by a competent legislature or other
competent authority or until the expiration of
one year from such commencement, whichever
is earlier.
However, all the Panchayats existing
immediately before such Government shall

continue till the expiration of their duration,
unless sooner dissolved by resolution passed by
the Legislative Assembly of that State or in a
State having a Legislative Council, by each
House of the Legislature of the State.
<br>
<b>Courts not to interfere in Electoral Matters:</b>

Article 243-O bars the interference by courts in
electoral matters of Panchayats. It provides that
notwithstanding anything in this Constitution
the validity of any law relating to the delimitation
of constituencies or the allotment of seats to
such constituencies made under Article 243K
shall not be called in question in any court.
The validity of an election to any Panchayat
can be challenged only through an election
petition presented to such authority and in such
manner as provided by any law made by the
legislature of a State.

<div class="subheading"><strong><i>Financial Powers of Panchayati Raj
Institutions</i></strong></div>

Article 243-G of the Constitution of India—
introduced by the 73rd Amendment—deals with
the question of powers, authority and
responsibilities of Panchayats. It reads thus :
“Subject to the provisions of the Constitution,
the legislature of the State may, by law, endow
the Panchayats with such powers and authority
as may be necessary to enable them to function
as institutions of self-government and such law
may contain provisions for the devolution of
powers and responsibilities upon Panchayats at
the appropriate level, subject to such conditions
as may be specified therein, with respect to—(a)
the preparation of plans for economic
development and social justice; and (b) the
implementation of such schemes for economic
development and social justice as may be
entrusted to them including those in relation to
the matters listed in the Eleventh Schedule.”
While Article 243-G of the Constitution
visualises Panchayats as institutions for selfgovernment, it subjects the extent of devolution
of powers and functions to the will of the State
Legislatures.
As per Article 243-H of the Constitution,
State Legislatures have been empowered to
enact laws;
<ol>
    <ul>
        <li>to authorise a Panchayat to levy, collect and
appropriate some taxes, duties, tolls and
fees;</li>
<li>to assign to the Panchayat, some taxes,
duties, tolls levied and collected by the State
Government;</li>
<li>to provide for making grants-in-aid to the
Panchayats from the Consolidated Fund of
the State; and</li>
<li>to provide for constitution of such funds for
Panchayats for crediting all money received
by or on behalf of Panchayats and also the
withdrawal of such money therefrom.</li>
    </ul>
</ol> 

Article 243-I of the Constitution provides for
constitution of a State Finance Commission
(SFC) to review the financial position of
Panchayats and to make recommendations to
the Governor regarding the principles governing
the major issues mentioned in Article 243-H.

<div class="subheading"><strong><i>Urban Local Bodies</i></strong></div>

The Narsimha Rao Government introduced
a Constitution Amendment Bill pertaining to
urban local bodies in the Lok Sabha on September
16, 1991. With a few modifications, it was
essentially based on the 65th Amendment Bill
brought by the then Prime Minister, Rajiv Gandhi
and which was defeated in the Rajya Sabha in
October 1989. The Bill was passed by both the
Houses in December 1992. It received the assent
of the President on April 20, 1993 and was
published in the gazette on the same day as the
Constitution 74th Amendment Act, 1992.
Part IX-A has been added to the constitution.
This amendment has added 18 new articles and
a new twelfth schedule relating to urban local
bodies in the constitution.
The objectives of 74th Constitution
Amendment Act include decentralization of
power and ensuring popular participation in
planning management and delivery of civil
services. It introduces some fundamental changes
in the system of municipal governance. Duration
of elected municipal governments, participation
of women and weaker sections in urban affairs
and enabling framework for efficient delivery of
public services were regarded as appropriate
subject covered by constitutional provisions
<div>
<b>Accordingly, the 74th Constitutional
Amendment Act provided for:</b>
</div>
Definitions:
In this Part, unless the context otherwise
requires,
<ol>
    <ul>
        <li>“Committee” means a Committee constituted
under article 243S;</li>
<li>“District” means a district in a State;</li>
<li>“Metropolitian area” means an area having
a population of ten lakhs or more, comprised
in one or more districts and consisting of two
or more Municipalities or Panchayats or
other contiguous areas, specified by the
Governor by public notification to be a
Metropolitan area for the purposes of this
Part;</li>
<li>“Municipal area” means the territorial area
of a Municipality as is notified by the
Governor;</li>
<li>“Municipality” means an institution of selfgovernment constituted under article 243Q;</li>
<li>“Panchayat” means a Panchayat constituted
article 243B;</li>
<li>“Population” means the population as
ascertained at the last preceding census of
which the relevant figures have been
published.</li>
    </ul>
</ol>

<div>
<b>Constitution of Municipalities:</b>
</div>
There shall be constituted in every State:
<ol>
    <ul>
        <li>A Nagar Panchayat (by whatever
name called) for a transitional area,
that is to say, an area in transition
from a rural area to an urban area;</li>
<li>A Municipal Council for a smaller
urban area; and</li>
<li>A Municipal Corporation for a larger
urban area.</li>
    </ul>
</ol>

In accordance with the provisions of this
Part:
Provided that a Municipality under this
clause may not be constituted in such urban
area or part thereof as the Governor may,
having regard to the population of the area, the
density of the population therein, the revenue
generated for local administration, the
percentage of employment in non-agricultural
activities, the economic importance or such
other factors as he may deem fit, specify by
public notification for the purpose of this Part.
Composition of Municipalities:
<ol>
    <ul>
        <li>Save as provided in clause (2), all the seats
in Municipality shall be filled by persons
chosen by direct election from the territorial constituencies in the Municipal area
and for this purpose each Municipal area
shall be divided into territorial constituencies to be known as wards.</li>
<li>The Legislature of a State may, by law,
provide:
<ol>
    <ul>
        <li>For the representation in a
Municipality of:</li>
<ol>
    <ul>
        <li>Persons having special knowledge
or experience in Municipal
administration;</li>
<li>The members of the House of the
People and the members of the
Legislative Assembly of the State
representing constituencies which
comprise wholly or partly the
Municipal area;</li>
<li>The members of the Council of
States and the members of the
legislative Council of the State
registered as electors within the
Municipal area;</li>
<li>The Chairpersons of the
Committees constituted under
clause (5) of article 243S:</li>
</ul>
</ol>
Provided that the persons referred to in
paragraph (i) shall not have the right to vote in
the meetings of the Municipality;
(b) The manner of election of the Chairperson
of a Municipality.

    </ul>
</ol>
</li>
    </ul>
</ol>
<div>
<b>Constitution and Composition of Wards
Committees:</b>
<ol>
    <ul>
        <li>There shall be constituted Wards Committees, consisting of one or more wards,
within the territorial area of a Municipality having a population of three lakh or
more.</li>
<li>The Legislature of a State may, by law,
make provision with respect to:
<ol>
