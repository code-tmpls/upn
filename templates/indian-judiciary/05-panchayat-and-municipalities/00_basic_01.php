reference to the Administrator of the Union
territory appointed under Article 239 and
references to the Legislature or the Legislature
Assembly of a State were references in relation
to a Union territory having a Legislative
Assembly, to that Legislative Assembly.
Provided that the President may, by public
notification, direct that the provisions of this
Part apply to any Union territory or part thereof
subject to such exception and modification as he
may specify in the notification.
Part not to apply to certain areas:
<ol>
    <ul>
        <li>Nothing in this Part shall apply to the
Scheduled Areas referred to in clause (1),
and the tribal areas referred to in clause (2), of Article 244.</li>
<li>Nothing in this Part shall be construed to
affect the function and powers of the
Darjeeling Gorkha Hill Council constituted under any law for the time being in
force for the hill areas of the district of
