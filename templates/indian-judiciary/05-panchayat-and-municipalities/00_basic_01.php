<div class="subheading"><strong><i>Continuance of existing laws and Municipalities:</i></strong></div>

Notwithstanding anything in this Part, any
provision of any law relating to Municipalities
in force in a State immediately before the
commencement of the Constitution (Seventyfourth Amendment) Act, 1992, which is
inconsistent with the provisions of this Part,
shall continue to be in force until amended or
repealed by a competent Legislature or other
competent authority or until the expiration of
one year from such commencement whichever
is earlier:
Provided that all the Municipalities existing
immediately before such commencement shall
continue till the expiration of their duration,
unless sooner dissolved by a resolution passed
to that effect by the Legislative Assembly of that
State or, in the case of a State having a Legislative 
Council, by each House of the Legislature of
that State.
<br>
<b>Bar to interference by courts in electoral matters</b>
Notwithstanding anything in this
Constitution,
<ol>
    <ul>
        <li>The validity of any law relating to the
delimitation of constituencies or the allotment
of seats to such constituencies, made or
purporting to be made under Article 243ZA
shall not be called in question in any court;</li>
<li>No election to any Municipality shall be
called in question except by an election
petition presented to such authority and in
such manner as is provided for by or under
any law made by the legislature of a State.</li>    
</ul>
</ol>

Thus, the 74th Constitution Amendment
Act] 1992 aims at constitutional guarantees to
safeguard the interests of urban local
governments to enable them function as effective
democratic and self-governing institutions at
grass-root level. It provides a constitutional
status to the structure and mandate of these
bodies. The Act also provides for public
participation through elected Municipalities,
Wards Committees and Planning Committees,
with the Committee members being mostly from
elected representatives.


