<h3>Official Language</h3>

Part XVII of the Constitution deals with the
official language in Articles 343 to 351. Its
provisions are divided into four headsâ€”Languages of the Union, Regional languages, Language of the judiciary and texts of laws and
Special directives.
Language of the Union
The Constitution contains the following
provisions in respect of the official language of
the Union.
<ol>
    <ul>
        <li>Hindi written in Devanagari script is to be
the official language of the Union. But, the
form of numerals to be used for the official
purposes of the Union has to be the international form of Indian numerals and not
the Devanagari form of numerals.</li>
<li>However, for a period of fifteen years from
the commencement of the Constitution
(i.e., from 1950 to 1965), the English language would continue to be used for all
the official purposes of the Union for
which it was being used before 1950.</li>
<li>Even after fifteen years, the Parliament
may provide for the continued use of
English language for the specified purposes.</li>
<li>At the end of years, and again at the end of
ten years, from the commencement of the
Constitution, the president should appoint a
commission to make recommendations with
regard to the progressive use of the Hindi
language, strictions on the use of the English
language and other related issues.</li>
<li>A committee of Parliament is to be constituted to examine the recommendations of
the commission and to report its views on
them to the president.</li>
    </ul>
</ol>

Accordingly, in 1955, the president appointed
in Official Language Commission under the
chairmanship of BG Kher. The commission
submitted its report to the President in 1965.
The report was examined by a committee of
Parliament constituted in 1957 under the chairmanship of Gobind Ballabh Pant. However,
another Official Language Commission (as envisaged by the Constitution) was not appointed
in 1960.
Subsequently, the Parliament enacted the
Official Language Act in 1963. The act provides
for the continued use to English (even after
1965), in addition to Hindi, for all official
purposes of the Union and also for the transaction of business in Parliament Notably, this act
enables the use of English indefinitely (without
any time-limit). Further, this act was amended
in 1967 to make the use to English, in addition
to Hindi, compulsory in certain cases.

<div class="subheading"><strong><i>Regional Languages</i></strong></div>

The Constitution does not specify the official language of different states. In this regard,
it makes the following provisions:
<ol>
    <ul>
        <li>The legislature of states may adopt any
one or more of languages in use in the state
or Hindi as the official language of that
state. Until that is done, English is to
continue as official language of that state.</li>
Under this provision, most of the states have
adopted the major regional language as their
official language. For example, Andhra Pradesh
has adopted Telugu, Kerala-Malayalam, AssamAssamese, West Bengal-Orissa-Oriya. The six
northern states of Himachal Pradesh Uttar
Pradesh, Madhya Pradesh, Bihar, Haryana and
Rajasthan have adopted Hindi. Gujarat and
Punjab have and adopted Hindi in addition to
Gujarat and Punjabi respectively. Goa has
adopted Konkani in addition to Marathi and
Gujarat. Jammu and Kashmir has adopted Urdu
(and not Kashmiri). On the other hand, certain
north-eastern States like Meghalaya, Arunachal
Pradesh and Nagaland have adopted English.
Notably, the choice of the state is not limited to
the languages enumerated in the Eighth Schedule of the Constitution.    
<li>For the time being, the official language of
the Union (i.e., English) would remain the
link language for communications between
the Union and the states or between the
Union and the states or between various
states. But, two or more states are free to
agree to use Hindi (instead of English) for
communication between themselves.
Rajasthan, Uttar Pradesh, Madhya
Pradesh and Bihar are some of the states
that have entered into such agreements.</li>
The Official Language Act (1963) lays down
that English should be used for purpose of
communication between the Union and the
non-Hindi states (that is, the states that have not
adopted Hindi as there official language). Further, where Hindi is used for communication
between a Hindi and a non-Hindi state, such
communication in Hindi be accompanied by an
English translation.
<li>When the President (on a demand being
made) is satisfied that a substantial proportion of the population of a state desire
the use of any language spoken by them
to be recognised by the state, then he may
direct that such language shall also be
officially recognised in that state. This
provision aims at protecting the linguistic
interests of minorities in the states.</li>
</ul>
</ol>

<div class="subheading"><strong><i>Language of the Judiciary and Texts of
Laws</i></strong></div>

The constitutional provisions dealing with
the language of the courts and legislation are as
follows:
<ol>
    <ul>
        <li>Until Parliament provides otherwise, the
following are to be in the English language
only:
<ol>
    <ul>
        <li>All proceedings in the Supreme Court
and in every high court.</li>
<li>The authoritative texts of all bills, acts,
ordinances, orders, rules regulations
and bye-laws at the Central and state
levels.</li>
    </ul>
</ol>
</li>
<li>However, the governor of a state, with the
previous consent of the president, can
authorities the use of Hind or any other
official language of the state, in the proceedings in the high court of the state, but
not with the respect to the judgments,
decrees and orders passed by it. In other
words, the judgments, decrees and orders
of the high court must continue to be in
English only (until Parliament otherwise
provides).</li>
<li>Similarly, a states legislature can prescribe
the use of any language (other than English) with respect to bills, acts, ordinances, orders, rules, regulations or byelaws,but a translation of the same in the
English language is to be published.
The Official Language Act of 1963 lays</li>
down that Hindi translation of acts, ordinances,
orders, regulations and bye-laws published.
Under the authority of the president are deemed
to be authoritative texts. Further, every bill
introduced in the Parliament is to be accompanied by a Hindi translation. Similarly, there is
to be a Hindi translation of state acts or ordinances in certain cases.
The act also enables the governor of state,
with the previous consent of the president, to
authorize the use of Hindi or any other official
language of the state for judgments, decrees and
orders passed by the high court of the state but
they should be accompanied by an English
translation. For example, Hindi is used in Uttar
Pradesh, Madhya Pradesh, Bihar and Rajasthan
for this purpose.
However, the Parliament has not made any
provision for the use of Hindi in the Supreme
Court. Hence, the Supreme Court hears only
those who petition or appeal in English. In 1971,
a petitioner insisted on arguing in Hindi a
habeas corpus petition in the Supreme Court.
But, the Court cancelled his petition on the
ground that the language of the Court was
English and allowing Hindi would be unconstitutional.
    </ul>
</ol>



<div class="subheading"><strong><i>Special Directives</i></strong></div>

The Constitution contain special directives
to protect the interests of linguistic minorities
and to promote the development of Hindi language. There are:

<div class="subheading"><strong><i>Protection of Linguistic Minorities</i></strong></div>

In this regard, the Constitution makes the
following provisions:
<ol>
    <ul>
        <li>Every aggrieved person has the right to
submit a representation for the redress of
any grievance to any to the languages
used in the Union or in the state, as the
casemay be. This means that a representation cannot be rejected on the ground
that it is not in the official language.</li>
<li>Every state and a local authority in the
state should provide adequate facilities for
instruction in the mother-tongue at the
primary stage of education to children
belonging to linguistic minority groups.
The president can issue necessary directions for this purpose.</li>
<li>The president should appoint a special
officer for linguistic minorities to investigate
all matters relating to the constitutional
safeguards for linguistic minorities and to
report to him. The president should place
all such report before the Parliament and
send to the state government concerned.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Development of Hindi Language</i></strong></div>

The Constitution imposes a duty upon the
Centre to promote the spread and development
of the Hindi language so that it may become the
lingua franca of the composite culture of India.
Further, the Centre is directed to secure the
enrichment of Hindi by assimilating the forms,
style and expressions used in hindustani and in
other languages specified in the Eighth Schedule and by drawing its vocabulary, primarily on
sanskrit and secondarily on other languages.
At present, the Eighth Schedule of the Constitution specifies 18 languages (originally 14
languages). These are Assamese, Bengali,
Gujarat, Hindi, Kannada, Kashmiri, Konkani,
Malayalam, Manipuri, Marathi, Nepali, Oriya,
Punjabi, Sanskrit, Sindhi, Tamil, Telugu and
Urdu. Sidhi was added by the 21st Amendment
Act of 1967 while Konkani, Manipuri and
Nepali were added by the 71st Amendment Act
of 1962.
In terms of the Constitution provisions, there
are two objective behind the specification of the
above regional languages in the Eighth Schedule:
<ol>
    <ul>
        <li>the members of these languages are to be
given representation in the Official Language
Commission; and</li>
<li>the forms, style and expressions of these
languages are to be used for the enrichment
of the Hindi language.</li>
    </ul>
</ol>


