        <li>To investigate and monitor all the matters
relating to the safeguards provided for the
SCs and STs under the Constitution of India
or under any other law and to evaluate the
working of such scapegoats.</li>
<li>To enquire into specific complaints with
respect to the deprivation of the rights and
the safeguards of the SCs and the STs.</li>
<li>To participate and advise on the planning
process of socio-economic development of
the Scheduled Castes and the Scheduled
Tribes and to evaluate the progress of their
development under the Union and any State.</li>
<li>To present to the President, annually and at
