<div class="subheading"><strong><i>Central Information Commission</i></strong></div>

The Right to Information (RTI) Act, 2005,
which came into force on 12 October 2005,
marked a new and higher level of evolution of
India's parliamentary democracy.
The Supreme Court has, in various judgements, held that the right to information is a
part of the fundamental right to freedom of
speech and expression under Article 19 (1) of
the Constitution, since the right cannot be
properly exercised if the people did not have the
right to information.
The clearest enunciation of the fundamental
right to information was seen in the Supreme
Court ruling in the State of U.P vs. Raj Narain
in which Justice K.K. Mathew said:
“The people of this country have a right to
know every public act, everything that is done
in a public way, by their public functionaries.
They are entitled to know the particulars of
every public transaction in all its bearing. The
right to know, which is derived from the concept of freedom of speech, though not absolute,
is a factor which should make one wary, when
secrecy is claimed for transactions which can,
at any rate, have no repercussion on public
security… “
Thus the Right to Information (RTI) Act,
2005 is designed to set up a practical regime for
citizens to access information available with
public authorities, in order to promote transparency and accountability in their working. This
Act provides for the constitution of the Central
Information Commission (CIC) to be responsible for the implementation of the Act.
Central Information Commission was constituted by the Central Government through a
Gazette Notification in October 2005. The
Commission includes 1 Chief Information Commissioner (CIC) and not more than 10 Information Commissioners (IC) who are appointed by
the President of India. Oath of Office is administered by the President of India according to the
form set out in the First Schedule.
CIC defines “information” as any material
in any form including records, documents,
memos, e-mails, opinions, advices, press releases, circulars, orders, logbooks, contracts,
reports, papers, samples, models, data material
held in any electronic form and information
relating to any private body which can be
accessed by a public authority under any other
law for the time being in force.

<div class="subheading"><strong><i>Powers and Functions of the Central Information
Commission</i></strong></div>
<ol>
    <ul>
        <li>The CIC is empowered to receive and inquire
into complaints from any person relating to
access to information under the control of
public authorities and to decide appeals
against the decisions of designated appellate
officers.</li>
<li>The Commission shall impose penalties on
erring Central Public Information Officers
and recommend disciplinary action against
those who have, without any reasonable
cause, denied access to information under
the provisions of the Act.</li>
<li>The quantum of penalty liable to be imposed
is Rs. 250 each day till the application is
received or information is furnished subject
to the total amount not exceeding Rs. 25,000.</li>
<li>The decision of the Commission on an appeal
is binding and is not subject to further
appeal in a court of law.</li>
<li>The Commission may make recommendations
to public authorities not conforming to the
provisions or the spirit of the Act, specifying
the steps which, in its opinion, they ought
to take for promoting such conformity.</li>
<li>The Commission may, during the inquiry
into any complaint, examine any record
under the control of the public authority,
and no such record may be withheld from
it on any grounds.</li>
<li>The Commission shall recommend to the
Government every year, reforms on any
“matter relevant for operationalising the
right to access information”.</li>
    </ul>
</ol>
