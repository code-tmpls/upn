The term Scheduled Tribes is defined in the
Constitution of India under Article 366(25) as
such tribes or tribal communities or parts of
groups within such tribes or tribal communities
as are deemed under Article 342 to be Scheduled Tribes for the purposes of this Constitution.
According to the Article 342(1), the President may, with respect to any State or Union
Territory, and where it is State, after consultation with the Governor thereof, notifies tribes or
tribal communities or parts thereof as Scheduled Tribes. This confers on the tribe or part of
it a Constitutional status invoking the safeguards provided for in the Constitution, to these
communities in their respective States/UTs.

Thus only those communities who have
been declared as such by the President through
an initial public notification will be considered
as Scheduled Tribes. Parliament may, by law,
include in or exclude from the list of Scheduled
Tribes, any tribe or tribal community or parts of
thereof. The list of Scheduled Tribes is Statespecific. In other words, a community declared
as Scheduled Tribe in one State need not be so
in another State.
The Commission presents an annual report
to the President. The President places all the
reports before the Parliament along with memorandum explaining the action taken on the
recommendations made by the Commission.
The memorandum also contains the reasons for
the non acceptance of any recommendation.

