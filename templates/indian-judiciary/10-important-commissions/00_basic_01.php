        <div class="subheading"><strong><i>National Commission for Backward
Classes</i></strong></div>
        
National Commission for Backward Classes
came into effect on the 2nd April, 1993. The Act
provides that the Commission shall consist of
five Members, comprising of a Chairperson who
is or has been a judge of the Supreme Court or
of a High Court; a social scientist; two persons,
who have special knowledge in matters relating
to backward classes; and a Member-Secretary,
who is or has been an officer of the Central
Government in the rank of a Secretary to the
Government of India.

<div class="subheading"><strong><i>Functions of the Commission:</i></strong></div>
<ol>
    <ul>
        <li>The Commission shall examine requests for
inclusion of any class of citizens as a
backward class in the lists and hear
complaints of over-inclusion or underinclusion of any backward class in such lists
and tender such advice to the Central
Government as it deems appropriate.</li>
<li>The advice of the Commission shall ordinarily
be binding upon the Central Government.</li>
    </ul>
</ol>

