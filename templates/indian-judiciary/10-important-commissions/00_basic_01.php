        <div class="subheading"><strong><i>National Commission on Scheduled
Tribes</i></strong></div>
        
The National Commission for Scheduled
Tribes was first formed by the Government of
India in 1978 as a Non-statutory Multi-Member
Commission. Initially, the Commission was set
up through a resolution for both the Schedule
Castes and Schedule Tribes. In the year 1987,
the Government of India re-structured the duties of the Commission by authorizing it to
advice on the Broad Policy Issues and Levels of
Development of SCs and STs. The National
Commission for Scheduled Tribes (NCST) was
established by amending Article 338 and inserting a new Article 338A in the Constitution
through the Constitution (89th Amendment)
Act, 2003. By this amendment, the erstwhile
National Commission for Scheduled Castes and
Scheduled Tribes was replaced by two separate
Commissions namely- (i) the National Commission for Scheduled Castes (NCSC), and (ii) the
National Commission for Scheduled Tribes
(NCST) in 2004.
Scheduled Tribes live in contiguous areas
unlike other communities. It is, therefore, much
simpler to have an area-approach for development activities and also regulatory provisions to
protect their interests. In order to protect the
interests of Scheduled Tribes with regard to
land alienation and other social factors, provisions of “Fifth Schedule” and "Sixth Schedule"
have been enshrined in the Constitution.

