<div class="subheading"><strong><i>National Human Rights Commission</i></strong></div>

The National Human Rights Commission is
a statutory (and not a constitutional) body. It
was established under a legislation enacted by
the Parliament, namely, the Protection, namely,
the Protection of Human Rights Act, 1993.
The commission is the watchdog of human
rights in relating to life, liberty, equality and
dignity of the individual guaranteed by the
Constitution or embodied in the international
covenants and enforceable by courts in India.
The specific objectives of the establishment
of the commission are :
<ol>
    <ul>
        <li>To strengthen the institutional arrangement
through which human rights Issues could be
addressed in their entirety in a more focussed
manner;</li>
<li>To look into allegations of excesses,
independently of the government, in a
manner that would underline the government's commitment to protect human
rights; and</li>
<li>To complement and strengthen the efforts
that have already been made in this direction.</li>

<div class="subheading"><strong><i>Composition of the commission</i></strong></div>

The commission is a multi-member body
consisting of a chairman should be a retired
chief justice of India, and other members should
be a serving or retired judge of the Supreme
Court, a serving or retired chief justice of a high
court and two persons havingj knowledge or
practical experience with respect to human
rights. In addition to these full-time members,
the commission also has three ex-officio members-the chairmen of the National Commission
for Minorities, the National Commission for Scs
and Sts, and the National Commission for
Women.
The chairman and members are appointed
by the president on the recommendations of a
six-members committee consisting of the prime
minister as its head, the Speaker of the Lok
Sabha, the Deputy Chairman of the Rajya
Sabha, leaders of the Opposition in both the
Houses of Parliament and the Central home
minister. Further, a sitting judge of the Supreme
Court or a sitting chief justice of a high court
can be appointed only after consulation with
the chief justice of India.
The chairman and members hold office for
a term of five years or until they attain the attain
the age of 70 years, whichever is earlier. After
