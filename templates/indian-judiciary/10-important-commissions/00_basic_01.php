deem fit, reports upon the working of those
safeguards;</li>
<li>To make in such reports or recommendations
as to the measures that should be taken by
the Union or any State for the effective
implementation of those the protection,
welfare and socio-economic development of
the Schedule Castes and the Schedule Tribes
