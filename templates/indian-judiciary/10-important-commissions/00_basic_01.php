        <div class="subheading"><strong><i>National commission for Scheduled
Castes</i></strong></div>
        
The National Commission for Scheduled
Castes, a Constitutional body monitors the safeguards provided for Scheduled Castes and also
reviews issues concerning their welfare.
The SCs constitute 16.23% of India's population spread all over the country, with 80% of
them living in the rural areas. They constitute
more than a fifth of the population of UP,
Punjab, Himachal Pradesh and West Bengal.
Punjab has the highest proportion of SCs to the
State population. More than half of the SC
population is concentrated in the five States of
Uttar Pradesh (35.1 million), West Bengal (18.4
million), Tamil Nadu (11.8 million), Andhra
Pradesh (12.3 million) and Bihar (13.0 million).
Due to their social disability and economic
backwardness, they were grossly handicapped
in getting reasonable share in elected offices,
Government jobs and educational institutions
and, therefore, it was considered necessary to
follow a policy of reservations in their favour to
ensure their equitable participation in governance.

<div class="subheading"><strong><i>Functions and duties of the commission are:</i></strong></div>
<ol>
    <ul>
        <li> To investigate and monitor all matters relating
to the safeguards provided for the Scheduled
Castes under this Constitution or under any
other law for the time being in force or under
any order of the Government and to evaluate
the working of such safeguard.</li>
<li>To inquire into specific complaints with
respect to the deprivation of rights and
safeguards of the Scheduled Castes;</li>
<li>To participate and advise on the planning
process of socio-economic development of
the Scheduled Castes and to evaluate the
progress of their development under
the Union and any State;</li>
<li>To present to the President, annually and at
such other times as the Commission may deem fit, reports upon the working of those
safeguards;</li>
<li>To make in such reports recommendations
as to the measures that should be taken by
the Union or any State for the effective
implementation of those safeguards and other
measures for the protection, welfare and
socio-economic development of the Scheduled
Castes; and</li>
<li>To discharge such other functions in relation
to the protection, welfare and development
and advancement of the Scheduled Castes
as the President may, subject to
the provisions of any law made by
Parliament, by rule specify.</li>
The Commission presents an annual report
to the President. The President places all the
reports before the Parliament along with memorandum explaining the action taken on the
recommendations made by the Commission.
The memorandum also contains the reasons for
the non acceptance of any recommendation.
