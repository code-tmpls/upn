<div class="subheading"><strong><i>Central Vigilance Commission</i></strong></div>

The Central Vigilance Commission (CVC) is
them agancy for preventing corruption in the
Central government. It was established in 1964
by an executive resolution of the Central government. Its establishment was recommended
by the Santhanam Committee on Prevention of
Corruption (1962-64).
Thus, originally the CVC was neither a
constitutional body nor a statutory body. Recently, in September 2003, the Parliament enacted a law conferring statutory status on the
CVC.

<div class="subheading"><strong><i>Composition</i></strong></div>

The CVC is a multi-member body consisting
of a Central Vigilance Commissioner (chairperson) and not more than two vigilance commissioner. They are appointed by the president by
warrant under his hand and seal on the recommendation of a three-member as its head, the
Union minister of home affairs and the Leader
of the Opposition in the Lok Sabha. They hold
office for a term of four years or until they attain
the age of sixty five years, whichever is earlier.
After their tenure, they are not eligible for
further empoyment under the Central or a state
government.
The president can remove the Central Vigilance Commissioner or any vigilance 
commissioner from the office under the following circumstances:
<ol>
    <ul>
        <li>If he is adjudged an insolvent; or</li>
        <li>If he has been convicted of an offence which
(in the opinion of the Central government )
involves a moral turpitdue; or</li>
<li>If he engages, during his term of office, in
any paid employment outside the duties
of his office; or</li>
<li>If he is (in the opinion of the president), unfit
to continue in office by reason of
infirmity of mind or body; or</li>
<li>If he has acquired such financial or other
interset as is likely to affect prejudicially
his official functions.</li>
    </ul>
</ol>

In addition to these, the president can also
remove the Central Vigilance Commissioner or
any vigilance commissioner on the ground of
proved misbehaviour or incapacity, however, in
these cases, the president has to refer the matter
to the Supreme Court for an enquiry. If the
Supreme Court, after the enquiry, upholds the
cause of removal and advises so, then the
president can remove him. He is deemed to be
guilty of misbehaviour, if he (a) is concerned or
interested in any contract or agreement made
by the Central government, or (b) participates
in any way in the profit of such contract or
agreement or in any benefit or emolument
arising thereform otherwise than as a member
and in common with the other members or an
incorporated company.
The salary, allowances and other conditions
of services of the Central Vigilance Commissioner are similar to those of the Chairman of
UPSC and that of the vigilance commissioner
are similar to those of a member of UPSC. But
they cannot be varied to his disadvantage after
his appointment.

