<h3>Tribunals</h3>

<div class="subheading"><strong><i>Central Administrative Tribunal</i></strong></div>

It has been established for adjudication of
disputes with respect to recruitment and conditions of service of persons appointed to public 


services and posts in connection with the affairs
of the Union or other local authorities within
the territory of India or under the control of
Government of India and for matters connected
therewith or incidental thereto. This was done
in pursuance of the 42nd amendment of the
Constitution of India by Articles 323A. In the
statement of objects and reasons on the introduction of the Administrative Tribunals Act,
1985, it was mentioned that the setting up of
such Administrative Tribunals exclusively would
go a long way in reducing the burden on the
various courts and reduce pendency and would
also provide to the persons covered by the
Administrative Tribunals a speedy and relatively cheap and effective remedy. In addition
to Central Government employees, the Government of India has notified 45 other organizations to bring them within the jurisdiction of the
Central Administrative Tribunal.
The provisions of the Administrative Tribunals Act, 1985 do not, however, apply to members of paramilitary forces, armed forces of the
Union, officers or employees of the Supreme
Court, or to persons appointed to the Secretariat
Staff of either House of Parliament or the
Secretariat staff of State/Union Territory Legislatures. Besides the Chairman, who has been a
sitting or retired Judge of a High Court, the
authorized strength of CAT consists of 16 ViceChairmen and 49 Members.

<div class="subheading"><strong><i>State Administrative Tribunals</i></strong></div>

The 42nd Amendment Act, 1976 also provided for the establishment of State Administrative Tribunals with an Act of Parliament to deal
with the cases pertaining to the conditions of
service of the State employees. SATs play the
same role at state level as played by CAT at the
Centre.

<div class="subheading"><strong><i>Water Disputes Tribunal</i></strong></div>

The Water Disputes Act, 1956 provides for
the reference of an inter-state dispute for arbitration by a Water Disputes Tribunal, whose award
would be final according to Art. 262 (2A).

