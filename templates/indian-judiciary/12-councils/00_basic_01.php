<h3>Councils</h3>

<div class="subheading"><strong><i>National Development Council</i></strong></div>

It is an extra-constitutional and legal body
which was formed in 1952. Constituted of the
Union Prime Minister and the Chief Ministers
of States, the functions of the Council are "to
strengthen and mobilize the efforts and resources of the nation in support of the plans; to
promote common economic policies in all vital
spheres and to ensure the balanced and rapid
development of all parts of the country" and in
particular,
<ol>
    <ul>
        <li>to review the working of the National Plan
from time to time; and</li>
<li>to recommend measures for the achievement of the aims and targets set out in the
National Plan.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Zonal Councils</i></strong></div>

Zonal Councils have been established by the
State Reorganization Act, 1956. They advise on
matters of common interest to each of the five
zones into which the territory of India has been
divided such as Northern, Southern, Eastern,
Western and Central. It is also an extra constitutional body. Each Zonal Council consists of
the Chief Minister and two other Ministers of
each of the State in the Zone and the Administrator in the case of the Union Territory. There
is also provision for holding joint meetings of
two or more zonal councils. The Union Home
Minister is the common chairman of all the
zonal councils.
The Zonal Councils discuss matters of common concern to the states and Union Territories
comprised in each Zone, such as, economic and
social planning, border disputes, inter-state transport, matters arising out of the reorganization
of States and the like, and give advice to the
Government of the State concerned as well as
the Government of India.
Now the whole of India is divided under six
zones and the states covered under each zone
are as follows :
<ol>
    <ul>
        <li><b>Eastern Zone :</b> Bihar, Jharkhand, Orissa,
West Bengal, Sikkim</li>
<li><b>Western Zone :</b> Gujarat, Maharashtra, Goa,
Daman, Diu & Dadra & Nagar Haveli.</li>
<li><b>Northern Zone :</b> Punjab, Haryana, Himachal
Pradesh, J & K, Rajasthan, Chandigarh and
Delhi.</li>
<li><b>Southern Zone :</b> Andhra Pradesh, Kerala,
Karnataka, Tamil Nadu and Pondicherry.</li>
<li><b>Central Zone :</b> U.P., Chhatisgarh,
Uttaranchal and M.P.</li>
<li><b>North Eastern Zone :</b>  Assam, Meghalaya,
Nagaland, Manipur, Tripura, Mizoram,
Arunachal Pradesh.</li>
    </ul>
</ol>

North-Eastern Council was set up under the
North-Eastern Council Act, 1971.

<div class="subheading"><strong><i>National Security Council</i></strong></div>

On August 24, 1990, the Prime Minister Mr.
V.P. Singh announced the formation of National Security Council to take comprehensive
and coordinated view of all matters relating to
the country's security. It is headed by the Prime
Minister and will include the Ministers of Defence, Finance, Home Affairs, External Affairs
etc.
Following are the subjects to be considered
by the Council :
<ol>
    <ul>
        <li>External threats scenario, strategic defence
policies.</li>
<li>Other security threats, especially those involving energy, space and high tech.</li>
<li>Internal security covering insurgency,
counter-terrorism & counter intelligence.</li>
<li>Patterns of alienation likely to emerge within
the country, esp. those with a social, communal or regional dimension.</li>
<li>Security implications of evolving trends in
the world economy on India's economic and
foreign policies.</li>
<li>External economic threats in areas such as
energy, commerce, food & finance.</li>
    </ul>
</ol>


