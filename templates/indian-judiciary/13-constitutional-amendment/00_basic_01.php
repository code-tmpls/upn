<h3>Constitutional Amendment</h3>

<div class="subheading"><strong><i>Procedure for amendment</i></strong></div>

The procedure for the amendment of the
Constitution as laid down in Article 368 is as
following:
<ol>
    <ul>
        <li>An amendment of the Constitution can be
initiated only by the introduction of a bill
for the purpose in either House of Parliament and not in the state legislature.</li>
<li>The bill can be introduced either by a
minister or by private member and does not
require prior permission of the president</li>
<li>The bill must be passed in each House by
a special majority, that is, a majority (that
is, more than 50%) of the total membership of the House and a majority of twothirds of the members of the House present
and voting.</li>
<li>Each House must pass the bill separately.
In case of a disagreement between the two
Houses, there is no provision for holding
a joint sitting of the two Houses for the
purpose of deliberation and passage of the
bill.</li>
<li>If the bill seeks to amend the federal
provisions of the Constitution, it must also
be ratified by the Constitution, it must also
be ratified by the legislatures of half of the
states by a simple majority, that is, a
majority of the members of the House
present and voting.</li>
<li>After duly passed by both the Houses of
Parliament and ratified by the state legislatures, 
where necessary, the bill is presented to the president for assent.</li>
<li>The president must give his assent to the
bill. He can neither withhold his assent to
the bill nor return the bill for reconsideration of the Parliament.</li>
<li>After the president's assent, the bill becomes an Act (i.e., a constitutional amendment act) and the Constitution stands
amended in accordance with the terms of
the Act.</li>
    </ul>
</ol>

