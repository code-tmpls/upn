<h3>Social and Cultural Awakening in India</h3>


<div class="subheading"><strong><i>Hindu Reform Movements and Main
Proponents</i></strong></div>

<ol>
    <ul>
        <li>

        <div class="subheading"><strong><i>Brahmo Samaj and Raja Ram Mohan Roy
(1772 - 1833)</i></strong></div>
        
In August 1828, Roy founded the Atmiya Sabha,
which was later renamed Brahma Samaj at Calcutta in
order to purify Hinduism and preach monotheism.
The Samaj under him was based on the twin pillars
of reason and the ancient Hindu scriptures (only the
Vedas and the Upanishads) and incorporated the best
teachings of the other religions as well.
Raja Ram Mohan Roy
<ol>
    <ul>
        <li>Born in 1772 in Radhanagar in Burdwan district
in West Bengal and died in Bristol in England. He
is considered as the first 'modern man' as he was
the pioneer of socio-religious and political reform
movements in modern India.</li>
        <li>He studied numerous languages - Persian, Arabic,
Sanskrit, English, French, Latin, Greek, Hebrew,
etc. inorder to study the various religious scriptures
in their original.</li>
        <li>He believed in monotheism i.e. doctrine of the unity
of God-head and opposed idol-worship. In 1803
he published a Persian treatise named 'Tuhfat-ulMuwahidin' or 'A Gift to Monotheists' wherein he
explains his concept of monotheism.</li>
        <li>He established the Atmiya Sabha in Calcutta in
1815 in order to propagate monotheism and fight
against the evil practices in Hinduism. Later in
1928 he established the Brahmo Samaj in Calcutta
to purify Hinduism and preach monotheism.</li>
        <li>He laid emphasis on human reason and rationality
to Christianity by publishing a book 'Percepts of
Jesus, the Guide to Peace and Happiness' in 1920
which embodied the moral and spiritual percepts
of Jesus without the narratives of the miracles.</li>
        <li>He defended Hinduism and its Vedanta philosophy,
as found in the Vedas and the Upanishads, from
the ignorant attacks of Christian missionaries.</li> 
        <li>He led a lifelong crusade against sati and finally
in 1829 he succeeded in persuading Lord William
Bentinck in abolishing it. He championed women's
rights like right of inheritance and property and
attacked polygamy and the degraded state of
widows.</li>
        <li>He fought for the introduction and spread of
modern education through the medium of English
and made Bengali the vehicle of intellectual
intercourse in Bengal.</li>
        <li>In his political ideas and reforms, he believed in
the unification of the divergent groups of Indian
society in order to bring about national
consciousness in India. He initiated public
agitation on political questions like the need for
reforms in the British administration, trade and
economic policies, etc.</li>
        <li>He also pioneered Indian journalism through the
Mirat-ul-Akbar inorder to educate the public on
current issues and to represent the public opinion
before the government. His journal was called
Samvad Kumudhi.
Brahmo Samaj after Roy - Debendranath Tagore (1817- 1905)</li>
        <li>Debendranath Tagore established the
Tattvabodhini Sabha (1839) at Calcutta to
propagate Raja Ram Mohan Roy's ideas. He
formally joined the Brahmo Samaj in 1843 and
reorganized it.</li>
        <li>Keshav Chandra Sen promoted the Samaj in 1857
and became the right hand man of Debendranath.
During this time problems emerged between the
older and the conservative section led by
Debendranath and the newer and progressive
section led by Sen over the issues of social reform
particularly the caste system and over the
relationship between Hinduism and Brahmoism
(while the latter stood for the complete abolition of
the caste system and maintained that Brahmoism is
different form Hinduism, the former group wanted
to retain caste system, though criticizing its rigidity
and asserted that Brahmoism is Hinduism).</li>
        <li>This led to the secession of Sen's group from the
parent body (which had come to be known as Adi
Brahmo Samaj) in 1865 and formation of a new
organization known as Brahmo Samaj of India by
it in 1866.</li>
        <li>Debendranath Tagore spread the message of
Brahmo Samaj in other parts of India including
Bombay and Madras by his tours and adopted a
much more radical and comprehensive scheme of
social reforms and infused bhakti into Brahmoism.</li>
        <li>Further he formed the Indian Reform Association
in 1870 and persuaded the British government to
enact the Native Marriage Act of 1872 (popularly
known as the Civil Marriage Act) which legalized
Brahmo marriages and fixed the minimum age for
the groom and the bride at 18 and 24 respectively.

<div class="subheading"><strong><i>Second Schism in Brahmoism</i></strong></div>
<ol>
    <ul>
        <li>The second schism in Brahmoism occurred in 1878
when a group of Sen’s followers under Ananda
Mohan Bose and Shivanatha Shastri left him and
formed the Sadharana Brahmo Samaj.</li>
        <li>The cause for this split was the question of
management of the samaj and the violation of the
Native Marriage Act by Sen himself (he gave his
daughter in marriage to the ruler of Cooch Behar
but neither of them had attained the marriageable
age under the Act).</li>
    </ul>
</ol>
</li>
    </ul>
</ol>
        </li>
        <li>

        <div class="subheading"><strong><i>Manav Dharam Sabha</i></strong></div>
<ol>
    <ul>
        <li>Durgaram Manchharam (1809-1878) was a leading
figure among the small group of educated Gujaratis
who in the 1830s became strong critics of
contemporary society.</li>
        <li>Other participants included Dadoba Panderung,
Dinmuni Shanar, Dalpatram Bhagubai and
Damodar Das. They founded the Manav Dharam
Sabha at Surat in 1844 and held open meetings
every Sunday.</li>
        <li>The sabha had only a short career as an active
organization as it began to shatter in 1846 when
Dadoba Panderung returned to Bombay and
ceased to function in 1852 when Durgaram
Manichharan left for Rajkot.</li>
        <li></li>
        <li></li>
    </ul>
</ol>        
        </li>
        <li>

        <div class="subheading"><strong><i>Paramahansa Mandli</i></strong></div>
       <ol>
        <ul>
            <li>The movement was closely linked to Manav
Dharam Sabha and to the leadership of Dadoba
Panderung. Dadoba outlined his doctrine or
principles in the Dharma Vivechan (1848) and it
denied the polytheism of popular Hinduism, the
caste system and the Brahmanical monopoly of
knowledge.</li>
            <li>It was radical socio-religious society that was
formed in 1849 and that met in secret.</li>
            <li>Ram Bal Krishna Jayakar became President of the
mandali. All members were required to pledge that
they would abandon caste restrictions and each
inmate had to take food and drink from prepared
by a member of a lower caste.</li>
            <li>The group came to an agreement on two major
principles: firstly, they would not attack any
religion and secondly, they rejected any religion
which claimed infallibility.</li>
            <li>Branches of the organization were established at
Poona, Ahmednagar and Ratnagiri.</li>
            <li>Its insistence on remaining a secret organization
illustrated an unwillingness to openly challenge
Hindu orthodox.</li>
        </ul>
       </ol>
        </li>
        <li>

        <div class="subheading"><strong><i>Prarthana Samaj</i></strong></div>
<ol>
    <ul>
        <li>The Brahmo ideas spread in Maharashtra where
Paramhansa Sabha was founded in 1849.</li>
        <li>In 1867 under the guidance of Keshab, the
Prarthana Samaj was established in Bombay by
Atmaram Pandurang.</li>
        <li>Apart from worship of one God, in western India
the main emphasis has been on social reform
works rather than faith.</li>
        <li>In the field of social reform the focus was on
disapproval of caste system, raising the age of
marriage for both boys and girls, widow remarriage
and women education.</li>
        <li>Prominent leaders of the Prarthana Samaj were
Mahadev Govind Ranade, R.G. Bhandarkar and
N.G. Chandravarkar.</li>
        <li>In Punjab, the Dayal Singh Trust sought to implent
Brahma ideas by the opening of Dayal Singh
college at Lahore in 1910.</li>
    </ul>
</ol>        
        </li>
        <li>

        <div class="subheading"><strong><i>Arya Samaj and Swami Dayanand araswati
(1824 - 1883)</i></strong></div>
<ol>
    <ul>
        <li>It was revivalist in form though not in content.</li>
        <li>It was founded by Swami Dayananda. He rejected
western ideas and sought to revive the ancient
religion of the Aryans.</li>
        <li>In 1875, Dayananda formally organised the first Arya
Samaj unit at Bombay. A few years later the headquarters
of the Arya Samaj was established at Lahore.</li>
        <li>He looked on the Vedas as India’s ‘rock of the
ages’, the true original seed of hinduism. His motto
was ‘go back to the Vedas’.</li>
        <li>He condemned idol worship and preached unity
of God head.</li>
        <li>His views were published in his work ‘Satyartha
Prakash’.</li>
        <li>He accepted the doctrine of Karma but rejected the
theory of niyati (destiny).</li>
        <li>He pleaded for widow remarriage and condemned
child marriages.</li>
        <li>Anglo-vedic school was established at Lahore in 1866.</li>
        <li>The orthodox opinion in the Arya samaj which
stands for the revival of vedic ideal in modern life
set up the gurukul pathsala at Hardwar in 1902
which was exclusively for boys.</li>
        <li>The Samaj started the Shuddhi movement to
convert non-Hindus to Hinduism.</li>
        <li>Lala Hans Raj, Pandit Guru Dutt and Lala Lajpat
Rai were prominent leaders of the other section
who stood for the spread of English education
and established a number of DAV schools and
colleges for both boys and girls.</li>
        <li>Dayanand’s political slogan was ‘India for the
Indians’.</li>
    </ul>
</ol>        
        </li>
        <li>

        <div class="subheading"><strong><i>Ramakrishna Paramhansa (1836 - 1886),
Ramakrishna Paramhansa and the Ramakrishna
Movement</i></strong></div>
        <ol>
            <ul>
                <li>Originally known as Godadhar Chattopadhay, he
was born in 1836 in Kamarpukur village in Hoogly
district in West Bengal.</li>
                <li>He was a priest at the Kali temple in Dakshineswar
near Calcutta and considered and emphasised that
Krishna, Hari, Ram, Christ, Allah are different
names of the same God.</li>
                <li>Unlike Arya samaj, Ram Krishna Mission
recognises the utility and value of single worship
in developing spiritual fervour and worship of the
Eternal Omnipotent God.</li>
                <li>Vivekanand emerged as the preacher of newHinduism.</li>
                <li>He attended the Parliament of Religions at Chicago
in 1893.</li>
                <li>The famous magazines of Ramkrishna Mission
were Prabudh Bharat and Udbodhan.</li>

            </ul>
        </ol>

        <div class="subheading"><strong><i>Ramakrishna Mission and Ramakrishna Math</i></strong></div>

<ol>
    <ul>
        <li>The former is a social service and charitable society
formed by Swami Vivekananda in 1897 at Belur,
with the objective of carrying on humanitarian
relief and social work through the establishment
of schools, colleges, orphanages, hospitals, etc.</li>
        <li>The latter is a religious order or trust founded by
Vivekananda in 1887 at Baranagar (Though Belur
has become the headquarters of both the Mission
and the Math since 1898) with the objective of
bringing in to existence a band of dedicated monks
who would propagate the teachings of
Ramakrishna Paramhansa (the universal message
of the Vedanta).</li>
        <li>Though legally two distinct entities, with separate
funds and finances, the Mission and the Math are
in practice a single body, with the members of the
Math forming the principal workers of the Mission
and the trustees of the Math forming the governing
body of the Mission.</li>
    </ul>
</ol>
        </li>
        <li>

        <div class="subheading"><strong><i>Swami Vivekananda (1863-1902)</i></strong></div>
<ol>
    <ul>
        <li>Originally known as Narendranath Datta, he was
born in 1863 in Calcutta.</li>
        <li>He first visited Ramakrishna in 1881 and made
frequent visits thereafter. He established a monastery
in Baranagar in 1887 after the death of his guru.</li>
        <li>He toured India extensively, attended the World
Parliament of Religions in 1893 at Chicago and
spoke at the Congress of History of Religions at
Paris in 1900.</li>
        <li>He published two papers: the monthly Prabudha
Bharata in English and the fortnightly Udbodhana
in Bengali.</li>
        <li>He popularized the teachings of his guru,
proclaimed the essential oneness of all religions
and held Vedanta as a fully rational system.</li>
    </ul>
</ol>        
        </li>
        <li>

        <div class="subheading"><strong><i>Theosophical Society</i></strong></div>
<ol>
    <ul>
        <li>Madam H.P. Blavatsky laid the foundation of the
movement in the Unites States in 1875. Later
Colonel M.S. Olcott joined her.</li>
        <li>In 1882 they shifted their headquarters to India at Adyar.</li>
        <li>The members of this society believe that a special
relationship can be established between a person’s
soul and God by contemplation, prayer, revolution.</li>
        <li>The society believes in re-incarnation, Karma and
draws from the philosophy of the upanishads and
Samkhya, yoga and vedanta schools of thought.</li>
        <li>The theosophical movement came to be allied with
Hindu renaissance.</li>
        <li>After the death of Olcott in 1907 Annie Besant was
elected as its President. She had joined the society
in 1889.</li>
        <li>The society under Besant concentrated on the revival
of Hinduism and its ancient ideas and inorder to
provide Hindu religious instruction\ she founded
the Central Hindu University at Varanasi in 1898
which was later developed into the Benaras Hindu
University by Madan Mohan Malaviya.</li>
    </ul>
</ol>        

<div class="subheading"><strong><i>Young Bengal Movement</i></strong></div>
<ol>
    <ul>
        <li>Its founder was Henry Vivian Derozio, who was
born in Calcutta in 1809 and who taught at the
Hindu college between 1826 and 1831. He died of
cholera in 1831.</li>
        <li>His followers were known as the Derozians and
their movement as the Young Bengal Movement.</li>
        <li>The movement attacked old traditions and
decadent customs, advocating women’s rights and
education and educating the public on the current
socio-economic and political questions through
press and public associations.</li>
        <li>They carried on public agitation on public
questions like freedom of the press, trial by jury
and protection of peasants, etc.</li>
    </ul>
</ol>
        </li>
        <li>

        <div class="subheading"><strong><i>Muslim Reform Movements</i></strong></div>
        

        <div class="subheading"><strong><i>Aligarh Movement</i></strong></div>
  <ol>
    <ul>
        <li>It was founded by Sir Syed Ahmad Khan for the
social and educational advancement of muslims
in India.</li>
        <li>Other prominent members of the movement were
Altaf Hussain Hali, Dr. Nazir Ahmed, Nawab
Mushin-ul-Mulk, Chirag Ali, etc.</li>
        <li>He established two madarasas at Muradapur and
Gazipur and in 1870 published Tahzib ul Akhalaq
and Asbad-i-Bhagvati.</li>
        <li>He advocated a rational approach towards religion,
rejected blind adherence to religious law and asked
for a reinterpretation of the Quran in the light of
reason to suit the new trends of the time.</li>
        <li>Sir Syed Ahmad Khan was in the judicial services
of the company at the time of rebellion of 1857 and
stood loyal to the government.</li>
        <li>He tried to reconcile his co-religionists to modern
scientific thoughts and to the British rule and urged
them to accept services under the government.</li>
        <li>He condemned the system of Piri and Muridi.</li>
        <li>He opened a modern school in Aligarh in 1875
which developed into the Mohammadan Anglo
Oriental College at Aligarh in 1877. The school
became the nucleus of the formation of the Aligarh
Muslim University in 1920.</li>
        <li>Theodore Beck was his associate and was the first
Principal. Beck founded the union Indian Patriotic
Association, Aligarh.</li>
        <li>In 1866 he founded the Muhhammadan
Educational Conference as a general forum for
spreading liberal ideas among the muslims.</li>
        <li>Unfortunately the movement in later stages became
anti-congress and anti-Hindu and pro-British due
to misconceived fears of Hindu domination.</li>
    </ul>
  </ol>
        </li>
        <li>

        <div class="subheading"><strong><i>Deoband Movement</i></strong></div>
        <ol>
            <ul>
                <li>The orthodox section among the Muslim Ulema
organised the Deoband movement which began
after the foundation of the Dar-ul-Ulum at Deoband
in 1866 by Maulana Hussain Ahmad and others
with the aim of resuscitating classical Islam and
improving the spiritual and moral conditions of
the muslims.</li>
                <li>It was a revivalist movement where objectives were
to propagate among the Muslims, the pure
teachings of the Quran and the Hadis and to keep
alive the spirit of Jihad against the foreign rulers.</li>
                <li>The ulema under the leadership of Mohammad
Qasim Wanotavi and Rashid Ahmad Gangohi
founded the school of Deoband in the Saharanpur
district of UP in 1866.</li>
                <li>The school curriculum shut out English education.</li>
                <li>Deoband school welcomed the formation of the
Indian National Congress in 1885.</li>
                <li>In 1888 Deoband ulema issued a religious decree
against Syed Ahmad Khan’s organisations.</li>
            </ul>
        </ol>
        </li>
        <li>

        <div class="subheading"><strong><i>Ahrar Movement</i></strong></div>
<ol>
    <ul>
        <li>It was a movement founded in 1910 under the
leadership of Maulana Muhammad Ali, Hakim
Ajmal Khan, Hasan Imam, Maulana Zafar Ali Khar
and Mazhar-ul-Haq in opposition to the loyalist
policies of the Aligarh movement.</li>
        <li>Moved by modern ideas of self-government its
members advocated active participation in the
nationalist movement.</li>
    </ul>
</ol>        
        </li>
        <li>

        <div class="subheading"><strong><i>Ahmadia Movement</i></strong></div>
<ol>
    <ul>
        <li>Also known as the Qadiani movement, it was
founded by Mirza Gulam Ahmad at Qadiani in
Punjab, towards the end of the 19th century with
the objective of reforming Islam and defending it
against the onslaught of Christian missionaries
and the Arya Samajists.</li>
        <li>It gave religious recognition to modern industrial and
technological progress and it became the most closely
knit and the best organized Muslim group in India.</li>
    </ul>
</ol>        

<div class="subheading"><strong><i>Parsi Reform Movements</i></strong></div>

<ol>
    <ul>
        <li>In 1746 the Indian Parsis got divided into two
groups, when a group of them decided to accept
the Iranian calendar and came to be known as the
‘Kadami’ (ancient section) as opposed to the
‘Shahanshahis’ (royalists), who retained the
calendar used in Gujarat. This major division of
the community lasted into the 20th century.</li>
        <li>A second division was created by the movement of
Parsis into Bombay where many of them became
wealthy as merchants, ship builders, commercial
brokers, etc. Besides the Parsi religion was
frequently targeted by the Christian missionaries.</li>
        <li>In this background, Naroji Furdunji edited in 1840s
the Fam-i-Famshid, a journal aimed at defending
the cause of Zoroastrianism. He also wrote a
number of pamphlets and published the book
Tarika Farthest in 1850. All these events led to the
formation of a socio-religious movement designed
to codify the Zoroastrian religion and reshape
Parsi social life.</li>
        <li>In 1851 a small group of educated Parsis formed
the Rahnumai Mazdayasnan Sabha (Parsi Reform
Society) with funds provided by K.N. Kama.
Furdunji Naoroji became its President and S.S.
Bengali its secretary.</li>
        <li>In 1850 Bengali started publishing a monthly
journal Jagat Mitra and the Jagat Premi in 1851.
The sabha’s journal Rast Goftar was the main
voice of the movement.</li>
        <li>The leaders criticized elaborate ceremonies at
betrothals, marriages and funerals and opposed
infant marriage and the use of astrology.</li>
        <li>But the activities of the sabha divided the Parsis
into two groups: those who advocated radical
change and those who wished only limited
altercations in rituals and customs, organized
under the Raherastnumi Mazdayasnan in
opposition to the radicals.</li>
    </ul>
</ol>
        </li>
        <li>
        <div class="subheading"><strong><i>Sikh Reform Movements</i></strong></div>
        <div class="subheading"><strong><i>Nirankaris</i></strong></div>
        
        <ol>
            <ul>
                <li>Baba Dayal Das (1783-1855) was the founder of
this movement of purification and return. In 1840s
he called for the return of Sikhism to its origin and
emphasized the worship of one God and nirankar
(formless).</li>
                <li>Such an approach meant a rejection of idolatry
and also prohibition of eating meat, drinking
liquor, lying, cheating, etc.</li>
                <li>It laid emphasis on Guru Nanak and on Sikhism
before the establishment of Khalsa by Guru Gobind
Sing at Anandpur and this separated them from
the Namdaris.  </li>
            </ul>
        </ol> 
        </li>
        <li>

        <div class="subheading"><strong><i>Namdharis</i></strong></div>
<ol>
    <ul>
        <li>Founded by Baba Ram Singh (1816-1885) in 1857,
who in 1841 became a disciple of Balak Singh of
the Kuka movement.</li>
        <li>The movement was founded on a set of rituals
modeled after Guru Gobind Singh’s founding of
the Khalsa with the requirement of wearing the
five symbols but instead of the sword the followers
were supposed to carry a stick.</li>
        <li>The movement required the followers to abandon
the worship of gods, idols, tombs, trees, snakes,
etc. and abstain from drinking, stealing, falsehood,
slandering, backbiting, etc.</li>
        <li>Further the consumption of beef was strictly
forbidden as protection of cattle was important.</li>
    </ul>
</ol>
        </li>
        <li>

        <div class="subheading"><strong><i>Singh Sabhas</i></strong></div>
<ol>
    <ul>
        <li>Shaken by Namdhari unrest, the speeches of
Shraddha Ram of Arya Samaj and by the Christian
conversions a small group of prominent Sikhs
decided to form the Singh Sabha of Amritsar which
held its first meeting on 1st October, 1873 with
Thakur Singh Sandwhawalia as its President.</li>
        <li>Soon it was rivaled by a new organization, the
Lahore Singh Sabha which held its first meeting
on 2nd November, 1879 led by Prof. Gurmukh
Singh and Bhai Ditt Singh.</li>
    </ul>
</ol>        
        </li>
        <li>
        <div class="subheading"><strong><i>Akali Movement</i></strong></div>
<ol>
    <ul>
        <li>The main aim of the Akali movement of 1920s was
to purify the management of the Sikh Gurudwaras
or shrines by removing the corrupt or selfish
Mahants or priests.</li>
        <li>The movement led to the enactment of the new
Sikh Gurudwara Act by the British in 1925 and
removed corrupt priests through the act and also
through the Shiromani Gurudwara Prabhandhak
Committee (SPGC).</li>
    </ul>
</ol>
<div class="subheading"><strong><i>Other Reformers and Social workers</i></strong></div>        

<ol>
    <ul>
        <li>In western India Prof D.K. Karve took up the cause
of widow remarriage and in Madras
Veerasalingam Pantulu made Herculean efforts in
the same direction. Prof. Karve opened a widow’s
home in Poona in 1899. He crowned his work by
setting up the Indian Womens University at
Bombay in 1916.</li>
        <li>B.M. Malbari started a crusade against child
marriage and his efforts were crowned by the
enactment of the age of consent Act, 1891.</li>
        <li>In 1849 J.E.D. Bethune founded a girl’s school in
Calcutta.</li>
        <li>All India women’s conference was organised in
1936.</li>
        <li>Radha Soami Satsang was founded by Tulsi Ram.</li>
        <li>Deva Samaj was founded by Shiva Narain
Agnihotri.</li>
        <li>Nadwah ul ulama was founded by Maulana Shibli
Numani in 1894 in Lucknow.</li>
        <li>Justice movement was started in 1915-16 by C.N.
Mudaliar, T.M. Nair and P. Tyagaraja Chetti in
Madras. It was against the predominance of the
Brahmins in education, government services and
politics.</li>
        <li>Self respect movement was started in 1925 by E.V.
Ramaswamy Naicker popularly known as Periyar.</li>
        <li>Periyar waged movement for forcibly temple entry,
burning of Manusmriti and wedding without
Brahmin priest. He started his journal Kudi Arasu
in Tamil in 1929 to propagate his ideas.</li>
        <li>Ezhava movement was launched by Sri Narayan
Guru. He started the movement of untouchable
Ezhava against the Brahmin dominance in Kerala.
He rejected the caste system and developed the
concept of one caste, one religion and one God for
mankind. His disciple Ayappan made it into no
religion, no caste and no God for mankind.</li>
        <li>In Kerala, the Nairs started movement against the
dominance of Nambudari Brahmins. C.V. Raman
Pillai organised the Malyali Memorial. He wrote a
novel Martanda Verma to show the military glory
of the Nairs. Padmanabha Pillai founded the nair
service society in 1914.</li>
        <li>In 1873, Satya Sodhak movement was launched
by Jyotiba Phule in Maharashtra to save the lower
castes from the Brahmins. He wrote ‘Gulamgiri’
and ‘Sarvajanik Satyadharma Pustak’. His theory
of exploitation of lower castes was focused on
cultural and ethnic factor rather than on political
and economic one.</li>
        <li>The Mahars were organised by Gopal Baba
Walangkar in late 19th century against Brahmins
in Maharashta. Baba Bhim Rao Ambedkar became
their leader in the 20th century. Under his
leadership the Mahars started burning Manusmriti
and tried to break with the Hinduism.</li>
        <li>In 1932 Gandhiji founded the Harijan Sevak
Sangh.</li>
        <li>Ambedkar founded the Scheduled Castes Federation.</li>
    </ul>
</ol>
        </li>
    </ul>
