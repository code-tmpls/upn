<h3>Resistance to British Rule</h3>

<div class="subheading"><strong><i>Early Uprisings against the British in
India</i></strong></div>


<div class="subheading"><strong><i>NON TRIBAL</i></strong></div>

These revolts were mainly politically driven with
the stakes of zaminders, Indian rulers and even their
dependents.
<br>
<br>
Causes of these popular movements were the following:
<ol>
    <ul>
        <li>Attempts of the British to force Indian rulers to
accept their suzerainty.</li>
        <li>Forceful occupation of the estate of local rulers.</li>
        <li>Financial burden imposed by the British through
Subsidiary Alliance System.</li>
        <li>Doctrine of Lapse used by the British also resulted
in resentments.</li>
        <li>Frequent interference of local British residents in
internal affairs of the State;</li>
        <li>Resentment against British land revenue policy.</li>
        <li>Seizure of Zamindari estate in case of failure of
payments.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Revolt of Kattabomman</i></strong></div>

The revolt was organized in Tirunelveli in Tamil
Nadu in 1792-1799 by Veerpandya, the ruler of
Panchalakerich who was eventually killed in 1799.
Rebellion of Paiks
<br>
<br>
The rebellion took place in Orissa in 1804-1806 for
the first time under Khurda Raja and later under
Jagabandhu.
<br>
<br>
<div class="subheading"><strong><i>Revolt of Veluphanampi/Veluthambi</i></strong></div>

The revolt was organized in Travancore in 1808-
1809 by the Diwan of Travancore as the British tried
to remove him.

<div class="subheading"><strong><i>Revolt of Rao Bharmal</i></strong></div>

The ruler of Cutch revolted in Cutch and Kathiawar
in 1811-19.

<div class="subheading"><strong><i>Revolt of Ramosis</i></strong></div>

The revolt took place in Poona in 1822-1829 under
the leadership of Chitur Singh and Omaji when the
Peshwa lands were captured by the British.

<div class="subheading"><strong><i>Kittur Rising</i></strong></div>

The revolt materialized under the leadership of
Channamma who was the widow of Rudra Desai, the
chief of Kittur and had no son and Rayappa in 1824-
1829 in Karnataka.

<div class="subheading"><strong><i>Sambalpur Outbreak</i></strong></div>

The outbreak took place in Orissa in 1827-1840
under Surendra Sai due to interference in internal
matters by the British.

<div class="subheading"><strong><i>Satora Disturbance</i></strong></div>

King Pratap Singh was removed which instigated
a revolt in 1840-1841 in Maharashtra under Dhar Rao
and Narsingh Dattatreya.

<div class="subheading"><strong><i>Bundela Revolt</i></strong></div>
 The revolt was held in 1842 in Bundelkhand under
Madhukar Shah and Jawahir Singh against the British revenue system.

<div class="subheading"><strong><i>Gadkari Rebellion</i></strong></div>

Organized in Kolhapur in Maharashtra in 1844-
1845 in revolt against the overtake of the Kolhapur
administration by the British

<div class="subheading"><strong><i>Satvandi Revolt</i></strong></div>

It was in Maharashtra in 1839-1845 under Phond
Savant and Anna Sahib in protest against disposing
the chief of the Savant region Kheen Savant.

<div class="subheading"><strong><i>Raju Rebellion</i></strong></div>

Occurred in Vishakhapatnam in 1827-1833 under
Birbhadra Raju who was removed by the British.

<div class="subheading"><strong><i>Palakonda Outbreak</i></strong></div>

In Andhra Pradesh in 1831-1832 with no clearcut
leadership as all zamindars of the area mobilized the
peasantry.

<div class="subheading"><strong><i>Gumsur Outbreak</i></strong></div>

In Ganjam District in Andhra Pradesh in 1835-1837
under the leadership of Dhanjaya Bhauja who was a
zamindar of Ganjam. He was in debt due to non-payment of revenue and his land was confiscated.

<div class="subheading"><strong><i>Parlakimedi Outbreak</i></strong></div>

It was organized in Orissa in 1829-1835 under
Jagannath Gajapati as the British acquired the land of
zamindars due to nonpayment of revenue.

<div class="subheading"><strong><i>Tribal Revolts</i></strong></div>

Tribal movements were basically directed to
preserve the tribal identity which was thought to be in
danger due to intrusion of external people affecting
the social, political and geo-economical position of the
tribes. These movements were mostly violent, isolated
and frequent. There were about seventy tribal
movements from 1778 to 1947. These movements can
be broadly divided into two parts i.e., movements of
the frontier tribes and movements of the non-frontier
tribes depending upon the geographical area of their
initiation. Both these types of revolts had different sets
of causes. Movement of frontier tribes was mostly
revivalist and tended to be political and secular. On
the contrary, the non-frontier tribes revolted usually
against the 'outsiders' and the British administration.
Responsible Factors
<br>
<br>
The factors which were responsible for the outbreak
of the tribal movements are as follows:
<ol>
        <ul>
                <li>Resentment of the tribes against the extension of the
British rule to their areas. They also resented the
introduction of general administration & laws which
were considered by them as intrusions into the
traditional political system of the tribal community.</li>
                <li>Reaction against the penetration of tribal areas by
peoples from plains in form of money lenders,
traders, contractors, etc. and the protection given
to these out-siders by British government.</li>
                <li>The activities of Christian missionaries in their
areas were looked upon by them as anti-religious
and hence resented.</li>
                <li>British attempts to suppress certain tribal
traditions and practices like infanticide, human
sacrifices etc hurt the tribal social beliefs.</li>
                <li>The British colonialism devoid the tribal people of
their traditional economic set up and hence they
were forced to serve as menial labourers, coolies in
plantation, mines and factories.</li>
        </ul>
</ol>

<div class="subheading"><strong><i>Chaura Rising</i></strong></div>

Organized twice between 1768 and 1832 in
Naanbhum and Bara Bhum areas in West Bengal.
<div class="subheading"><strong><i>Bhil Revolt</i></strong></div>

Was in Khandesh in the Western Ghats in 1818-1848.
<div class="subheading"><strong><i>Hos Rising</i></strong></div>

Three revolts in 1820, 1822 and 1832 in Singhbhum
and Chhota Nagpur areas.
<div class="subheading"><strong><i>Koli Rising</i></strong></div>

In 1824, 1828, 1839 and 1844-1848 in the areas of
the Shahadari hills near Western Ghats of Gujarat
and Maharashtra.
<div class="subheading"><strong><i>Khasi Rising</i></strong></div>

In 1829-1832 in Khasi mountain range in Assam
and Meghalaya under Chief of Noumklow tribe, Tirut
Singh and Barmanik, the chief of Molim tribe.
<div class="subheading"><strong><i>Singpo Rising</i></strong></div>

Took place in 1830-1839 in Assam where the Englishman Mr. White was murdered.
<div class="subheading"><strong><i>Kol Rising</i></strong></div>

Broke out under Buddho Bhagat in 1831-1832 in
Chhota Nagpur area.
<div class="subheading"><strong><i>Khoya Revolt</i></strong></div>

Was organized in Rampa in Andhra Pradesh in
1840, 1845, 1858, 1861-1862, 1879-1880 and 1922-1924,
the last one under the leadership of Alluri Sitaramraju.
<div class="subheading"><strong><i>Khoni Rising</i></strong></div>

The first two revolts in 1846-48 and in 1855 were
under Chakra Bisaji and in 1914 in Orissa's Khondmal
region.
<div class="subheading"><strong><i>Santhal Revolt</i></strong></div>
<ol>
        <ul>
                <li>Was under Sado and Kanhu in 1855-1856 in
Rajmahal hills in Bihar.</li>
                <li>Began primarily as a reaction against their
exploitation by the outsiders (especially people
from the plains) and later developed into an antiBritish movement.</li>
                <li>Fought the British to have complete control of the
area between Bhagalpur and Rajmahal but were
crushed.</li>
        </ul>
</ol>

<div class="subheading"><strong><i>Naikdas Revolt</i></strong></div>

Was in Panchmahal in Gujarat in 1858-1859 and1868
under Roop Singh and Joria Bhagat respectively.
<div class="subheading"><strong><i>Kacha Nags Revolt</i></strong></div>

Was in 1882 in Assam under Sambhudan.
<div class="subheading"><strong><i>Munda Revolt</i></strong></div>

Was in Chota Nagpur region in Ranchi Under
Birsa Munda in 1899-1900.
<div class="subheading"><strong><i>Bhils Revolt</i></strong></div>

Under Govind Guru in Banswar and Dungapur
area of South Rajasthan in 1913.
<div class="subheading"><strong><i>Oraon Revolt</i></strong></div>

Under Jatra Bhagat in 1914-1915 in Chhota Nagpur area.
<div class="subheading"><strong><i>Thadoe Kuki Revolt</i></strong></div>

Under Jadonang and Rani Gaidinliu in 1917-1919
in Manipur.
<div class="subheading"><strong><i>Chenchus Revolt</i></strong></div>

Under Hanumanthu in 1921-1922 in Nallamala
range of Andhra Pradesh.
<div class="subheading"><strong><i>Rampa Revolt</i></strong></div>

Under Rajan Anatayya in 1884 and Alluri Sitaram
Raju in 1922-1924 in Andhra Pradesh.

