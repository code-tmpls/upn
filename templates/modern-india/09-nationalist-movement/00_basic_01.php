<h3>Nationalist Movement</h3>


<div class="subheading"><strong><i>Partition of Bengal, 1905</i></strong></div>

<ol>
    <ul>
        <li>By Lord Curzon on 16th October, 1905, through a
royal proclamation reducing the old province of
Bengal in size by creating East Bengal and Assam out
of rest of Bengal.</li>
        <li>The government said that it was done to stimulate
growth in the eastern region when actually the
objective was to set up a communal gulf between the
Hindus and the Muslims.</li>
        <li>A mighty upsurge swept the country against the
partition. National movement found real expression
in the movement against the partition of Bengal in
1905.</li>
        <li>Rabindranath Tagore composed the national song
'Amar Sonar Bangla' for the occasion which was
later adopted as the national anthem of Bangladesh
in 1971 after liberation from Pakistan.</li>
        <li>The ceremony of Raksha Bandhan was observed on
16th October, 1905 where Hindus and Muslims tied
rakhis to each other to show solidarity.</li>
        <li>The newspapers played a significant role in the
movement. The main newspapers were K.K. Mitra's
Sanjeevani, S.N. Banerjee's Bengali, Motilal Ghosh's
Amrit Bazaar Patrika, B.B. Upadhyaya's Yugantar,
Bipin Chandra Pal's New India, Aurobindo Ghosh's
Bande Mataram and Ajit Singh's Bharat Mata.</li>

    </ul>
</ol>


<div class="subheading"><strong><i>Rise of Extremism</i></strong></div>
<ol>
    <ul>
        <li>A section of Congress lost faith in moderate
programme and moved towards militant
nationalism.</li>
        <li>The leaders of this group gave up the soft approach
of appeals and petitions. Instead they made radical
demands and adopted strong ways of political
agitation.</li>
        <li>Ashwini Kumar Datt said that the Amravati session
was a three day tamasha.</li>
        <li>Lala Lajpat Rai regarded the congress as a factuous
annual festival of British educated elites.</li>
        <li>The extremists aimed at achieving Swaraj that meant
complete independence from British rule.</li>
        <li>They advocated boycott of foreign goods, use of
Swadeshi goods, national education and passive
resistance. They had deep faith in the masses and they
planned to achieve Swaraj through mass action.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Swadeshi Movement (1905)</i></strong></div>

<ol>
    <ul>
        <li>The movement had its origin in the anti-partition
movement of Bengal. The leaders of Bengal felt that
mere demonstrations, public meetings and
resolutions were not enough and something more
was needed; the answer was felt as Swadeshi and
Boycott.</li>
        <li>An important aspect of the Swadeshi movement was
emphasis placed on self-reliance.</li>
        <li>Lal, Bal and Pal and Aurobindo Ghosh played an
important role.</li>
        <li>The INC took the Swadeshi call first at the Benaras
Session, 1905 presoded over by G.K. Gokhale.</li>
        <li>A resolution to boycott British goods was adopted on
7th August, 1905 at a meeting of the INC at Calcutta
and bonfires of foreign goods at various places were
organised.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Muslim League, 1906</i></strong></div>

<ol>
    <ul>
        <li>Setup in 1906 under the leadership of Aga Khan,
Nawab Salimullah of Dhaka and Nawab Mohsinul-Mulk.</li>
        <li>It was a loyalist, communal and conservative political
organization which supported the partition of Bengal,
opposed the Swadeshi movement, and demanded
special safeguards of its community and a separate
electorate for Muslims.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Calcutta Session of INC, 1906</i></strong></div>

Swaraj was adopted in December 1906 at the Calcutta
session by the INC under Dadabhai Naoroji as the goal
of Indian people.

<div class="subheading"><strong><i>Surat Session of INC, 1907</i></strong></div>
<ol>
    <ul>
        <li>The INC split into two groups - the extremists and the
moderates at the Surat session in 1907. The Extremists
were led by Bal, Pal, Lal while the moderates by G.K.
Gokhale.</li>
        <li>A controversy arose over the elected president, Rash
Bihari Ghosh, as extremists did not accept him as
they wanted Lala Lajpat Rai to be chosen.</li>
        <li>The moderates wanted to modify the congress
resolutions on Swadeshi and boycott passed in the
1906 session. The extremists wanted to intensify them.</li>
        <li>The moderates wanted to insert a clause in the
congress constitution that Swaraj was to be achieved
only through constitutional means and by reforms in
Administration.</li>
        <li>The extremists were in favour of direct agitation
through the Swadeshi and boycott movements.</li>
        <li>The government after this launched a massive attack
on the extremists by suppressing their newspapers
and arresting their leaders.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Indian Council Act/Morley-Minto Act, 1909</i></strong></div>
<ol>
    <ul>
        <li>The Act introduced at the time of Morley who was the
Secretary of State and Minto who was the Indian
Viceroy.</li>
        <li>Legislative Councils both at the centre and the
Provinces were expanded.</li>
        <li>With regard to the Central Government, an Indian
member was taken in the Executive Council of the
Governor General.</li>
        <li>The size of the Provincial legislative Councils was
enlarged by including elected non-official members
so that the official majority was gone. Their function
was also increased and now they could move
resolutions on budget and some matters of public
importance.</li>
        <li>An element of election was also introduced in the
Central Legislative Council, but the official majority
was maintained.</li>
        <li>The most notable and retrograde change introduced
was that muslims were given separate
representation. Thus communal representation was
introduced which aimed at dividing the nationalist
ranks and at rallying the Moderates and the Muslims
to the Government's side.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Ghadar Party, 1913</i></strong></div>
<ol>
    <ul>
        <li>Formed by Lala Hardayal, Taraknath Das and Sohan
Singh Bhakna the name was taken from the weekly
Ghadar which had been started on 1 November,
1913 to commemorate the 1857 revolt.</li>
        <li>It was headquartered at San Francisco.</li>
        <li>The outbreak of the first World War provided the
Ghadarites with an opportunity to free India from a
government which was indifferent to their cause.</li>
        <li>And therefore they returned to India in thousands for
a coordinated revolt in collaboration with the Bengal
revolutionaries. Their plan was foiled at the last
moment due to treachery.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Komagata Maru Incident, 1914</i></strong></div>

<ol>
    <ul>
        <li>Komagata Maru was the name of the ship which
carried a shipload of Sikh and Muslim immigrants
from Punjab to Vancouver, Canada. But the Canadian
immigration authorities turned them back after
months of uncertainty.</li>
        <li>The ship finally anchored at Calcutta on 29
September, 1914 but the inmates refused to board the
Punjab bound train and there was a clash with the
police in which 22 persons were killed.</li>
        <li>This incident fired up the revolutionary activities
which sought to avenge the death of the innocents.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Home Rule Movement, 1916</i></strong></div>

<ol>
    <ul>
        <li>After Tilak's return having served a sentence of six
years in Mandalay, Burma, he tried to secure his
readmission along with other extremists in the INC.
With the need being felt for popular pressure to attain
concession, disillusionment with the Morley-Minto
Reforms and wartime miseries, Tilak and Annie
Besant readied to assume leadership.</li>
        <li>The movement was started by B.G. Tilak in Poona in
April 1916 and Annie Besant and S. Subramania Iyer
at Adyar near Madras in September 1916.</li>
        <li>Tilak's league was to work in Maharashtra,
Karnataka, Central Province and Berar and Annie
Besant's in the rest of India.</li>
        <li>The objective of the movement was self-government
for India in the British empire.</li>
        <li>Tilak linked up the question of Swaraj with the demand
for the formation of linguistic states and education in
the vernacular languages. He gave the slogan 'Swaraj
is my birthright and I shall have it'.</li>
        <li>The Maharatta and Kesari of Tilak and Annie Besant's
New India, Commonwealth and Young India became
the organs of the Home Rule movement</li>
        <li>The Muslim League also supported it.</li>

    </ul>
</ol>


<div class="subheading"><strong><i>Lucknow Pact, 1916</i></strong></div>

<ol>
    <ul>
        <li>Was signed following the war between Britain and
Turkey leading to anti-British feelings among
Muslims.</li>
        <li>Both INC and the Muslim League concluded it, the
Congress accepting the separate electorates and both
jointly demanding a representative government and
dominion status for the country.</li>

    </ul>
</ol>


<div class="subheading"><strong><i>August Declaration, 1917</i></strong></div>

<ol>
    <ul>
        <li>After the Lucknow Pact, a British policy was
announced which aimed at 'increasing the
association of Indians in every branch of the
administration for progressive realization of
responsible government in India as an integral part
of the British empire'. This came to be called the
August Declaration.</li>
        <li> Was attributed to the Hindu-Muslim unity exhibited
in the Lucknow Pact.</li>
        <li>The Montague Chelmsford reforms or the Act of 1919 was 
            based on this declaration.</li>
    </ul>
</ol>


