<h3>Struggle for Swaraj-I , 1919-1927</h3>


<div class="subheading"><strong><i>Government of India Act/Montague
Chelmsford Reforms, 1919</i></strong></div>

<ol>
    <ul>
        <li>Dyarchy system was introduced in the provinces.
It was considered to be a substantial step towards
transfer of power to the Indians. The provincial
subjects of the administration were to be divided
into two categories: Transferred and Reserved.
The transferred subjects were to be administered
by the Governor with the aid of ministers
responsible to the Legislative Council. The
Governor and the Executive Council were to
administer the reserved subjects without any
responsibility to the legislature.</li>
        <li>Devolution Rules: Subjects of administration were
divided into two categories - Central and
Provincial. Subjects of all India importance (like
railways and finance) were brought under the
category of Central while matters relating to the
administration of the provinces were classified as
Provincial.</li>
        <li>The Provincial Legislature was to consist of one
House only - Legislative Council.</li>
        <li>The number of Indians in the Governor General's
Executive Council was raised to three in a council
of eight. The Indian members were entrusted with
departments such as Law, Education, Labour,
Health and Industries.</li>
        <li>The Centre was now to have a bicameral legislature
for the first time. It actually happened after the
1935 Act.</li>
        <li>Communal representation was extended to Sikhs,
Christians and Anglo-Indians, etc.</li>
        <li>Secretary of State was to be henceforth paid salary
out of the British revenue.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Rowlatt Act, 1919</i></strong></div>

<ol>
    <ul>
        <li>In 1917 a Rowlatt committee was formed under
Justice Rowlatt. This committee was formed to curb
revolutionary activities. The Rowlatt Bill sought to
curtail the liberty of the people.</li>
        <li>It provided for speedy trial of offences by a special
court of 3 High court judges. There was to be no
appeal.</li>
        <li>The provincial government had powers to search
a place and arrest a suspected person without
warrant.</li>
        <li>This gave unbridled powers to the government to
arrest and imprison suspects without trial for two
years maximum. This law enabled the Government
to suspend the right of Habeas Corpus, which had
the foundation of civil liberties in Britain.</li>
        <li>It caused a wave of anger in all sections spreading
a country-wide agitation by Gandhiji and marked
the foundation of the Non-Cooperation Movement.</li>
        <li>Gandhiji organised the Satyagraha on 14th
February, 1919. The government gave consent to
the Act in March, 1919. On 8th April, 1919 Gandhiji
was arrested.</li>
        <li>Earlier Gandhiji's first great experiment in
Satyagraha had come in 1917 in Champaran
(Bihar) where the peasants were forced by their
European planters to grow indigo on at least 3/20
of their land and sell it at prices fixed by the
planters.</li>
        <li>Also in 1918 he had supported the cause of the
textile workers of Ahmedabad. It was here that he
used the weapon of hunger strike and won for the
workers a 35% increase in wages and in 1918 the
Kheda peasant struggle of Gujarat (demanding
suspension of revenue collection due to failure of
crop) involved Gandhiji and Sardar Vallabh-bhai
Patel.</li>
        <li>During March and April 1919, the country
witnessed a remarkable political awakening in
India. There were hartals, processions and
demonstrations everywhere.</li>

    </ul>
</ol>


<div class="subheading"><strong><i>Jallianwala Bagh Massacre, 1919</i></strong></div>

<ol>
    <ul>
        <li>People were agitated over the arrest of Dr. Kitchlu
and Dr. Satyapal on April 10, 1919 and assembled
in the Jallianwala Bagh in Amritsar.</li>
        <li>General O' Dyer fired at the people as a result of
which hundreds of people were killed and
thousands injured.</li>
        <li>Rabindranath Tagore returned his Knighthood in
protest and Sir Shankaran Nair resigned from
Viceroy's Executive Council after this.</li>
        <li>The Hunter Commission was appointed to enquire
into the matter.</li>
        <li>On 13 March, 1940, Sardar Udham Singh killed O'
Dyer when the latter was addressing a meeting in
Caxton Hall, London.</li>

    </ul>
</ol>


<div class="subheading"><strong><i>Khilafat Movement, 1920</i></strong></div>

<ol>
    <ul>
        <li>The main object the Khilafat movement was to
force the British government change its attitude
towards Turkey and restore the Khalifa to his
former position.</li>
        <li>Muslims were agitated by the treatment done with
Turkey by the British in the Treaty that followed
the First World War.</li>
        <li>Two brothers, Mohammad Ali and Shaukat Ali
started this movement along with Maulana Azad,
Jakim Ajmal Khan and Hasrai Mohani. It was
jointly led by the Khilafat leaders and the Congress.</li>
        <li>Gandhiji viewed the Khilafat agitation as a golden
opportunity for bringing the Hindus and Muslims
together.</li>
        <li>On 31 August, 1920, the Khilafat Committee
launched a non-cooperation movement.</li>
        <li>Gandhiji now pressed the Congress to adopt a
similar plan of action. Although it was initially
opposed by C.R. Das, but was later unopposed.</li>
        <li>Very soon the Khilafat movement lost its relevance
because Mustafa Kamal Pasha abolished Khilafat
and made Turkey a secular state.</li>

    </ul>
</ol>


<div class="subheading"><strong><i>Non-Cooperation Movement, 1920</i></strong></div>

<ol>
    <ul>
        <li>It was the first mass-based political movement
under Gandhiji.</li>
        <li> Congress passed the resolution in its Calcutta
session in September 1920 with three main
demands before the government (i) redressal of the
Punjab grievances (ii) Khilafat wrongs (iii)
establishment of Swaraj.
<ol>
    <ul>
        <li>The movement envisaged:</li>
        <li>Surrender of titles and honorary offices.</li>
        <li>Resignation from nominated offices and posts
in the local bodies.</li>
        <li>Refusal to attend government darbars and
boycott of British courts by the lawyers.</li>
        <li>Refusal of general public to offer themselves
for military and other government jobs and
boycott of foreign goods, etc.</li>
    </ul>
</ol>
</li>

        <li>C.R. Das and Motilal Nehru gave up their legal
practice and Subash Chandra Bose resigned from
the Civil Service.</li>
        <li>Tilak passed away on August 1, 1920, Tilak Swaraj
funds was started to fund the Non-Cooperation
Movement.</li>
        <li>The Prince of Wales visited India during this period
but he was greeted with empty streets and downed
shutters when he came on 17 November, 1921.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Chauri-Chaura Incident, 1922</i></strong></div>

<ol>
    <ul>
        <li>The Congress Session at Allahabad in December
1921 decided to launch the Civil Disobedience
Movement. Gandhiji was appointed as its leader.</li>
        <li>But before it could be launched a mob of people at
Chauri Chaura (near Gorakhpur) clashed with the
police and burnt 22 policemen on 5th February,
1922.</li>
        <li>This compelled Gandhiji to withdraw the Non
Cooperation Movement on 12th February, 1922</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Swaraj Party, 1923</i></strong></div>

<ol>
    <ul>
        <li>The sudden calling off of the non-cooperation
movement disappointed many congress leaders.</li>
        <li>Motilal Nehru, C.R. Das and N.C. Kelkar (called
Pro-changers) demanded that the Nationalists
should end the boycott of the legislative councils,
enter them and expose them. But the No-changers
like Rajendra Prasad and Rajagopalachari adhered
to the Gandhian programme of Boycott of
legislatures.</li>
        <li>The Pro-Changers formed the Swaraj party on
January 1, 1923, contested the elections and
embarrassed the government by opposing its
measures.</li>
        <li>The party got a majority in the 1923 elections in
Bengal and the Central Province.</li>
        <li>The Swarajists were split by communalism. The
'responsivist' group including Madan Mohan
Malviya, Lala Lajpat Rai and N.G. Kelkar offered
cooperation to the government to safeguard Hindu
interests.</li>
        <li>The Swarajists finally walked out of legislature in
1930 as a result of the Lahore congress resolution
and the beginning of the civil disobedience
movement.</li>
        <li>The two sections were reunited in 1930 after the
Lahore session.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Emergence of Gandhi</i></strong></div>

The last phase of the national movement began in
1919 when the era of popular mass movements was
initiated. During this period a new leader, Mohandas
Karamchand Gandhi took command. The new leader
made good one of the basic weaknesses of the previous
leadership. Gandhiji was greatly influenced by the
works of Leo Tolstoy's Civil Disobedience and Ruskin's
'unto to the last'. Tolstoy's ideal of non-possession
was developed by Gandhiji in his concept of
'trusteeship'. He was also influenced by the life and
teachings of Swami Vivekananda. His political Guru
Gokhale and Dadabhai Naroji also influenced him.
Besides he had an experience of struggle in South
Africa between (1984-1914). He came to India in 1915.
His non-violent satyagraha involved peaceful violation
of specific laws. He resorted to mass courting arrest
and occasional hartals and spectacular marches. He
had readiness for negotiations and compromise. His
struggle against foreign rule is popularly known as
'struggle-truce-struggle'.
<ol>
    <ul>
        <li>Champaran Satyagraha (1917): Gandi's first great
experiment in satyagraha came in 1917 in
Champaran, a district in Bihar. The peasantry on
the indigo plantations in the district was excessively
oppressed by the European planters. They were
compelled to grow indigo on at least 3/20th of their
land and so sell it at prices fixed by the planters.
This system was popularly known as 'Tin-Kathia
system'. Several peasants of Champaran invited
Gandhi to come and help them. Accompanied by
Babu Rajendra Prasad, Mazhar-ul-Huq, J.B.
Kripalani, Narhari Parekh and Mahadev Desai,
Gandhji reached Champaran in 1917 and through
his method and efforts, the disabilities from which
the peasantry was suffering were reduced and
Gandhiji won his first battle of civil disobedience in
India.</li>
        <li>Ahmedabad Mill Strike (1918): Gandhiji did his
second experiment at Ahmedabad in 1918 when
he had to intervene in a dispute between the
workers and the mill-owners. He advised the
workers to go on strike and to demand a 35 per
cent increase in wages. But he insisted that the
workers should not use violence against the
employers during the strike. He undertook a fast
unto death to strengthen the workers' resolve to
continue the strike. This put pressure on the millowners who relented on the fourth day and agreed
to give the workers a 35 per cent increase in wages.
3. Kheda Satyagraha (1918): The farmers of Kheda
district in Gujarat were in distress because of the
failure of crops. The government refused to remit
land revenue and insisted on its full collection. As
part of the experiment, Mahatma Gandhi advised
the peasants to withhold payment of revenue till
their demand for its remission was met. The
struggle was withdrawn when it was learnt that
the government had issued instructions that
revenue should be recovered only from those
peasants who could afford to pay. Sardar
Vallabhbhai Patel became the follower of
Gandhiji during the Kheda movement</li>
    </ul>
</ol>


