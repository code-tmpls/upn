<h3>Struggle for Swaraj-II, 1927-1947</h3>


<div class="subheading"><strong><i>Simon Commission, 1927</i></strong></div>

<ol>
    <ul>
        <li>In 1927, the British government appointed a
Commission to look into the working of the
Government of India Act, 1919.</li>
        <li>Constituted under John Simon to review the
political situation in India and to introduce further
reforms and extention of parliamentary democracy.</li>
        <li>Indian leaders opposed the Commission as there
were no Indian in it and the Congress turned the
boycott into a movement.</li>
        <li>Simon and his colleagues landed in Bombay on 3
February, 1928 and were greeted with hartals and
black flag demonstrations.</li>
        <li>The government used brutal repression and police
attacks to break the popular opposition. At Lahore,
Lala Lajpat Rai was severely beaten in lathi charge
and he succumbed to his injuries on 17 November,
1928.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Butler Committee, 1927</i></strong></div>

<ol>
    <ul>
        <li>Alongwith the Simon Commission the British
Government also announced the setting up of a threemember committee consisting of 
Harcourt Butler, W.S. Holdsworth and S.C. Peel to inquire into the
relationship between the Indian states and
paramount power and to suggest ways and means
for a more satisfactory adjustment of the existing
economic relations between Britain and British India.</li>
        <li>Officially called the Indian States Committee, it
visited 16 Indian states and submitted its report in
1929.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>The Nehru Report, 1928</i></strong></div>

<ol>
    <ul>
        <li>After boycotting the Simon Commission, all
political parties constituted a committee under the
chairmanship of Motilal Nehru to evolve and
determine the principles of a constitution for India.
It remains memorable as the first major Indian
effort to draft a constitutional framework for India,
complete with lists of central and provincial
subjects and fundamental rights. It suggested
dominion status for the country.</li>
        <li>The committee comprised of Tej Bahadur Sapru,
Ali Imam, M.S. Aney, Mangal Singh, Shoaib
Querishi, G.R. Pradhan and Subash Chandra Bose.</li>
        <li>The report had a different chapter on minority
rights apart from the Fundamental Rights.</li>
        <li>However when the report was placed before the
All Parties Convention in Calcutta, there was a
violent clash between Jinnah (representing the
Muslim League) and M.R. Jayakar (who put forth
the Hindu Mahasabha viewpoint) on the former's
demand of one-third of the total seats in the central
legislatures for Muslims.</li>
        <li>Consequently, Jinnah's proposed amendments
were overwhelmingly outvoted and the Report
proved to be a non-starter and became a mere
historical document.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>The Fourteen Points, 1929</i></strong></div>

<ol>
    <ul>
        <li>At a meeting of the Muslim League in Delhi on
March 28, 1929, Jinnah announced the Fourteen
Points.</li>
        <li>Rejecting the Nehru Report he maintained that no
scheme for the future government of India would
be acceptable to Muslims until and unless the
fourteen points were given effect to.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Lahore Session, 1929</i></strong></div>

<ol>
    <ul>
        <li>On December 19, 1929, under the Presidentship of
Jawaharlal Nehru, the INC at its Lahore session
declared Poorna Swaraj as its ultimate goal.</li>
        <li>On 31 December, 1929 the newly adopted tricolour
flag was unfurled and 26 January, 1930 was fixed
as the First Independence Day, which was to be
celebrated every year.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Dandi March, 1930 and the Civil
Disobedience Movement (1930-1931)</i></strong></div>


<ol>
    <ul>
        <li>Gandhi submitted the Eleven Point Ultimatum to
the British but after no response from the latter he
launched the Civil Disobedience Movement with
the Dandi March also called the Salt Satyagraha.</li>
        <li>Along with 78 followers, Gandhiji started his march
from Sabarmati Ashram on March 12, 1930 from
the small village Dandi to break the salt law. He
reached the sea shore on 6 April, 1930, picked up
a handful of salt and inaugurated the Civil
Disobedience Movement.</li>
        <li>The salt satyagraha movement was taken up by C.
Rajagopalachari in Tamil Nadu and the Vaikon
Satyagraha by K. Kalappan in Malabar.</li>
        <li>The salt satyagraha sparked off other forms of
defiance. In the north east, the Pathans under Khan
Abdul Gafar Khan, popularly known as Frontier
Gandhi, organized the society of 'Khudai
Khidmadgars' which was also known as Red Shirts.
This movement even sparked off patriotism among
the Indian soldiers in the British army. The Gharwal
soldiers refused to fire on the people at Peshawar.</li>
        <li>In Bengal, the Chittagong army raid was carried
out in April 1930.</li>
        <li>In Bihar there was a protest against Chowkidari
tax in Saran, Bhagalpur and Monghyr.</li>

    </ul>
</ol>


<div class="subheading"><strong><i>First Round Table Conference, 1930</i></strong></div>

<ol>
    <ul>
        <li>It was the first conference arranged between the
British and the Indians as equals. It was held on
12 November, 1930 in London to discuss the Simon
Commission recommendations.</li>
        <li>The conference was boycotted by the INC, but the
Muslim League, Hindu Mahasabha, Liberals and
other groups were present.</li>
        <li>The conference was postponed to 2 January, 1931
in the absence of any major political party.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Gandhi-Irwin Pact, 1931</i></strong></div>

<ol>
    <ul>
        <li>The moderate statesmen Sapru, Jaikar and Srinivas
Shastri initiated efforts to break the ice between
Gandhiji and the government.</li>
        <li>The Gandhi-Irwin Pact was signed on 5 March,
1931.</li>
        <li>In this the INC called off the Civil Disobedience
Movement and agreed to join the second Round
Table Conference.</li>
        <li>The government on its part released political
prisoners and conceded the right to make salt for
consumption for villages along the coast.</li>
        <li>The Karachi session of 1931 endorsed the Pact
and is also memorable for its resolution on
Fundamental Rights and the National Economic
Programme.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Second Round Table Conference, 1931</i></strong></div>

<ol>
    <ul>
<li>Gandhiji represented the INC and went to London
to meet the British PM Ramsay Macdonald.</li>
<li>In the conference Gandhi demanded immediate
establishment of a full responsible government at
the Centre as well as in the provinces with complete
control over defense, external affairs and finance.</li>
<li>However, the session was soon deadlocked on the
minorities issue and this time separate electorates
were demanded not only by the Muslims but also
by the depressed classes, Indian Christians and
Anglo-Indians.</li>
<li>MacDonald ended the session with an address
announcing the creation of two new Muslim
majority provinces, North West Frontier Province
and Sindh, set up a committee on franchise, finance
and states and held out the humiliating and
dangerous prospect of a unilateral British
Communal Award.</li>
<li>In second round table conference the people who
took part were Ambedkar, Sapru, Jayakar, Sarojini
Naidu, Malaviya, etc.</li>
<li>On Gandhiji's arrival in Bombay, the Congress
Working Committee decided to resume the Civil
Disobedience Movement. In 1932, INC was declared
an illegal organization and all its leaders arrested.
Gandhiji was sent to the Yeravada jail in Poona.</li>
<li>The Civil Disobedience Movement was withdrawn
in 1934, as after that Gandhiji decided to make
Harijan work the central plank of his new rural
constructive program.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>The Communal Award, 1932</i></strong></div>

<ol>
    <ul>
        <li>Announced by Ramsay MacDonald it showcased
the Divide and Rule policy of the British by
envisaging representation of Muslims, Sikhs, Indian
Christians, Anglo-Indians, women and backward
classes.</li>
        <li>Gandhiji who was in Yeravada jail in Poona at
the time started a fast unto death against it.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Poona Pact, 1932</i></strong></div>

<ol>
    <ul>
<li>After the announcement of the communal award
and the subsequent fast of Gandhiji, mass meetings
took place everywhere.</li>
<li>Political leaders like Madan Mohan Malviya, B.R.
Ambedkar and M.C. Rajah became active.</li>
<li>Eventually the Poona Pact was reached and
Gandhiji broke his fast on the sixth day, 25
September, 1932.</li>
<li>In this the idea of separate electorate for the
depressed classes was abandoned, but seats
reserved to them in the provincial legislatures were
increased. Seats reserved for depressed classes
increased from 71 to 147 in provincial legislative
council and in central legislative council 18% of
the seats increased.</li>
<li>Thus the Poona Pact agreed upon a joint electorate
for the upper and the lower classes.</li>
<li>Harijan upliftment now became the principal
concern of Gandhiji. An All India AntiUntouchability League was started in September
1932 and the weekly 'Harijan' in 1933. On May 8,
1933 Gandhiji decided to begin a 21 day fast for
the purification of himself and his associates for
the Harijan cause.</li>
<li>He started the Individual Civil Disobedience on 1
August, 1933.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Revolutionary Movements</i></strong></div>

<ol>
    <ul>
<li>Chapekar brothers (Damodar and Balkrishna)
murdered the unpopular Mr. Rand and Lt. Amherst
in 1897 at Pune.</li>
<li>Unsuccessful attempts of murder of Lt. Governor
of East Bengal by Barindra Kumar Ghosh and
Bhupendra Nath Dutt in 1906 in Bengal.</li>
<li>Murder of Kennedy brothers and two English
women by Khudiram Bose and Praful Chaki in
1908 at Muzaffarpur (Bihar). The main target,
unpopular judge, Kinsford escaped unhurt.</li>
<li>Attempt of murder of Lord Hardinge by throwing
crude bomb in 1912 by Rash Bihari Bose and
Sachindra Nath Sanyal. Lord Hardinge escaped
unhurt.</li>
<li>Madan Lal Dhingra shot dead Curzon Wyllie, an
officer of India Office in London in 1909.</li>
<li>Assistant Superintendent of Police, Saunders was
shot dead by Bhagat Singh, Chandra Shekhar Azad
and Rajguru in 1928. They were convicted in the
Lahore Conspiracy case.</li>
<li>Bhagat Singh and Batukeshwar Dutt threw bombs
in the Central Legislative Assembly in April 1929
during session.</li>
<li>Unsuccessful attempts of blowing up the train
baggage of Lord Irwin were made by some members
of Hindustan Socialist Republican Association in
December 1929.</li>
<li>Two schoolgirls, Shanti and Suniti Chaudhuri shot
dead the Magistrate of Tipperra, Mr. Steven in
December 1932 in Bengal.</li>
<li>Attempt of blowing up train and occupation of
Fort William under the leadership of Jatin
Mukherjee of Bengal (Known also as Bagha Jatin).
The attempt failed due to the death of Jatin
Mukherjee in Police encounter in Balasore in Orissa
in September 1915.</li>
<li>An organised attempt of revolt by Rash Bihari
Bose, Sachindra Nath Sanyal and other
revolutionaries on the lines of revolt of Ferozpur,
Lahore and Rawalpindi on 21st February 1915,
was failed due to treachery on the part of some
revolutionaries. After the failure, Rash Bihari Bose
escaped to Japan and Sachindra Sanyal was given
life imprisonment and deportation for life.</li>
<li>Formation of 'Indian Independence Committee' by
Virendra Nath Chattopadhya, Bhupendra Nath
Dutt, Hardayal and others in 1915 with the
assistance of German foreign Ministry under the
"Zimmerman Plan".</li>
<li>An Internal Government of free India was
established in 1915 by Raja Mahendra Pratap,
Barkatullah, Obaidullah Sindhi (A Deoband
Mullah) and others in Kabul.</li>
<li>Suryasen declared independence at Chittagong in
1930 and formed "Indian Republican Army".</li>
<li>The Komagata Maru was a Japanese steamer,
commissioned by Gurdit Singh, to transport Indian
immigrants to Canada.</li>
<li>Martyrdom of Jatin Das who died in jail on the
64th day of a hunger strike in 1929 for improvement
in the status of political prisoners.</li>
<li>Execution of Bhagat Singh, Sukhdev and Rajguru
by the British on March 23, 1931 in Lahore
conspiracy case.</li>
<li>Surya Sen was arrested in 1933 and was tried and
hanged.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>The Left Movement</i></strong></div>

<ol>
    <ul>
        <li>Shripad Amrit Dange published a pamphlet
entitled 'Gandhi and Lenin' in Bombay and also
published the first socialist weekly `The Socialist'
from Bombay.</li>
        <li>Muzaffer Ahmad started publishing 'Navyug' in
Bengal and founded 'Langal' with the assistance
of Qazi Nazrul Islam.</li>
        <li>Ghulam Husain started publishing 'Inquilab' in
Punjab.</li>
        <li>M. Sringarvellu founded the 'Labour Kishan
Gazette' in Madras.</li>
        <li>Peshawar Case (1922-23) was the conspiracy
against communists coming to India from abroad.</li>
        <li>Under Kanpur Conspiracy Case (1924-25) S.A.
Dange, Muzaffer Ahmed, Nalini Gupta and
Shaukat Usmani were convicted.</li>
        <li>Meerut Conspiracy Case (1929-33) was the longest
conspiracy trial. Seventeen people were convicted.</li>
        <li>Three British Communists-Phillip Spratt, Ben
Bradely and Laster Huchinson were convicted in
Meerut conspiracy case. Jawahar Lal Nehru, M. A.
Ansari & M.C. Chagla were the Defense Counsel
in favour of convicts.</li>
        <li>Three main socialists in the working committee of
Jawahar Lal Nehru were-Jayaprakash Narayan,
Acharya Narendra Dev and Achutya Patwardhan.</li>
        <li>The Newspaper 'Kranti' was published by S. S.
Mirajkar.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>The Working Class Movement</i></strong></div>

<ol>
    <ul>
        <li>S. S. Bengalee proposed a Bill in favour of the
workers in Bombay Legislative Assembly in 1878.
His initiative however failed.</li>
        <li>Sasipada Banerjee established a working men's
club in Bengal in 1870 and published a monthly
journal 'Bharat Sramjeevi' in 1874.</li>
        <li>First Factory Act was passed in 1881 and was
mainly related with the child workers, working
hours and defining a factory.</li>
        <li>Second Factory Act was passed in 1891. It was
related mainly with working hours of female
workers.</li>
        <li>N. M. Lokhanday formed the 'Bombay Millhands
Association' in 1884. He also published a journal
named 'Din Bandhu'.</li>
        <li>The strike of the Signalers of the Great Indian
peninsular Railway in 1899 was the first organised
worker's strike.</li>
        <li>B.P. Wadia, a close associate of Annie Besant
formed the 'Madras Labour Union' in 1918. It was
the first trade union of India.</li>
        <li>In 1920, the All India Trade Union Congress
(A.I.T.U.C.) was organised at Bombay by N. M.
Joshi and others. The first session (1920) was
presided over by the then Congress president, Lala
Lajpat Rai.</li>
        <li>In 1926, Trade Union Act was passed. The Act
made provisions for voluntary registration and
gave certain rights and privileges to registered trade
unions in return for certain obligations.</li>
        <li>In 1926, 'Bombay Cotton Textile Workers Union'
was formed which was the first registered Trade
union under the Trade Union Act of 1926. Its
President was N. M. Joshi.</li>
        <li>The Moderates under the leadership of N. M. Joshi
walked out of the A.I.T.U.C. and formed the All
India Trade Union Federation (A.I.T.U.F.) in 1929.</li>
        <li>The Royal Commission on labour was set up in
1929 under the chairmanship of John Henry
Whitely.</li>
        <li>Trade Disputes Amendment Act was passed in
1938.</li>
        <li>In 1931, another division in A.I.T.U.C. was made.
The communists formed the 'Red Trade Union
Congress'.</li>
        <li>Through the Meerut Conspiracy Trials (1929-31)
the trade union movement was suppressed.</li>
        <li>M. N. Roy formed a pro-Government Trade Union
called 'Indian Federation of Labour'.</li>
        <li>Sardar Vallabhbhai Patel formed the Indian
National Trade Union Congress in 1944.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Third Round Table Conference, 1932</i></strong></div>

<ol>
    <ul>
        <li>The session was held without Congress
representation as they were all imprisoned and was
attended by an even smaller number of
representatives who agreed on almost all the issues.</li>
        <li>The British Government on the basis of the
discussions of the three sessions drafted its
proposals for the reform of the Indian Constitution
which was embodied in the White Paper published
in March 1933.</li>
        <li>The White Paper was examined and approved by
a joint committee of the British Parliament in
October 1934 and a bill, based on the report of the
committee was introduced and passed in the
British Parliament as the Government of India Act
of 1935.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>The Government of India
Act, 1935 (details covered earlier)</i></strong></div>

<ol>
    <ul>
        <li>The Congress rejected the Act and demanded the
convening of a Constituent Assembly elected on
the basis of adult franchise to frame a Constitution
for an Independent India.</li>
        <li>The INC fought the elections in 1937 when the
Constitution was introduced and formed ministries
in seven out of eleven provinces. Later Congress
formed coalition governments in two others. Only
Punjab was under the Unionist Party and Bengal
under the Krishak Praja-Muslim League coalition.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>World War II and the National Movement</i></strong></div>

<ol>
    <ul>
        <li>The Congress ministries coming to power did
wonders to the morale of the people. They
continued to function till the Second World War
in 1939. When the war broke out, Lord Linlithgow
declared India to be at war without the prior assent
of the central legislatures.</li>
        <li>The Congress agreed to support Britain only in
return of independence being granted. The Viceroy
could promise that only after the war. In OctoberNovember 1939, the Congress 
ministries resigned in protest. The Muslim League observed this as the
Deliverance Day (22 December, 1939) as a mark of
relief that the Congress had atlast ceased to function.</li>
        <li>INC was willing to help the forces of democracy
in their struggle against fascist powers but asked
how it was possible for an enslaved nation to help
others in their fight for democracy. They declared
that India must be declared free or at least effective
power should be put in Indian hands before it
could actively participate in the war.</li>
        <li>The Viceroy refused to accept preconditions set by
the Congress i.e. a Constituent Assembly for the
establishment of genuine responsible government
at the centre. Eventually, the British government
was eager for the INC to support their war efforts.
Subsequently it tried to pacify the Congress and
the Indian leaders by a series of offers through the
Cripps Mission and the August Offer.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>August Offer, 1940</i></strong></div>

<ol>
    <ul>
        <li>The August Offer of 8 August, 1940 offered:
            <ol>
                <ul>
                    <li>Dominoin Status in the unspecified future.</li>
                    <li>A post-war body to enact the Constitution.</li>
                    <li>To expand the Governor General's Executive
Council to give full weightage to minority
opinion.</li>
                </ul>
            </ol>
        </li>
        <li>It was rejected by the INC because there was no
suggestion of the national government and because
the demand for the dominion status was already
discarded in favour of Poorna Swaraj.</li>
        <li>It was accepted by the Muslim League.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>The Cripps Mission, 1942</i></strong></div>

<ol>
    <ul>
        <li>In December 1941 Japan entered the World War II
and advanced towards Indian borders. By March
7, 1942, Rangoon fell and Japan occupied the entire
South East Asia.</li>
        <li>The British government with a view to get the
cooperation from Indians sent Sir Stanford Cripps,
leader of the House of Commons to settle terms
with the Indian leaders.</li>
        <li>He offered a draft which contained the following
proposal:
<ol>
    <ul>
        <li>Dominion status to be granted after the war.</li>
        <li>Setting up a Constitution making body for India
after the war whose members would be elected
by the Provincial assemblies and nominated
by the rulers in case of the Princely States.</li>
    </ul>
</ol>
</li>
        <li>The British government undertook to accept and
implement the Constitution so framed subject to
two conditions:
<ol>
    <ul>
        <li>Any province not willing to accept the new
Constitution could form a separate union and
a separate Constitution.</li>
        <li>The new Constitution-making body and the
British government would negotiate a treaty to
sort out matters arising out of transfer of
powers to Indian hands.</li>
    </ul>
</ol>
</li>
        <li>The proposals were rejected by the Congress as it
did not want to rely upon future promises.</li>
        <li>Gandhiji termed it as a 'post-dated cheque in a
crashing bank'.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>The Revolt of 1942 and the
Quit India Movement</i></strong></div>

<ol>
    <ul>
        <li>Also called the Vardha Proposal and the Leaderless
Revolt as all the Congress leaders were in jail.</li>
        <li>The resolution for the movement was passed on 8
August, 1942 at Bombay. Gandhiji gave the slogan
'Do or Die'.</li>
        <li>On 9 August the Congress was banned and its
important leaders arrested. Gandhiji was kept at
the Aga Khan Palace, Pune.</li>
        <li>The arrests provoked indignation among the
masses and there being no program of action, the
movement became spontaneous and violent as
violence spread throughout the country. Several
government offices were destroyed, telegraph wires
cut and communication paralysed.</li>
        <li>The trend of underground revolutionary activities
also started during this phase. J.P. Narayan, R.M.
Lohia and Aruna Asaf Alistarted consolidating
underground networks.</li>
        <li>The most daring act of the underground movement
was the establishment of the Congress Radio with
Usha Mehta as its announcer.</li>
        <li>Parallel governments were set up at various places
such as the one in Ballia in eastern U.P. under the
leadership of Chittu Pande. Others were in Satara,
Talcher, parts of Eastern U.P. and Bihar.</li>
        <li>The Muslim League kept aloof and the Hindu
Mahasabha condemned the movement. The
Communist Party of India also didn't support the
movement.</li>
        <li>The movement was however crushed.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>INA, 1942</i></strong></div>

<ol>
    <ul>
        <li>The idea of the Indian National Army (INA) to
liberate India was originally conceived by
Rasbehari Bose who had fled to Japan in 1915 and
had become a Japanese citizen, with the help of
Mohan Singh, and Indian officer of the British
Indian Army in Malaya.</li>
        <li>Subash Chandra Bose secretly escaped from India
in January, 1941 and reached Berlin. In July 1943
he joined the INA at Singapore. There Rashbehari
Bose handed over the leadership to him.</li>
        <li>The soldiers were mostly raised from the Indian
soldiers of the British army who had been taken
prisoner by the Japanese after they conquered
South-east Asia.</li>
        <li>Two INA headquarters were Rangoon and
Singapore.</li>
        <li>INA had three fighting brigades named after
Gandhi, Azad and Nehru. The Rani of Jhansi
Brigade was an exclusive women force.</li>
        <li>The INA launched an attack on India and
penetrated into Indian territory. It annexed
Andaman and Nicobar islands with Japanese help
and named them 'Shaheed' and 'Swaraj'.</li>
        <li>S.C.Bose gave the call 'Dilli Chalo'.</li>
        <li>But it couldn't match the British army and
surrendered.</li>
        <li>INA trials were held at Red Fort, Delhi. P.K.Sehgal,
Shah Nawaz (Commander of the INA battalion
that had reached the Indo-Burma front) and
gurbaksh Singh Dhillon were put on trial.</li>
        <li>The Congress took up their defence with Bhulabhai
Desai, Tejbahadur Sapru, Jawaharlal Nehru, K.N.
Katju and Asif Ali defending them. Muslim League
also joined in for the country-wide protest.</li>
        <li>INA Day was celebrated on 12 November, 1945.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Hindus-Muslims over the Years</i></strong></div>

<ol>
    <ul>
        <li>The British rule had acted on the tested and tried
Roman maxim of 'Divide and Rule' in India.</li>
        <li>In the early 19th Century, British historian, James
Mill, described the ancient period of Indian history
as the Hindu period and the medieval period as
the Muslim period.</li>
        <li>Both the Hindus and the Muslims participated
equally in the revolt of 1857. The British adopted
the policy of favouring the Hindus and
suppressing the Muslims after the revolt of 1857.</li>
        <li>In August 1888, Syed Ahmed Khan set up the
"United Indian Patriotic Association'' with the
avowed objective of countering the Congress
propaganda and policy in England.</li>
        <li>A few years later he formed the ``Muhammaden
Anglo Oriental Defence Association'' of Upper
India to keep the Muslims aloof from political
agitation and to strengthen the British rule in India.</li>
        <li>The Aligarh College was founded by Syed Ahmed.
The three Principles of the M.A.O. College, Aligarh,
W.A.J. Archbold, Thedore Beck and Thedore
Morrison gave the pro-British and anti-Hindu bias
to the Aligarh movement.</li>
        <li>Inspired by W. A. J. Archbold, Aga khan waited in
a deputation on Lord Minto at Simla on 10th
October 1906.</li>
        <li>The Deputation demanded reservation of seats for
the Muslims and the Morley-Minto reforms of 1909,
accepted the Muslim demand for separate
communal electorate.</li>
        <li>The All-India Muslim League was formerly
inaugurated on 30th December 1906 by Aga Khan,
Nawab Mohsin-ul-Mulk and others.</li>
        <li>The militantly nationalist Ahrar movement was
founded by Maulana Mohammed Ali, Hakim Ajmal
Khan, Hasan Imam, Maulana Zafar Ali Khan, and
Mazhar-ul-Haq.</li>
        <li>In 1916, both the Muslim League and the Congress
held their sessions at Lucknow. An agreement was
