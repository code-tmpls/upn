<h3>Constitutional Developments</h3>



Clive established dual government in Bengal which
continued from 1765 to 1772. As per the dual
government, two diwans were appointed by the
Company: Mohammad Raza Khan for Bengal and Raja
Shitab Ray for Bihar.

<div class="subheading"><strong><i>Regulating Act, 1773</i></strong></div>

A Bill regarding the Regulating Act was placed in
British Parliament by Lord North. The Regulating Act
1773 was the first important Parliamentary Act
regarding the Company's affairs and entailed:
<ol>
    <ul>
        <li>The subjugation of the company's actions to the
supervision of the British Government.</li>
        <li>End of Dual Government.</li>
        <li>Governor General in Council was to superintend
and control Presidencies of Madras and Bombay
and was to be the Governor-General of the British
territories of India.</li>
        <li>Governor General was under direct control of Court
of Directors.</li>
        <li>Establishment of a Supreme Court in Calcutta for
justice of Europeans, their employees and citizens
of Calcutta. The Supreme Court was constituted in
1774 with Sir Elijah Empey as Chief Justice and
Chambers, Lemiaster and Hyde as puisine judges.</li>
        <li>Servants of the Company were forbidden to engage
in private trade, accept presents or bribes, etc.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Pitts India Act, 1784</i></strong></div>

<ol>
    <ul>
        <li>The commercial and political activities of the
Company were now separated. Pitts India Act of
1784 established a Board of Control of six members
(including two Cabinet Ministers) to control civil,
military and revenue affairs of the Company in
India.</li>
        <li>Provincial Governor in Councils strength reduced
from four to three and the Presidencies of Madras
and Bombay were subordinated to the GovernorGeneral and Council of Bengal in all matters of
diplomacy, revenue and war.</li>
        <li>A secret committee of three Directors was to look
into political and military affairs (The GovernorGeneral and the Council were forbidden to declare
war and make treaties without the sanction of the
secret committee).</li>
        <li>To investigate into matters impending against
Company officials a Court was set up at London.</li>
        <li>This act gave the British government a measure of
control over the Company's affairs; making the
Company a subordinate department of the State.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>The Act of 1786</i></strong></div>

<ol>
    <ul>
        <li>Governor-General was given powers to override his
council. Governor-General was made commander
in chief.</li>
        <li>Declaratory Act of 1788 gave full power and
supremacy to the Board of Control; as step towards
transfer of power of Company to the Crown.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Charter Act of 1793</i></strong></div>

<ol>
    <ul>
        <li>Company's commercial privileges were extended
for another 20 years.</li>
        <li>Power specially given to Cornwallis to override
council was extended to all future Governor-Generals.</li>
        <li>A Regular code of all regulations prepared for
administration of British territory of Bengal, and
bound the court to regulate their decisions by rules
and regulations contained therein. Therefore it laid
the foundation of the government by written laws,
interpreted by courts.</li>
        <li>All laws were be translated into Indian languages.</li>
        <li> Expenses and salaries of the Board of Control to
be charged on Indian revenue.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Charter Act of 1813</i></strong></div>

<ol>
    <ul>
        <li>The Company was deprived of its monopoly trade
with India but still enjoyed its monopoly of trade
with China and trade in tea.</li>
        <li>Constitutional position of British territories in
India was thus explicitly defined for first time.</li>
        <li>First constitutional measure for propagation of
Christianity in India adopted as officials were
appointed for propagations of Christianity in India.</li>
        <li>A sum of Rs 1 lakh earmarked annually for the
education of Indians.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Charter Act of 1833</i></strong></div>

<ol>
    <ul>
        <li>Company lost its monopoly of tea and China trade,
and was also asked to close its commercial business.</li>
        <li>All restrictions of European immigration into India
and acquisition of land and property in India by
the Company was removed, legalizing European
colonization of India.</li>
        <li>Governor-General of Bengal became GovernorGeneral of India and took all 
            Governor-Generals of
Madras, Bombay etc. under his control. All powers
administrative and financial were centralized in
the hands of the governor-General-in-Council.</li>
        <li>President of Board of Control became the Minister
for Indian Affairs.</li>
        <li>A law commission was constituted for codification
of laws. A law member (without the power to vote)
was added to the Executive Council of the
Governor General and Macaulay was the first law
member of Governor General's council. This
increased the Council's strength to four and with
it began the first Indian Legislature.</li>
        <li>All vacancies in India were to be filled by
competitive examinations. Thus throwing open to
all the services irrespective of religion, place of
birth, descent and colour.</li>
        <li>By Act of 1833 slavery was abolished.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Charter Act of 1853</i></strong></div>

<ol>
    <ul>
        <li>The Act renewed the powers of the company and
allowed it to retain the possession of Indian
territories in trust for the British crown but not for
any specified period.</li>
        <li>The number of members of the Court of Directors
was reduced from 24 to 18 of which were to be
nominated by the Crown.</li>
        <li>The law member was made full member of the
Governor General's Executive Council.</li>
        <li>Legislative functions were for the first time treated
separate from Executive functions.</li>
        <li>Questions could be asked and the policy of the
Executive Council could be discussed, though the
Executive Council could veto a bill of the
Legislative Council.</li>
        <li>Act appointed a Law commission in England to
examine reports and draft of Indian Law Commission.</li>
        <li>Recruitment to the Civil Services was based on open
annual competition examination (excluding Indians).</li>

    </ul>
</ol>
<div class="subheading"><strong><i>Government of India Act, 1858</i></strong></div>

<ol>
    <ul>
        <li>Authority transferred from Company to the Crown.
The system of Dual Government ended and the
Court of Directors and Board of control was
abolished. India was to be governed in name of
sovereign through a Secretary of State for India
assisted by the Indian Council of 15 members.</li>
        <li>The Governor-General received title of Viceroy,
who became direct representative of the Secretary
of State and the Crown.</li>
        <li>Secretary of State for India was established as a
Corporate Body.</li>
        <li>Thus a highly centralized administrative structure
was created.</li>
        <li>Appointment to the coveted Civil Services to be
made by open competition under the rules laid
down by Secretary of state with help of Civil Service
Commission.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Indian Council Act, 1861</i></strong></div>

<ol>
    <ul>
        <li>A fifth member, who was a jurist to be added to
the Viceroy's Executive Council</li>
        <li>6-12 additional members to be added to the
Executive Council for legislation purpose. This
implied that Viceroy's Executive council which was
so long composed of officials would now include
certain additional non-official members. Some of
the non-official seats were offered to natives of
higher ranks. Thus a minute element of popular
participation was introduced in the legislative
process. The additional members though had little
powers.</li>
        <li>The Executive Council was now to be called
Central Legislative Council.</li>
        <li>Viceroy could issue ordinances in case of emergency.
Indian Council Act, 1892</li>
        <li>Two improvements in both the Central and the
Provincial Legislative councils were suggested:</li>
        <li>Though the majority of the official members were
retained, the non-official members were to be
nominated by the Bengal Chamber of Commerce
and Provincial Legislative Councils. (The nonofficial members of the Provincial Councils were
to be nominated by certain local bodies such as
universities, district boards and municipalities.)
Indian leaders like G.K. Gokhale, Ashutosh
Mukherjee, S.N. Banerjee found their way in the
Legislative Council.</li>
        <li>The councils were to have the power to discuss
the annual statement of revenue and expenditure
and of addressing questions to the Executive. They
could also put questions with certain limitations
to the Government on matters of public interest
after giving six days notice.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Indian Council Act/Morley-Minto Act, 1909</i></strong></div>

<ol>
    <ul>
        <li>The act introduced at the time of Morley who was
the Secretary of State and Minto who was the
Indian Viceroy.</li>
        <li>Legislative Councils both at the centre and the
Provinces were expanded.</li>
        <li>With regard to the Central Government, an Indian
member was taken in the Executive Council of the
Governor General.</li>
        <li>The size of the Provincial legislative Councils was
enlarged by including elected non-official members
so that the official majority was gone. Their
functions was also increased and now they could
move resolutions on budget and some matters of
public importance.</li>
        <li>An element of election was also introduced in the
Central Legislative Council, but the official majority
was maintained.</li>
        <li>The most notable and retrograde change
introduced was that muslims were given separate
representation. Thus communal representation was
introduced.</li>

    </ul>
</ol>
<div class="subheading"><strong><i>Government of India Act/MontagueChelmsford Reforms,
     1919</i></strong></div>
<ol>
    <ul>
        <li>Dyarchy system was introduced in the provinces. It
was considered to be a substantial step towards
transfer of power to the Indians. The provincial
subjects of the administration were to be divided
into two categories: Transferred and Reserved. The
transferred subjects were to be administered by the
Governor with the aid of ministers responsible to
the Legislative Council. The Governor and the
Executive Council were to administer the reserved
subjects without any responsibility to the legislature.</li>
        <li>Devolution Rules: Subjects of administration were
divided into two categories - Central and Provincial.
Subjects of all India importance (like railways and
finance) were brought under the category of Central
while matters relating to the administration of the
provinces were classified as Provincial.</li>
        <li>The Provincial Legislature was to consist of one
House only - Legislative Council.</li>
        <li>The number of Indians in the Governor General's
Executive Council was raised to three in a council
of eight. The Indian members were entrusted with
departments such as Law, Education, Labour,
Health and Industries.</li>
        <li>The Centre was now to have a bicameral legislature
for the first time. It actually happened after the
1935 Act.</li>
        <li>Communal representation was extended to Sikhs,
Christians and Anglo-Indians, etc.</li>
        <li>Secretary of State was to be henceforth paid salary
out of the British revenue.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Government of India Act, 1935</i></strong></div>

<ol>
    <ul>
        <li>Provided for the establishment of the All India
Federation consisting of the British Provinces and
the Princely States. The joining of the Princely
States was voluntary and as a result the federation
did not come into existence.</li>
        <li>Dyarchy was introduced at the Centre (e.g.
Department of Foreign Affairs and Defence were
reserved for the Governor General). The other
federal subjects were to be administered by the
Governor General with the assistance and advice
of a council of Ministers to be chosen by him (but
to include representatives of Princely states and
minorities, and to be responsible to the Central
Legislature). Residuary powers were to be with
the Governor General only.</li>
        <li>The Federal Legislature i.e. the Central Legislature
was to have two chambers (bicameral): the Council
of State and the Federal Assembly. The Council of
State was to be a permanent body with one-third
of its membership being vacated and renewed
triennially. The Federal Assembly's duration was
fixed for five years.</li>
        <li>It made a three-fold division of power: Federal
(Central) Legislative List, Provincial Legislative List
and the Concurrent Legislative List. Residuary
legislative powers were subject to the discretion of
the Governor General. Even if a bill was passed by
the Federal Legislature, the Governor General could
veto it, while even Acts assented by the Governor
General could be disallowed by the King-in-Council.</li>
        <li>Provincial autonomy replaced Dyarchy in
Provinces i.e. the distinction between Reserved and
Transferred subjects was abolished and full
responsible government was established, subject
to certain safeguards. They were granted separate
legal identity.</li>
        <li>The Governor was the head of the Provincial
Executive and was expected to be guided by the
advice of the popular ministries. However the Act
gave arbitrary powers to the Governors to act in
their 'discretions' in certain matters.</li>
        <li>The Act also provided for a Federal Court (which
was established in 1935), which original and
appellate powers to interpret the Constitution. A
Federal Bank (the reserve bank of India) was also
established. The Indian council of Secretary of State
was abolished.</li>
        <li>Principle of separate electorate was extended to
include Anglo-Indians, Indian-Christians and
Europeans.</li>
        <li>Burma (now Myanmar) and Aden were separated
from India and two new provinces, Orissa and
Sind were created.</li>
    </ul>
</ol>


