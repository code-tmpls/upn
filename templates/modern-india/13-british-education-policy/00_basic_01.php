<h3>British Education Policy</h3>


<div class="subheading"><strong><i>First Phase (1758-1812)</i></strong></div>

The British East India Company showed very little
interest in the education of its subjects during this
period, the few exceptions being:

<ol>
    <ul>
        <li>The Calcutta Madrasa set up by Warren Hastings
in 1781 for the study and teaching of Muslim law
and Persian and Arabic subjects.</li>
        <li>Jonathan Duncan started a Sanskrit College at
Varanasi where he was resident for study of Hindu
law and Philosophy.</li>
        <li>Both were designed to provide a regular supply of
qualified Indians to help in the administration of
law in the courts of the Company.</li>
        <li>The Asiatic Society of Bengal was founded by
William Jones in Calcutta in 1784.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Second Phase (1813-1853)</i></strong></div>

<ol>
    <ul>
        <li>Due to the strong pressure exerted on the Company
by the Christian missionaries and many
humanitarians, including some Indians to
encourage and promote modern education in India,
the Charter Act of 1813 required the Company to
spend Rs. 1 lakh annually for encouraging learned
Indians and promoting the knowledge of modern
sciences in India.</li>
        <li>A ten member committee on Public Instruction was
set up in 1823 when Horace Haymon as the first
President for the development of education.</li>
        <li>Two controversies about the nature of education
arose during this phase:
<ol>
    <ul>
        <li>Whether to lay emphasis on the promotion of
modern western education or on the expansion
of traditional Indian learning and</li>
        <li> Whether to adopt Indian languages or English
as the medium of instruction in modern schools
and colleges to spread western education.</li>
    </ul>
</ol>
</li>
<li></li>
        <li>Macaulay wrote the famous Minute on Educational
policy dated 2 February 1835 which outlined that
western education through English as a medium
instruction was a great notary of western education.</li>
        <li>Lord William Bentinck in the Resolution of 7th
March, 1835 accepted Macaulay viewpoint which
led to the promotion of European science and
literature. Thus settling the controversy.</li>
        <li>In 1844, Lord Hardinge decided to give government
employment to Indians educated in English
schools. This ensured the spread of English
education. It made good progress in the three
residencies of Bengal, Bombay and Madras where
the number of schools and colleges increased.</li>
        <li>Other developments include a great upsurge in
the activities of the missionaries who pioneered
modern education, establishment of medical,
engineering and law colleges which marked a
beginning in professional education and according
official sanction to the education of girls - Lord
Dalhousie offered open support of the government
in this case.</li>
        <li>However the government policy of opening a few
English schools and colleges instead of a large
number of elementary schools led to the neglect of
the education of the masses.</li>
        <li>To cover up this defect in policy the British took
recourse in the so-called 'downward filtration
theory' which meant that education and modern
ideas were meant to filter or radiate downwards
from the upper classes. This policy continued till
the very end of the British rule although it was
officially abandoned in 1854.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>James Thomson Plan (1843-53)</i></strong></div>

<ol>
    <ul>
        <li>In Northwest provinces, he tried to develop a
comprehensive system of village education through
the vernaculars.</li>
        <li>A Department of Education was set up for
inspection and improvement of indigenous schools,
with aim to train personnel for employment in the
revenue, public works department and the judiciary.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Third Phase (1854-1900)</i></strong></div> 

<div class="subheading"><strong><i>Sir Charles Wood Dispatch, 1854</i></strong></div> 


<ol>
    <ul>
        <li>The Education Dispatch of 1854 was also called
the Wood's Dispatch (after Sir Charles wood, the
then President of the Board of Control, who became
the first Secretary of State of India.</li>
        <li>Considered the Magna Carta of English education
in India it entailed:
<ol>
    <ul>
        <li>Promotion of Western Education, art, science,
philosophy and literature of Europe.</li>
        <li>English language to be medium for higher
education while vernaculars to be used for
primary education.</li>
        <li>Three Education Departments were set up in
provinces of Bengal, Bombay, Madras,
Northwest Provinces and Punjab in 1855;
organization of Indian Education Services in
1897 to cover the senior most posts.</li>
        <li>Establishment of Universities in Calcutta,
Bombay and Madras in 1857, in Punjab in
1882 and Allahabad in 1887.</li>
        <li>First chancellor of Calcutta University was
Lord Canning and the first vice chancellor
William Colvite.</li>
    </ul>
</ol>
</li>
    </ul>
</ol>

<div class="subheading"><strong><i>The Hunter Commission (1882-1883)</i></strong></div>

Lord Ripon appointed Hunter Commission under
Sir W.W. Hunter to review the progress which
recommended:

<ol>
    <ul>
        <li>Local bodies (district boards and municipalities)
should be entrusted with the management of
primary education.</li>
        <li>It also said that the government should maintain
only a few schools and colleges, others to be left
to private hands.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Fourth Phase (1901-1920)</i></strong></div>

<ol>
    <ul>
        <li>Lord Curzon appointed a Universities Commission
under Sir Thomas Raleigh (Law member of the
Viceroy's Executive Council) in 1902, and based
on his recommendations the Indian Universities
Act of 1904 was passed.</li>
        <li>It enabled the Universities to assume teaching
functions (hitherto they were mainly examining
bodies), periodic inspection of institutions, speedier
transaction of business, strict conditions for
affiliation, etc.</li>
        <li>However it was criticized by nationalists and in
1910 a separate Department of Education was
established at the Centre.</li>
        <li>The Sadler Commission was appointed in 1917 by
Lord Chelmsford to review the working of Calcutta
University with two Indian members: Ashutosh
Mukherjee and Dr. Ziauddin Ahmed. The main
recommendations were:
<ol>
    <ul>
        <li>Secondary education by a Board of Secondary
Education and duration of the degree course
to be three years.</li>
        <li>7 new universities were opened: Benaras,
Mysore, Patna, Aligarh, Osmania, Lucknow
and Dhaka.</li>

    </ul>
</ol>
</li>
        <li>Kashi Vidyapeeth and Jamia Milia Islamia were
established.</li>
        <li>University courses were divided into Pass course
and Honors course.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Fifth Phase (1921-1947)</i></strong></div>

<ol>
    <ul>
        <li>Education came under Indian control officially as
it became a Provincial subject administered by
Provincial legislatures.</li>
        <li>There was an increase in the number of universities
(20 in 1947); improvement in the quality of higher
education (on the recommendation of the Sadler
Commission); establishment of an Inter-University
Board (1924) and the beginning of inter-collegiate
and inter-university activities.</li>
        <li>Considerable achievements in women's education
and the education of backward classes.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Hartog Committee, 1929</i></strong></div>
The committee made a number of recommendations
including:
<ol>
    <ul>
        <li>The policy of consolidation and improvement of
primary education.</li>
        <li>A selective system of admission to universities and
diversification of courses leading to industrial and
commercial careers.</li>
        <li>The universities should be improved.</li>

    </ul>
</ol>
<div class="subheading"><strong><i>The Wardha Scheme of Basic Education,
1937</i></strong></div>

<ol>
    <ul>
        <li>Worked out by Zakir Hussain Committee after
Mahatma Gandhi published a series of articles in
the Harijan.</li>
        <li>Scheme centered on manual productive work/
vocational courses which would cover
remuneration of the teachers.</li>
        <li>It envisaged a seven years course through the
mother tongue of the students.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>Sergeant Plan of Education, 1944</i></strong></div>

<ol>
    <ul>
        <li>Envisaged establishment of elementary schools
and high school and introduction of universal and
free compulsory education.</li>
        <li>School course of six years was to be provided for
children between ages eleven and seventeen.</li>
    </ul>
</ol>



