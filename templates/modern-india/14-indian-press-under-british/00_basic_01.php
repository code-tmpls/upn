<h3>Indian Press Under British</h3>


The Portuguese were the first Europeans who
brought a printing press to India. In 1684 the East
India Company set up a printing press in Bombay.
The first paper was started in India by James Augustus
Hickey and was called the Bengal Gazette (1780), also
known as the Calcutta General Advertiser. However
all their printing material was seized and they were
asked to leave India in 1782. The other papers of the
time included the Calcutta Gazette, 1784; Bengal
Journals, 1785; Oriental Magazine of Calcutta/Calcutta
Amusement, 1785; Calcutta Chronicle, 1786; Madras
Couriers, 1788; Bombay Herald, 1789. These papers
didn't criticize the government and therefore the British
adopted a soft policy towards them.

<div class="subheading"><strong><i>Censorship of Press Act, 1799</i></strong></div>

<ol>
    <ul>
        <li>Imposed by Lord Wellesley due to threat of French
invasion in India.</li>
        <li>It placed restrictions such that newspapers had to
mention the name of the printer, editor and
proprietor in every issue and the publisher had to
submit all material for pre-censorship to the
Secretary to government.</li>
        <li>In 1802, the Act ws extended to all magazines,
pamphlets, journals, books, etc.</li>
        <li>Lord Hastings relaxed some of the restrictions in
1818 and the pre-censorship provision was
abolished.</li>

    </ul>
</ol>


<div class="subheading"><strong><i>Licencing Regulating Act, 1823</i></strong></div>

<ol>
    <ul>
        <li>Imposed by John Adams the acting Governor
General.</li>
        <li>It provided that every printer and publisher had to
obtain a licence for starting a press or for using it
and a penalty of Rs. 400 for each publication
without permission, with a rigorous punishment
on default.</li>
        <li>Magistrates were authorized to seal the press and
the Governor General could revoke the licence too.</li>
        <li>Raja Ram Mohan Roy had to stop his paper Miratul-Akhbaar.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Licencing Act of 1857</i></strong></div>

<ol>
    <ul>
        <li>Also called the XV Act of 1857.</li>
        <li>Three clauses of the 1823 Act were restored that
had been abolished by Metcalfe.</li>
        <li>Later in 1870 the Indian Penal Code was amended
and it was written that any violation of Press
Regulations would be dealt under Section 124 of
the IPC.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Vernacular Press Act of 1878</i></strong></div>

<ol>
    <ul>
        <li>Also called the 'Gagging Act' as it was only for
local/vernacular papers and not English papers.</li>
        <li>Passed by Lord Lytton.</li>
        <li>Its provisions included:
            <ol>
                <ul>
                    <li>District Magistrate with the previous
permission of local government to ask printer
and publisher of any vernacular paper to enter
into an undertaking not to publish anything
likely to excite dissatisfaction against the
government.</li>
                    <li>Publisher was now required to deposit security.</li>
                    <li>No appeal against this action.</li>
                    <li>No exemption for any vernacular paper.</li>
                </ul>
            </ol>
        </li>
        <li>Ishwar Chandra Sagar's 'Som Prakash' stopped
by this act.</li>
        <li>Later Cranbrook, Secretary of State was hostile to the
idea of pre-censorship and in 1878 it was done away
with and a Press Commissioner was appointed and
the entire Act repealed by Lord rippon in 1882.</li>

    </ul>
</ol>
<div class="subheading"><strong><i>Newspapers Incitement to
Offences Act, 1908</i></strong></div>

<ol>
    <ul>
        <li>It was passed due to the dissatisfaction caused by
the unpopular acts of Lord Curzon and the
resultant growth of Extremism in India.</li>
        <li>It provided:</li>
        <li>Magistrates were empowered to confiscate
printing presses and the property connected
to these if anything objectionable was printed.</li>
        <li>Local government was empowered to take
away with any declaration made by the printer
and publisher of an offending paper.</li>
        <li>The newspaper's editor could appeal to the
High Court against any action of the
government but within 15 days.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Indian Press Act, 1910</i></strong></div>

<ol>
    <ul>
        <li>It revived the Lytton's Press Act of 1878.</li>
        <li>A new provision was added wherein the security
deposit was increased from Rs. 500 to Rs. 2000.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Press Committee, 1921</i></strong></div>

<ol>
    <ul>
        <li>Under the chairmanship of Sir Tej Bahadur Sapru
(law member in Viceroy's Executive Council).</li>
        <li>It recommended the abolition of the Act of 1908
and 1910.</li>

    </ul>
</ol>
<div class="subheading"><strong><i>The Indian Press Emergency Act, 1931</i></strong></div>

<ol>
    <ul>
        <li>The main feature of the Act was that if any
publication directly or indirectly admired any
offence of any person, real or fictious, the
government could seize the press.</li>
    </ul>
</ol>


