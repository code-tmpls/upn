to prevent Indians from entering Civil Services.</li>
        <li>Appointment of the first Famine Commission under
Sir Richard Strachey.</li>
        <li>Second Afghan War (1878-80).</li>
        <li>Policy of Masterly Inactivity towards Afghanistan
was replaced by Forward Policy.</li>
    </ul>
</ol>

