<h3>Governor-General of India</h3>

<div class="subheading"><strong><i>Governors of Calcutta - Fort William
Roger Drake (1756 - 58)</i></strong></div>

<ol>
    <ul>
        <li>During his governorship, Calcutta was captured
by Siraj-ul-Daula in the third Carnatic War.</li>
        <li>The Black Hole tragedy on 20th June, 1756 in which
123 Englishmen died and which was one of the
causes of the Battle of Plassey of 1757 took place.</li>
        <li>Calcutta was recaptured by Clive.</li>

    </ul>
</ol>

<div class="subheading"><strong><i>Robert Clive (1758 - 60)</i></strong></div>

<ol>
    <ul>
        <li>Battle of Bedara in 1759, Dutch were defeated by
the East India Company.</li>
        <li>During this Governorship Clive did not do
anything significant apart from collecting wealth
from the victorious wars.</li>

    </ul>
</ol>
<div class="subheading"><strong><i>Henry Vansittart (1760 - 65)</i></strong></div>

<ol>
    <ul>
        <li>Mir Jafar was replaced by Mir Qasim as the Nawab
of Bengal in 1760.</li>
        <li>However, Mir Jafar was again reinstated.</li>
        <li>The Battle of Buxar was fought in 1764.</li>
        <li>Death of Mir Jafar and succession of his son Najmud-daula.</li>
        <li>The Treaty of Allahabad was concluded on 20
February, 1765 according to which the administration
was left in the hands of a deputy Subedar who was
to be a British nominee.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Robert Clive (1765 - 67) second term</i></strong></div>

<ol>
    <ul>
        <li>The Company got Diwani rights in Bengal, Bihar
and Orissa.</li>
        <li>Started Dual Government in Bengal in 1765.</li>
        <li>He forbade the servants of the company from
indulging in private trade and made payment of
internal duties obligatory.</li>
        <li>Established Society of Trade in 1765 with
monopoly of trade in salt, betelnut and tobacco.
This was abolished in 1767.</li>
        <li>Harry Verclst, 1767-69 and John Cartier (1769-72)
Had uneventful tenures as Governors of Calcutta
with nothing much of grave historical significance
occurring during their time.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Warren Hastings (1772 - 73)</i></strong></div>

<ol>
    <ul>
        <li>Put an end to the Dual System of government in
1772 which was started by Clive</li>
        <li>He signed the treaty of Benaras in 1773 with Nawab
Shujaud-daula of Awadh. Nawab was returned
Allahabad and Kara in lieu of 50 lakhs rupees.</li>
        <li>Quinquennial settlement of land revenue in 1772
farming out lands to the highest bidder which
was later reversed.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Governor-Generals of Bengal
Warren Hastings (1773 - 85)</i></strong></div>

<ol>
    <ul>
        <li>Quinquennial settlement of land revenue in 1772
farming out lands to the highest bidder, later on
he reversed to Annual Settlement (1777) on the
basis of open auction to the highest bidder.</li>
        <li>Treasury was transferred from Murshidabad to
Calcutta, misuse of Dastak was checked.</li>
        <li>Dismissed Deputy Diwans - Muhammad Reza
Khan (Bengal) and Raja Shitab Rai (Bihar).</li>
        <li>Appointed Munni Begum as regent of minor
Nawab Mubarak-ud-Daula of Bengal.</li>
        <li>Signed treaty of Faizabad (1775) with Asaf-udDaula of Awadh.</li>
        <li>The Rohilla War was fought in 1774 and
Rohilkhand was captured by the Nawab of Awadh
with the help of the British.</li>
        <li>The First Maratha War (1775-82) was concluded
with the Treaty of Salbai.</li>
        <li>The Second Mysore War (1780-84) was concluded
with the Treaty of Mangalore.</li>
        <li>Stopped annual allowance of Rs 20 lakh to Shah
Alam II on charges of seeking protection from the
Marathas.</li>
        <li>Took away Allahabad and Kara from Mughal
Emperor and sold it to Nawab of Awadh for Rs.
50 Lakh.</li>
        <li>Codified Hindu and Muslim laws.</li>
        <li>Trial of Nand Kumar and his judicial murder in
1775.</li>
        <li>Foundation of Asiatic Society of Bengal with the
help of Sir William Jones in 1784</li>
        <li>Associated with the Chait Singh (Banaras Raj)
affair (1778) wherein the Raja of Benaras was
executed without any crime.</li>
        <li>After his return to England in 1785, impeachment
proceedings were initiated against him in the
House of Lords but after a long trial of 7 years he
was eventually acquitted.</li>
        <li>Associated with the Begums of Oudh Affair (1782)
wherein the Begums were thrown out of the palace
to place a puppet king.</li>
        <li>In 1776 Manu's Law was translated into English
as the Code of Gentoo Laws.</li>
        <li> Fatawa-i-Alamgiri was also translated.</li>
        <li>Three important Acts introduced during his time
that defined the early constitutional development
in India:
<ol>
    <ul>
        <li>The Regulating Act of 1773</li>
        <li>The Act of 1781 and</li>
        <li>The Pitts India Act of 1784</li>
    </ul>
</ol>
</li>
    </ul>
</ol>n
<div class="subheading"><strong><i>Lord Cornwallis (1786 - 93)</i></strong></div>

<ol>
    <ul>
        <li>Third Anglo-Mysore War (1790-92) was concluded
with the Treaty of Seringapatnam (1792).</li>
        <li>Set up courts at different levels and separation of
revenue administration from judicial
administration in order to reform the judiciary.</li>
        <li>Introduction of the civil Services in India.</li>
        <li>Introduced Cornwallis Code based on the principle
of separation of powers in 1793.</li>
        <li>Superintendent of Police was made the head of
district police and became the incharge of an area
of 1000 sq. km.</li>
        <li>Besides the English magistrates were given police
powers. And the Zamindars were divested of all
police powers.</li>
        <li>Introduction of the Permanent Settlement in Bihar
and Bengal (1793) which was also called as the
Zamindari System.</li>
        <li>A ten-year settlement was concluded with the
Zamindars in 1790 which was made permanent
in 1793.</li>
        <li>Raised salaries of the officials. Collectors were to
get 1% of the revenue collection in addition to
their salary of Rs. 1500.</li>
        <li>This administrative system remained in force till
1858.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Sir John Shore (1793 - 98)</i></strong></div>
<ol>
    <ul>
        <li>He was the main person instrumental in designing
the Permanent Settlement under Lord Cornwallis
and was the President of the Board of Revenue
under Cornwallis.</li>
        <li>First Charter Act of 1793 was passed.</li>
        <li>Famous for his policy of non-intervention.</li>
        <li>Ahmad Shah Abdali's grandson named Zaman
Shah attacked India in this period.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Lord Wellesley (1798 - 1805)</i></strong></div>

<ol>
    <ul>
        <li>Described himself as Bengal Tiger.</li>
        <li>Created Madras Presidency after the annexation
of the Kingdoms of Tanjore and Carnatic.</li>
        <li>Introduced the system of Subsidiary Alliance in
1798.</li>
        <li>Signed Treaty of Bassein (1802) with the Peshawa
and fought Second Anglo-Maratha War during
1803-05.</li>
        <li>The Fourth Mysore War (1799) concluded with the
death of Tipu Sultan and Mysore as a state was
finished.</li>
        <li>Lord Lake captured Delhi and Agra in 1803 and
the Mughal Emperor was put under Company's
protection.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Sir George Burlow (1805 - 07)</i></strong></div>

<ol>
    <ul>
        <li>Followed a policy of non-intervention.</li>
        <li>Sepoy Mutiny at Vellore in 1806 when William
Bentinck was Governor of Madras Presidency.</li>
        <li>Third to restore peace between Scindhia and
Holkar.</li>
        <li>Holkar accepted Subsidiary Alliance by Treaty of
Rajpurghat in 1805.</li>
        <li>End of Second Anglo-Maratha War.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>Lord Minto I (1807 - 13)</i></strong></div>

<ol>
    <ul>
        <li>Signed Treaty of Amritsar in 1809 between Ranjit
Singh and English.</li>
        <li>Sent in 1808 Mission of Malcolm to Persia and
that of Elphinstone to Kabul.</li>
        <li>The Charter Act of 1813 was introduced.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>Lord Hastings (1813 - 23)</i></strong></div>

<ol>
    <ul>
        <li>Adopted the policy of intervention and war.</li>
        <li>Success in the Gorkha war of the Anglo-Nepalese
War (1813 1823). Signed Treaty of Sogauli after
defeating the Gorkha leader Amar Singh.</li>
        <li>Hastings was awarded the title of 'Marquis of
Hastings' in 1816.</li>
        <li>Abolished Peshwaship and annexed all his
territories and created the Bombay Presidency after
the Third Anglo-Maratha War (1817-1818).</li>
        <li>Pindari War fought (1817-1818).</li>
        <li>Appointed Darogas in districts after forcing
zamindars to surrender their police force.</li>
        <li>The Company signed separate treaty with Raja of
Sikkim in 1817 by which the Company handed
over the territory between Hechi and Tista rivers.</li>
        <li>Introduction of Ryotwari Settlement in Madras
Presidency by Governor, Thomas Munro (1820).</li>
        <li>Mahalwari (village community) system of land
revenue was made in North-West Province by James
Thomson.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Lord Amherst (1823 - 28)</i></strong></div>
<ol>
    <ul>
        <li>Received by Emperor Akbar II on terms of equality
in 1827.</li>
        <li>Signed Treaty of Yandaboo in 1826 with lower
Burma or Pegu by which British merchants were
allowed to settle on southern coast of Burma and
Rangoon.</li>
        <li>The First Burmese War was fought (1824-26).</li>
        <li>Acquisition of territories in Malay Peninsula.</li>
        <li>Capture of Bharatpur (1826).</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Lord William Bentinck (1828 - 33)</i></strong></div>
<ol>
    <ul>
        <li>He was the last Governor General of Bengal.</li>
        <li>Known as the Benevolent Governor-General.</li>
        <li>Introduced a number of social reforms. Banned the
practice of Sati in 1829 (pioneering efforts made in
this connection by Raja Ram Mohan Roy),
suppressed Thugi in 1830 (Military operation led
by William Sleeman).</li>
        <li>Annexed Mysore (1830), Kachar (1832), Jaintia
(1832) and Coorg (1834).</li>
        <li>The raja of Mysore was deposed (1831)</li>
        <li>Concluded a treaty of perpetual friendship.</li>
        <li>The Regulation of 1833 on land revenue settlement
by Mertins Bird (called father of land-revenue
settlement in the North). Use of field maps and
filed registers were prescribed for the first time.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Governor Generals of India</i></strong></div>

<div class="subheading"><strong><i>Lord William Bentinck (1833 - 35)</i></strong></div>

<ol>
    <ul>
        <li>The Charter Act of 1833.</li>
        <li>Defined the aims and objectives of the educational
policy of the British Government, appointed Lord
Macaulay, the President of the Committee of Public
Instruction which recommended English as the
medium of instruction and introduction of English
language literature, Social and Natural Sciences
in the curriculum in 1835.</li>
        <li>Raja ram Mohan Roy visited England and died in
Bristol in 1833</li>
        <li>Created Province of Agra in 1834.</li>
        <li>English was to be court language at higher courts
but Persian continued in lower courts.</li>
        <li>Abolished Provincial Courts of appeal and
appointed commissioners instead.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>Sir Metcalfe (1835)</i></strong></div>
<ol>
    <ul>
        <li>Abolished restriction on press.</li>
        <li>Called "Liberator of press."</li>

    </ul>
</ol>
<div class="subheading"><strong><i>Lord Auckland (1835 - 42)</i></strong></div>
<ol>
    <ul>
        <li>First Afghan War (1836-42) gave a great blow to
the prestige of British in India and Auckland was
immediately called back during the war.</li>
        <li> Tripatriate Treaty was signed between the
Company, Ranjit Singh and Shah Shuja by which
Ranjit Singh accepted Company's mediation in
disputes of the Amirs of Sind.</li>
        <li>Maharaja Ranjit Singh died in 1839.</li>
        <li>Shah Shuja conceded his sovereign right to the
Company over Sind on condition of receiving the
arrears of the tribute, the amount of which was to
be determined by the Company.</li>
        <li>Mandavi State was annexed in 1839.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>Lord Ellenborough (1842 - 44)</i></strong></div>
<ol>
    <ul>
