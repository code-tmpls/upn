        <li>War with Gwalior 1843.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>Lord Hardinge I (1844 - 48)</i></strong></div>
<ol>
    <ul>
        <li>Fought the First Sikh War (1845-46) which ended
with the Treaty of Lahore 1846.</li>
        <li> Prohibited female infanticide.</li>
        <li>Suppressed the practice of human sacrifice among
the Gond tribe of Central India.</li>
        <li>Preference to English educated in employment.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>Lord Dalhousie (1848 - 56)</i></strong></div>

<ol>
    <ul>
        <li>Second Sikh War (1848-49) and annexed Punjab.</li>
        <li>Second Burmese War (1852) and annexation of
Lower Burma (Pegu).</li>
        <li>The Indian States annexed by the application of
Doctrine of Lapse and Law of Escheat were Satara
(1848), Jaitpur and Sambalpur (1849), Bhaghat
(1850), Udaipur, Jhansi (1853) and Nagpur (1854).</li>
        <li>Boosted up the development of railways-laid the
first railway line 1853 from Bombay to Thane and
second from Calcutta to Raniganj.</li>
        <li>Gave a great impetus to post and telegraph.
Telegraph lines were laid, (First line from Calcutta
to Agra).</li>
        <li>Organised a separate Public Works Department in
every province by divesting the Military Board of
this power.</li>
        <li>Shimla was made summer capital and army
headquarter.</li>
        <li>Hindu Widow Remarriage Act was passed in 1856.</li>
        <li>Annexed Awadh in 1856 on excuse of
maladministration when Nawab Wajid Ali Shah
refused to abdicate. Every house in Oudh had a
son in the British army, thus sowed the seeds for
the revolt of 1857.</li>
        <li>Annexed Berar in 1853 from Nizam on account of
arrears for auxiliary.</li>
        <li>Darjeeling and its outlying districts annexed.</li>
        <li>Stopped and abolished pensions and titles of
various states. For e.g. stopped annual payment of
Nana Sahib, adopted son of Peshwa Baji Rao II
after his death.</li>
        <li> Planned to stop pensions and remove legal titles
of the Mughal Emperor after death of Bahadur
Shah II, but this was rejected by the Court of
Directors.</li>
        <li>The Woods Dispatch of 1854 according to which
the British took the responsibility for mass
education.</li>
        <li>The Santhal uprising (1855-57)</li>
    </ul>
</ol>
<div class="subheading"><strong><i>Lord Canning (1856 - 57)</i></strong></div>
<ol>
    <ul>
        <li>Foundation of the universities at Calcutta, Bombay
