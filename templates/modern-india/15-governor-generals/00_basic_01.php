<div class="subheading"><strong><i>Lord Lytton (1876 - 80)</i></strong></div>

<ol>
    <ul>
        <li>Passing of the Royal Title Act of 1876 and the
assumption of the title Empress of India (Kaiser-IHind) by Queen Victoria.</li>
        <li>Holding of Delhi Durbar in 1877.</li>
        <li>Passing of Vernacular Press Act of 1878.</li>
        <li>Passing of Arms Act of 1878 which empowered a
