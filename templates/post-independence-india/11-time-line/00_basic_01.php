<h3>Time Line Since 1947</h3>

A summary of the key events in India according
to since independence in 1947.

<div class="subheading"><strong><i>1947</i></strong></div>

<ol>
    <ul>
        <li>Aug. 15 – India’s First Independence Day</li>
        <li>Oct. 22 – Armed tribals supported by
Pakistan invade Kashmir to dislodge
Maharaja Hari Singh and secure Kashmir
for Pakistan.</li>
        <li>Oct. 26 – Hari Singh accedes Kashmir to
India in return for military help to ward
off the tribal invasion.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>1948</i></strong></div>

<ol>
    <ul>
        <li>Jan. 1 – India takes the Kashmir issue to
the UN.</li>
        <li>Jan. 30 – Mahatma Gandhi assassinated.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>1949</i></strong></div>

<ol>
    <ul>
        <li>Nov. 26 – Constituent Assembly adopts the
finished constitution.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>1950</i></strong></div>
<ol>
    <ul>
        <li>Jan. 26 – Constitution comes into power,
Republic Day.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>1952</i></strong></div>
<ol>
    <ul>
        <li>Apr. 17 – The First Lok Sabha constituted
by the first General Elections.</li>
        <li>Dec. 15 – Potti Sriramulu dies, 58 days into
his fast to get a Telugu speaking province
of Andhra Pradesh. Eventually leads to all
states being reorganized on the basis of
language.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>1953</i></strong></div>
<ol>
    <ul>
        <li>Aug. 8 – Sheikh Abdullah imprisoned by
the Nehru Government.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>1955</i></strong></div>
<ol>
    <ul>
        <li>Hindu Marriage Act passed by the
Parliament.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>1956</i></strong></div>
<ol>
    <ul>
        <li>Mar. 22 – Angami Zapu Phizo declares the
formation of a “Naga Central Government”.
Full scale war between Naga rebels and the
army by the middle of 1956. War subsides
by December.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>1957</i></strong></div>
<ol>
    <ul>
        <li>Apr. 5 – EMS Namboodripad becomes CM
of Kerala after the victory of CPI in the
assembly elections.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>1962</i></strong></div>
<ol>
    <ul>
        <li>Oct. 20 – India China War over border
dispute begins (till November 21).</li>
    </ul>
</ol>
<div class="subheading"><strong><i>1964</i></strong></div>
<ol>
    <ul>
        <li>May 27 – Death of Jawaharlal Nehru.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>1965</i></strong></div>
<ol>
    <ul>
        <li>Aug. 15 – Second Indo-Pak war hostilities
begin.</li>
        <li>Sep. 23 – Ceasefire ends second Indo-Pak
war.</li>
    </ul>
</ol>

<div class="subheading"><strong><i>1966</i></strong></div>

<ol>
    <ul>
        <li>Jan. 10 – Tashkent Agreement signed
between Lal Bahadur Shastri and Ayub
Khan.</li>
        <li>Jan. 11 – Lal Bahadur Shastri dies a
mysterious death at Tashkent.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>1971</i></strong></div>
<ol>
    <ul>
        <li>Dec. 16 – Surrender of Eastern command
of Pakistan Military in the 1971 war.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>1975</i></strong></div>
<ol>
    <ul>
        <li>Jun. 12 – Allahabad High Court upholds
Raj Narain’s accusations of electoral fraud
against Indira Gandhi (Rae Bareilly
constituency, 1971 General Elections) and
disqualifies her from contesting Lok Sabha
elections for six years.</li>
        <li>Jun. 25 – Indira Gandhi imposes emergency.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>1977</i></strong></div>
<ol>
    <ul>
        <li>Mar. 24 – Morarji Desai of the Janata Party
becomes the first non-Congress PM of India.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>1980</i></strong></div>
<ol>
    <ul>
        <li>Jan. 14 – Indira Gandhi becomes Prime
Minister again.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>1984</i></strong></div>
<ol>
    <ul>
        <li>Providing Jun. 3 – Operation Blue Star to
flush out extremists from the Harimandir
Sahib.</li>
        <li>Oct. 31 – Indira Gandhi assassinated by her
Sikh bodyguards; Hindu-Sikh riots ensue.
Her son Rajiv Gandhi becomes Prime
Minister.</li>
        <li>Dec. 2/3 – Bhopal Gas Tragedy.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>1985</i></strong></div>
<ol>
    <ul>
        <li>Jun. 23 – Air India Kanishka plane bombing.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>1990</i></strong></div>
<ol>
    <ul>
        <li>Aug. 7 – V.P. Singh plans to implement the
Mandal Commission report favoring
reservations for SCs, STs and OBCs.</li>
        <li>Sep. 25 – L.K. Advani begins his rath yatra
from the Somnath Temple in Gujarat to
Ayodhya.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>1991</i></strong></div>
<ol>
    <ul>
        <li>May 21 – Rajiv Gandhi assassinated.</li>
        <li>June 21 – P.V. Narsimha Rao appoints
Manmohan Singh as his finance minister;
start of economic reforms and liberalization.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>1992</i></strong></div>
<ol>
    <ul>
        <li>Dec. 6 – Babri Masjid demolished, HinduMuslim riots ensue.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>1999</i></strong></div>
<ol>
    <ul>
        <li>May 26 – Operation Vijay launched to fight
Pakistani incursion in Kargil.</li>
        <li>Dec. 24 – Indian Airlines flight IC-814
hijacked to Qandahar by terrorists.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>2000</i></strong></div>
<ol>
    <ul>
        <li>Dec. 22 – Terror attack on the Red Fort by
LeT.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>2001</i></strong></div>
<ol>
    <ul>
        <li>Dec. 13 – Attack on Indian Parliament by
LeT and JeM terrorists.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>2002</i></strong></div>
<ol>
    <ul>
        <li>Feb. 27 – Godhra train carnage, HinduMuslim riots ensue.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>2005</i></strong></div>
<ol>
    <ul>
        <li>Jun. 15 – Right to Information Act passed
by the Parliament.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>2008</i></strong></div>
<ol>
    <ul>
        <li>Apr. 10 – Supreme Court upholds the 27%
OBC quota, taking reservation in IITs and
IIMs to a whopping 49.5%.</li>
        <li>Nov. 26 – Terror attacks in Mumbai at
Hotels Taj and Oberoi, CST train terminal
and other places.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>2009</i></strong></div>
<ol>
    <ul>
        <li>May – United Progressive Alliance formed
Government for second term.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>2010</i></strong></div>
<ol>
    <ul>
        <li>Oct – Commonwealth Games, officially
known as the XIX Commonwealth Games,
were held in Delhi.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>2011</i></strong></div>
<ol>
    <ul>
        <li>April 5 – The anti-corruption movement
intended to establish strong legislation and
enforcement against perceived endemic political
corruption under the leadership of anticorruption activist Anna Hazare.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>2012</i></strong></div>
<ol>
    <ul>
        <li>Dec. 16 – Horrific Nirbhya Case and allover
India protest for women safety.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>2013</i></strong></div>
<ol>
    <ul>
        <li>Lokpal Bill passed in Parliament.</li>
    </ul>
</ol>
<div class="subheading"><strong><i>2014</i></strong></div>
<ol>
    <ul>
        <li>May 16 – BJP attains clear majority in 16th
Lok Sabha Eletions.</li>
        <li>May 26 – Narendra Modi was sworn in as
Prime Minister of India.</li>
        <li>June 2 – Telangana became the 29th state
of India.</li>
    </ul>
</ol>


