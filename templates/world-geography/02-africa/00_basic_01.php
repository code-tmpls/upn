The average population density is very low.
Nile delta is the high density area accompanied by the Mediterranean regions in the north
and the south. 750 million people inhabit the
continent.
<br>
<br>
As far as religion is concerned, Saharan
Africa is predomiantly Islamic while sub-Saharan Africa is Christian and animistic. The
African transition zone which marks a zone of
conflict between the followers of Islam and
Christianity provides a key to understanding
the civil war prevalent in this zone.
After the industrial revolution, Africa has
developed as an exporter of primary commodities. In the present century, Africa has made a
grand departure in the field of biotechnology
because it is a huge resource of biodiversity.
The vast stretching area of Savanna Grassland
has enough potential to grow food for the rapidly increasing population. Now the continent
is rapidly developing in various spheres such
as Science and Technology, quality of life, agriculture, economy, etc. Thus, it would be very
apt to entitle the continent as the Continent of
Hope or 'Rising star'.


<div class="subheading"><strong><i>Suez Canal</i></strong></div>

The Suez Canal is one of the world's most
important waterways. Opened in 1869, the sea
level artificial waterway crosses the narrow
Isthmus of Suez, joining Africa and Asia and
permits ocean going vessels to travel between
the Arabian Sea and the Mediterranean Sea
via the Gulf of Suez and the Red Sea. The canal
is owned and operated by Egypt, and separates the main part of that country on the west
bank from the Sinai Peninsula on the east bank.
The northern terminus of the canal is Port Said.
Southward, the canal continues in a straight
line through Manzala and Timshah lakes and
Ismailia where the administrative headquarters
of the canal is located. Further south the canal
passes through the Great and Little Bear lakes
(now a single lake) and then passes through
the city of Suez.

