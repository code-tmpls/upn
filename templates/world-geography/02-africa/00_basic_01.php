<h3>Africa</h3>

<div class="subheading"><strong><i>Landscape</i></strong></div>

The continent of Africa ranks second next
only to Asia in terms of size by occupying 20
per cent of the total land area. Its physiographic
features consist of the mightiest rivers, greatest
deserts and some well-known waterfalls. The
entire continent appears like a huge plateau.
One of the characteristic features of African
landscape is the absence of elongated parallel
ranges of continental dimensions such as Andes
and Himalayas. Mount Atlas in the northwest
and Drakensberg in the south-east are notable
ranges. The towering mountain Kilimanjaro
with snow - capped peaks all year around is
well known. The Kalahari and Sahara desert
also run alongside the mountain. Besides, Nile
and Zaire rivers, there are so many rivers and
waterfalls which have boosted up the hydroelectricity generation in the continent in which
it occupies next position after Asia.


<div class="subheading"><strong><i>Climate and Vegetation</i></strong></div>

It is the most tropical continent and the
average temperature remains high throughout
the year. Its equatorial belt has only one climate 
that is hot and humid with the least diurnal and annual range of temperature. The
convectional type of rainfall occurs in the late
afternoon which is also called 4'o clock rains.
The equatorial rain forest of Africa is also called
gallery forest. Some of the well-known species
such as Mahogany, Rosewood and Ebony are
found in this type of forest. The entire Savanna
region is prevalent in the southern and northern side where the summer season is very warm
and winter is warm. It is also known as the
Sudan type of climate. This region has some of
the largest wild animals thus called 'The Big
Game Country'.

<ol>
    <ul>
        <li>
        <div class="subheading"><strong><i>The Mediterranean Climatic Region</i></strong></div>
            
This type of climate is prevalent in southwestern part of S.Africa and the 
bordering areas of Mediterranean Sea in northern Africa. The entire region 
experiences warm dry summers because of offshore trade winds and cool moist
winters because of onshore westerlies.
<br>
<br>
A prominent feature of this region is the
prevalence of local winds such as Sirocco
and Khamsin. The vegetation is marked
by shunted trees and xerophytic adaptation. Some of the important trees are:
cork, oak, figs and olives. This region is
also known for the cultivation of citrus
fruits and is called 'Orchards of the
World'.</li>
        <li><b>Temperate Grassland:</b> This region covers the areas between Drakensberg and
the Kalahari and is further subdivided
into a more tropical Bush - veldt and
the temperate High-veldt. Parts of east
Africa and Madagascar are under the
influence of onshore trade winds all the
year round and experiences evenly distributed rainfall which is known as the
Tropical Marine Climate Region. This
region is also known for the cultivation
of cloves, spices and Coconuts.</li>
        <li><b>The China Type of Climate:</b> It is found
in the south-eastern part of South Africa. The narrowness of the continent,
maritime influences and onshore trade
winds bring about a uniform distribution of rains and a small annual range
of temperature. The natural vegetation
includes the broad leaved deciduous forests on the low lands and coniferous on
the highlands. The trees like bamboo,
palm, wattle, etc. are more suitable for
the climate. Besides, Mangrove is found
along the Gulf of Guinea and Montane
vegetation on the highlands of Ethiopia,
Tanzania and Kenya.</li>
    </ul>
</ol>


<div class="subheading"><strong><i>Social Features</i></strong></div>

Africa is well known for the diverse races
and tribes. The Hausa of Nigeria are agriculturalists, the Masais of East Africa are
pastoralists while the Pygmies of Congo are
hunters and food gatherers. There is a plethora
of tribal language spoken in sub-Saharan Africa. Here, nearly 10,000 languages of the world
are being spoken, but Swahili, the lingua franca,
is understood by most. The Afro -Asiatic family of language and the Arabic language dominates in Saharan Africa.

