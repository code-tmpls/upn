The Western region comprises 11 states and
accounts for 33 per cent of the land area and
20 percent of the population. In spite of water
shortage, the region accounts for 20 per cent of
the agricultural production. In southern California, the favorable climate and irrigation from
the mountain streams supports the production
of citrus fruits. Today the central valley is
highly productive. "Oregon and Washington"
are the leading producer of timber especially
softwood varieties like "Douglas fir". The "Silicon Valley" of San Francisco region is well
known for computers and biotechnology. The
"Boeing Company of aircraft manufacturing"
is located in Seattle. The copper in "Arizona"
and oil and coal in "Utah" is well known. The
western Pacific is now emerging as the world's
most economically dynamic region.

<div class="subheading"><strong><i>Canada</i></strong></div>

At present, Canada is one of the most industrialized and technologically advanced and
urbanized state of the world. The prominent
characteristic of Canada is the greater reliance
on the export of raw and semi-finished material while it imports the manufactured goods.
The overbearing influence on U.S.A. on Canada
has been a sure point to many Canadians who
think of themselves as "American Colony".

<div class="subheading"><strong><i>Social Aspect</i></strong></div>

