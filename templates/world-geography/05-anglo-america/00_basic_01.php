<h3>Anglo-America</h3>

<div class="subheading"><strong><i>Introduction</i></strong></div>

The continent of Anglo-America comprises
the world's two largest countries- the United
States of America (USA) and Canada. In spite
of a wide range of socio-economic inequality,
diversity of national background and language,
the indigenous American culture is flourishing.
Its culture encourages the entrepreneurship,
development of skill and pursuit of excellence.
This is the reason why USA and Canada have
emerged as the two most developed countries
of the world. The natural resources have facilitated 
the development of ward's largest manufacturing complexes and as a result of it large
scale urbanisation. The entire agricultural
activities in the continent are mechanized, export oriented and commercial.

<div class="subheading"><strong><i>Landscape</i></strong></div>

The entire physiography of Anglo-America
varies from Swampy plains to high mountains.
Some of the well-known plains are "the Canadian Shield" the "Arctic Coastal Plain," the
"Gulf Atlantic Plain", the "Piedmont Plain" and
the "Interior Plains". The interior plains lie
mostly within the drainage basins of the rivers
Mississippi, Mackenzie, Saskatchewan and St
Lawrence. A large part of Canadian Shield is
covered with swamps and numbers of lakes of
glacial origin such as "Great Bear Winnipeg"
and the "Great Lakes". The "Appalachians",
"Western Cordillera" and "Interior Highland"
are some of the important highlands. The Arkansas river divides the interior highlands into
two major segments- the "Boston" and the
"Ozark" mountains in the north and "Ouachita"
Mountain" in the south. The mountain range
comprises the "Rocky Mountain" and "Sierra
Nevada". The "Great Basin" is the largest plateau basin. "St. Lawrence" and "Mackenzie"
river which flow northward are the important
rivers. The Mississippi flows southward.

<div class="subheading"><strong><i>Climate and Vegetation</i></strong></div>

The "non-tropical" type of climate is the
most prevalent one in Anglo-America except
Florida, where "tropical climatic" type is found.
The "Tundra Type of Climate" is found in areas from the Alaska to Labrador and
Greenland. The mean annual temperature is
very low in the region. Within the Arctic Circle,
there are weeks of continuous darkness or light.
The "cool temperate continental" or "Siberian"
or "Taiga" type of climate is found south of the
Tundra region. Here, the mean annual range
of temperature is very high and the precipitation is very low. The "Cool Temperate Eastern
Margin" or "Laurentia type" of climate is found
in south of Taiga. Here, deciduous trees species
oak, beech, maple and birch are important. The
"Cool Temperate Western Margin" or "British
type" of climatic condition is mainly found in
the north-west where westerlies influences may
be traced. Due to oceanic influence, the mean
annual temperature is not very large. The important species include elm, beech, poplar, alders and aspen. "The Warm Temperate Eastern Margin or China type" of climate is found
in the south-east. The annual rainfall is heavy
and well distributed throughout the year. The
occurrence of "tornadoes" and "twister" is very
common in this region. The "Warm Temperate
Western Margin or Mediterranean type" of climate is found in California. The summer is dry
and warm and the winter is moist. The "Desert
type" of climate is found in the region of
Mohave, Sonoran and California deserts. Bordering the desert, in the interior of the continents lies the temperate Grassland. It is almost
completely treeless. In North America, these are
called "Prairies". An interesting feature of the
Prairies is the hot local wind "Chinook" or
"Snow Eater". It blows down the eastern slopes
of Rockies. The Prairies is covered with Grass
and no trees. Species like "Low Willow", alders
and poplars are found here.

<div class="subheading"><strong><i>USA</i></strong></div>

The USA is also known as the "Meltingpot of the world" because according to the latest
census report nearly 85 per cent of the US
population identified themselves with one of
more than 140 different national backgrounds.
Some of the important nationalities here are;
British Irish, French, Italian, Scandinavian,
African and more recently Chinese, Indian and
Korean. The immigrants have brought with
them different religions. The important ones are
Islam, Buddhism, Confucianism and Hinduism.
Nevertheless, Anglo-America remains Christian
dominated.
<br>
<br>
The North-East region of U.S.A. consists of
6 New England states; Maine, New Hampshire,
Vermont, Massachusetts, Connecticut and
Rhode Island and five middle Atlantic states;
New York, Jersey, Pennsylvania, Delaware,
Maryland and the districts of Columbia. These
areas are intensely developed and most densely
populated. These are also the most urbanized
region. The extraordinary urbanisation is the
by-product of industrialization in this region.
The old textile town "Lowell and Lawrence"
are the new avenues of employment.
The southern side includes the 14 states
and accounts for 25 per cent of the total area
and little over 30% of the population. This region has intermediate population density and
lower level of urbanisation. The economy of
the south is dominated by production of cash
crops for the world market. The cotton growing region has now shifted to Atlantic coastal
region of Georgia and South Carolina. Which
is also called "Sea-Island". By 1860, "the BlackBelt" extending from Alabama of Mississippi
had come to the principal centres of the U.S.A.
The Midwest includes 12 states and accounts
for 20 per cent of the total land area and approximately 25 per cent of the population. This
region is the leading producer of agricultural
products and manufactured goods. In value
term, the Midwest accounts for the 40 percent
of the total agricultural production. The industrial development decreases from east to west.


