The easternmost of the Atlantic region is
most populated regions. It consists of the province of Newfoundland, New Brunswick, Nova
Scotia and Prince Edward Islands. The Prince
Edward Island is a lowland occupied by farm
and small agglomerated settlement. The Labrador located on the mainland, is a part of the
province of Newfoundland. In this region, the
fishing industry is the most important. The
Atlantic province provides a hard environment
for agriculture. At present, agriculture is mainly
concentrated in very small patches of good land
dedicated to specialized farming. These include
the ''Prince Edward Island" and "AnnapolisCornwallis Valley" of Nova Scotia where potatoes and apple are grown. Prince Edward Island has the highest proportion of tilled land
of any Canadian province.
<br>
<br>
The core region of Canada consists of the
provinces of "Quebec" and "Ontario". In
Ontario, the American Midwest is repeated on
a somewhat smaller scale with corn and livestock production, dairy farming and other special crops like fruits and vegetables while in
Quebec, one find the "long lot" pattern of landholding with dairy farming being the predominant form of agriculture. This region produces
a wide variety of industrial products and accounts for more than ¾th of the Canada's
output. The "Ontario" is well-known for iron
and steel-industry. Other important industries
include automobiles and auto-spare parts.
In Quebec 82 per cent of the people speak
French and only 11 per cent speak English
while it is quite the reverse in Ontario. The
government in Quebec is French dominated.
The Prairies province or Pacific Region includes Manitoba, Saskatchewan and Alberta.
This region is full of the tall Prairie grasses.
Agriculture is the basis of this region's economy.
Together these provinces produce around half
of the farm produce of Canada and sell about
a fifth of world's wheat exports. Of the three
Prairie Provinces, Saskatchewan, with nearly
20 per cent of the total employment in farming, is agriculturally the most important. In recent times, minerals, especially oil and natural
gas have become the foundation of advances
in the region's economy. Coal, potash, nickel,
copper, zinc and uranium are important minerals are mined in this region.


<div class="subheading"><strong><i>Vancouver Region</i></strong></div>

This province lies within the high coast
range and Rockies. This pacific province of
British Columbia displays an environmental
difference from the rest of the Canada. It is
also a distinctive area, climatically, with British
type of climate. The province is also sparsely
populated with concentration of population
around the Vancouver. It consists of more than
10 percent of the land area but it has over half
of the province population. The economy of
this region is chiefly based on the primary products especially timber and minerals including
coal, oil and natural gas. This province is also
well developed in hydro electricity. The fishing
industry is also very well developed. In the
recent times, this region has also experienced
rapid urban growth.
<br>
<br>
The Canadian North comprises most of the
territory. The population is dispersed in a small
pockets along the southern fringe of the North
called "Near North" - the southern edge of the
sub-arctic climate zone. Mining settlements form
many of such pockets of population. Sudbury
and Ontario are the most important one. Metal
and paper industries are commonly found here.
The agriculture potential of this region is severely limited because of harsh climatic condition. The agricultural activities are mainly concentrated around the Sanguinary River, the clay
Belt of Ontario and Quebec, the Peace River
plain of Alberta and British Columbia.


<div class="subheading"><strong><i>Yukon and North-West Territory</i></strong></div>

This region is also known as "Territorial
Canada" which is in federal control and comprises over a third of Canada's areas. The population consists of Native Indians, Eskimos and
the people of mixed white origin. The white
people are primarily engaged in the service
sector while Red Indians and Eskimos still carry
on their traditional hunting and gathering activities. The Native Indians and the Eskimos,
on the other hand are generally unemployed
and dependent on state support. In the recent
time, the political assertiveness of the Indians
and Eskimos, who have demanded a greater
control over their affairs, has further delayed
the process of development.


